-- ================================================
-- ファイル名: 11_create_postalhistory.sql
-- 役割:
--   郵便番号の変更履歴を表す PostalHistory テーブルを作成する。
--
-- このSQLの目的:
--   ある人物(name)について、
--   郵便番号がどのように変更されてきたかを
--   「現在の郵便番号 -> 次の郵便番号(new_pcode)」
--   という鎖の形で保存できるようにすること。
--
-- 学習上の主題:
--   - 履歴データを自己参照的な形で持つ設計
--   - 1件の行が「1回の変更関係」を表すこと
--   - 後続の再帰共通表式でたどれるようなデータ構造
--
-- 今回の最重要ポイント:
--   この表は単なる一覧表ではなく、
--   レコードどうしが
--     pcode -> new_pcode
--   でつながる「連鎖構造」になっている。
--
--   つまり後続の再帰共通表式では、
--   1行だけを見るのではなく、
--   このつながりを何段もたどって
--   「一番古い住所」を探しにいく。
-- ================================================

CREATE TABLE PostalHistory
(
    name CHAR(1),
    -- name:
    --   履歴の持ち主を表す識別子である。
    --
    --   例:
    --     'A'
    --     'B'
    --     'C'
    --
    --   後続の再帰検索では、
    --   同じ人物の履歴だけをたどる必要があるため、
    --   join 条件にもこの列が入る。

    pcode CHAR(7),
    -- pcode:
    --   ある時点の郵便番号である。
    --
    --   この列は「現在のノード」と考えると分かりやすい。
    --
    --   後続の再帰では、
    --   親の pcode と子の new_pcode をつなぐことで
    --   1つ前の履歴を逆向きにたどる。

    new_pcode CHAR(7),
    -- new_pcode:
    --   pcode が変更された後の郵便番号である。
    --
    --   もし NULL なら、
    --   その pcode は現時点で最新の郵便番号であることを意味する。
    --
    --   つまり
    --     new_pcode IS NULL
    --   の行は、
    --   履歴連鎖の終端(現在地点)を表す。
    --
    --   後続の再帰共通表式では、
    --   この終端を「探索の開始点」にして、
    --   そこから過去へさかのぼる。

    CONSTRAINT pk_name_pcode PRIMARY KEY(name, pcode)
    -- PRIMARY KEY(name, pcode):
    --   同じ人物について同じ pcode が重複しないようにする複合主キーである。
    --
    --   これにより、
    --   1人の履歴の中で各郵便番号は一意に扱われる。
);