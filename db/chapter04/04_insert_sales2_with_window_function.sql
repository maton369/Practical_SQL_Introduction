-- ================================================
-- ファイル名: 04_insert_sales2_with_window_function.sql
-- 役割:
--   Sales テーブルを集合として一括処理し、
--   同じ会社の直前売上との差分記号を計算して Sales2 に挿入する。
--
-- このSQLの目的:
--   以前の「ぐるぐる系」プロシージャのように
--   1行ずつループして直前値を保持するのではなく、
--   ウィンドウ関数を使って
--   「各行の直前レコードの売上」をSQLの中で直接求め、
--   その比較結果を一括で Sales2 に書き込むこと。
--
-- 学習上の主題:
--   ウィンドウ関数、
--   PARTITION BY,
--   ORDER BY,
--   フレーム指定
--     ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
--   CASE 式、
--   SIGN 関数
--   を組み合わせて、逐次処理を集合指向SQLへ置き換える方法を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは、ミックさんのいう「ぐるぐる系」に対する
--   「ガツン系」の典型例である。
--
--   つまり
--     1. company ごとに行を分ける
--     2. year 順に並べる
--     3. その各行について「1つ前の売上」をウィンドウ関数で取る
--     4. 現在売上との差を SIGN で符号化する
--     5. CASE で '+', '-', '=' に変換する
--     6. その結果を INSERT ... SELECT で一括挿入する
--   という流れになっている。
--
--   以前のプロシージャ版では
--     直前会社
--     直前売上
--   を変数で保持していたが、
--   今回はそれを
--     ウィンドウ関数 + パーティション + 並び順
--   で表現している。
--
--   つまり「前回値を覚える」という手続き型の発想を、
--   「前の行を参照する」という宣言的なSQLへ変換している。
-- ================================================


INSERT INTO Sales2
-- INSERT INTO Sales2:
--   最終的な計算結果を Sales2 に一括挿入する。
--
--   ここが「ガツン系」らしい重要点である。
--   以前のぐるぐる系プロシージャでは、
--   1件 FETCH するたびに 1件 INSERT していた。
--
--   今回は
--     SELECT で全行分の比較結果を作り、
--     その結果をまとめて INSERT
--   している。
--
--   つまり
--     行ごとの命令実行
--   ではなく
--     集合結果の一括反映
--   になっている。

SELECT
       company,
       -- company:
       --   元データ Sales.company をそのまま返す。
       --
       --   この company は後ろの PARTITION BY にも使われており、
       --   「同じ会社の中でだけ直前値を参照する」
       --   ための境界になる。

       year,
       -- year:
       --   元データ Sales.year をそのまま返す。
       --
       --   この列は OVER 句の ORDER BY year に使われ、
       --   各会社の時系列順を決める。
       --
       --   つまり「直前」とは
       --   company ごとの year 昇順で見た1つ前の行
       --   を意味する。

       sale,
       -- sale:
       --   元データ Sales.sale をそのまま返す。
       --
       --   現在行の売上値であり、
       --   これと「1つ前の売上」を比較して var を決める。

       CASE
       -- CASE:
       --   SIGN 関数で得られた
       --     -1, 0, 1, NULL
       -- のような値を、
       --     '-', '=', '+', NULL
       -- に変換する。
       --
       --   つまり
       --     数値的な差の向き
       --   を
       --     人間が読みやすい記号
       --   へ変換している。

       SIGN(
            sale
            -
            MAX(sale)
            OVER (
                PARTITION BY company
                ORDER BY year
                ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
            )
       )
       -- SIGN( ... ):
       --   引数の符号を返す関数である。
       --
       --   一般に
       --     正の値  ->  1
       --     0      ->  0
       --     負の値  -> -1
       --     NULL   -> NULL
       -- のような結果になる。
       --
       --   今回の引数は
       --     現在売上 - 直前売上
       -- である。
       --
       --   したがって
       --     現在 > 直前 ->  1
       --     現在 = 直前 ->  0
       --     現在 < 直前 -> -1
       --     比較不能   -> NULL
       -- となる。
       --
       --   このように、
       --   「前回との差」をいったん符号値へ圧縮してから
       --   CASE で文字へ変換している。
       --
       --   ここでの大きな利点は、
       --   以前のプロシージャの
       --     IF prev < current THEN '+'
       --     ELSIF prev > current THEN '-'
       --     ELSE '='
       --   という手続き型の三分岐を、
       --   数学的な関数 SIGN で簡潔に置き換えられる点である。
       --
       -- MAX(sale) OVER (...):
       --   ここが今回のSQLの核心である。
       --
       --   一見すると「最大値」を求めているように見えるが、
       --   実際の本質は
       --     フレームの中に含まれる1行だけの sale を取る
       --   ことである。
       --
       --   なぜ MAX なのか:
       --   今回のフレームは
       --     ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
       --   なので、
       --   対象範囲は「ちょうど1つ前の行」だけである。
       --
       --   そのため、そのフレーム内に存在する値は高々1つであり、
       --   MAX を取ってもその値自身が返る。
       --
       --   つまり MAX はここでは
       --     最大値計算のため
       --   というより、
       --     フレームから1つ前の値を取り出す器
       --   として使われている。
       --
       -- OVER (...):
       --   これにより MAX が通常の集約関数ではなく、
       --   ウィンドウ関数として動作する。
       --
       --   その結果、
       --   行を潰さずに
       --   「各行ごとに前の行の値を見る」
       --   ことができる。
       --
       -- PARTITION BY company:
       --   company ごとに独立したパーティションを作る。
       --
       --   つまり A社の直前売上は A社の中だけで探し、
       --   B社の行に移ったときには B社の中で先頭からやり直す。
       --
       --   これが以前のプロシージャで
       --     c_company = rec_sales.company
       --   を見て「同じ会社かどうか」を判定していた処理に対応する。
       --
       --   つまり PARTITION BY company は、
       --   手続き型コードにおける
       --     会社が変わったら比較をリセットする
       --   というロジックを、
       --   宣言的に表現している。
       --
       -- ORDER BY year:
       --   各 company パーティションの中で year 昇順に並べる。
       --
       --   これにより「1つ前の行」が
       --   年次の直前レコードになる。
       --
       --   もし ORDER BY year が無ければ、
       --   「前の行」が何を意味するか決まらないため、
       --   直前売上比較というアルゴリズムは成立しない。
       --
       -- ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING:
       --   これが最も大事なフレーム指定である。
       --
       --   意味は、
       --     現在行から見て1つ前の行だけをフレームに含める
       --   である。
       --
       --   つまりこのフレームは
       --     [前の行]
       --   という1行だけの窓になっている。
       --
       --   これにより、
       --   現在行ごとに
       --     「自分の1つ前の売上」
       --   を取得できる。
       --
       --   以前のぐるぐる系コードでは
       --     i_pre_sale
       --   という変数で保持していた「前回値」を、
       --   今回はこのフレーム指定で表している。
       --
       --   つまり
       --     状態変数 -> ウィンドウフレーム
       --   へと発想を移している。
       --
       --   これが「ぐるぐる系をガツン系へ変える」本質である。

       WHEN 0 THEN '='
       -- 差分が 0 のとき:
       --   現在売上と直前売上が同じなので '=' を返す。

       WHEN 1 THEN '+'
       -- 差分が正のとき:
       --   現在売上の方が大きいので '+' を返す。

       WHEN -1 THEN '-'
       -- 差分が負のとき:
       --   現在売上の方が小さいので '-' を返す。

       ELSE NULL
       -- ELSE NULL:
       --   会社の先頭行など、1つ前の行が存在しない場合は
       --   MAX(...) OVER (...) の結果が NULL になる。
       --
       --   そのため
       --     sale - NULL
       --   も NULL になり、
       --   SIGN(NULL) も NULL になる。
       --
       --   このとき CASE はどの WHEN にも一致しないため、
       --   ELSE NULL が選ばれる。
       --
       --   これは以前のぐるぐる系コードで
       --     会社が変わった最初の行なら c_var := NULL;
       --   としていた処理に対応する。
       END AS var
       -- AS var:
       --   比較結果記号の列名を var として返す。
       --
       --   Sales2.var に入る最終値である。

  FROM Sales;
  -- FROM Sales:
  --   元データ表である。
  --
  --   この SELECT 全体は、
  --   Sales の各行に対して
  --     直前売上比較結果 var
  --   を付与した結果集合を作っている。
  --
  --   そして INSERT INTO Sales2 と組み合わさることで、
  --   その結果集合を一括で書き込む。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Sales の各行を company ごとにパーティション分割する
-- 2. 各パーティション内で year 昇順に並べる
-- 3. 各行について「1つ前の行の sale」を取得する
-- 4. 現在売上 sale から前回売上を引く
-- 5. SIGN 関数で
--      増加 ->  1
--      同値 ->  0
--      減少 -> -1
--      比較不能 -> NULL
--    に変換する
-- 6. CASE 式で
--      1   -> '+'
--      0   -> '='
--      -1  -> '-'
--      NULL -> NULL
--    に変換する
-- 7. company, year, sale, var を Sales2 に一括挿入する
--
-- 概念的には次のような処理に近い。
--
--   for each company partition:
--       sort rows by year
--
--       for each row:
--           prev_sale = sale of previous row in same company
--
--           if prev_sale does not exist:
--               var = NULL
--           else:
--               diff = current_sale - prev_sale
--
--               if diff > 0:
--                   var = '+'
--               elif diff < 0:
--                   var = '-'
--               else:
--                   var = '='
--
--   insert all rows with var into Sales2
--
-- つまりこれは、
--   手続き型の
--     prev_company, prev_sale を更新しながら回す
--   ロジックを、
--   ウィンドウ関数によって集合として表現したSQLである。


-- ================================================
-- ぐるぐる系とガツン系の比較
-- ================================================
--
-- 以前のぐるぐる系プロシージャでは、
--
--   1. カーソルを開く
--   2. 1件 FETCH
--   3. 直前会社と同じか判定
--   4. 直前売上と比較
--   5. 1件 INSERT
--   6. 状態を更新
--   7. これを最後まで繰り返す
--
-- という構造だった。
--
-- これはアルゴリズムとして分かりやすい一方で、
--   「前回値を変数で持つ」
--   「1件ずつ処理する」
-- という手続き型の発想である。
--
-- 一方今回のガツン系SQLでは、
--
--   1. company ごとにパーティションを切る
--   2. year 順に並べる
--   3. 1つ前の行をウィンドウ関数で参照する
--   4. 差分記号を式で計算する
--   5. 全体を一括で INSERT する
--
-- という集合指向の発想になっている。
--
-- つまり
--
--   ぐるぐる系:
--     状態変数を持ち、1件ずつ比較し、1件ずつ書く
--
--   ガツン系:
--     パーティションと並び順を宣言し、全体をまとめて計算する
--
-- という違いがある。
--
-- 今回のリスト5.3は、ミックさんのいう
--   「ぐるぐる系をガツン系へ書き換える」
-- そのものの典型例といえる。


-- ================================================
-- A社での具体例
-- ================================================
--
-- A社の Sales は次の通りである。
--
--   2002 -> 50
--   2003 -> 52
--   2004 -> 55
--   2007 -> 55
--
-- この company='A' パーティションに対して
-- ORDER BY year を適用するとこの順になる。
--
-- 各行で
--   ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
-- により「1つ前の行の sale」を見ると、
--
--   2002 -> 前行なし      -> NULL
--   2003 -> 前行は 50     -> 52 - 50 =  2 -> SIGN =  1 -> '+'
--   2004 -> 前行は 52     -> 55 - 52 =  3 -> SIGN =  1 -> '+'
--   2007 -> 前行は 55     -> 55 - 55 =  0 -> SIGN =  0 -> '='
--
-- となる。
--
-- これは以前のプロシージャ版で得られた結果と同じである。
--
-- つまりウィンドウ関数版は、
--   手続き型の逐次比較を
--   集合的に同値変換したもの
-- と見ることができる。


-- ================================================
-- 図解: ウィンドウ関数による前回値参照
-- ================================================
--
--   Sales
--      |
--      v
--   company ごとに PARTITION
--      |
--      +------------------+------------------+------------------+
--      |        A         |        B         |        C         |
--      +------------------+------------------+------------------+
--      |
--      v
--   各 company 内で ORDER BY year
--      |
--      v
--   各行について「1つ前の行」の sale を参照
--      |
--      v
--   現在 sale - 前回 sale
--      |
--      v
--   SIGN で差の向きを数値化
--      |
--      +------------------------------+
--      |  1  -> 増加                  |
--      |  0  -> 同値                  |
--      | -1  -> 減少                  |
--      | NULL -> 比較不能             |
--      +------------------------------+
--      |
--      v
--   CASE で '+', '=', '-', NULL に変換
--      |
--      v
--   INSERT INTO Sales2
--
-- つまり、
--   前回値を保持する変数を使わずに、
--   「前の行」を窓関数で直接参照している。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- INSERT INTO Sales2
--   → 計算結果を Sales2 に一括挿入する
--
-- SELECT company, year, sale, ...
--   → Sales の各行に比較結果 var を付けて返す
--
-- PARTITION BY company
--   → 会社ごとに独立した比較範囲を作る
--
-- ORDER BY year
--   → 各会社内で年順に直前行を定める
--
-- ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
--   → 1つ前の行だけを対象フレームにする
--
-- MAX(sale) OVER (...)
--   → そのフレーム内の sale を取り出す
--
-- sale - 前回sale
--   → 増減差を求める
--
-- SIGN(...)
--   → 差の向きを -1 / 0 / 1 / NULL に圧縮する
--
-- CASE ... END AS var
--   → 符号値を '+', '-', '=', NULL に変換する
--
-- FROM Sales
--   → 元データ表は Sales
--
-- ;
--   → SQL文の終了を表す


