-- ================================================
-- ファイル名: 05_select_sales_previous_row_with_window.sql
-- 役割:
--   Sales テーブルの各行について、
--   同じ company の中で「1行前の会社名」と「1行前の売上」を取得する。
--
-- このSQLの目的:
--   各売上レコードに対して、
--   直前レコードの情報
--     pre_company
--     pre_sale
--   を付与すること。
--
-- 学習上の主題:
--   - ウィンドウ関数による前行参照
--   - PARTITION BY / ORDER BY / フレーム指定の意味
--   - 「相関サブクエリで逐次的に前行を探す発想」と
--     「ウィンドウ関数で前行を宣言的に参照する発想」の違い
--
-- 今回の最重要ポイント:
--   このSQLは見た目としてはウィンドウ関数の例だが、
--   本質的には
--     「各行ごとに、その行に対応する前行を求める」
--   という処理である。
--
--   これは相関サブクエリでも書ける題材であり、
--   たとえば
--     「今見ている行の company と同じ会社で、
--       今の year より前の最大 year の行を探す」
--   というように、
--   外側の行に依存して内側の検索条件が変わる
--   相関サブクエリの典型題材になりやすい。
--
--   しかし今回のSQLは、
--   そうした「各行ごとに探しに行く」書き方をせず、
--   company ごとに並べたうえで
--   「1行前だけを見る」と宣言している。
--
--   つまり
--     相関サブクエリ的な問題
--   を
--     ウィンドウ関数でより直接的に書いた解
--   と理解すると非常に分かりやすい。
-- ================================================


SELECT
       company,
       -- company:
       --   現在行の会社コードをそのまま返す。
       --
       --   この列は「今見ているレコードがどの会社のものか」を表す。
       --
       --   後ろの pre_company は
       --   「その会社パーティションの中で1つ前にあった company」
       --   を返すので、
       --   今回のデータでは基本的に
       --     company = pre_company
       -- になる。
       --
       --   ただし先頭行では前行が無いので pre_company は NULL になる。

       year,
       -- year:
       --   現在行の年である。
       --
       --   各 company の中で ORDER BY year をかけることで、
       --   「前の行」が時間順の直前レコードになる。
       --
       --   ここが相関サブクエリとの比較で重要である。
       --   相関サブクエリなら、
       --     今の year より小さい year の中で最大のもの
       --   を探すような条件を書くことが多い。
       --
       --   一方ウィンドウ関数では、
       --   ORDER BY year によってあらかじめ順序を定めておき、
       --   その順序上の「1行前」を直接参照している。

       sale,
       -- sale:
       --   現在行の売上値である。
       --
       --   この current sale と pre_sale を比較することで、
       --   以前のリスト5.3のように
       --     '+'
       --     '-'
       --     '='
       -- を求めることができる。
       --
       --   つまり今回のSQLは、
       --   差分記号を作る前段階として
       --   「前回値そのものを明示的に取得している」
       --   形になっている。

       MAX(company)
         OVER (
             PARTITION BY company
             ORDER BY year
             ROWS BETWEEN 1 PRECEDING
                      AND 1 PRECEDING
         ) AS pre_company,
       -- pre_company:
       --   同じ company パーティションの中で、
       --   year 順に見たときの「1行前の company」を返す。
       --
       --   一見すると MAX(company) なので、
       --   何か最大会社名を取っているように見える。
       --   しかし本質はそうではない。
       --
       --   今回のフレーム指定は
       --     ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
       --   であり、
       --   フレーム内の対象行は「1行前の行だけ」である。
       --
       --   そのため、そのフレームに company 値が存在するとしても
       --   高々1つしかない。
       --
       --   したがって MAX(company) は、
       --   実質的には
       --     「1行前の company 値をそのまま返している」
       --   のと同じである。
       --
       --   つまり MAX はここでは
       --     最大値探索のため
       --   ではなく、
       --     フレーム内の唯一の値を取り出すための器
       --   として使われている。
       --
       -- 相関サブクエリとの関係:
       --   相関サブクエリでこれに近いことをやるなら、
       --   たとえば
       --     今の行 s1 に対して
       --     Sales s2 の中から
       --       s2.company = s1.company
       --       s2.year < s1.year
       --     を満たす行を探し、
       --     その中で最大 year を持つ行の company を返す
       --   という発想になる。
       --
       --   つまり
       --     「各行ごとに前行候補を探す」
       --   のが相関サブクエリであり、
       --     「パーティションを作って1行前を見る」
       --   のが今回のウィンドウ関数である。
       --
       -- PARTITION BY company:
       --   company ごとに独立した計算空間を作る。
       --
       --   これにより、A社の前行は A社の中だけで探され、
       --   B社やC社の行は混ざらない。
       --
       --   以前のぐるぐる系コードで
       --     c_company = rec_sales.company
       --   を見て「同じ会社の続きかどうか」を判定していたのに対応する。
       --
       -- ORDER BY year:
       --   各会社の中で年順に並べる。
       --
       --   これが無いと
       --     「1つ前の行」
       --   が何を意味するか定まらない。
       --
       --   したがって、前行参照アルゴリズムの本質は
       --   ORDER BY year による順序付けにある。
       --
       -- ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING:
       --   現在行から見て、直前1行だけをフレームに含める。
       --
       --   つまり
       --     [現在行の直前1行]
       --   という長さ1の窓を作っている。
       --
       --   これにより、
       --   各行ごとに「前の1件」の情報を参照できる。
       --
       --   company パーティションの先頭行には前行が存在しないため、
       --   その場合の pre_company は NULL になる。

       MAX(sale)
         OVER (
             PARTITION BY company
             ORDER BY year
             ROWS BETWEEN 1 PRECEDING
                      AND 1 PRECEDING
         ) AS pre_sale
       -- pre_sale:
       --   同じ company パーティションの中で、
       --   year 順に見たときの「1行前の sale」を返す。
       --
       --   これも pre_company と同様、
       --   MAX(sale) は実質的に
       --     直前1行の sale 値
       --   をそのまま取り出している。
       --
       --   今回のクエリで一番重要なのはこの列である。
       --   なぜなら、
       --   リスト5.3の差分記号計算
       --     sale - pre_sale
       --   を理解するための、前回値そのものだからである。
       --
       -- 相関サブクエリとの関係:
       --   相関サブクエリで書くなら、
       --   各行 s1 ごとに
       --     s2.company = s1.company
       --     s2.year < s1.year
       --   を満たす行のうち
       --     最大の s2.year
       --   を持つ行の sale を返す、
       --   という構造になりやすい。
       --
       --   つまり
       --     外側の行が変わるたびに、内側の探索条件も変わる
       --   ので「相関」になる。
       --
       --   一方このウィンドウ関数版では、
       --   すべての行に対して
       --     company ごとの順序
       --     直前1行のフレーム
       --   をあらかじめ定義し、
       --   その定義の上で pre_sale を一括で計算している。
       --
       --   したがって発想としては
       --     相関サブクエリで1行ずつ前行を探す
       --   よりも
       --     パーティションと順序の上で前行を直接参照する
       --   方が自然で、SQLの意図も読みやすい。
       --
       --   company の先頭行では前行が無いので、
       --   pre_sale は NULL になる。
       --
       --   この NULL は、
       --   「その会社の最初の年なので比較対象が存在しない」
       --   ことを意味する。

  FROM Sales;
  -- FROM Sales:
  --   元データ表である。
  --
  --   この SELECT は Sales の各行を残したまま、
  --   その行に対応する
  --     pre_company
  --     pre_sale
  --   を追加計算して返している。
  --
  --   ここが GROUP BY と決定的に違う点である。
  --
  --   GROUP BY なら company ごとに1行へ潰れてしまうが、
  --   今回は
  --     各元行を残しつつ、
  --     その行の前行情報を付ける
  --   ことが目的なので、
  --   ウィンドウ関数が適している。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Sales を company ごとにパーティション分割する
-- 2. 各 company パーティション内で year 昇順に並べる
-- 3. 各行に対して、1行前だけを含むウィンドウフレームを作る
-- 4. そのフレーム内の company を pre_company として返す
-- 5. そのフレーム内の sale を pre_sale として返す
-- 6. 元の行を残したまま、前行情報付きの結果を返す
--
-- 概念的には次のような処理に近い。
--
--   for each company partition:
--       sort rows by year
--
--       for each row:
--           pre_company = company of previous row
--           pre_sale = sale of previous row
--
--   output all rows
--
-- つまりこれは、
--   「各行ごとに、その会社内での前行情報を付ける」
--   前行参照アルゴリズムである。
--
--   そしてこの問題設定自体は、
--   相関サブクエリでもよく現れる題材である。


-- ================================================
-- 相関サブクエリの観点で見るとどうなるか
-- ================================================
--
-- 相関サブクエリとは、
--   内側のサブクエリが外側の行の値を参照する
--   サブクエリである。
--
-- 今回の題材で相関サブクエリ的に考えると、
-- 各行 s1 に対して、
--
--   「同じ company の中で、
--     s1.year より前の行を探し、
--     その中で最も year が大きい行の sale を返す」
--
-- という発想になる。
--
-- 擬似的には次のようなイメージである。
--
--   for each row s1 in Sales:
--       search row s2 in Sales
--         where s2.company = s1.company
--           and s2.year < s1.year
--         with maximum s2.year
--
--       pre_sale = s2.sale
--
-- これは
--   外側の行 s1 が変わるたびに
--   内側で探す条件も変わる
-- ので、典型的な相関サブクエリの考え方である。
--
-- 一方、今回のウィンドウ関数版では、
--   company ごとの順序を一度定義して、
--   その順序上の「1つ前」を見る
-- だけでよい。
--
-- つまり
--
--   相関サブクエリ:
--     各行ごとに「前行候補を探索」する発想
--
--   ウィンドウ関数:
--     パーティションと順序の中で「前行位置を参照」する発想
--
-- という違いがある。
--
-- この違いを意識すると、
--   なぜ前行取得にウィンドウ関数が相性良いのか
-- がよく分かる。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- A社:
--   2002 -> 50
--   2003 -> 52
--   2004 -> 55
--   2007 -> 55
--
-- これに対して結果は概ね
--
--   company | year | sale | pre_company | pre_sale
--   --------+------+------|-------------+---------
--   A       | 2002 | 50   | NULL        | NULL
--   A       | 2003 | 52   | A           | 50
--   A       | 2004 | 55   | A           | 52
--   A       | 2007 | 55   | A           | 55
--
-- となる。
--
-- B社:
--   2001 -> 27
--   2005 -> 28
--   2006 -> 28
--   2009 -> 30
--
--   pre_sale は
--     NULL, 27, 28, 28
--
-- C社:
--   2001 -> 40
--   2005 -> 39
--   2006 -> 38
--   2010 -> 35
--
--   pre_sale は
--     NULL, 40, 39, 38
--
-- つまり各会社の先頭だけが NULL で、
-- それ以外は「前回値」が付く。


-- ================================================
-- 図解: 前行取得アルゴリズム
-- ================================================
--
--   Sales
--      |
--      v
--   company ごとに PARTITION
--      |
--      v
--   各 company 内で year 順に並べる
--      |
--      v
--   各行について
--     [1つ前の行だけ]
--   をフレームにする
--      |
--      +----------------------------------+
--      | 前行の company -> pre_company    |
--      | 前行の sale    -> pre_sale       |
--      +----------------------------------+
--      |
--      v
--   各元行を残したまま結果を返す
--
-- つまり、
--   行を潰さずに
--   「前の行の情報」を列として追加している。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   「前行を取りたい」
-- という、一見すると逐次処理や相関サブクエリ向きに見える問題を、
--   ウィンドウ関数で宣言的に解いている
-- ところにある。
--
-- しかも今回は
--   pre_company
--   pre_sale
-- をそのまま出しているので、
--   リスト5.3で行っていた差分記号計算の前段階
-- として非常に理解しやすい。
--
-- つまりこれは、
--   まず前行情報を明示的に見える形で出し、
--   そのあとで差分計算へ進む
-- ための橋渡し的なクエリでもある。
--
-- 相関サブクエリの観点から見ると、
--   外側行ごとに前行を探索する問題
-- を
--   パーティション + 順序 + フレーム
-- で一括表現している点が重要である。
--
-- ここがこのSQLの学習価値である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT company, year, sale, ...
--   → 元の売上情報に前行情報を追加して返す
--
-- MAX(company) OVER (...)
--   → 1行前の company を取得する
--
-- MAX(sale) OVER (...)
--   → 1行前の sale を取得する
--
-- PARTITION BY company
--   → 会社ごとに比較範囲を分ける
--
-- ORDER BY year
--   → 会社内で年順に前後関係を決める
--
-- ROWS BETWEEN 1 PRECEDING AND 1 PRECEDING
--   → ちょうど1行前だけを見る
--
-- FROM Sales
--   → 元データ表は Sales
--
-- ;
--   → SQL文の終了を表す


