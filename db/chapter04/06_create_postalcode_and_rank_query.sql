-- 原因
-- MySQL 8系では RANK はウィンドウ関数名として解釈されるため、
-- 別名にそのまま rank を使うと構文エラーになることがある。
--
-- したがって、次のどちらかで直す。
--
-- 1. 別名を変える（推奨）
--    rank -> match_rank
--
-- 2. バッククォートで囲う
--    AS `rank`
--
-- 今回は分かりやすさのため、別名を match_rank に変更した修正版を示す。


-- ================================================
-- ファイル名: 06_create_postalcode_and_rank_query.sql
-- 役割:
--   郵便番号テーブル PostalCode を作成し、
--   与えられた郵便番号に対して
--   「どれだけ先頭一致しているか」を LIKE で段階評価する。
--
-- このSQLの目的:
--   郵便番号検索において、
--   完全一致を最優先としつつ、
--   その次に
--     先頭6桁一致
--     先頭5桁一致
--     先頭4桁一致
--     先頭3桁一致
--     先頭2桁一致
--     先頭1桁一致
--   のような「近さ」を順位として表現すること。
--
-- 学習上の主題:
--   - LIKE による前方一致検索
--   - '%' ワイルドカードの意味
--   - CASE 式による段階的な優先順位付け
--   - 文字列の「どこまで一致しているか」をスコア化する発想
--
-- 今回の最重要ポイント:
--   このSQLの中心は LIKE である。
--
--   LIKE は
--     文字列があるパターンに合うか
--   を調べる演算子であり、
--   今回は特に
--     '413003%'
--   のような前方一致パターンを使っている。
--
--   つまり
--     郵便番号が先頭から何桁一致するか
--   を、
--     CASE + LIKE
--   で段階的に判定している。
--
--   これは発想としては
--     「郵便番号の類似度ランキング」
--   を作っているのと同じである。
-- ================================================


-- ================================================
-- 図5.5: 郵便番号テーブルの定義
-- ================================================

CREATE TABLE PostalCode
(
    pcode CHAR(7),
    -- pcode:
    --   郵便番号本体である。
    --
    --   今回は 7 桁の郵便番号を CHAR(7) で保持している。
    --
    --   CHAR を使っている理由:
    --   郵便番号は数値として計算するものではなく、
    --   文字列として先頭一致を調べたいからである。
    --
    --   たとえば
    --     0410001
    --   のように先頭に 0 が来る可能性も、
    --   文字列として持つ方が自然である。
    --
    --   今回の LIKE はこの pcode に対して適用される。

    district_name VARCHAR(256),
    -- district_name:
    --   その郵便番号に対応する地名である。
    --
    --   今回のランキングSQLでは、
    --   一致度と一緒にどの地名が候補になったかを表示するために返している。

    CONSTRAINT pk_pcode PRIMARY KEY(pcode)
    -- PRIMARY KEY(pcode):
    --   郵便番号を一意にする主キーである。
);

INSERT INTO PostalCode VALUES ('4130001', '静岡県熱海市泉');
INSERT INTO PostalCode VALUES ('4130002', '静岡県熱海市伊豆山');
INSERT INTO PostalCode VALUES ('4130103', '静岡県熱海市網代');
INSERT INTO PostalCode VALUES ('4130041', '静岡県熱海市青葉町');
INSERT INTO PostalCode VALUES ('4103213', '静岡県伊豆市青羽根');
INSERT INTO PostalCode VALUES ('4380824', '静岡県磐田市赤池');


-- ================================================
-- リスト5.6: 郵便番号のランキングを求めるクエリ
-- ================================================

SELECT
       pcode,
       -- pcode:
       --   元の郵便番号をそのまま返す。

       district_name,
       -- district_name:
       --   郵便番号に対応する地名を返す。

       CASE
            WHEN pcode = '4130033' THEN 0
            -- 完全一致:
            --   pcode が検索基準値 '4130033' と完全に一致する場合である。
            --   最も近いので 0 を返す。

            WHEN pcode LIKE '413003%' THEN 1
            -- LIKE '413003%':
            --   先頭6文字が 413003 と一致する場合である。
            --
            --   '%' は「後ろに何文字続いてもよい」ことを表すワイルドカードである。
            --   したがってこれは前方一致検索である。

            WHEN pcode LIKE '41300%' THEN 2
            -- LIKE '41300%':
            --   先頭5文字が 41300 と一致する場合。

            WHEN pcode LIKE '4130%' THEN 3
            -- LIKE '4130%':
            --   先頭4文字が 4130 と一致する場合。

            WHEN pcode LIKE '413%' THEN 4
            -- LIKE '413%':
            --   先頭3文字が 413 と一致する場合。

            WHEN pcode LIKE '41%' THEN 5
            -- LIKE '41%':
            --   先頭2文字が 41 と一致する場合。

            WHEN pcode LIKE '4%' THEN 6
            -- LIKE '4%':
            --   先頭1文字が 4 と一致する場合。

            ELSE NULL
            -- ELSE NULL:
            --   先頭1文字も一致しない場合である。
       END AS match_rank
       -- AS match_rank:
       --   MySQL では rank を別名にすると構文エラーになることがあるため、
       --   ここでは match_rank に変更している。
       --
       --   値の意味:
       --     0 : 完全一致
       --     1 : 先頭6桁一致
       --     2 : 先頭5桁一致
       --     3 : 先頭4桁一致
       --     4 : 先頭3桁一致
       --     5 : 先頭2桁一致
       --     6 : 先頭1桁一致
       --     NULL : 一致なし
       --
       --   つまり値が小さいほど近い候補である。

  FROM PostalCode;
  -- FROM PostalCode:
  --   郵便番号候補表を対象としている。
  --
  --   このクエリは各行を調べ、
  --   その pcode が検索対象郵便番号に対して
  --   どこまで LIKE で前方一致するかを評価している。


-- ================================================
-- LIKE の本質
-- ================================================
--
-- LIKE は文字列が特定のパターンに合うかを判定する演算子である。
--
-- 今回使っている '%' は、
--   任意長の任意文字列
-- を表すワイルドカードである。
--
-- したがって
--
--   pcode LIKE '413003%'
--
-- は、
--   「pcode が 413003 で始まるか」
-- を意味する。
--
-- つまりこのクエリでは、
--   完全一致以外は全部
--   「先頭何文字まで一致しているか」
-- を LIKE の前方一致で判定している。
--
-- これは見方を変えると、
--   郵便番号同士の共通接頭辞の長さ
-- をランク化しているのと同じである。


-- ================================================
-- CASE の評価順序が重要である理由
-- ================================================
--
-- CASE は上から順に評価され、
-- 最初に真になった条件で止まる。
--
-- たとえば
--   pcode = '4130033'
-- の場合、
--   LIKE '413003%'
--   LIKE '41300%'
--   LIKE '4130%'
--   LIKE '413%'
--   LIKE '41%'
--   LIKE '4%'
-- のすべてにも一致しうる。
--
-- しかし最初に真になる
--   pcode = '4130033'
-- で止まるので、match_rank は 0 になる。
--
-- つまりこのSQLでは、
--   強い一致条件から順に並べること
-- 自体がアルゴリズムである。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. PostalCode の各行を1件ずつ見る
-- 2. pcode が '4130033' に完全一致するか調べる
-- 3. 一致しなければ、LIKE '413003%' を調べる
-- 4. さらに一致しなければ、LIKE '41300%' を調べる
-- 5. 以降、LIKE '4130%', '413%', '41%', '4%' を順に調べる
-- 6. 最初に一致した条件に応じて match_rank を決める
-- 7. pcode, district_name, match_rank を返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in PostalCode:
--       if pcode == '4130033':
--           match_rank = 0
--       elif pcode starts with '413003':
--           match_rank = 1
--       elif pcode starts with '41300':
--           match_rank = 2
--       elif pcode starts with '4130':
--           match_rank = 3
--       elif pcode starts with '413':
--           match_rank = 4
--       elif pcode starts with '41':
--           match_rank = 5
--       elif pcode starts with '4':
--           match_rank = 6
--       else:
--           match_rank = NULL
--
-- つまりこれは、
--   検索対象郵便番号との共通接頭辞の長さを
--   上から順に調べてスコア化する
--   前方一致ランキングアルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- 比較対象:
--   4130033
--
-- 1. 4130001
--   完全一致?           -> いいえ
--   LIKE '413003%'?     -> いいえ
--   LIKE '41300%'?      -> はい
--   → match_rank = 2
--
-- 2. 4130002
--   → match_rank = 2
--
-- 3. 4130103
--   完全一致?           -> いいえ
--   LIKE '413003%'?     -> いいえ
--   LIKE '41300%'?      -> いいえ
--   LIKE '4130%'?       -> はい
--   → match_rank = 3
--
-- 4. 4130041
--   → match_rank = 2
--
-- 5. 4103213
--   LIKE '41%'?         -> はい
--   → match_rank = 5
--
-- 6. 4380824
--   LIKE '4%'?          -> はい
--   → match_rank = 6
--
-- したがって、
--   4130033 に近いほど小さい match_rank が付く。


-- ================================================
-- 図解: LIKE による前方一致ランキング
-- ================================================
--
--   PostalCode の各 pcode
--      |
--      v
--   '4130033' と比較
--      |
--      +---------------------------------------------+
--      | 完全一致?                -> 0              |
--      | 先頭6桁一致?             -> 1              |
--      | 先頭5桁一致?             -> 2              |
--      | 先頭4桁一致?             -> 3              |
--      | 先頭3桁一致?             -> 4              |
--      | 先頭2桁一致?             -> 5              |
--      | 先頭1桁一致?             -> 6              |
--      | どれも一致しない         -> NULL           |
--      +---------------------------------------------+
--      |
--      v
--   pcode, district_name, match_rank を返す


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   郵便番号の完全一致検索
-- ではなく、
--   「どれだけ近いか」を前方一致で段階評価している
-- 点にある。
--
-- そのため LIKE は単なる検索演算子ではなく、
--   類似度の粗いランキングを作るための道具
-- として使われている。
--
-- そして CASE は、
--   その一致度を数値スコアに落とし込む
-- ための制御構造になっている。
--
-- つまり
--   LIKE が一致判定
--   CASE が順位付け
-- を担当している。


