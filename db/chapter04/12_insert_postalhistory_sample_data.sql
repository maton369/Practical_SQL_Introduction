-- ================================================
-- ファイル名: 12_insert_postalhistory_sample_data.sql
-- 役割:
--   PostalHistory テーブルへ郵便番号変更履歴のサンプルデータを投入する。
--
-- このSQLの目的:
--   再帰共通表式で
--     最新地点から過去へさかのぼる
--   処理を試せるようにすること。
--
-- 学習上の主題:
--   - 履歴の鎖を複数人物分持つこと
--   - 変更回数が人物によって異なること
--   - NULL が「最新地点」を意味すること
--
-- 今回の最重要ポイント:
--   各人物について、
--   郵便番号履歴が「1本の連鎖」になっている。
--
--   たとえば A は
--     4130001 -> 4130002 -> 4130103 -> NULL
--   であり、
--   一番古いのは 4130001、
--   一番新しいのは 4130103 である。
--
--   後続の再帰共通表式は、
--   この連鎖を最新から古い方へ逆向きにたどる。
-- ================================================

INSERT INTO PostalHistory VALUES ('A', '4130001', '4130002');
INSERT INTO PostalHistory VALUES ('A', '4130002', '4130103');
INSERT INTO PostalHistory VALUES ('A', '4130103', NULL     );

INSERT INTO PostalHistory VALUES ('B', '4130041', NULL     );

INSERT INTO PostalHistory VALUES ('C', '4103213', '4380824');
INSERT INTO PostalHistory VALUES ('C', '4380824', NULL     );

-- ================================================
-- データの見方
-- ================================================
--
-- A の履歴:
--   4130001 -> 4130002
--   4130002 -> 4130103
--   4130103 -> NULL
--
--   つまり
--     一番古い郵便番号 = 4130001
--     現在の郵便番号   = 4130103
--
-- B の履歴:
--   4130041 -> NULL
--
--   変更履歴が無いので、
--   最初であり最新でもある。
--
-- C の履歴:
--   4103213 -> 4380824
--   4380824 -> NULL
--
--   つまり
--     一番古い郵便番号 = 4103213
--     現在の郵便番号   = 4380824
--
-- 後続の再帰共通表式では、
--   たとえば A を指定したとき
--   4130103 から開始して
--   4130002
--   4130001
-- の順に過去へたどる。