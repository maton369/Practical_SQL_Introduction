-- ================================================
-- ファイル名: 15_insert_postalhistory2_sample_data.sql
-- 役割:
--   PostalHistory2 テーブルへ、入れ子集合モデルで表現した
--   郵便番号履歴サンプルを投入する。
--
-- このSQLの目的:
--   各人物の履歴を
--   「外側の円が古い履歴、内側の円が新しい履歴」
--   とみなせる区間構造で表現し、
--   後続のクエリで一番外側の円を取り出せるようにすること。
--
-- 学習上の主題:
--   - 入れ子集合モデルにおける実データの見方
--   - lft/rgt の大小関係が階層関係を決めること
--   - 外側ノードと内側ノードの違い
--
-- 今回の最重要ポイント:
--   このデータは、
--   郵便番号履歴を単なる線形リストとしてではなく、
--   包含関係を持つ区間として表している。
--
--   つまり A の履歴は
--     4130001 : [0, 27]
--     4130002 : [9, 18]
--     4130103 : [12, 15]
--   のように、
--   新しい履歴ほど内側へ潜る形で表現されている。
--
--   このため一番古い履歴は
--     一番外側の区間
--   に対応する。
-- ================================================

INSERT INTO PostalHistory2 VALUES ('A', '4130001', 0,   27);
INSERT INTO PostalHistory2 VALUES ('A', '4130002', 9,   18);
INSERT INTO PostalHistory2 VALUES ('A', '4130103', 12,  15);

INSERT INTO PostalHistory2 VALUES ('B', '4130041', 0,   27);

INSERT INTO PostalHistory2 VALUES ('C', '4103213', 0,   27);
INSERT INTO PostalHistory2 VALUES ('C', '4380824', 9,   18);

-- ================================================
-- データの見方
-- ================================================
--
-- A の入れ子構造:
--
--   4130001 : [0, 27]
--     └─ 4130002 : [9, 18]
--          └─ 4130103 : [12, 15]
--
-- これは図で描くと次のようなイメージである。
--
--   A:
--   [0.......................................27]  4130001
--        [9.....................18]               4130002
--           [12.........15]                       4130103
--
-- つまり
--   一番外側 = 4130001
--   その内側 = 4130002
--   一番内側 = 4130103
--
-- となる。
--
-- ここで
--   外側ほど古い履歴
--   内側ほど新しい履歴
-- と解釈している。
--
-- B の入れ子構造:
--
--   [0.......................................27]  4130041
--
-- 変更履歴が無いので、
--   外側であり内側でもある1個だけのノードである。
--
-- C の入れ子構造:
--
--   [0.......................................27]  4103213
--        [9.....................18]               4380824
--
-- つまり
--   一番外側 = 4103213
--   その内側 = 4380824
--
-- であり、
--   C の最も古い履歴は 4103213 である。
--
-- 入れ子集合モデルの見方として大事なのは、
--   親子関係をポインタで持つのではなく、
--   区間の包含関係で表現している
-- 点である。
-- ================================================