-- ================================================
-- ファイル名: 07_select_nearest_postalcode_with_subquery.sql
-- 役割:
--   基準郵便番号 4130033 に対して、
--   最も近い郵便番号候補だけを PostalCode から取り出す。
--
-- このSQLの目的:
--   前のリスト5.6で作っていた「一致度 rank」の考え方を使い、
--   単に全候補へスコアを付けるだけではなく、
--   その中で最小のスコア、つまり最も近い候補だけを抽出すること。
--
-- 学習上の主題:
--   - CASE + LIKE による一致度の数値化
--   - 集約関数 MIN による最良スコアの取得
--   - サブクエリによる「全体最小値」との比較
--   - 同じ式が外側と内側に重複する理由
--
-- 今回の最重要ポイント:
--   このSQLは2段階で動く。
--
--   1. まず内側のサブクエリで、
--      全郵便番号候補の中の「最小 rank」を1つ求める
--
--   2. そのあと外側のクエリで、
--      各行の rank がその最小 rank と等しい行だけ残す
--
--   つまりアルゴリズムとしては
--     「各候補の一致度を計算する」
--     「その最良値を求める」
--     「最良値と同じ候補だけ残す」
--   という構造になっている。
--
--   これはランキング問題に対する典型的な
--     スコア計算 -> 最小値抽出 -> 最良候補抽出
--   の形である。
-- ================================================

SELECT
       pcode,
       -- pcode:
       --   最寄り候補として返したい郵便番号である。

       district_name
       -- district_name:
       --   その郵便番号に対応する地名である。
       --
       --   pcode と一緒に返すことで、
       --   「どの候補が最寄り判定されたか」を確認できる。

  FROM PostalCode
  -- FROM PostalCode:
  --   郵便番号候補表を対象とする。

 WHERE
       CASE
            WHEN pcode = '4130033' THEN 0
            -- 完全一致:
            --   最優先なので score は 0。

            WHEN pcode LIKE '413003%' THEN 1
            -- 先頭6桁一致:
            --   413003 で始まる候補。

            WHEN pcode LIKE '41300%'  THEN 2
            -- 先頭5桁一致:
            --   41300 で始まる候補。

            WHEN pcode LIKE '4130%'   THEN 3
            -- 先頭4桁一致。

            WHEN pcode LIKE '413%'    THEN 4
            -- 先頭3桁一致。

            WHEN pcode LIKE '41%'     THEN 5
            -- 先頭2桁一致。

            WHEN pcode LIKE '4%'      THEN 6
            -- 先頭1桁一致。

            ELSE NULL
            -- 先頭1桁も一致しない場合。
       END
       =
       (
           SELECT
                  MIN(
                      CASE
                           WHEN pcode = '4130033' THEN 0
                           WHEN pcode LIKE '413003%' THEN 1
                           WHEN pcode LIKE '41300%'  THEN 2
                           WHEN pcode LIKE '4130%'   THEN 3
                           WHEN pcode LIKE '413%'    THEN 4
                           WHEN pcode LIKE '41%'     THEN 5
                           WHEN pcode LIKE '4%'      THEN 6
                           ELSE NULL
                      END
                  )
             FROM PostalCode
       );
       -- サブクエリ:
       --   PostalCode 全体を見て、
       --   各行の一致度 score の最小値を 1つ求める。
       --
       --   MIN(...) を使う理由:
       --   今回は score が小さいほど「近い」からである。
       --
       --   したがって
       --     最小 score = 最良候補の score
       --   を意味する。
       --
       -- 外側との関係:
       --   外側では各行について同じ CASE 式で score を計算し、
       --   それがサブクエリの最小値と等しいかを判定している。
       --
       --   つまり
       --     自分の score = 全体最良 score
       --   の行だけが残る。
       --
       --   これにより、
       --   もし最良候補が複数行あるなら、それらを全部返せる。
       --
       --   つまりこのSQLは「最小値の1行だけ」ではなく、
       --   「最良タイを含む全件」を返す設計になっている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 内側のサブクエリで、PostalCode 全行に対して一致度 score を計算する
-- 2. その中から MIN(score) を求める
-- 3. 外側のクエリで、各行に対して再び同じ score を計算する
-- 4. その score が内側で求めた最小値と等しい行だけ残す
-- 5. pcode と district_name を返す
--
-- 概念的には次のような処理に近い。
--
--   best_score = min(score(row) for row in PostalCode)
--
--   for each row in PostalCode:
--       if score(row) == best_score:
--           output row
--
-- つまりこれは、
--   「各候補を採点し、最良点だけを持つ候補を返す」
--   スコア最小化アルゴリズムである。


-- ================================================
-- なぜ CASE 式が2回出てくるのか
-- ================================================
--
-- このSQLでは同じ CASE 式が
--   外側の WHERE
--   内側の MIN(...)
-- の両方に現れる。
--
-- 理由は単純で、
--   外側は「各行の score」
--   内側は「全体の最小 score」
-- を計算しているからである。
--
-- つまり
--   score(row) = min(score(all rows))
-- を比較したいので、
--   同じ採点関数を2か所で使っている。
--
-- この重複はやや冗長だが、ロジックとしては自然である。
--
-- 実務では可読性のために
--   CTE や派生表で score を先に作る
-- 書き方もよく使われる。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- 基準値:
--   4130033
--
-- 候補の score は概ね
--
--   4103213 -> 5
--   4130001 -> 2
--   4130002 -> 2
--   4130041 -> 2
--   4130103 -> 3
--   4380824 -> 6
--
-- である。
--
-- したがって
--   MIN(score) = 2
--
-- 外側では
--   score = 2
-- の行だけが残るので、
--
--   4130001 静岡県熱海市泉
--   4130002 静岡県熱海市伊豆山
--   4130041 静岡県熱海市青葉町
--
-- が返る。
--
-- つまり「最寄り」が複数ある場合は、それらを全部返す。


-- ================================================
-- 図解: サブクエリ版のアルゴリズム
-- ================================================
--
--   PostalCode 全体
--      |
--      +-------------------------------+
--      | 内側サブクエリ                |
--      | 各行の score を計算           |
--      | MIN(score) を求める           |
--      +-------------------------------+
--      |
--      v
--   best_score
--      |
--      v
--   外側クエリで各行の score を計算
--      |
--      v
--   score = best_score ?
--      |
--      +---------------------+
--      | yes -> 行を返す     |
--      | no  -> 行を捨てる   |
--      +---------------------+
--
-- つまり
--   「最良 score を先に求めてから、
--     それと同じ候補を取り出す」
-- 構造である。