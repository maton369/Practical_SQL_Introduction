-- ================================================
-- ファイル名: 16_select_outermost_postalcode_with_nested_sets.sql
-- 役割:
--   入れ子集合モデルで表現された PostalHistory2 から、
--   指定人物の「一番外側の円」に対応する郵便番号を求める。
--
-- このSQLの目的:
--   入れ子集合モデルにおいて、
--   一番外側のノード = 最も古い履歴
--   とみなし、
--   それを NOT EXISTS で取り出すこと。
--
-- 学習上の主題:
--   - 入れ子集合モデルでの「外側/内側」の意味
--   - lft の大小比較による最外周判定
--   - NOT EXISTS による「それより外側が存在しない」条件
--   - 再帰を使わずに最上位ノードを取る発想
--
-- 今回の最重要ポイント:
--   入れ子集合モデルでは、
--   一番外側のノードは
--     最小の lft
--   を持つ。
--
--   したがって
--     「自分より小さい lft を持つ同人物のノードが存在しない」
--   という条件で、
--   最外周ノードを特定できる。
--
--   今回の SQL はその考え方を
--     NOT EXISTS
--   で書いている。
--
--   つまりアルゴリズムとしては
--     1. A の候補行 PH1 を1つ取る
--     2. A の中に PH1 よりさらに外側(より小さい lft)の行 PH2 があるか探す
--     3. 無ければ PH1 は最外周である
--   という構造である。
-- ================================================

SELECT
       name,
       -- name:
       --   対象人物をそのまま返す。
       --
       --   今回は name = 'A' に固定しているため、
       --   返る行の name は A になる。

       pcode
       -- pcode:
       --   最外周ノードに対応する郵便番号を返す。
       --
       --   入れ子集合モデルでは、
       --   最外周ノードを「最も古い履歴」と解釈しているため、
       --   これが A の最古の郵便番号になる。

  FROM PostalHistory2 PH1
  -- PH1:
  --   外側クエリ側の候補ノードである。
  --
  --   まず A の各行を候補として1つずつ見る。

 WHERE name = 'A'
   -- name = 'A':
   --   対象人物を A に限定している。
   --
   --   入れ子集合モデルの比較は同じ人物内でだけ意味を持つので、
   --   まず対象人物を固定している。

   AND NOT EXISTS
       (
           SELECT *
             FROM PostalHistory2 PH2
            WHERE PH2.name = 'A'
              AND PH1.lft > PH2.lft
       );
       -- NOT EXISTS:
       --   「そのような行が存在しない」ことを条件にする。
       --
       --   今回は
       --     PH1 より小さい lft を持つ A の行 PH2
       --   が存在しないことを要求している。
       --
       --   つまり
       --     自分よりさらに左側に開始する区間が存在しない
       --   という意味であり、
       --   これは
       --     自分が最も外側
       --   であることを表す。
       --
       -- PH1.lft > PH2.lft:
       --   PH2 の方が lft が小さい、
       --   つまり PH2 の方がより外側に位置していることを意味する。
       --
       --   入れ子集合モデルでは
       --     外側ほど lft が小さい
       --   ので、
       --   これが「PH1 より外側のノードがあるか」を調べる条件になる。
       --
       --   したがって
       --     より外側が1つでもあれば PH1 は不採用
       --     より外側が1つも無ければ PH1 は最外周
       --   となる。
       --
       -- これは実質的に
       --   「A の中で最小の lft を持つ行を取る」
       -- と同じ意味である。
       --
       -- ただし NOT EXISTS で書くことで、
       --   「それより外側が存在しない」
       -- という入れ子集合モデルらしい見方がはっきり表現されている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. A の各行を PH1 として候補にする
-- 2. その PH1 に対して、
--    A の中に PH1 より小さい lft を持つ PH2 があるか調べる
-- 3. もし存在すれば PH1 はもっと外側に包まれているので除外する
-- 4. 存在しなければ PH1 は最外周ノードなので残す
-- 5. name と pcode を返す
--
-- 概念的には次のような処理に近い。
--
--   for each node PH1 of A:
--       if there does NOT exist another node PH2 of A
--          with PH2.lft < PH1.lft:
--           output PH1
--
-- つまりこれは、
--   「自分より左に始まるノードが存在しない」
--   ノードを求めるアルゴリズムである。
--
-- 入れ子集合モデルでは、
--   それが一番外側の円に対応する。


-- ================================================
-- 入れ子集合モデルとして見ると何をしているか
-- ================================================
--
-- A のデータは
--
--   4130001 : [0, 27]
--   4130002 : [9, 18]
--   4130103 : [12, 15]
--
-- である。
--
-- 各候補について
--   「もっと小さい lft を持つ A の行があるか」
-- を見ると、
--
-- 1. 4130001 (lft=0)
--   0 より小さい lft の A は無い
--   → 採用
--
-- 2. 4130002 (lft=9)
--   0 を持つ 4130001 がある
--   → 除外
--
-- 3. 4130103 (lft=12)
--   0 を持つ 4130001 も 9 を持つ 4130002 もある
--   → 除外
--
-- したがって返るのは
--   A, 4130001
-- だけである。
--
-- これは
--   最も外側の区間 = 最も古い履歴
-- を正しく取れていることを意味する。


-- ================================================
-- 図解: 入れ子集合モデルで最外周を探す
-- ================================================
--
--   A の区間
--
--   [0.......................................27]  4130001
--        [9.....................18]               4130002
--           [12.........15]                       4130103
--
--   判定:
--
--   4130001:
--     自分より左で始まる区間が無い
--     -> 最外周
--
--   4130002:
--     4130001 の方が左で始まる
--     -> 内側
--
--   4130103:
--     4130001, 4130002 の方が左で始まる
--     -> さらに内側
--
--   結果:
--     4130001 が返る
--
-- つまり
--   「一番左に始まる区間」が
--   最外周ノードである。


-- ================================================
-- 入れ子集合モデルの本質
-- ================================================
--
-- 入れ子集合モデルの本質は、
--   階層関係や包含関係を
--   参照ポインタではなく
--   数値区間で表す
-- 点にある。
--
-- 通常の親子ポインタ型モデルでは、
--   親を1つずつたどる
--   子を再帰で集める
-- という処理が必要になりやすい。
--
-- 一方、入れ子集合モデルでは
--   親が子を包含する
-- ことが
--     parent.lft < child.lft
--     parent.rgt > child.rgt
-- のような大小比較だけで表現できる。
--
-- 今回はそのもっとも単純な利用例として、
--   一番外側のノード
--   = 最小の lft を持つノード
-- を求めている。
--
-- つまりこのSQLは、
--   再帰を使わずに最上位ノードを求める
-- 入れ子集合モデルの代表的な発想を示している。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- FROM PostalHistory2 PH1
--   → 候補ノードを PH1 として1つずつ見る
--
-- WHERE name = 'A'
--   → A の履歴だけに限定する
--
-- NOT EXISTS (SELECT ... )
--   → 条件を満たす「より外側のノード」が存在しないことを要求する
--
-- PH1.lft > PH2.lft
--   → PH2 の方がより外側に位置することを意味する
--
-- name, pcode
--   → 最外周ノードの人物と郵便番号を返す
--
-- ;
--   → SQL文の終了を表す
-- ================================================


