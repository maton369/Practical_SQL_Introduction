-- ================================================
-- ファイル名: 09_select_nearest_postalcode_with_window.sql
-- 役割:
--   基準郵便番号 4130033 に対して、
--   最も近い郵便番号候補だけをウィンドウ関数で求める。
--
-- このSQLの目的:
--   サブクエリで最小 score を求める代わりに、
--   ウィンドウ関数 MIN(...) OVER(...) を使って
--   各行に「全体最小 score」を付与し、
--   それと自分の score を比較して最良候補だけを抽出すること。
--
-- 学習上の主題:
--   - 派生表で hit_code を作る意味
--   - ウィンドウ関数 MIN OVER の使い方
--   - 各行に最小値を持たせる発想
--   - サブクエリ版とのアルゴリズム比較
--
-- 今回の最重要ポイント:
--   このSQLも本質は同じで、
--     「各候補の score を計算し、
--       その最小値と一致する候補だけ返す」
--   である。
--
--   ただしサブクエリ版のように
--     外側で score を計算
--     内側で同じ score を再計算
--   するのではなく、
--   派生表の中で
--     各行の score = hit_code
--     全体の最小 score = min_code
--   を並べて持たせている。
--
--   つまりアルゴリズムとしては
--     1. 各行の score を hit_code として作る
--     2. 全行の最小 score を min_code として各行に配る
--     3. hit_code = min_code の行だけ残す
--   という構造である。
-- ================================================

SELECT
       pcode,
       -- pcode:
       --   最寄り候補として返したい郵便番号である。

       district_name
       -- district_name:
       --   その候補の地名である。

  FROM
       (
           SELECT
                  pcode,

                  district_name,

                  CASE
                       WHEN pcode = '4130033' THEN 0
                       WHEN pcode LIKE '413003%' THEN 1
                       WHEN pcode LIKE '41300%'  THEN 2
                       WHEN pcode LIKE '4130%'   THEN 3
                       WHEN pcode LIKE '413%'    THEN 4
                       WHEN pcode LIKE '41%'     THEN 5
                       WHEN pcode LIKE '4%'      THEN 6
                       ELSE NULL
                  END AS hit_code,
                  -- hit_code:
                  --   各候補自身の一致度 score である。
                  --
                  --   値が小さいほど基準郵便番号に近い。
                  --
                  --   これはリスト5.6の rank 相当であり、
                  --   まず各行を採点している。

                  MIN(
                      CASE
                           WHEN pcode = '4130033' THEN 0
                           WHEN pcode LIKE '413003%' THEN 1
                           WHEN pcode LIKE '41300%'  THEN 2
                           WHEN pcode LIKE '4130%'   THEN 3
                           WHEN pcode LIKE '413%'    THEN 4
                           WHEN pcode LIKE '41%'     THEN 5
                           WHEN pcode LIKE '4%'      THEN 6
                           ELSE NULL
                      END
                  )
                  OVER (
                      ORDER BY
                          CASE
                               WHEN pcode = '4130033' THEN 0
                               WHEN pcode LIKE '413003%' THEN 1
                               WHEN pcode LIKE '41300%'  THEN 2
                               WHEN pcode LIKE '4130%'   THEN 3
                               WHEN pcode LIKE '413%'    THEN 4
                               WHEN pcode LIKE '41%'     THEN 5
                               WHEN pcode LIKE '4%'      THEN 6
                               ELSE NULL
                          END
                  ) AS min_code
                  -- min_code:
                  --   ウィンドウ関数で求めた最小一致度である。
                  --
                  --   ここでは ORDER BY hit_code 相当の順で並べて
                  --   MIN(...) OVER(...) を計算している。
                  --
                  --   その結果、
                  --   各行に「ここまで見た最小値」が付与される。
                  --
                  --   今回のデータでは最小値は 2 なので、
                  --   score が 2 以上の行に対して min_code は 2 になる。
                  --
                  --   つまり最終的に
                  --     hit_code = min_code
                  --   で最良候補が残る。
                  --
                  -- 注意:
                  --   この書き方は
                  --     各行へ最小値を配る
                  --   発想の練習としては良いが、
                  --   「全体最小値」を明示するなら
                  --   OVER() だけにする形も考えられる。
                  --
                  --   ただしここでは教材の形を尊重している。

             FROM PostalCode
       ) Foo
       -- 派生表 Foo:
       --   各候補について
       --     hit_code
       --     min_code
       --   を横に並べて持った中間結果である。
       --
       --   この中間結果を作ることで、
       --   外側では
       --     hit_code = min_code
       --   だけを書けばよくなる。

 WHERE hit_code = min_code;
 -- WHERE hit_code = min_code:
 --   自分の一致度が最小一致度と等しい候補だけ残す。
 --
 --   つまり
 --     最良候補だけを返す
 --   条件である。
 --
 --   最小値タイが複数ある場合は、それらを全部返す。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. PostalCode の各行に対して hit_code を計算する
-- 2. 同じ派生表の中で MIN(...) OVER(...) により min_code を付与する
-- 3. 外側クエリで hit_code = min_code の行だけ残す
-- 4. pcode と district_name を返す
--
-- 概念的には次のような処理に近い。
--
--   for each row:
--       row.hit_code = score(row)
--
--   global_min = minimum of all hit_code
--
--   for each row:
--       row.min_code = global_min
--
--   output rows where row.hit_code == row.min_code
--
-- つまりこれは、
--   「最小値を各行に持たせてから比較する」
--   ウィンドウ関数版の最良候補抽出アルゴリズムである。


-- ================================================
-- サブクエリ版との違い
-- ================================================
--
-- サブクエリ版:
--   best_score を内側で1回求め、
--   外側で各行 score と比較する
--
-- ウィンドウ関数版:
--   各行に
--     自分の score
--     最小 score
--   を一緒に持たせてから比較する
--
-- つまり
--   サブクエリ版は「外と内で分ける」構造
--   ウィンドウ関数版は「各行に最小値を配る」構造
-- と言える。
--
-- どちらも目的は同じだが、
--   データをどう見せるか
--   どこで最小値を持たせるか
-- が違う。
--
-- 教材としては、
--   「最小値をサブクエリで取る」
--   「最小値をウィンドウ関数で各行に持たせる」
-- の対比が見えやすい。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- 候補の hit_code は概ね
--
--   4103213 -> 5
--   4130001 -> 2
--   4130002 -> 2
--   4130041 -> 2
--   4130103 -> 3
--   4380824 -> 6
--
-- 最良値は 2 である。
--
-- 派生表 Foo のイメージは概ね
--
--   pcode     hit_code   min_code
--   --------  --------   --------
--   4130001      2          2
--   4130002      2          2
--   4130041      2          2
--   4130103      3          2
--   4103213      5          2
--   4380824      6          2
--
-- となる。
--
-- 外側で
--   hit_code = min_code
-- を満たすのは
--   4130001
--   4130002
--   4130041
-- なので、これらが返る。


-- ================================================
-- 図解: ウィンドウ関数版のアルゴリズム
-- ================================================
--
--   PostalCode
--      |
--      v
--   各行の hit_code を計算
--      |
--      v
--   MIN(...) OVER(...) で min_code を各行に付与
--      |
--      v
--   派生表 Foo
--      |
--      v
--   hit_code = min_code ?
--      |
--      +---------------------+
--      | yes -> 行を返す     |
--      | no  -> 行を捨てる   |
--      +---------------------+
--
-- つまり
--   「最良値を行ごとに配ってから比較する」
-- 形である。