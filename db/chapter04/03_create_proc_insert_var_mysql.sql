-- ================================================
-- ファイル名: 03_create_proc_insert_var_mysql.sql
-- 役割:
--   Sales テーブルを company, year 順に走査し、
--   同じ会社の直前売上と比較した結果を Sales2 に書き込む
--   MySQL 用のストアドプロシージャを定義する。
--
-- このSQLの目的:
--   逐次処理(いわゆる「ぐるぐる系」)で、
--   前の行との比較結果を1件ずつ計算して保存すること。
--
-- 学習上の主題:
--   MySQL の
--     カーソル
--     FETCH
--     CONTINUE HANDLER
--     状態変数による前回値保持
--     IF / ELSEIF による条件分岐
--     INSERT による結果書き込み
--   を理解すること。
--
-- 今回の最重要ポイント:
--   このプロシージャは集合演算で一気に処理するのではなく、
--   1行ずつ順番に読む。
--
--   そのためアルゴリズムとしては
--     1. Sales を company, year 順に並べる
--     2. 1行ずつ取り出す
--     3. 直前に読んだ同じ会社の売上と比較する
--     4. 比較結果を Sales2 に INSERT する
--     5. 「直前値」を現在行の値へ更新する
--   という状態遷移型の処理になっている。
--
--   いわゆる「前回値を覚えながら走査する」アルゴリズムである。
--
-- MySQL 化の重要ポイント:
--   Oracle の
--     CREATE OR REPLACE PROCEDURE
--     IS
--     %ROWTYPE
--     OPEN / CLOSE / %NOTFOUND
--   はそのまま使えない。
--
--   MySQL では
--     DELIMITER
--     DECLARE CURSOR
--     DECLARE CONTINUE HANDLER FOR NOT FOUND
--     個別変数への FETCH
--   を使って同じ処理を書く。
-- ================================================

DELIMITER $$

DROP PROCEDURE IF EXISTS PROC_INSERT_VAR$$

CREATE PROCEDURE PROC_INSERT_VAR()
BEGIN
    -- =========================================================
    -- 1. ループ終了判定用のフラグ
    -- =========================================================
    --
    -- done:
    --   カーソルからもう取得できる行が無くなったことを表すフラグである。
    --
    --   MySQL では Oracle の c_sales%NOTFOUND の代わりに、
    --   NOT FOUND ハンドラでこの変数を 1 にする。
    DECLARE done INT DEFAULT 0;

    -- =========================================================
    -- 2. FETCH 先の現在行変数
    -- =========================================================
    --
    -- Oracle 版の rec_sales%ROWTYPE に相当する考え方を、
    -- MySQL では個別変数で表現する。
    --
    -- v_company, v_year, v_sale は
    --   「今処理している1行」
    -- を表す。
    DECLARE v_company CHAR(1);
    DECLARE v_year INT;
    DECLARE v_sale INT;

    -- =========================================================
    -- 3. 状態保持用の変数
    -- =========================================================

    DECLARE i_pre_sale INT DEFAULT 0;
    -- i_pre_sale:
    --   直前に処理したレコードの売上値を保持する。
    --
    --   現在行の売上 v_sale と比較して、
    --   増減記号を決めるために使う。
    --
    --   ただし最初の比較では使わず、
    --   「同じ会社の続きかどうか」は c_company 側で判定する。

    DECLARE c_company CHAR(1) DEFAULT '*';
    -- c_company:
    --   直前に処理したレコードの会社コードを保持する。
    --
    --   初期値 '*' は番兵値であり、
    --   実データの会社 A/B/C と一致しない値を入れている。
    --
    --   これにより最初の1件目では
    --     c_company = v_company
    --   が偽になり、
    --   「その会社の先頭行」と判断できる。

    DECLARE c_var CHAR(1) DEFAULT '*';
    -- c_var:
    --   Sales2.var に入れる比較結果記号を保持する。
    --
    --   値の意味:
    --     '+' : 直前売上より増えた
    --     '-' : 直前売上より減った
    --     '=' : 直前売上と同じ
    --     NULL: その会社の先頭行なので比較不能

    -- =========================================================
    -- 4. カーソル宣言
    -- =========================================================
    --
    -- c_sales は Sales テーブルを
    --   company, year
    -- の順で読み出すカーソルである。
    --
    -- この順序が非常に重要である。
    --
    -- なぜなら、このプロシージャは
    --   「同じ会社の直前レコード」と現在レコード
    -- を比較するからである。
    --
    -- もし ORDER BY が無ければ、
    --   どの順番で比較されるか保証できず、
    --   直前比較の意味が崩れる。
    DECLARE c_sales CURSOR FOR
        SELECT company, year, sale
          FROM Sales
         ORDER BY company, year;

    -- =========================================================
    -- 5. NOT FOUND ハンドラ
    -- =========================================================
    --
    -- Oracle の c_sales%NOTFOUND に相当する仕組みである。
    --
    -- FETCH したときに取得できる行が無ければ、
    -- done = 1 にする。
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    -- =========================================================
    -- 6. 必要なら出力表を空にする
    -- =========================================================
    --
    -- Sales2 は (company, year) が主キーなので、
    -- 既にデータが入っている状態で再実行すると重複エラーになる。
    --
    -- 教材として毎回同じ結果を作りたいなら、
    -- 事前に空にしておく方が分かりやすい。
    --
    -- 必要に応じて DELETE ではなく TRUNCATE を使ってもよいが、
    -- ここでは単純に DELETE を使う。
    DELETE FROM Sales2;

    -- =========================================================
    -- 7. カーソルを開く
    -- =========================================================
    OPEN c_sales;

    -- =========================================================
    -- 8. 1行ずつ処理するループ
    -- =========================================================
    read_loop: LOOP

        -- -----------------------------------------------------
        -- 8-1. 1行 FETCH
        -- -----------------------------------------------------
        --
        -- c_sales の現在位置から1行を取り出し、
        -- v_company, v_year, v_sale に代入する。
        FETCH c_sales INTO v_company, v_year, v_sale;

        -- -----------------------------------------------------
        -- 8-2. もう行が無ければ終了
        -- -----------------------------------------------------
        --
        -- NOT FOUND が発生すると done = 1 になるので、
        -- ここでループを抜ける。
        IF done = 1 THEN
            LEAVE read_loop;
        END IF;

        -- -----------------------------------------------------
        -- 8-3. 同じ会社の続きかどうかを判定
        -- -----------------------------------------------------
        --
        -- 現在行の会社 v_company が、
        -- 直前行の会社 c_company と同じなら比較可能である。
        --
        -- 同じでない場合は、
        -- その会社の最初の行とみなして var は NULL にする。
        IF c_company = v_company THEN

            -- -------------------------------------------------
            -- 同じ会社のレコードだった場合
            -- -------------------------------------------------
            --
            -- このときだけ、
            -- 直前売上 i_pre_sale と現在売上 v_sale を比較する。
            --
            -- つまり比較対象は
            --   「前年」
            -- ではなく
            --   「直前に読み込んだ同一会社レコード」
            -- である点が重要である。
            IF i_pre_sale < v_sale THEN
                SET c_var = '+';
                -- 直前売上より大きければ増加
                -- したがって '+' を設定する。

            ELSEIF i_pre_sale > v_sale THEN
                SET c_var = '-';
                -- 直前売上より小さければ減少
                -- したがって '-' を設定する。

            ELSE
                SET c_var = '=';
                -- 上のどちらでもない場合は等しい
                -- したがって '=' を設定する。
            END IF;

        ELSE
            -- -------------------------------------------------
            -- 会社が変わった場合
            -- -------------------------------------------------
            --
            -- この行はその会社の最初の行なので、
            -- 比較対象となる「直前の同一会社売上」が存在しない。
            --
            -- したがって var は NULL にする。
            SET c_var = NULL;
        END IF;

        -- -----------------------------------------------------
        -- 8-4. 比較結果を Sales2 に書き込む
        -- -----------------------------------------------------
        --
        -- 現在行の company, year, sale と、
        -- 今計算した c_var を Sales2 へ INSERT する。
        --
        -- これにより、
        -- 元データ + 比較記号
        -- の結果表が作られる。
        INSERT INTO Sales2 (company, year, sale, var)
        VALUES (v_company, v_year, v_sale, c_var);

        -- -----------------------------------------------------
        -- 8-5. 状態変数を現在行で更新する
        -- -----------------------------------------------------
        --
        -- 次のループでは現在行が「直前行」になるため、
        -- ここで状態を更新しておく。
        --
        -- つまり
        --   c_company には現在会社
        --   i_pre_sale には現在売上
        -- を保存する。
        --
        -- これが「前回値を覚えながら進む」アルゴリズムの核心である。
        SET c_company = v_company;
        SET i_pre_sale = v_sale;

    END LOOP;

    -- =========================================================
    -- 9. カーソルを閉じる
    -- =========================================================
    CLOSE c_sales;

END$$

DELIMITER ;

-- ================================================
-- このプロシージャの処理の流れ
-- ================================================
--
-- 1. Sales を company, year の順に並べたカーソルを開く
-- 2. 1行ずつ FETCH して現在レコードを得る
-- 3. 直前会社 c_company と現在会社 v_company を比較する
-- 4. 同じ会社なら直前売上 i_pre_sale と現在売上 v_sale を比較する
-- 5. 比較結果から c_var を '+', '-', '=' に決める
-- 6. 会社が変わった最初の行なら c_var を NULL にする
-- 7. company, year, sale, c_var を Sales2 に INSERT する
-- 8. 現在会社・現在売上を「次回の直前状態」として更新する
-- 9. 全件終了まで繰り返す
--
-- 概念的には次のような処理に近い。
--
--   prev_company = none
--   prev_sale = none
--
--   for each row in Sales ordered by company, year:
--       if row.company == prev_company:
--           if prev_sale < row.sale:
--               var = '+'
--           elif prev_sale > row.sale:
--               var = '-'
--           else:
--               var = '='
--       else:
--           var = NULL
--
--       insert row + var into Sales2
--
--       prev_company = row.company
--       prev_sale = row.sale
--
-- つまりこれは、
--   「現在の行を処理するたびに直前状態を更新する」
--   状態機械的アルゴリズムである。
--
-- ここが、いわゆる「ぐるぐる系」の本質である。
--
--
-- ================================================
-- 実行方法
-- ================================================
--
-- プロシージャ作成後は次のように呼び出す。
--
--   CALL PROC_INSERT_VAR();
--
-- 結果確認は次のように行う。
--
--   SELECT *
--     FROM Sales2
--    ORDER BY company, year;
--
--
-- ================================================
-- 期待される Sales2 のイメージ
-- ================================================
--
-- A社:
--   2002 50  -> NULL
--   2003 52  -> +
--   2004 55  -> +
--   2007 55  -> =
--
-- B社:
--   2001 27  -> NULL
--   2005 28  -> +
--   2006 28  -> =
--   2009 30  -> +
--
-- C社:
--   2001 40  -> NULL
--   2005 39  -> -
--   2006 38  -> -
--   2010 35  -> -
-- ================================================