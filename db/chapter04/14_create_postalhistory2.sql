-- ================================================
-- ファイル名: 14_create_postalhistory2.sql
-- 役割:
--   郵便番号履歴を「入れ子集合モデル」で表現する
--   PostalHistory2 テーブルを作成する。
--
-- このSQLの目的:
--   通常の
--     pcode -> new_pcode
--   のようなポインタ型の履歴表現ではなく、
--   各履歴ノードに
--     lft
--     rgt
--   という区間境界を持たせることで、
--   履歴の包含関係を数値の大小比較で表現できるようにすること。
--
-- 学習上の主題:
--   - 入れ子集合モデル (nested sets model)
--   - lft / rgt の意味
--   - 区間包含で親子関係や外側/内側を表す考え方
--   - 再帰なしで階層の位置関係を判定する発想
--
-- 今回の最重要ポイント:
--   入れ子集合モデルでは、
--   「親が子を含む」
--   という関係を、
--   文字通り区間 [lft, rgt] の包含関係で表す。
--
--   つまり
--     親ノード:   [0, 27]
--     子ノード:   [9, 18]
--     孫ノード:   [12, 15]
--   のように、
--   外側のノードほど広い区間を持ち、
--   内側のノードほど狭い区間を持つ。
--
--   このため、
--     どのノードが一番外側か
--   は
--     最小の lft を持つか
--   あるいは
--     自分より外側の区間が存在しないか
--   で判定できる。
--
--   これがリスト5.13の NOT EXISTS の背景にある考え方である。
-- ================================================

CREATE TABLE PostalHistory2
(
    name CHAR(1),
    -- name:
    --   人物を表す識別子である。
    --
    --   例:
    --     'A'
    --     'B'
    --     'C'
    --
    --   入れ子集合モデルは人物ごとに独立した構造を持つので、
    --   lft/rgt の比較も name ごとに行う。

    pcode CHAR(7),
    -- pcode:
    --   その履歴ノードに対応する郵便番号である。
    --
    --   入れ子集合モデルでは、
    --   pcode 自体が他の pcode を直接参照するわけではない。
    --
    --   代わりに、この pcode が
    --   どの位置の区間 [lft, rgt] を持つかで
    --   階層関係を表す。

    lft REAL NOT NULL,
    -- lft:
    --   入れ子集合モデルにおける左境界である。
    --
    --   同じ人物(name)の中で、
    --   外側のノードほど小さい lft を持つ。
    --
    --   たとえば A では
    --     4130001 -> lft = 0
    --     4130002 -> lft = 9
    --     4130103 -> lft = 12
    -- となっており、
    --   より古い・より外側のノードほど左側へ位置している。
    --
    --   リスト5.13では
    --     「自分より小さい lft を持つ同人物ノードが存在するか」
    --   を見て、一番外側かどうかを判定している。

    rgt REAL NOT NULL,
    -- rgt:
    --   入れ子集合モデルにおける右境界である。
    --
    --   同じ人物(name)の中で、
    --   外側のノードほど大きい rgt を持つ。
    --
    --   つまり
    --     外側ノードほど広い区間
    --   を持ち、
    --     内側ノードほど狭い区間
    --   を持つ。
    --
    --   例:
    --     [0, 27] の中に [9, 18] が入り、
    --     [9, 18] の中に [12, 15] が入る。
    --
    --   これが入れ子集合モデルの名前の由来である。

    CONSTRAINT pk_name_pcode2 PRIMARY KEY(name, pcode),
    -- PRIMARY KEY(name, pcode):
    --   同じ人物に対して同じ郵便番号が重複しないようにする主キーである。

    CONSTRAINT uq_name_lft UNIQUE (name, lft),
    -- UNIQUE(name, lft):
    --   同じ人物の中で lft が重複しないようにする。
    --
    --   入れ子集合モデルでは各ノードが固有の左境界を持つため、
    --   これを一意に保つのは自然である。

    CONSTRAINT uq_name_rgt UNIQUE (name, rgt),
    -- UNIQUE(name, rgt):
    --   同じ人物の中で rgt が重複しないようにする。
    --
    --   こちらも各ノードが固有の右境界を持つことを保証する。

    CHECK(lft < rgt)
    -- CHECK(lft < rgt):
    --   区間として意味を持つための整合性制約である。
    --
    --   左境界が右境界以上では、
    --   区間として成立しない。
);