-- ================================================
-- ファイル名: 13_select_oldest_postalcode_with_recursive_cte_postgresql.sql
-- 役割:
--   指定人物の郵便番号履歴を再帰共通表式でたどり、
--   一番古い郵便番号を求める。
--
-- このSQLの目的:
--   PostalHistory の
--     pcode -> new_pcode
--   という履歴連鎖を、
--   最新地点から過去方向へ何段もたどって、
--   最も古い住所履歴を特定すること。
--
-- 学習上の主題:
--   - WITH RECURSIVE の基本構造
--   - 再帰共通表式の「初期部」と「再帰部」
--   - 深さ(depth)を持たせる意味
--   - 再帰的に自己参照しながら履歴をさかのぼる方法
--
-- 今回の最重要ポイント:
--   再帰共通表式は
--     1. 探索の開始点を決める
--     2. そこから1段ずつ関係をたどる
--     3. 結果を自分自身へ追加していく
--   ことで、履歴の鎖や木構造のようなデータをたどれる。
--
--   今回は
--     最新の郵便番号(new_pcode IS NULL)を起点
--   にして、
--     Parent.pcode = Child.new_pcode
--   という関係で
--   過去の履歴を逆向きにさかのぼっている。
--
--   つまりアルゴリズムとしては
--     最新地点からスタート
--     1つ前の履歴を探す
--     さらにその1つ前を探す
--     ...
--     最後までたどる
--   という再帰探索である。
-- ================================================

WITH RECURSIVE Explosion (name, pcode, new_pcode, depth)
AS
(
    -- =========================================================
    -- 1. 初期部(アンカー部)
    -- =========================================================
    --
    -- 再帰共通表式はまず「開始点」を1回だけ作る必要がある。
    --
    -- 今回の開始点は、
    --   指定した name の中で
    --   new_pcode IS NULL
    -- の行である。
    --
    -- これは「最新の郵便番号」を意味する。
    --
    -- たとえば A なら
    --   ('A', '4130103', NULL)
    -- が開始点になる。
    --
    -- この開始点に depth = 1 を付けて、
    -- 再帰探索の起点とする。
    SELECT
           name,
           pcode,
           new_pcode,
           1
      FROM PostalHistory
     WHERE name = 'A'
       AND new_pcode IS NULL
       -- 探索の開始点:
       --   new_pcode が NULL ということは、
       --   その pcode が「履歴の最新地点」であることを意味する。
       --
       --   今回は最新から古い方へ逆向きにたどりたいので、
       --   ここを起点にする。

    UNION

    -- =========================================================
    -- 2. 再帰部
    -- =========================================================
    --
    -- ここでは、すでに Explosion に入った行(Parent)をもとに、
    -- その1つ前の履歴(Child)を PostalHistory から探す。
    --
    -- つなぎ方の本質は
    --
    --   Parent.pcode = Child.new_pcode
    --
    -- である。
    --
    -- つまり
    --   「親の現在郵便番号は、
    --     子の new_pcode だった」
    --
    -- と考えて、
    --   1つ前の履歴を逆向きに見つけている。
    --
    -- A の場合:
    --   Parent = 4130103
    --   Child  = 4130002 -> 4130103
    --
    --   さらに次は
    --   Parent = 4130002
    --   Child  = 4130001 -> 4130002
    --
    -- というふうに過去へ進む。
    SELECT
           Child.name,
           Child.pcode,
           Child.new_pcode,
           depth + 1
      FROM Explosion AS Parent,
           PostalHistory AS Child
     WHERE Parent.pcode = Child.new_pcode
       AND Parent.name = Child.name
       -- Parent.name = Child.name:
       --   同じ人物の履歴だけをたどるための条件である。
       --
       --   これが無いと、
       --   別人物の履歴が誤ってつながる可能性がある。
       --
       -- depth + 1:
       --   1段さかのぼるたびに深さを増やしている。
       --
       --   今回の depth は
       --     何段過去へたどったか
       --   を表すカウンタであり、
       --   後で「一番古い行」を見つけるために使う。
)

-- =========================================================
-- 3. メインSELECT
-- =========================================================
--
-- Explosion には、
--   最新地点から順に過去へたどった全履歴
-- が入っている。
--
-- depth は
--   最新地点    -> 1
--   1つ前       -> 2
--   2つ前       -> 3
-- のように増える。
--
-- したがって、
--   最大の depth を持つ行
-- が「一番古い履歴」である。
SELECT
       name,
       pcode,
       new_pcode
  FROM Explosion
 WHERE depth =
       (
           SELECT MAX(depth)
             FROM Explosion
       );
       -- MAX(depth):
       --   Explosion の中で最も深い行、
       --   つまり最も過去までたどった行を求める。
       --
       --   これが「一番古い住所」に対応する。
       --
       --   A の場合、
       --   Explosion の内容は概ね
       --
       --     depth=1 -> 4130103
       --     depth=2 -> 4130002
       --     depth=3 -> 4130001
       --
       --   となるので、
       --   MAX(depth)=3 を持つ
       --     4130001
       --   が返る。


-- ================================================
-- 再帰共通表式のアルゴリズムを言葉でまとめると
-- ================================================
--
-- 1. 最新郵便番号を開始点として Explosion に入れる
-- 2. Explosion の各行について、
--    「その pcode を new_pcode に持つ1つ前の履歴」を探す
-- 3. 見つかったら Explosion に追加する
-- 4. もう追加できなくなるまで繰り返す
-- 5. もっとも depth が大きい行を返す
--
-- 概念的には次のような処理に近い。
--
--   explosion = [latest_record]
--
--   while there exists previous_record:
--       previous_record = record whose new_pcode == current.pcode
--       add previous_record to explosion
--       current = previous_record
--
--   return deepest_record
--
-- つまりこれは、
--   連鎖構造を再帰的に逆向き探索する
--   線形履歴トレースアルゴリズムである。
--
--
-- ================================================
-- 実データ A に対する具体例
-- ================================================
--
-- A の PostalHistory:
--   4130001 -> 4130002
--   4130002 -> 4130103
--   4130103 -> NULL
--
-- 初期部:
--   new_pcode IS NULL なので
--   4130103 が開始点になる。
--
-- Explosion:
--   depth=1, pcode=4130103
--
-- 再帰1回目:
--   Parent.pcode = 4130103
--   Child.new_pcode = 4130103
--   を探すと、
--   4130002 -> 4130103
--   が見つかる。
--
-- Explosion:
--   depth=2, pcode=4130002
--
-- 再帰2回目:
--   Parent.pcode = 4130002
--   Child.new_pcode = 4130002
--   を探すと、
--   4130001 -> 4130002
--   が見つかる。
--
-- Explosion:
--   depth=3, pcode=4130001
--
-- 再帰3回目:
--   Parent.pcode = 4130001
--   Child.new_pcode = 4130001
--   を探すが、見つからない。
--
-- ここで再帰終了。
--
-- 最後に MAX(depth)=3 の行、
--   4130001
-- が返る。
--
-- これが「一番古い住所」である。


-- ================================================
-- 図解: 再帰共通表式による履歴探索
-- ================================================
--
--   A の履歴連鎖
--
--   4130001 ----> 4130002 ----> 4130103 ----> NULL
--      ^             ^              ^
--      |             |              |
--    depth=3       depth=2        depth=1
--
--   探索の流れ:
--
--   1. new_pcode IS NULL の行から開始
--         4130103
--
--   2. Parent.pcode = Child.new_pcode で1つ前を探す
--         4130002 -> 4130103
--
--   3. さらに1つ前を探す
--         4130001 -> 4130002
--
--   4. これ以上前が無くなったら終了
--
--   5. 最も depth が大きい行を返す
--
-- つまり、
--   最新から出発して過去へさかのぼる
--   逆向きの再帰探索になっている。


-- ================================================
-- 再帰共通表式の本質
-- ================================================
--
-- WITH RECURSIVE の本質は、
--   「途中結果を自分自身に足しながら広げていく」
-- 点にある。
--
-- 今回の Explosion は、
--   まず開始点1行から始まり、
--   再帰部がその1行を使って次の行を追加し、
--   追加した行をさらに使ってまた次を追加する。
--
-- つまり Explosion は
--   毎回少しずつ大きくなる集合
-- である。
--
-- そしてその成長が止まったとき、
--   履歴全体が集まっている。
--
-- 今回は履歴が単純な鎖なので分かりやすいが、
--   木構造
--   階層構造
--   経路探索
-- などにも同じ発想を使える。
--
-- ここが再帰共通表式の非常に重要な考え方である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- WITH RECURSIVE Explosion (...)
--   → 再帰的に拡張される共通表式 Explosion を定義する
--
-- SELECT ... WHERE new_pcode IS NULL
--   → 探索の開始点(最新郵便番号)を作る
--
-- UNION
--   → 初期部と再帰部の結果を1つにまとめる
--
-- FROM Explosion AS Parent, PostalHistory AS Child
--   → これまで見つかった履歴(Parent)をもとに、
--     次にたどる履歴(Child)を探す
--
-- WHERE Parent.pcode = Child.new_pcode
--   → 現在地点の1つ前の履歴を逆向きに結びつける
--
-- depth + 1
--   → 何段過去へ進んだかを記録する
--
-- SELECT ... WHERE depth = (SELECT MAX(depth) ...)
--   → 一番深い、つまり一番古い履歴を返す
-- ================================================


-- ================================================
-- 補足1:
--   このSQLは PostgreSQL の再帰共通表式の例である。
--
--   MySQL 8 でも WITH RECURSIVE 自体は使えるが、
--   DB 製品ごとに最適化や挙動の見え方が多少異なることがある。
-- ================================================


-- ================================================
-- 補足2:
--   今回は履歴が単純な鎖で循環が無い前提である。
--
--   もし
--     A -> B -> C -> A
--   のような循環があると、
--   再帰が止まらない危険がある。
--
--   実務では循環検出や最大深さ制限を入れる設計も重要である。
-- ================================================


-- ================================================
