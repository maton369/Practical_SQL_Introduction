--   その順序が「直前比較」に使われることを理解すること。
--
-- 今回の最重要ポイント:
--   PROC_INSERT_VAR は
--     ORDER BY company, year
--   でデータを読む。
--
--   そのため、このサンプルは
--   「会社ごとに年順に見たときの増減」
--   を分かりやすく観察できるようになっている。
-- ================================================


