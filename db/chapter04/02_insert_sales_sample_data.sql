-- ================================================
-- データの見方
-- ================================================
--
-- A社:
--   2002 -> 50
--   2003 -> 52  (増加)
--   2004 -> 55  (増加)
--   2007 -> 55  (同値)
--
--   したがって var は概ね
--     2002 -> NULL
--     2003 -> '+'
--     2004 -> '+'
--     2007 -> '='
--   になる。
--
-- B社:
--   2001 -> 27
--   2005 -> 28  (増加)
--   2006 -> 28  (同値)
--   2009 -> 30  (増加)
--
--   したがって var は概ね
--     2001 -> NULL
--     2005 -> '+'
--     2006 -> '='
--     2009 -> '+'
--   になる。
--
-- C社:
--   2001 -> 40
--   2005 -> 39  (減少)
--   2006 -> 38  (減少)
--   2010 -> 35  (減少)
--
--   したがって var は概ね
--     2001 -> NULL
--     2005 -> '-'
--     2006 -> '-'
--     2010 -> '-'
--   になる。
--
-- 重要:
--   年が連続していなくても、
--   このプロシージャは「直前に読んだ同一会社のレコード」と比較する。
--
--   つまり
--     2004年の次が2007年
--   のように年が飛んでいても、
--   2007年は2004年と比較される。
--
--   これは「前年差」ではなく、
--   「直前レコード差」である点に注意する。