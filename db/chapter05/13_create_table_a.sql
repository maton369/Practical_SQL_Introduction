-- ================================================
-- ファイル名: 13_create_table_a.sql
-- 役割:
--   三角結合の説明に使う Table_A を作成する。
--
-- このSQLの目的:
--   三角結合では、複数テーブルが
--   1つのキー列を共有するように結び付けられる。
--
--   そのためまずは、
--   最小構成の1列表を用意して
--   結合構造だけに集中できるようにする。
--
-- 学習上の主題:
--   - 結合の骨格だけを理解するための簡単な表
--   - 後続の INNER JOIN における「基準点」としての表
--
-- 今回の最重要ポイント:
--   Table_A は後続のクエリで
--     Table_B
--     Table_C
--   の両方と結び付く中心になる。
--
--   つまり三角結合では、
--   A が B と結ばれ、
--   さらに A が C と結ばれる。
--
--   この「A を中心に2本の結合条件が伸びる」形が
--   三角結合の基本イメージである。
-- ================================================

CREATE TABLE Table_A
(
    col_a CHAR(1)
    -- col_a:
    --   Table_A 側の結合キー候補である。
    --
    --   後続のクエリでは
    --     A.col_a = B.col_b
    --     A.col_a = C.col_c
    --   の2つの条件で使われる。
    --
    --   つまり col_a は
    --   B と C の両方をつなぐ中心キーとして機能する。
);