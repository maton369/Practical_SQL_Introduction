-- ================================================
-- ファイル名: 08_select_correlated_subquery_employees_departments.sql
-- 役割:
--   Employees テーブルを基準にして、
--   各社員に対応する部門名を
--   相関サブクエリで取得する。
--
-- このSQLの目的:
--   リスト6.4では INNER JOIN を使って
--   社員と部門を結び付けていたが、
--   今回は同じ結果を
--   「各社員行ごとにサブクエリで部門名を引く」
--   形で表現すること。
--
-- 学習上の主題:
--   - 相関サブクエリの意味
--   - 外側クエリの行に依存して内側クエリが変わること
--   - INNER JOIN と相関サブクエリの対応関係
--   - 「1行ごとに参照値を引く」アルゴリズム
--
-- 今回の最重要ポイント:
--   このSQLの中心は相関サブクエリである。
--
--   相関サブクエリとは、
--   内側のサブクエリが
--   外側クエリの列を参照するサブクエリである。
--
--   今回でいえば、
--     外側の Employees の1行
--   が変わるたびに、
--     内側の WHERE E.dept_id = D.dept_id
--   の E.dept_id の値も変わる。
--
--   つまりアルゴリズムとしては
--     1. Employees の1行を取る
--     2. その行の dept_id を使って
--        Departments から対応する部門名を探す
--     3. 見つかった dept_name をその社員行に付ける
--   という「1行ごとの参照引き」になっている。
--
--   これが相関サブクエリの本質である。
-- ================================================

SELECT
       E.emp_id,
       -- E.emp_id:
       --   社員IDを返す。
       --
       --   Employees 側の主キーであり、
       --   どの社員について部門名を引いてきたのかを示す。

       E.emp_name,
       -- E.emp_name:
       --   社員名を返す。
       --
       --   部門名と組み合わせて
       --   「誰がどの部門か」を人間に分かりやすく表示する。

       E.dept_id,
       -- E.dept_id:
       --   社員が保持している所属部門IDである。
       --
       --   この列が内側の相関サブクエリで使われ、
       --   Departments テーブルのどの行を探すかを決める。
       --
       --   つまり相関サブクエリにおける検索キーである。

       (
           SELECT D.dept_name
             FROM Departments D
            WHERE E.dept_id = D.dept_id
       ) AS dept_name
       -- 相関サブクエリ:
       --   ここが今回の主役である。
       --
       --   内側の SELECT は Departments から dept_name を1つ取り出す。
       --
       --   しかし単なる独立サブクエリではなく、
       --   WHERE 句で
       --
       --     E.dept_id = D.dept_id
       --
       --   と書いているため、
       --   外側クエリの E.dept_id を参照している。
       --
       --   ここでの E は外側の Employees の現在行を意味する。
       --
       --   したがってこのサブクエリは、
       --   外側で見ている社員行ごとに
       --   内容が変わる。
       --
       --   たとえば外側が
       --     石田, dept_id='10'
       --   を見ているとき、
       --   内側は実質的に
       --
       --     SELECT D.dept_name
       --       FROM Departments D
       --      WHERE '10' = D.dept_id
       --
       --   のように評価される。
       --
       --   すると
       --     総務
       --   が返る。
       --
       --   次に外側が
       --     小笠原, dept_id='11'
       --   を見ているとき、
       --   内側は実質的に
       --
       --     SELECT D.dept_name
       --       FROM Departments D
       --      WHERE '11' = D.dept_id
       --
       --   のように評価される。
       --
       --   すると
       --     人事
       --   が返る。
       --
       --   このように、
       --   外側の1行ごとに内側の条件が変わるので
       --   「相関」している。
       --
       -- AS dept_name:
       --   サブクエリで得た部門名に dept_name という列名を付けている。
       --
       --   結果として、
       --   Employees に存在しない部門名列を
       --   あたかも追加したように見せている。

  FROM Employees E;
  -- FROM Employees E:
  --   外側クエリの基準表である。
  --
  --   まず Employees の各行を1件ずつ見ていき、
  --   その各行に対して相関サブクエリを実行する。
  --
  --   つまり全体の流れとしては
  --     外側: 社員行を順に読む
  --     内側: その社員の dept_id に一致する部門名を取る
  --   となる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Employees の1行目を取る
-- 2. その行の E.dept_id を使って Departments を検索する
-- 3. 一致する D.dept_id の D.dept_name を返す
-- 4. その dept_name を外側行へ付ける
-- 5. 次の Employees 行でも同じことを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each employee in Employees:
--       dept_name = lookup in Departments
--                   where Departments.dept_id == employee.dept_id
--
--       output employee.emp_id,
--              employee.emp_name,
--              employee.dept_id,
--              dept_name
--
-- つまりこれは、
--   「各社員行に対して、参照表から1つ値を引く」
--   ルックアップ型アルゴリズムである。
--
-- INNER JOIN と比べると、
--   JOIN が「行どうしを結ぶ」発想なのに対し、
--   相関サブクエリは「各行に対して値を引く」発想に近い。
--
-- ここが理解のポイントである。


-- ================================================
-- INNER JOIN との対応関係
-- ================================================
--
-- 前のリスト6.4では、
--
--   SELECT E.emp_id, E.emp_name, E.dept_id, D.dept_name
--     FROM Employees E INNER JOIN Departments D
--       ON E.dept_id = D.dept_id;
--
-- という形で書いていた。
--
-- これは
--   社員行と部門行を結び付けてから、
--   必要列を抜き出す
-- という発想である。
--
-- 一方今回の相関サブクエリ版では、
--
--   SELECT E.emp_id, E.emp_name, E.dept_id,
--          (SELECT D.dept_name
--             FROM Departments D
--            WHERE E.dept_id = D.dept_id) AS dept_name
--     FROM Employees E;
--
-- となっている。
--
-- これは
--   社員行を基準にし、
--   その各行ごとに部門名だけを引いてくる
-- という発想である。
--
-- つまり
--
--   INNER JOIN:
--     行と行を結び付ける
--
--   相関サブクエリ:
--     外側の各行に対して内側から値を引く
--
-- という見方ができる。
--
-- 結果は同じでも、
--   書き方の発想
-- が違う。
--
-- これがリスト6.5の学習価値である。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Employees:
--   001 石田   10
--   002 小笠原 11
--   003 夏目   11
--   004 米田   12
--   005 釜本   12
--   006 岩瀬   12
--
-- Departments:
--   10 総務
--   11 人事
--   12 開発
--   13 営業
--
-- 外側が各社員を見ているとき、
-- 内側サブクエリは次のように評価される。
--
-- 1. 石田 (dept_id='10')
--   内側:
--     SELECT D.dept_name
--       FROM Departments D
--      WHERE '10' = D.dept_id
--   -> 総務
--
-- 2. 小笠原 (dept_id='11')
--   内側:
--     SELECT D.dept_name
--       FROM Departments D
--      WHERE '11' = D.dept_id
--   -> 人事
--
-- 3. 夏目 (dept_id='11')
--   -> 人事
--
-- 4. 米田 (dept_id='12')
--   -> 開発
--
-- 5. 釜本 (dept_id='12')
--   -> 開発
--
-- 6. 岩瀬 (dept_id='12')
--   -> 開発
--
-- したがって最終結果は概ね
--
--   001 石田   10 総務
--   002 小笠原 11 人事
--   003 夏目   11 人事
--   004 米田   12 開発
--   005 釜本   12 開発
--   006 岩瀬   12 開発
--
-- となる。
--
-- これは INNER JOIN 版と同じ結果である。


-- ================================================
-- 図解: 相関サブクエリ
-- ================================================
--
--   外側クエリ: Employees の1行を取る
--
--      石田, dept_id='10'
--              |
--              v
--   内側サブクエリ:
--      Departments の中から
--      dept_id='10' の行を探す
--              |
--              v
--            総務
--              |
--              v
--   外側行に dept_name として付ける
--
--   次の社員へ
--
--      小笠原, dept_id='11'
--              |
--              v
--   内側サブクエリ:
--      Departments の中から
--      dept_id='11' の行を探す
--              |
--              v
--            人事
--              |
--              v
--   外側行に dept_name として付ける
--
-- つまり、
--   外側1行ごとに内側検索が走る
--   構造になっている。
--
-- これが「相関」の意味である。


-- ================================================
-- なぜ「相関」なのか
-- ================================================
--
-- 普通の独立サブクエリなら、
--   内側クエリは外側とは無関係に1回評価できる。
--
-- しかし今回の内側クエリは
--
--   WHERE E.dept_id = D.dept_id
--
-- の E.dept_id を参照している。
--
-- この E.dept_id は外側行によって変わる。
--
-- したがって内側クエリは、
--   外側の現在行が何か
-- に依存する。
--
-- これが相関サブクエリの定義である。
--
-- つまり
--   外側行が変わる
--   -> 内側条件も変わる
--   -> 内側結果も変わる
--
-- という依存関係がある。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   Employees を主表として見ながら、
--   各行に対して必要な補足情報を
--   Departments から1つ引いてくる
-- ことにある。
--
-- つまり
--   結合で表現できる対応関係
-- を、
--   「列値を引くサブクエリ」
-- として表している。
--
-- これは
--   マスタ表から属性を1つ引く
--   各行ごとに関連値を求める
-- ような場面でよく現れる発想である。
--
-- ただし、発想としては分かりやすい一方で、
--   外側行ごとに内側検索を考える必要がある
-- ため、
--   データ量が大きいと性能面では JOIN の方が有利になりやすい場合もある。
--
-- したがってこのSQLは、
--   相関サブクエリの意味を学ぶ教材
-- として非常に良い。


-- ================================================
-- 注意点
-- ================================================
--
-- このサブクエリは、
--   1つの社員に対して Departments 側から
--   ちょうど1行だけ返る
-- ことを前提にしている。
--
-- 今回は Departments.dept_id が主キーなので、
--   dept_id が一致すれば返る部門名は高々1つ
-- である。
--
-- だからこの形で安全に書ける。
--
-- もし Departments 側で同じ dept_id が重複していたなら、
--   サブクエリが複数行を返してエラー
-- になる可能性がある。
--
-- つまり相関サブクエリで単一値を取りたいときは、
--   内側結果が1行に決まること
-- が非常に重要である。


-- ================================================
-- INNER JOIN との違いを図で整理
-- ================================================
--
--   INNER JOIN の発想
--
--   Employees 行 -----条件一致----- Departments 行
--         |                                |
--         +---------- 行どうしを結ぶ -------+
--
--
--   相関サブクエリの発想
--
--   Employees の1行
--         |
--         v
--   その行の dept_id を使って
--   Departments から値を1つ引く
--         |
--         v
--   dept_name を列として付与する
--
-- つまり
--   INNER JOIN は「行結合」
--   相関サブクエリは「行ごとの値参照」
-- と考えると理解しやすい。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT E.emp_id, E.emp_name, E.dept_id, (...)
--   → 外側の社員情報と、内側で引いた部門名を返す
--
-- FROM Employees E
--   → 外側クエリの基準表
--
-- (SELECT D.dept_name FROM Departments D WHERE E.dept_id = D.dept_id)
--   → 外側の現在社員に対応する部門名を引く相関サブクエリ
--
-- AS dept_name
--   → サブクエリ結果に列名を付ける
--
-- ;
--   → SQL文の終了を表す
-- ================================================


