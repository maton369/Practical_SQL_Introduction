-- ================================================
-- ファイル名: 10_create_digits.sql
-- 役割:
--   0 〜 9 の1桁数字を保持する Digits テーブルを作成する。
--
-- このSQLの目的:
--   後続の自己結合 + クロス結合によって、
--   2桁の数列を生成するための「部品表」を用意すること。
--
-- 学習上の主題:
--   - 自己結合の土台となる単純な表
--   - 1桁の数字を行として持つ発想
--   - 同じ表を複数回使うための基礎
--
-- 今回の最重要ポイント:
--   Digits は単なる数字一覧であるが、
--   同じ表を
--     1回目は1の位の候補
--     2回目は10の位の候補
--   として使うことで、
--   2桁の全組み合わせを作れる。
--
--   つまりこの表は、
--   自己結合の「素材」になる。
-- ================================================

CREATE TABLE Digits
(
    digit INTEGER PRIMARY KEY
    -- digit:
    --   0 〜 9 の1桁数字を表す列である。
    --
    --   今回は各数字を1行として保持する。
    --
    --   例:
    --     0
    --     1
    --     2
    --     ...
    --     9
    --
    --   PRIMARY KEY にしているので、
    --   同じ数字を重複して登録できない。
    --
    --   この表は後続のクエリで
    --     D1.digit
    --     D2.digit
    --   のように別名を付けて同じ表を2回参照する。
    --
    --   その意味で、自己結合の最小例になっている。
);