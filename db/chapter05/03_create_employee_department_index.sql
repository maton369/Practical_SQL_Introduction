-- ================================================
-- ファイル名: 03_create_employee_department_index.sql
-- 役割:
--   Employees.dept_id に索引を作成する。
--
-- このSQLの目的:
--   通常の結合や検索で dept_id を使う場合に備えて、
--   インデックスを用意すること。
--
-- 学習上の主題:
--   - インデックスの役割
--   - 「作ってあるが今回のクロス結合では使わない」列の理解
--
-- 今回の最重要ポイント:
--   このインデックスは dept_id を使った検索や等値結合では有効だが、
--   リスト6.2の CROSS JOIN には結合条件が無い。
--
--   そのため、
--   今回のクロス結合アルゴリズムの本質は
--     インデックス探索
--   ではなく
--     全件どうしの組み合わせ生成
--   にある。
-- ================================================

CREATE INDEX idx_dept_id ON Employees(dept_id);
-- idx_dept_id:
--   Employees の dept_id 列に対する索引である。
--
--   たとえば通常の内部結合
--
--     SELECT ...
--       FROM Employees E
--       JOIN Departments D
--         ON E.dept_id = D.dept_id;
--
--   のような場合には、dept_id による検索補助として意味がある。
--
--   しかし今回の
--
--     Employees CROSS JOIN Departments
--
--   では ON 句が無いため、
--   dept_id の一致判定そのものが存在しない。
--
--   したがってこの索引は、
--   「今回のクロス結合の本質には関与しない」
--   と理解するのが重要である。