-- ================================================
-- ファイル名: 16_select_triangular_join_example.sql
-- 役割:
--   Table_A, Table_B, Table_C の三角結合を行い、
--   A を中心に B と C が同時に一致する組だけを返す。
--
-- このSQLの目的:
--   複数の INNER JOIN が
--   1つの中心表を共有してつながるとき、
--   どういうアルゴリズムで行が絞り込まれるかを理解すること。
--
-- 学習上の主題:
--   - 三角結合とは何か
--   - A-B, A-C の2本の結合条件を同時に満たす意味
--   - 直線的な結合との違い
--   - 複数表結合における「中心表」の考え方
--
-- 今回の最重要ポイント:
--   このSQLは3表結合だが、
--   単に A->B->C と一直線につながっているわけではない。
--
--   条件を見ると
--     A.col_a = B.col_b
--     A.col_a = C.col_c
--   である。
--
--   つまり
--     A が B と結び付き、
--     同時に A が C とも結び付く
--   構造である。
--
--   これを図で書くと
--
--         B
--         |
--         A
--         |
--         C
--
--   のように A を中心に2本の結合条件が伸びる。
--
--   さらに B と C も
--     B.col_b = A.col_a = C.col_c
--   を通して同じ値でつながるので、
--   関係としては三角形的に見ることができる。
--
--   したがってアルゴリズムとしては
--     1. A と B が一致する組を作る
--     2. その組に対して C も A と一致するかを確認する
--     3. 3表すべてが同じキー値でつながる組だけを残す
--   という「共通キーによる3表同時対応付け」になっている。
--
--   これが三角結合の本質である。
-- ================================================

SELECT
       A.col_a,
       -- A.col_a:
       --   中心表 Table_A 側の結合キーを返す。
       --
       --   三角結合ではこの列が
       --     B.col_b
       --     C.col_c
       --   の両方と一致する必要がある。
       --
       --   つまり A.col_a は、
       --   3表を束ねる共通キーの役割を持つ。

       B.col_b,
       -- B.col_b:
       --   Table_B 側の列を返す。
       --
       --   ただし単独では結果に出ず、
       --   A.col_a = B.col_b
       --   を満たした場合だけ出る。
       --
       --   さらに今回のクエリ全体では、
       --   その A が C にも一致していなければ
       --   最終結果には残らない。
       --
       --   つまり B は
       --   A と一致し、
       --   かつその A が C とも一致している
       --   場合にだけ出力される。

       C.col_c
       -- C.col_c:
       --   Table_C 側の列を返す。
       --
       --   これも
       --     A.col_a = C.col_c
       --   を満たす必要がある。
       --
       --   したがって最終結果は
       --     A.col_a
       --     B.col_b
       --     C.col_c
       --   がすべて同じ値を共有している組になる。

  FROM Table_A A
  -- Table_A A:
  --   今回の中心表である。
  --
  --   三角結合では A がハブ(中心点)になっており、
  --   B と C はそれぞれ A に接続される。
  --
  --   そのため、
  --   アルゴリズム上はまず A の各行を起点にして
  --   B と C の対応行を探すと考えると分かりやすい。

       INNER JOIN Table_B B
       -- INNER JOIN Table_B B:
       --   まず A と B を内部結合している。
       --
       --   内部結合なので、
       --   A.col_a = B.col_b
       --   を満たす組だけが残る。
       --
       --   ここでできるのは
       --   「A-B の一致組」
       --   であり、
       --   まだ C の条件は見ていない。

          ON A.col_a = B.col_b
          -- ON A.col_a = B.col_b:
          --   A と B の結合条件である。
          --
          --   A 側の値と B 側の値が一致する場合だけ、
          --   その2行を対応付ける。
          --
          --   したがってここでまず
          --   「同じ値を持つ A と B の組」
          --   が作られる。

              INNER JOIN Table_C C
              -- INNER JOIN Table_C C:
              --   次に、その A-B の組へ C をさらに結合している。
              --
              --   ここでも内部結合なので、
              --   A.col_a = C.col_c
              --   を満たす場合だけ残る。
              --
              --   つまり結果としては
              --     A-B が一致
              --     かつ A-C も一致
              --   を同時に満たす組だけが残る。
              --
              --   見方を変えると、
              --   B と C は直接比較していないが、
              --   両方とも A と一致しているので
              --     B.col_b = A.col_a = C.col_c
              --   が成立する。
              --
              --   これが三角結合の「3者が同じキーで結ばれる」
              --   という意味である。

                 ON A.col_a = C.col_c;
                 -- ON A.col_a = C.col_c:
                 --   A と C の結合条件である。
                 --
                 --   ここで C がさらに絞り込まれるので、
                 --   最終的に残るのは
                 --   A, B, C の3表が同じキー値を共有する組だけである。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Table_A の1行を取る
-- 2. Table_B の中から A.col_a = B.col_b を満たす行を探す
-- 3. その A を基準にして、Table_C の中から A.col_a = C.col_c を満たす行を探す
-- 4. A, B, C が同じ値でつながる組だけを結果として返す
--
-- 概念的には次のような処理に近い。
--
--   for each a in Table_A:
--       for each b in Table_B:
--           if a.col_a == b.col_b:
--               for each c in Table_C:
--                   if a.col_a == c.col_c:
--                       output a.col_a, b.col_b, c.col_c
--
-- つまりこれは、
--   A を中心キーとして
--   B と C を同時に対応付ける
--   3表一致判定アルゴリズムである。
--
-- ポイントは、
--   B と C を直接比較していない
-- にもかかわらず、
--   両方が A と一致することで
--   結果的に3者が同じ値で束ねられていることである。


-- ================================================
-- 三角結合とは何か
-- ================================================
--
-- 三角結合という言い方は、
--   複数テーブルが相互に関連する結合グラフの形
-- をイメージした表現である。
--
-- 今回のSQLでは、
--   A-B
--   A-C
-- の2本の結合条件がある。
--
-- これを関係図として描くと、
--
--       B
--       |
--       A
--       |
--       C
--
-- のように A を中心にした形として理解できる。
--
-- さらに結果としては
--   B.col_b = A.col_a = C.col_c
-- となるため、
--   3者が同じキー値を共有する三角関係
-- とみなせる。
--
-- ここで重要なのは、
--   単に表を3つ並べているのではなく、
--   結合条件のネットワークがどう張られているか
-- がアルゴリズムを決めるということである。
--
-- つまり三角結合は、
--   「どの表がどの表とどうつながるか」
--   をグラフとして理解する題材でもある。


-- ================================================
-- 直線的な多段結合との違い
-- ================================================
--
-- たとえば直線的な結合なら、
--
--   A JOIN B ON ...
--     JOIN C ON B.xxx = C.xxx
--
-- のように、
--   A-B
--   B-C
-- という鎖になる。
--
-- その場合、C は B を介してつながる。
--
-- しかし今回のSQLでは、
--   A-B
--   A-C
-- なので、
--   C は B を介していない。
--
--   A が両方の中心になっている。
--
-- つまり
--
--   直線結合:
--     A -> B -> C
--
--   今回の三角結合的な構造:
--         B
--         |
--         A
--         |
--         C
--
-- という違いがある。
--
-- この違いは、
--   結合条件をどこに書くか
--   どの表を中心キーにするか
-- に現れる。
--
-- 今回は A.col_a が共通軸になっている点が重要である。


-- ================================================
-- 実データが入ったときのイメージ
-- ================================================
--
-- もし仮に次のようなデータが入っていたとする。
--
-- Table_A:
--   A
--   B
--   C
--
-- Table_B:
--   A
--   C
--
-- Table_C:
--   A
--   B
--
-- このとき、
--   A-B 条件を満たすのは
--     A
--     C
-- の候補だが、
--   A-C 条件も同時に満たす必要があるので、
--   最終的に残るのは
--     A
-- だけになる。
--
-- つまり三角結合では、
--   条件が増えるほど
--   「全表で共通するキー」
-- に近いものだけが残る。
--
-- 今回の構造は、
--   3表間の共通値の抽出
-- として理解すると分かりやすい。


-- ================================================
-- 図解: 三角結合
-- ================================================
--
--   Table_B          Table_C
--     col_b            col_c
--       \               /
--        \             /
--         \           /
--          \         /
--           \       /
--            \     /
--             \   /
--              \ /
--             Table_A
--              col_a
--
--   結合条件:
--     A.col_a = B.col_b
--     A.col_a = C.col_c
--
--   つまり、
--   A の値を中心に
--   B と C がぶら下がる構造である。
--
--   結果として残るのは
--     A-B が一致
--     かつ A-C も一致
--   する組だけである。
--
--   言い換えると、
--   A, B, C が同じキー値で束ねられる組だけが残る。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   3つの表を同時に「同じ値」で束ねる
-- ところにある。
--
-- 単純な2表結合では、
--   1本の対応関係
-- だけを見ればよかった。
--
-- しかし三角結合では、
--   A-B の対応
--   A-C の対応
-- を同時に満たす必要がある。
--
-- つまりこれは、
--   複数の結合条件を共有キーで満たす組だけを残す
--   多表整合アルゴリズム
-- である。
--
-- そのため、
--   どの表を中心に置くか
--   どの列を共通キーにするか
-- を意識して読むことが重要になる。
--
-- 今回は A.col_a が共通キーであり、
--   A が中心ノード
-- になっている。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT A.col_a, B.col_b, C.col_c
--   → 3表から対応付いた列を返す
--
-- FROM Table_A A
--   → 中心表 A を指定する
--
-- INNER JOIN Table_B B ON A.col_a = B.col_b
--   → A と B を一致条件で結び付ける
--
-- INNER JOIN Table_C C ON A.col_a = C.col_c
--   → A と C を一致条件で結び付ける
--
-- ;
--   → SQL文の終了を表す
-- ================================================


