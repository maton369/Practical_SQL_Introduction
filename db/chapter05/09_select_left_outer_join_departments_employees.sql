-- ================================================
-- ファイル名: 09_select_left_outer_join_departments_employees.sql
-- 役割:
--   Departments を基準表(マスタ)として Employees と左外部結合し、
--   各部門に所属する社員を対応付けて取得する。
--
-- このSQLの目的:
--   内部結合では「両方の表に対応する行があるもの」だけが返るが、
--   左外部結合では「左側の表の行は必ず残す」ことを確認すること。
--
-- 学習上の主題:
--   - 左外部結合(LEFT OUTER JOIN)の意味
--   - 左側の表を残す、という考え方
--   - 対応する右側の行が無いときに NULL が入ること
--   - 内部結合との違い
--
-- 今回の最重要ポイント:
--   このSQLでは Departments が左側にある。
--
--   したがってアルゴリズムとしては
--     1. Departments の各部門行を必ず1回は結果に出す
--     2. その部門IDに一致する Employees の行があれば結び付ける
--     3. 一致する社員がいなければ、社員側の列を NULL にして残す
--   という処理になる。
--
--   つまり
--     「左の表を全部残したい」
--   ときに使うのが左外部結合である。
-- ================================================

-- 左外部結合の場合（左のテーブルがマスタ）
SELECT
       E.emp_id,
       -- E.emp_id:
       --   Employees 側の社員IDを返す。
       --
       --   ただし左外部結合なので、
       --   対応する社員が存在しない部門行では
       --   この列は NULL になる。
       --
       --   たとえば今回のサンプルでは
       --     dept_id = '13' (営業)
       --   に所属する社員がいないため、
       --   営業の行では emp_id は NULL になる。

       E.emp_name,
       -- E.emp_name:
       --   Employees 側の社員名を返す。
       --
       --   これも対応する社員がいない場合は NULL になる。
       --
       --   左外部結合では、
       --   「右側に対応行が無くても左側は残す」
       --   ので、
       --   NULL が入ることで
       --     「部門は存在するが社員はいない」
       --   状態を表現できる。

       E.dept_id,
       -- E.dept_id:
       --   社員側が持っている所属部門IDを返す。
       --
       --   一致する社員がいる場合は
       --   Departments 側の dept_id と同じ値が入る。
       --
       --   しかし一致する社員がいない場合は、
       --   Employees 側の行そのものが存在しないので、
       --   この列も NULL になる。
       --
       --   注意:
       --   左側の D.dept_id は常に存在するが、
       --   ここで返しているのは E.dept_id なので
       --   未対応部門では NULL になる。

       D.dept_name
       -- D.dept_name:
       --   Departments 側の部門名を返す。
       --
       --   Departments は左側の表なので、
       --   この列は必ず残る。
       --
       --   つまり今回のクエリは、
       --   「全部門一覧をまず出し、
       --     そこに社員がいれば社員情報をぶら下げる」
       --   ような見え方になる。

  FROM Departments D
  -- Departments D:
  --   左側の表であり、今回のマスタ表である。
  --
  --   左外部結合では左側の表の行は必ず結果に残る。
  --
  --   今回のデータでは Departments に
  --     10 総務
  --     11 人事
  --     12 開発
  --     13 営業
  --   の4行があるため、
  --   少なくともこの4種類の部門は結果に現れる。
  --
  --   さらに、部門に複数社員が所属していれば、
  --   その部門行は社員数ぶんだけ複製される。

  LEFT OUTER JOIN Employees E
  -- LEFT OUTER JOIN Employees E:
  --   右側の表として Employees を結合している。
  --
  --   LEFT OUTER JOIN の意味:
  --     左側の表(D)の行は必ず残す
  --     右側の表(E)に一致行があれば結び付ける
  --     無ければ E 側の列を NULL にする
  --
  --   したがって今回の結果は、
  --     「部門を全部出したい」
  --   という要求に向いている。
  --
  --   INNER JOIN なら、
  --   対応社員がいない営業部門は結果から消える。
  --
  --   しかし LEFT OUTER JOIN では、
  --   営業部門は NULL 付きで残る。
  --
  --   ここが内部結合との決定的な違いである。

    ON D.dept_id = E.dept_id;
    -- ON D.dept_id = E.dept_id:
    --   結合条件である。
    --
    --   部門表の dept_id と
    --   社員表の dept_id が一致する行どうしを対応付ける。
    --
    --   たとえば
    --     D.dept_id = '11'
    --   のとき、
    --     E.dept_id = '11'
    --   の社員行（小笠原、夏目）が結び付く。
    --
    --   もし
    --     D.dept_id = '13'
    --   に対して一致する社員がいなければ、
    --   その部門行は削除されず、
    --   E 側だけ NULL で埋められる。
    --
    --   つまりこの ON 句は
    --     「対応付けの条件」
    --   を表しているが、
    --   LEFT OUTER JOIN のため
    --     一致しない左行を捨てない
    --   という点が重要である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Departments の1行を取る
-- 2. Employees の中から dept_id が一致する行を探す
-- 3. 一致する社員がいれば、その社員行と結び付けて出力する
-- 4. 一致する社員がいなければ、Employees 側を NULL にして出力する
-- 5. Departments の全行についてこれを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each department in Departments:
--       matched = false
--
--       for each employee in Employees:
--           if department.dept_id == employee.dept_id:
--               output employee.emp_id,
--                      employee.emp_name,
--                      employee.dept_id,
--                      department.dept_name
--               matched = true
--
--       if matched == false:
--           output NULL, NULL, NULL, department.dept_name
--
-- つまりこれは、
--   「左側全件を必ず出す」
--   条件付き結合アルゴリズムである。
--
-- INNER JOIN との違いは、
--   一致しなかった左行も最後に残す
-- ところにある。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Departments:
--   10 総務
--   11 人事
--   12 開発
--   13 営業
--
-- Employees:
--   001 石田   10
--   002 小笠原 11
--   003 夏目   11
--   004 米田   12
--   005 釜本   12
--   006 岩瀬   12
--
-- 結合条件:
--   D.dept_id = E.dept_id
--
-- このときの対応は次のようになる。
--
-- 1. 総務(10)
--   -> 石田(001) が一致
--
-- 2. 人事(11)
--   -> 小笠原(002), 夏目(003) が一致
--
-- 3. 開発(12)
--   -> 米田(004), 釜本(005), 岩瀬(006) が一致
--
-- 4. 営業(13)
--   -> 一致する社員なし
--   -> NULL, NULL, NULL, 営業
--
-- したがって結果は概ね次のようになる。
--
--   001 石田   10 総務
--   002 小笠原 11 人事
--   003 夏目   11 人事
--   004 米田   12 開発
--   005 釜本   12 開発
--   006 岩瀬   12 開発
--   NULL NULL  NULL 営業
--
-- 重要:
--   部門 13「営業」は社員がいなくても残る。
--
--   これが LEFT OUTER JOIN の最重要性質である。


-- ================================================
-- 図解: 左外部結合
-- ================================================
--
--   左側: Departments                     右側: Employees
--   -----------------                    --------------------
--   10 総務                              001 石田   10
--   11 人事                              002 小笠原 11
--   12 開発                              003 夏目   11
--   13 営業                              004 米田   12
--                                        005 釜本   12
--                                        006 岩瀬   12
--
--         LEFT OUTER JOIN ON D.dept_id = E.dept_id
--
--                         |
--                         v
--
--   総務  -> 石田   が結び付く
--   人事  -> 小笠原, 夏目 が結び付く
--   開発  -> 米田, 釜本, 岩瀬 が結び付く
--   営業  -> 一致する社員がいないので NULL 付きで残る
--
--   結果:
--     001 石田   10 総務
--     002 小笠原 11 人事
--     003 夏目   11 人事
--     004 米田   12 開発
--     005 釜本   12 開発
--     006 岩瀬   12 開発
--     NULL NULL  NULL 営業
--
-- つまり、
--   「左側の表は必ず全部残す」
--   のが左外部結合である。


-- ================================================
-- 内部結合との違い
-- ================================================
--
-- 前回の INNER JOIN では、
--   両側に存在し、しかも条件一致する行だけ
-- が残った。
--
-- そのため
--   13 営業
-- は社員がいないので結果に出なかった。
--
-- しかし今回の LEFT OUTER JOIN では、
--   Departments が左側なので
--   13 営業は必ず残る。
--
-- つまり
--
--   INNER JOIN:
--     一致する組だけ返す
--
--   LEFT OUTER JOIN:
--     一致する組を返しつつ、
--     一致しない左側行も NULL 付きで残す
--
-- という違いがある。
--
-- 今回は「部門マスタを全部見たい」ので、
--   Departments を左に置く
-- ことが重要である。
--
-- もし逆に
--   Employees LEFT OUTER JOIN Departments
-- と書いたら、
--   今度は「社員を全部残す」意味になってしまう。
--
-- つまり左外部結合では
--   どちらを左に置くか
-- がアルゴリズム上きわめて重要である。


-- ================================================
-- 右外部結合との関係
-- ================================================
--
-- コメントには
--   「左外部結合と右外部結合」
-- とあるが、
--   右外部結合(RIGHT OUTER JOIN)
-- は
--     右側の表を必ず残す
--   結合である。
--
-- したがって今回の SQL
--
--   Departments D LEFT OUTER JOIN Employees E
--
-- は、
--
--   Employees E RIGHT OUTER JOIN Departments D
--
-- と考えることもできる。
--
-- つまり
--   左外部結合と右外部結合は
--   左右を入れ替えることで対応する。
--
-- ただし読む側にとっては、
--   「何を必ず残したいか」
-- を左側に置いて LEFT OUTER JOIN と書く方が
--   直感的で分かりやすいことが多い。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   Departments をマスタとして全件残し、
--   社員が存在する部門には社員情報を付け、
--   存在しない部門には NULL を付ける
-- ところにある。
--
-- つまりこれは、
--   「存在しないことも情報として残したい」
--   結合である。
--
-- 内部結合では
--   存在しない部門所属
-- は結果に出ない。
--
-- しかし左外部結合では
--   社員がいない部門も結果に見える。
--
-- そのため、
--   マスタ表の欠損確認
--   未割当の部門の発見
--   対応漏れのチェック
-- などに非常に有効である。
--
-- 今回でいえば、
--   営業部門に社員がいない
-- という事実を結果から読み取れる。
--
-- これが外部結合の価値である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT E.emp_id, E.emp_name, E.dept_id, D.dept_name
--   → 社員側列と部門名を返す
--
-- FROM Departments D
--   → 左側の基準表(必ず残す表)を指定する
--
-- LEFT OUTER JOIN Employees E
--   → 右側の表を結合し、一致しなくても左行は残す
--
-- ON D.dept_id = E.dept_id
--   → 部門IDが一致する行どうしを対応付ける
--
-- ;
--   → SQL文の終了を表す
-- ================================================


