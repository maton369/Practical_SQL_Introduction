-- ================================================
-- ファイル名: 07_select_inner_join_employees_departments.sql
-- 役割:
--   Employees と Departments を内部結合し、
--   各社員に対応する正しい部門名を取得する。
--
-- このSQLの目的:
--   社員テーブルには部門IDしか入っていないため、
--   その dept_id を手がかりに Departments と結び付けて、
--   部門名まで含んだ結果を返すこと。
--
-- 学習上の主題:
--   - 内部結合(INNER JOIN)の意味
--   - ON 句による結合条件
--   - CROSS JOIN との違い
--   - 「対応する行だけを残す」アルゴリズムの理解
--
-- 今回の最重要ポイント:
--   内部結合は
--     左表の各行と右表の各行を全部掛け合わせる
--   のではなく、
--     ON 句の条件を満たす組だけを残す
--   結合である。
--
--   今回は
--     E.dept_id = D.dept_id
--   が条件なので、
--   各社員は「自分が所属する部門」とだけ結び付く。
--
--   つまりアルゴリズムとしては
--     1. Employees の各社員行を見る
--     2. Departments の中から dept_id が一致する行を探す
--     3. 一致した組だけを結果として返す
--   という「対応付け型」の結合になっている。
--
--   これが内部結合の本質である。
-- ================================================

SELECT
       E.emp_id,
       -- E.emp_id:
       --   社員IDを返す。
       --
       --   Employees 側の主キーであり、
       --   どの社員の情報かを識別する。

       E.emp_name,
       -- E.emp_name:
       --   社員名を返す。
       --
       --   部門名と対応付けることで、
       --   「誰がどの部門に所属しているか」
       --   を読みやすく表示できる。

       E.dept_id,
       -- E.dept_id:
       --   社員が持っている所属部門IDを返す。
       --
       --   この列が Departments.dept_id と一致することで、
       --   部門テーブル上のどの行と結び付くかが決まる。
       --
       --   つまり内部結合のキーである。

       D.dept_name
       -- D.dept_name:
       --   部門表から取得した部門名を返す。
       --
       --   Employees だけでは dept_id しか分からないが、
       --   Departments と結合することで
       --   人間に分かりやすい部門名へ変換できる。
       --
       --   これが今回の結合の直接的な目的である。

  FROM Employees E
       -- Employees E:
       --   左側の結合元である。
       --
       --   各社員行を起点として、
       --   それに対応する部門行を Departments 側から探す。
       --
       --   E は Employees の別名(alias)であり、
       --   列参照を
       --     E.emp_id
       --     E.emp_name
       --     E.dept_id
       -- のように短く書くために付けている。

       INNER JOIN Departments D
       -- INNER JOIN Departments D:
       --   右側の結合先である。
       --
       --   Departments の各行のうち、
       --   ON 句の条件を満たす行だけが
       --   Employees の行と結び付く。
       --
       --   D は Departments の別名であり、
       --   列参照を
       --     D.dept_id
       --     D.dept_name
       -- のように簡潔に書ける。
       --
       -- INNER JOIN の意味:
       --   内部結合は
       --     両方の表に存在し、
       --     かつ結合条件を満たす行だけ
       --   を結果に残す。
       --
       --   したがって、対応する部門が存在しない社員は結果から消えるし、
       --   対応する社員がいない部門も結果には出ない。
       --
       --   今回のサンプルでは Departments に
       --     13 営業
       --   があるが、
       --   Employees 側に dept_id='13' の社員がいないため、
       --   営業部門は結果に出てこない。
       --
       --   これが内部結合の重要な性質である。

    ON E.dept_id = D.dept_id;
    -- ON E.dept_id = D.dept_id:
    --   ここが内部結合の核心である。
    --
    --   「Employees の部門ID」と
    --   「Departments の部門ID」が一致する行だけを
    --   対応付ける。
    --
    --   つまり
    --     石田   10
    --   は
    --     10 総務
    --   とだけ結び付き、
    --
    --     石田   10
    --   が
    --     11 人事
    --   や
    --     12 開発
    --   と結び付くことはない。
    --
    --   ここが CROSS JOIN との決定的な違いである。
    --
    --   CROSS JOIN:
    --     条件なしで全部を掛け合わせる
    --
    --   INNER JOIN:
    --     ON 句の条件に一致する組だけ残す
    --
    --   つまり今回の内部結合は、
    --   直積を作ってから条件で絞ると考えてもよいが、
    --   本質的には
    --     「対応する部門を探して結ぶ」
    --   結合である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Employees の各行を1件ずつ見る
-- 2. Departments の中から dept_id が一致する行を探す
-- 3. 一致した組だけを結果に残す
-- 4. E.emp_id, E.emp_name, E.dept_id, D.dept_name を返す
--
-- 概念的には次のような処理に近い。
--
--   for each employee in Employees:
--       for each department in Departments:
--           if employee.dept_id == department.dept_id:
--               output employee.emp_id,
--                      employee.emp_name,
--                      employee.dept_id,
--                      department.dept_name
--
-- つまりこれは、
--   「条件を満たす対応だけを残す」
--   フィルタ付き組み合わせ生成アルゴリズムである。
--
-- CROSS JOIN との比較で言えば、
--   CROSS JOIN が「全部を出す」のに対し、
--   INNER JOIN は「一致するものだけを出す」。
--
-- この違いが最重要である。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Employees:
--   001 石田   10
--   002 小笠原 11
--   003 夏目   11
--   004 米田   12
--   005 釜本   12
--   006 岩瀬   12
--
-- Departments:
--   10 総務
--   11 人事
--   12 開発
--   13 営業
--
-- 結合条件:
--   E.dept_id = D.dept_id
--
-- この条件で対応付けると、
--
--   001 石田   10 -> 10 総務
--   002 小笠原 11 -> 11 人事
--   003 夏目   11 -> 11 人事
--   004 米田   12 -> 12 開発
--   005 釜本   12 -> 12 開発
--   006 岩瀬   12 -> 12 開発
--
-- となる。
--
-- 一方、
--   13 営業
-- は対応する社員がいないので結果に出ない。
--
-- つまり返る結果は概ね次の6行である。
--
--   001 石田   10 総務
--   002 小笠原 11 人事
--   003 夏目   11 人事
--   004 米田   12 開発
--   005 釜本   12 開発
--   006 岩瀬   12 開発
--
-- 行数も重要である。
--
-- Employees は 6 行、
-- Departments は 4 行だが、
-- INNER JOIN の結果は 6 × 4 = 24 行にはならない。
--
-- なぜなら、
--   結合条件を満たす組だけ
-- に減らされるからである。
--
-- 今回は各社員に対応する部門がちょうど1つあるので、
-- 結果は Employees の行数と同じ 6 行になる。


-- ================================================
-- 図解: 内部結合
-- ================================================
--
--   Employees                         Departments
--   ----------                        -----------
--   001 石田   10                     10 総務
--   002 小笠原 11                     11 人事
--   003 夏目   11                     12 開発
--   004 米田   12                     13 営業
--   005 釜本   12
--   006 岩瀬   12
--
--             INNER JOIN ON E.dept_id = D.dept_id
--
--                     |
--                     v
--
--   001 石田   10  ×  10 総務   -> 一致するので残す
--   002 小笠原 11  ×  11 人事   -> 一致するので残す
--   003 夏目   11  ×  11 人事   -> 一致するので残す
--   004 米田   12  ×  12 開発   -> 一致するので残す
--   005 釜本   12  ×  12 開発   -> 一致するので残す
--   006 岩瀬   12  ×  12 開発   -> 一致するので残す
--
--   13 営業は社員側に一致が無いので結果には出ない
--
-- つまり、
--   「部門IDが一致した組だけ」
--   を返している。


-- ================================================
-- CROSS JOIN との違い
-- ================================================
--
-- 前回の CROSS JOIN では
--
--   for each employee:
--       for each department:
--           output (employee, department)
--
-- だった。
--
-- つまり石田さんは
--   総務
--   人事
--   開発
--   営業
-- の全部と結び付いていた。
--
-- しかし今回の INNER JOIN では
--
--   for each employee:
--       for each department:
--           if employee.dept_id == department.dept_id:
--               output (employee, department)
--
-- となる。
--
-- したがって石田さんは
--   dept_id = '10'
-- なので
--   総務
-- とだけ結び付く。
--
-- つまり
--
--   CROSS JOIN:
--     条件なしで全組み合わせ
--
--   INNER JOIN:
--     結合条件に一致する組だけ
--
-- という違いがある。
--
-- 内部結合は、
--   対応関係を表したいとき
-- に使う基本的な結合である。


-- ================================================
-- インデックス idx_dept_id との関係
-- ================================================
--
-- Employees には
--
--   CREATE INDEX idx_dept_id ON Employees(dept_id);
--
-- が作られている。
--
-- dept_id は今回まさに結合条件に使われている列なので、
-- データ量が増えた場合には
--   結合相手探索の補助
-- として効く可能性がある。
--
-- 前回の CROSS JOIN では dept_id を条件に使っていなかったため、
-- インデックスの意味は薄かった。
--
-- しかし今回の INNER JOIN では、
--   E.dept_id = D.dept_id
--   という等値条件
-- があるため、
--   「一致する行だけ探す」
-- という内部結合の本質とインデックスの役割が噛み合っている。
--
-- つまり
--   CROSS JOIN は全組み合わせ生成
--   INNER JOIN は一致行探索
-- なので、
--   索引の価値は INNER JOIN の方で出やすい。


-- ================================================
-- 内部結合の本質
-- ================================================
--
-- 内部結合の本質は、
--   2つの表の間にある対応関係を、
--   ON 句の条件で明示し、
--   その条件を満たす行だけを取り出す
-- ことにある。
--
-- 今回でいえば
--   社員が所属している部門ID
-- と
--   部門表に書かれた部門ID
-- が対応関係である。
--
-- つまりこのSQLは、
--   社員表だけでは読めない「部門名」を、
--   部門表から引いてきて補完している。
--
-- 言い換えると、
--   キー(dept_id)を使って
--   正しいマスタ情報(dept_name)を結び付ける
-- 処理である。
--
-- これが実務で内部結合が多用される理由である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT E.emp_id, E.emp_name, E.dept_id, D.dept_name
--   → 必要な列だけを明示して返す
--
-- FROM Employees E
--   → 左側の表を指定する
--
-- INNER JOIN Departments D
--   → 右側の表を指定し、条件付きで結合する
--
-- ON E.dept_id = D.dept_id
--   → 部門IDが一致する行だけを結ぶ
--
-- ;
--   → SQL文の終了を表す
-- ================================================


-- ================================================
-- 補足1:
--   今回は Employees 側の dept_id が全て Departments に存在するため、
--   結果行数は Employees の行数と同じになる。
--
--   もし存在しない dept_id を持つ社員がいた場合、
--   その社員は内部結合結果から消える。
-- ================================================


-- ================================================
-- 補足2:
--   部門 13「営業」は Departments に存在するが、
--   それに属する社員がいないため結果に出ない。
--
--   これも内部結合の重要な性質である。
-- ================================================


-- ================================================
-- 補足3:
--   「社員を全部残したいが、対応する部門が無い場合は NULL でもよい」
--   という場合は、内部結合ではなく LEFT JOIN を使う。
--
--   つまり内部結合は
--   「両側に存在する対応だけが欲しい」
--   ときの結合である。
