-- ================================================
-- ファイル名: 01_create_employees.sql
-- 役割:
--   社員テーブル Employees を作成する。
--
-- このSQLの目的:
--   各社員について
--     社員ID
--     社員名
--     所属部門ID
--   を保持する表を定義すること。
--
-- 学習上の主題:
--   - 主キー制約
--   - 結合元テーブルの構造理解
--   - 後続のクロス結合における「左側の表」の意味
--
-- 今回の最重要ポイント:
--   後で CROSS JOIN を行うとき、
--   Employees の各行は Departments の全行と組み合わされる。
--
--   つまり Employees は
--   「掛け合わせの片側の集合」
--   として使われる。
-- ================================================

CREATE TABLE Employees
(
    emp_id CHAR(8),
    -- emp_id:
    --   社員を一意に識別するIDである。
    --
    --   例:
    --     '001'
    --     '002'
    --     '003'
    --
    --   この列が主キーになるため、
    --   同じ社員IDは重複できない。

    emp_name VARCHAR(32),
    -- emp_name:
    --   社員名である。
    --
    --   例:
    --     石田
    --     小笠原
    --     夏目
    --
    --   今回のクロス結合では、
    --   各社員名が全部門と組み合わされて結果に現れる。

    dept_id CHAR(2),
    -- dept_id:
    --   その社員が所属している部門IDである。
    --
    --   例:
    --     '10'
    --     '11'
    --     '12'
    --
    --   注意:
    --   今回のリスト6.2の CROSS JOIN では
    --   この dept_id を結合条件には使わない。
    --
    --   つまり、
    --   社員の所属部門と一致する行だけを結ぶのではなく、
    --   部門表の全行と機械的に掛け合わせる。

    CONSTRAINT pk_emp PRIMARY KEY(emp_id)
    -- PRIMARY KEY(emp_id):
    --   社員IDを主キーとする。
    --
    --   Employees は 1人につき1行という設計であり、
    --   各社員を一意に扱うための制約である。
);