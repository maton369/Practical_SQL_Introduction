-- データの見方
-- ================================================
--
-- Employees の行数:
--   6 行
--
-- 社員一覧:
--   001 石田   10
--   002 小笠原 11
--   003 夏目   11
--   004 米田   12
--   005 釜本   12
--   006 岩瀬   12
--
-- 今回のクロス結合では、
--   各社員行が Departments の全部門行と組み合わされる。
--
-- たとえば石田さん 1人だけを見ても、
--   石田 × 総務
