-- このSQLの目的:
--   クロス結合の結果件数や組み合わせを具体的に確認できるようにすること。
--
-- 学習上の主題:
--   - 行数がクロス結合結果件数にどう影響するか
--   - 元表の各行が「組み合わせの素材」になること
--
-- 今回の最重要ポイント:
--   Employees には 6 行入る。
--
--   後で Departments が 4 行ある状態で CROSS JOIN を行うと、
--   結果行数は
--
--     6 × 4 = 24 行
--
--   になる。
--
--   つまりクロス結合では、
--   左表の行数 × 右表の行数
--   が結果件数になる。
-- ================================================

