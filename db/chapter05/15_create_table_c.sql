-- ================================================
-- ファイル名: 15_create_table_c.sql
-- 役割:
--   三角結合の説明に使う Table_C を作成する。
--
-- このSQLの目的:
--   Table_A と結び付く3つ目の表を用意し、
--   三角結合のもう片側の辺を表現すること。
--
-- 学習上の主題:
--   - A と C の対応関係
--   - A を中心に B と C がぶら下がる構造
--
-- 今回の最重要ポイント:
--   Table_C も Table_B と同様に、
--   単独ではなく
--     A.col_a = C.col_c
--   という条件で A に接続される。
--
--   そのため三角結合では、
--   A-B の一致だけでなく
--   A-C の一致も同時に満たす必要がある。
-- ================================================

CREATE TABLE Table_C
(
    col_c CHAR(1)
    -- col_c:
    --   Table_C 側の結合キー候補である。
    --
    --   後続のクエリでは
    --     A.col_a = C.col_c
    --   に使われる。
    --
    --   つまり C も
    --   A と一致したときだけ結果に残る。
);