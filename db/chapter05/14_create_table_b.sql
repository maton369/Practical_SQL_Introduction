-- 役割:
--   三角結合の説明に使う Table_B を作成する。
--
-- このSQLの目的:
--   Table_A と結び付く2つ目の表を用意し、
--   三角結合の片側の辺を表現すること。
--
-- 学習上の主題:
--   - A と B の対応関係
--   - 複数の INNER JOIN の一部としての表の役割
--
-- 今回の最重要ポイント:
--   Table_B は単独で使うのではなく、
--   A.col_a = B.col_b
--   という条件で Table_A と結び付く。
--
--   三角結合では、
--   B は A の片側にあるノードと考えると分かりやすい。
-- ================================================

CREATE TABLE Table_B
(
    col_b CHAR(1)
    -- col_b:
    --   Table_B 側の結合キー候補である。
    --
    --   後続のクエリでは
    --     A.col_a = B.col_b
    --   に使われる。
    --
    --   つまり B は
    --   A と一致したときだけ結果に残る。
);