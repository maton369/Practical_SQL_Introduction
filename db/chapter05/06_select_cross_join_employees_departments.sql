-- ファイル名: 06_select_cross_join_employees_departments.sql
-- 役割:
--   Employees と Departments をクロス結合し、
--   全組み合わせを返す。
--
-- このSQLの目的:
--   クロス結合の基本動作、
--   すなわち
--     左表の各行 × 右表の各行
--   の全組み合わせを理解すること。
--
-- 学習上の主題:
--   - CROSS JOIN の意味
--   - 結合条件の無い結合
--   - 直積(cartesian product, Cartesian product)の考え方
--   - 行数の増え方
--
-- 今回の最重要ポイント:
--   クロス結合は
--     条件で対応する行を探す結合
--   ではなく、
--     2つの表の全行どうしを機械的に組み合わせる結合
--   である。
--
--   したがって今回のアルゴリズムは
--     1. Employees の各行を1つ取る
--     2. その社員行に対して Departments の全行を順に組み合わせる
--     3. 次の社員行でも同じことを繰り返す
--   という直積生成になっている。
--
--   つまり
--     社員数 6
--     部門数 4
--   なら
--     6 × 4 = 24 行
--   が結果になる。
--
--   これがクロス結合の本質である。
-- ================================================

SELECT *
  FROM Employees
       CROSS JOIN
       Departments;
-- SELECT *:
--   両テーブルの全列を返す。
--
--   したがって結果には
--     Employees.emp_id
--     Employees.emp_name
--     Employees.dept_id
--     Departments.dept_id
--     Departments.dept_name
--   が並ぶ。
--
--   注意:
--   同名列 dept_id が両表に存在するため、
--   SELECT * では結果に dept_id が2列現れる。
--   学習用としては分かりやすいが、
--   実務では列名を明示した方が見やすいことが多い。

-- FROM Employees CROSS JOIN Departments:
--   ここが今回の主役である。
--
--   CROSS JOIN は、結合条件 ON を持たない。
--   つまり
--     Employees.dept_id = Departments.dept_id
--   のような対応付けを一切しない。
--
--   代わりに、
--   Employees の各行と Departments の各行を
--   すべて組み合わせる。
--
--   これは数学でいう
--     直積
--   に対応する。
--
--   したがって、
--   アルゴリズム的には次のようになる。
--
--     for each employee row e in Employees:
--         for each department row d in Departments:
--             output (e, d)
--
--   つまり二重ループ的な全組み合わせ生成である。
--
--   今回のデータ数では
--     Employees = 6 行
--     Departments = 4 行
--   なので、
--     24 行
--   が返る。
--
--   これを具体的に見ると、
--
--   石田:
--     石田 × 総務
--     石田 × 人事
--     石田 × 開発
--     石田 × 営業
--
--   小笠原:
--     小笠原 × 総務
--     小笠原 × 人事
--     小笠原 × 開発
--     小笠原 × 営業
--
--   ...
--
--   のように展開される。
--
--   重要:
--   Employees.dept_id が '10' だからといって
--   Departments.dept_id='10' の行だけに限定はされない。
--
--   たとえば石田さんは dept_id='10' だが、
--   クロス結合結果には
--     石田 × 総務
--     石田 × 人事
--     石田 × 開発
--     石田 × 営業
--   の全部が出る。
--
--   つまりクロス結合は
--     「対応するものを結ぶ」
--   のではなく、
--     「全部を掛け合わせる」
--   結合である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Employees の1行目を取る
-- 2. その行に Departments の全4行を順に組み合わせる
