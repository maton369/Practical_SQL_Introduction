-- 4. その行にも Departments の全4行を順に組み合わせる
-- 5. これを Employees の全6行について繰り返す
-- 6. 全組み合わせ 24 行を返す
--
-- 概念的には次のような処理に近い。
--
--   for each employee in Employees:
--       for each department in Departments:
--           output employee, department
--
-- つまりこれは、
--   2つの集合の直積を作る
--   クロス結合アルゴリズムである。


