-- ================================================
-- クロス結合の本質
-- ================================================
--
-- クロス結合の本質は、
--   2つの表から「対応するもの」を見つけることではなく、
--   2つの表の全組み合わせを生成すること
-- にある。
--
-- 通常の内部結合は
--   ON E.dept_id = D.dept_id
-- のように条件を使って
--   意味のある対応
-- を取る。
--
-- しかしクロス結合は条件を持たないので、
--   対応の有無を考えない。
--
-- そのため
--   行数が急激に増えやすい
-- のが特徴である。
--
-- 今回のような小さい表なら問題ないが、
--   1000 行 × 1000 行
-- なら
--   100 万行
-- になる。
--
-- つまりクロス結合は強力だが、
--   使いどころを誤ると巨大結果を生む
--   という意味でも学習価値が高い。


-- ================================================
-- INNER JOIN との違い
-- ================================================
--
-- もし今回、
--
--   SELECT *
--     FROM Employees E
--     JOIN Departments D
--       ON E.dept_id = D.dept_id;
--
-- のように書いたなら、
--   社員の所属部門と一致する部門だけが返る。
--
-- その場合、石田さんは
--   総務
-- とだけ結びつく。
--
-- しかし今回の CROSS JOIN では、
--   石田さんは総務・人事・開発・営業の全部と結びつく。
--
-- つまり
--
--   INNER JOIN:
--     条件で対応を絞る
--
--   CROSS JOIN:
--     条件無しで全部を掛け合わせる
--
-- という違いがある。
--
-- ここを混同しないことが非常に重要である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT *
--   → 両表の全列を返す
--
-- FROM Employees
--   → 左側の集合を指定する
--
-- CROSS JOIN Departments
--   → 右側の集合を指定し、左側全行との直積を作る
--
-- ;
--   → SQL文の終了を表す
-- ================================================


