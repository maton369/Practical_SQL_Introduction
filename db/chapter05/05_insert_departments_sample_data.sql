-- ================================================
--
-- Departments の行数:
--   4 行
--
-- 部門一覧:
--   10 総務
--   11 人事
--   12 開発
--   13 営業
--
-- 今回のクロス結合では、
--   この 4 行すべてが
--   各社員行ごとに複製されるような形で組み合わされる。
--
-- たとえば Departments 側から見ると、
--   総務 は全社員 6 人と組み合わされる。
