-- 学習上の主題:
--   - 右表の行数がクロス結合結果件数にどう影響するか
--   - 部門表の各行が全社員へ展開されること
--
-- 今回の最重要ポイント:
--   Departments には 4 行入る。
--
--   したがって Employees 6 行とのクロス結合結果は
--
--     6 × 4 = 24 行
--
--   になる。
--
--   これは「所属部門が一致するものだけ」を選ぶ結合ではなく、
--   「存在する全部門候補を全社員へ付ける」ような組み合わせ生成である。
-- ================================================

