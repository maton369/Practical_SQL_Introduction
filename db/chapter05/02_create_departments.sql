-- ================================================
-- ファイル名: 02_create_departments.sql
-- 役割:
--   部門テーブル Departments を作成する。
--
-- このSQLの目的:
--   各部門について
--     部門ID
--     部門名
--   を保持する表を定義すること。
--
-- 学習上の主題:
--   - 主キー制約
--   - 結合先テーブルの構造理解
--   - 後続のクロス結合における「右側の表」の意味
--
-- 今回の最重要ポイント:
--   CROSS JOIN では、
--   Departments の各行が Employees の全行と組み合わされる。
--
--   つまり Departments も
--   「掛け合わせの片側の集合」
--   として扱われる。
-- ================================================

CREATE TABLE Departments
(
    dept_id CHAR(2),
    -- dept_id:
    --   部門を識別するIDである。
    --
    --   例:
    --     '10'
    --     '11'
    --     '12'
    --     '13'
    --
    --   通常の内部結合なら、
    --   Employees.dept_id と Departments.dept_id を突き合わせる
    --   条件に使うことが多い。
    --
    --   しかし今回のクロス結合では、
    --   この列を一致条件には使わない。

    dept_name VARCHAR(32),
    -- dept_name:
    --   部門名である。
    --
    --   例:
    --     総務
    --     人事
    --     開発
    --     営業
    --
    --   クロス結合後は、
    --   各社員に対して全ての部門名が順番に組み合わさって現れる。

    CONSTRAINT pk_dep PRIMARY KEY(dept_id)
    -- PRIMARY KEY(dept_id):
    --   部門IDを主キーとする。
    --
    --   Departments は 1部門につき1行という設計である。
);