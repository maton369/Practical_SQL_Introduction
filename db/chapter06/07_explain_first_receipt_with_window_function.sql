-- ================================================
-- ファイル名: 07_explain_first_receipt_with_window_function.sql
-- 役割:
--   リスト7.4のウィンドウ関数版について、
--   MySQL の実行計画を確認する。
--
-- このSQLの目的:
--   DBMS が
--     派生表 WORK
--     ROW_NUMBER() の計算
--     PARTITION BY / ORDER BY の処理
--     外側の row_seq = 1 の絞り込み
--   をどう扱うかを見ること。
--
-- 学習上の主題:
--   - EXPLAIN の見方
--   - 派生表(DERIVED)の扱い
--   - ウィンドウ関数でソートや一時領域が必要になりやすいこと
--   - 相関サブクエリ版との構造比較
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   各行へ順位を付けるために
--     PARTITION BY cust_id
--     ORDER BY seq
--   が必要になる。
--
--   そのため実行計画では、
--   派生表の生成や並び替え、
--   あるいは temporary / filesort 的な処理が現れることがある。
--
--   つまり
--     「外側行ごとに問い合わせる」
--   相関サブクエリ版とは違い、
--     「一度全体へ順位を付けた中間結果を作る」
--   形として見えやすい。
-- ================================================

EXPLAIN
SELECT
       cust_id,
       seq,
       price
  FROM
       (
           SELECT
                  cust_id,
                  seq,
                  price,
                  ROW_NUMBER()
                  OVER (
                      PARTITION BY cust_id
                      ORDER BY seq
                  ) AS row_seq
             FROM Receipts
       ) WORK
 WHERE WORK.row_seq = 1;

-- ================================================
-- 実行計画で見たいポイント
-- ================================================
--
-- 1. 派生表 WORK の生成
--   このクエリは
--     内側で順位付き中間表を作る
--   構造なので、
--   EXPLAIN では
--     DERIVED
--   のような形で見えることがある。
--
-- 2. ウィンドウ関数の ORDER BY
--   ROW_NUMBER() OVER(PARTITION BY cust_id ORDER BY seq)
--   を計算するには、
--   顧客ごとに seq 順の並びを意識する必要がある。
--
--   そのため
--     Using filesort
--     Using temporary
--   などが現れる可能性がある。
--
-- 3. 外側 WHERE row_seq = 1
--   派生表 WORK を作ったあとで、
--   その中から 1位の行だけを残す。
--
--   つまり
--     先に順位付け
--     後でフィルタ
--   という二段構造になっている。
--
-- 4. 相関サブクエリ版との比較
--   相関サブクエリ版:
--     外側全件 + 内側依存問い合わせ
--
--   ウィンドウ関数版:
--     先に全体へ順位付けした派生表を作る
--
--   つまり
--     各行判定型
--   と
--     一括順位付け型
--   の違いが実行計画にも現れやすい。


-- ================================================
-- 読み方の目安
-- ================================================
--
-- もし
--   PRIMARY
--   DERIVED
-- のような区別が出れば、
--   派生表 WORK を作ってから
--   外側がそれを読む構造だと理解しやすい。
--
-- もし Extra に
--   Using temporary
--   Using filesort
-- のような情報が出れば、
--   ウィンドウ関数の並び順計算や
--   派生表生成に伴う処理の可能性が高い。
--
-- 重要なのは、
--   このSQLが
--     「各行ごとに問い合わせる」のではなく、
--     「全体へ順位を付けてから1位を選ぶ」
--   構造である点である。
--
--   実行計画を見ると、
--   その発想の違いが
--   相関サブクエリ版との比較でよく分かる。


