-- ================================================
-- ファイル名: 05_explain_first_receipt_with_correlated_subquery.sql
-- 役割:
--   リスト7.3の相関サブクエリ版について、
--   MySQL の実行計画を確認する。
--
-- このSQLの目的:
--   DBMS が
--     外側クエリ
--     内側の相関サブクエリ
--   をどう処理しているかを見ること。
--
-- 学習上の主題:
--   - EXPLAIN の見方
--   - PRIMARY と DEPENDENT SUBQUERY / SUBQUERY などの違い
--   - 相関サブクエリが外側行に依存する構造
--   - 主キー (cust_id, seq) がどこで意味を持つか
--
-- 今回の最重要ポイント:
--   このSQLでは内側サブクエリが
--     R1.cust_id
--   を参照している。
--
--   そのため実行計画では、
--   内側が外側に依存して評価される形が見えやすい。
--
--   つまり
--     外側の各行に対して内側条件が変わる
--   という相関サブクエリの本質が、
--   実行計画にも現れる可能性がある。
-- ================================================

EXPLAIN
SELECT
       cust_id,
       seq,
       price
  FROM Receipts R1
 WHERE seq =
       (
           SELECT MIN(seq)
             FROM Receipts R2
            WHERE R1.cust_id = R2.cust_id
       );

-- ================================================
-- 実行計画で見たいポイント
-- ================================================
--
-- 1. 外側クエリ R1 の読み方
--   Receipts 全体を候補として見ているか、
--   あるいは索引をどう使っているかを見る。
--
-- 2. 内側サブクエリ R2 の select_type
--   相関サブクエリであるため、
--   実行計画上は
--     DEPENDENT SUBQUERY
--   のように見えることがある。
--
--   これは
--     「外側行に依存して毎回評価される」
--   ことを表している。
--
-- 3. cust_id による絞り込み
--   内側では
--     WHERE R1.cust_id = R2.cust_id
--   があるので、
--   R2 側では cust_id を使った絞り込みが中心になる。
--
-- 4. MIN(seq) の求め方
--   内側ではその顧客について最小 seq を求めている。
--   主キーが (cust_id, seq) なので、
--   条件次第ではこの複合キーの並びが意味を持つ可能性がある。
--
-- つまりこの実行計画では、
--   「各外側行に対して、同じ顧客の最小 seq を内側で探す」
--   という相関サブクエリの構造が
--   どう表現されるかを見るのが重要である。


-- ================================================
-- 読み方の目安
-- ================================================
--
-- もし実行計画で
--   PRIMARY
--   DEPENDENT SUBQUERY
-- のような区別が出れば、
--   外側クエリと内側の相関サブクエリが
--   分かれて評価されていると理解しやすい。
--
-- もし type が
--   ALL
--   ref
--   range
--   index
-- などになっていれば、
--   外側・内側それぞれが
--   どの程度効率よく行にアクセスしているかの手掛かりになる。
--
-- 特に今回の相関サブクエリは、
--   外側行が変わるたびに内側も意味が変わる
-- ので、
--   単純な独立サブクエリとは見え方が違う可能性が高い。


