-- ================================================
-- ファイル名: 12_create_companies.sql
-- 役割:
--   会社テーブル Companies を作成する。
--
-- このSQLの目的:
--   各会社について
--     会社コード
--     地区
--   を保持する表を定義すること。
--
-- 学習上の主題:
--   - マスタ表の役割
--   - 後続の JOIN における「会社側」の意味
--   - 集約前後で JOIN の相手になる基準表
--
-- 今回の最重要ポイント:
--   後続のSQLでは、
--   Companies は集約対象ではなく
--   「会社情報を付与する側」の表である。
--
--   つまり主な論点は
--     Shops を先に集約するか
--     先に Companies と JOIN するか
--   であり、
--   Companies 自体は比較的安定したマスタ表として振る舞う。
-- ================================================

CREATE TABLE Companies
(
    co_cd CHAR(3) NOT NULL,
    -- co_cd:
    --   会社コードである。
    --
    --   例:
    --     '001'
    --     '002'
    --     '003'
    --     '004'
    --
    --   後続の JOIN では、
    --   Shops.co_cd と結び付けるキーになる。

    district CHAR(1) NOT NULL,
    -- district:
    --   会社の地区を表す属性である。
    --
    --   集約対象ではないが、
    --   最終結果で会社コードに付随する属性として表示する。

    CONSTRAINT pk_Companies PRIMARY KEY (co_cd)
    -- PRIMARY KEY(co_cd):
    --   会社コードを主キーとする。
    --
    --   したがって co_cd ごとに1会社が一意に決まる。
    --
    --   これにより、
    --   Shops 側から co_cd で JOIN したとき
    --   会社情報は1つに定まる。
);