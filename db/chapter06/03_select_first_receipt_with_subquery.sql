-- ================================================
-- ファイル名: 03_select_first_receipt_with_subquery.sql
-- 役割:
--   サブクエリを使って、
--   顧客ごとの最初の購入明細を抽出する。
--
-- このSQLの目的:
--   Receipts から
--     cust_id ごとに最小の seq
--   をまずサブクエリで求め、
--   その結果を元の Receipts と結び付けることで
--   最初の購入明細の完全な行
--     cust_id
--     seq
--     price
--   を取得すること。
--
-- 学習上の主題:
--   - サブクエリの役割
--   - 集約結果を中間表として使う発想
--   - 集約だけでは取れない列を元表へ戻って取得する方法
--   - INNER JOIN とサブクエリの組み合わせ
--
-- 今回の最重要ポイント:
--   サブクエリは
--     顧客ごとの最小 seq
--   だけを返す。
--
--   しかし欲しい最終結果は
--     price も含む元の明細行
--   である。
--
--   そのためアルゴリズムとしては
--     1. サブクエリで cust_id ごとの min_seq を求める
--     2. その中間結果を元表 Receipts に結び付ける
--     3. 一致した元行を取り出す
--   という2段階になる。
--
--   つまりこのSQLの本質は、
--     「サブクエリで要約し、その要約結果を手掛かりに元表へ戻る」
--   ところにある。
-- ================================================

SELECT
       R1.cust_id,
       -- R1.cust_id:
       --   元の Receipts から取得した顧客IDである。
       --
       --   サブクエリ側でも cust_id は返っているが、
       --   最終結果としては元表行 R1 の一部として返している。
       --
       --   つまり
       --     「最初の購入明細として選ばれた元行の顧客ID」
       --   を表示している。

       R1.seq,
       -- R1.seq:
       --   元の Receipts の seq である。
       --
       --   これはサブクエリ側の min_seq と一致する行だけが残る。
       --
       --   したがって最終的には
       --     各顧客の最小 seq
       --   が表示される。
       --
       --   しかし重要なのは、
       --   この値はサブクエリから直接返しているのではなく、
       --   JOIN によって一致した元表行から返している点である。

       R1.price
       -- R1.price:
       --   最初の購入明細に対応する購入金額である。
       --
       --   ここが今回のサブクエリ利用の最大のポイントである。
       --
       --   サブクエリでは
       --     MIN(seq)
       --   しか求めていないので、
       --   その時点では price は分からない。
       --
       --   そこで、
       --   サブクエリ結果を元表へ JOIN し、
       --   その seq を持つ元の行を復元して
       --   price を取っている。
       --
       --   つまり
       --     サブクエリは「どの行か」を特定し、
       --     外側は「その行の内容」を取得する
       --   役割分担になっている。

  FROM Receipts R1
  -- Receipts R1:
  --   元表である。
  --
  --   ここにはすべての購入明細が入っている。
  --
  --   最終的に欲しいのは
  --     「各顧客の最初の購入明細そのもの」
  --   なので、
  --   要約済みの中間結果だけでは足りず、
  --   元表 R1 に戻る必要がある。
  --
  --   ここでの R1 は
  --   「元表側の別名」である。

       INNER JOIN
       (
           SELECT
                  cust_id,
                  MIN(seq) AS min_seq
                  -- MIN(seq) AS min_seq:
                  --   各顧客について最小の seq を求めている。
                  --
                  --   つまり
                  --     その顧客の最初の購入明細番号
                  --   を表す。
                  --
                  --   ここで重要なのは、
                  --   サブクエリは「最初の行番号」を返すだけで、
                  --   明細全体は返していないこと。
                  --
                  --   そのためこの中間結果だけでは
                  --     price
                  --   はまだ取れない。
                  --
                  --   ここが
                  --     サブクエリだけで完結しない
                  --   理由である。
             FROM Receipts
            GROUP BY cust_id
            -- GROUP BY cust_id:
            --   顧客ごとにグループを作る。
            --
            --   そのグループごとに MIN(seq) を計算することで、
            --   各顧客の最初の購入明細番号を得る。
            --
            --   つまりサブクエリ全体は
            --   概念的に次のような中間表を返す。
            --
            --     A 1
            --     B 5
            --     C 10
            --     D 3
            --
            --   この中間表が
            --     「どの seq を選ぶべきか」
            --   を教えてくれる。
       ) R2
       -- R2:
       --   サブクエリの結果に付けた別名である。
       --
       --   R2 は
       --     cust_id
       --     min_seq
       --   だけを持つ中間表として扱われる。
       --
       --   つまり
       --     「各顧客の最初の seq 一覧」
       --   である。

    ON R1.cust_id = R2.cust_id
   AND R1.seq = R2.min_seq;
   -- ON 句:
   --   サブクエリ結果 R2 と元表 R1 を結び付ける条件である。
   --
   --   条件は2つある。
   --
   --   1. R1.cust_id = R2.cust_id
   --      -> 同じ顧客であること
   --
   --   2. R1.seq = R2.min_seq
   --      -> その顧客の中で最小 seq の行であること
   --
   --   この2条件を両方満たすことで、
   --   元表 Receipts の中から
   --     「各顧客の最初の購入明細そのもの」
   --   が一意に選ばれる。
   --
   --   つまりサブクエリで求めた
   --     顧客ごとの最初の seq
   --   を鍵にして、
   --   元表の完全な行へ戻っている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. サブクエリで Receipts を cust_id ごとにグループ化する
-- 2. 各顧客について MIN(seq) を計算する
-- 3. その結果を
--      (cust_id, min_seq)
--    の中間表 R2 とする
-- 4. 元表 Receipts R1 と R2 を
--      cust_id
--      seq = min_seq
--    で結び付ける
-- 5. 一致した元の明細行を返す
--
-- 概念的には次のような処理に近い。
--
--   first_seq_per_customer = {
--       cust_id: minimum seq of that customer
--   }
--
--   for each row in Receipts:
--       if row.seq == first_seq_per_customer[row.cust_id]:
--           output row
--
-- つまりこれは、
--   「顧客ごとの基準値をサブクエリで先に作り、
--     その基準値に一致する元行だけを残す」
--   アルゴリズムである。
--
-- サブクエリは中間的な要約表を作る道具として使われている。


-- ================================================
-- サブクエリとは何か
-- ================================================
--
-- サブクエリとは、
--   あるSQL文の中に埋め込まれた別のSELECT文
-- である。
--
-- 今回のサブクエリは
--   FROM 句の中
-- に置かれている。
--
--   (SELECT cust_id, MIN(seq) AS min_seq
--      FROM Receipts
--     GROUP BY cust_id) R2
--
-- これは
--   一時的な中間表 R2
-- を作っているのと同じである。
--
-- つまり今回のサブクエリは、
--   値を1つ返すだけのサブクエリではなく、
--   「集約済みの結果セット」を返す表サブクエリ
-- である。
--
-- このR2を使うことで、
--   元表から直接は見えにくい
--   「顧客ごとの最初の seq」
--   という情報を一度整理して扱える。
--
-- これが今回のサブクエリの最大の役割である。


-- ================================================
-- なぜサブクエリが必要なのか
-- ================================================
--
-- もし単純に
--
--   SELECT cust_id, MIN(seq), price
--     FROM Receipts
--    GROUP BY cust_id;
--
-- のように書くと、
--   price をどう扱うか
-- が問題になる。
--
-- なぜなら、
--   MIN(seq) を持つ行の price
-- を正しく取りたいのに、
--   price は集約されていないからである。
--
-- つまり
--   「最小 seq の値」
-- と
--   「その seq を持つ元の行の price」
-- は別問題である。
--
-- そこで今回は
--
--   1. サブクエリで最小 seq だけを確定する
--   2. その結果を使って元表へ戻る
--
-- という2段階にしている。
--
-- これにより、
--   最初の購入明細の price
-- を正しく取れる。
--
-- ここがサブクエリを使う大きな理由である。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Receipts:
--
--   A 1  500
--   A 2 1000
--   A 3  700
--
--   B 5  100
--   B 6 5000
--   B 7  300
--   B 9  200
--   B 12 1000
--
--   C 10 600
--   C 20 100
--   C 45 200
--   C 70 50
--
--   D 3 2000
--
-- サブクエリ R2 は概ね次を返す。
--
--   A 1
--   B 5
--   C 10
--   D 3
--
-- これを元表 R1 に結合すると、
--
--   A 1  500
--   B 5  100
--   C 10 600
--   D 3 2000
--
-- が選ばれる。
--
-- つまり
--   サブクエリ = 「どの行を選ぶべきか」の要約表
--   外側JOIN   = 「その行の本体」を取る処理
-- と分担されている。


-- ================================================
-- 図解: サブクエリ + JOIN
-- ================================================
--
--   元表 Receipts
--        |
--        |  cust_id ごとに GROUP BY
--        v
--   サブクエリ R2
--   +----------------------+
--   | cust_id | min_seq    |
--   +----------------------+
--   | A       | 1          |
--   | B       | 5          |
--   | C       | 10         |
--   | D       | 3          |
--   +----------------------+
--        |
--        |  R1.cust_id = R2.cust_id
--        |  R1.seq     = R2.min_seq
--        v
--   元表 Receipts と再結合
--        |
--        v
--   最初の購入明細だけを抽出
--
--   A 1  500
--   B 5  100
--   C 10 600
--   D 3 2000
--
-- つまり、
--   サブクエリで「最初の seq 一覧」を作り、
--   それを手掛かりに元表から完全な行を回収している。


-- ================================================
-- INNER JOIN の役割
-- ================================================
--
-- 今回のサブクエリだけでは
--   cust_id と min_seq しか分からない。
--
-- しかし欲しいのは
--   price を含む元の行
-- である。
--
-- そこで INNER JOIN を使って、
--   サブクエリ結果 R2
-- と
--   元表 R1
-- を結び付けている。
--
-- つまり
--
--   サブクエリ:
--     どの行を選ぶかを決める
--
--   INNER JOIN:
--     その行の本体を元表から取り戻す
--
-- という役割分担である。
--
-- この分担を理解すると、
--   なぜサブクエリと結合を一緒に使うのか
-- がよく分かる。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   集約結果そのものが欲しいのではなく、
--   集約結果を満たす元の行が欲しい
-- ところにある。
--
-- つまり
--   MIN(seq)
-- を求めるのは目的ではなく、
--   「最初の購入明細を特定するための手段」
-- である。
--
-- そのため、
--   サブクエリで基準値を作り、
--   元表へ戻る
-- という設計になる。
--
-- これはSQLで非常によく使われるパターンであり、
--   最小値を持つ行
--   最大値を持つ行
--   グループごとの代表行
-- などを取るときの基本発想である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT R1.cust_id, R1.seq, R1.price
--   → 最終的に欲しい元明細行の列を返す
--
-- FROM Receipts R1
--   → 元表を基準にする
--
-- INNER JOIN (SELECT ... GROUP BY ...) R2
--   → 顧客ごとの最小 seq を持つ中間表を結合する
--
-- ON R1.cust_id = R2.cust_id
--   → 同じ顧客どうしを対応付ける
--
-- AND R1.seq = R2.min_seq
--   → その顧客の中で最小 seq の行だけを選ぶ
--
-- ;
--   → SQL文の終了を表す
-- ================================================


-- ================================================
-- 補足1:
--   今回の主キーは (cust_id, seq) なので、
--   顧客ごとの最小 seq は1行に一意に対応する。
--
--   そのため JOIN 後に重複せず、狙った1行を安全に取れる。
-- ================================================


-- ================================================
-- 補足2:
--   サブクエリは「要約結果を表として扱う」ための道具として
--   非常に便利である。
--
--   今回は FROM 句の中に置いた表サブクエリの例である。
-- ================================================


