-- ================================================
-- ファイル名: 01_create_receipts.sql
-- 役割:
--   購入明細を保持する Receipts テーブルを作成する。
--
-- このSQLの目的:
--   顧客ごとの購入履歴を
--     顧客ID(cust_id)
--     明細番号(seq)
--     購入金額(price)
--   の形で管理できるようにすること。
--
-- 学習上の主題:
--   - 明細表の基本設計
--   - 主キーの意味
--   - 後続のサブクエリで「顧客ごとの最小 seq」を探すための土台
--
-- 今回の最重要ポイント:
--   後続のSQLでは、
--   この表から
--     顧客ごとに最初の購入明細
--   を取り出す。
--
--   そのため、
--     cust_id ごとに複数行があり、
--     その中で seq の最小値を求められる
--   という構造になっていることが大切である。
-- ================================================

CREATE TABLE Receipts
(
    cust_id CHAR(1) NOT NULL,
    -- cust_id:
    --   顧客を識別するIDである。
    --
    --   例:
    --     'A'
    --     'B'
    --     'C'
    --     'D'
    --
    --   後続のサブクエリでは
    --     cust_id ごと
    --   にグループ化して最小 seq を求める。
    --
    --   つまりこの列が
    --   「グループ単位」を決めるキーになる。

    seq INTEGER NOT NULL,
    -- seq:
    --   顧客内での購入明細番号である。
    --
    --   今回はこの列の最小値を
    --   「最初の購入明細」とみなす。
    --
    --   たとえば顧客 B は
    --     5, 6, 7, 9, 12
    --   という seq を持つので、
    --   最小の 5 が最初の購入明細になる。
    --
    --   つまり後続のサブクエリで
    --     MIN(seq)
    --   の対象になる列である。

    price INTEGER NOT NULL,
    -- price:
    --   その明細の購入金額である。
    --
    --   最終結果では、
    --   最初の購入明細に対応する金額を表示するために使う。
    --
    --   注意:
    --   サブクエリ側では最小 seq しか求めていないので、
    --   price は外側の表 R1 から取り出す必要がある。
    --
    --   ここが今回のサブクエリ + JOIN の重要な意味である。

    PRIMARY KEY (cust_id, seq)
    -- PRIMARY KEY(cust_id, seq):
    --   同じ顧客について同じ seq が重複しないようにする複合主キーである。
    --
    --   これにより、
    --   各顧客の各購入明細は一意に識別される。
    --
    --   後続の JOIN では
    --     cust_id
    --     seq
    -- の組で元の行へ戻るので、
    --   この一意性が非常に重要である。
);