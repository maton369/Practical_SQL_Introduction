-- ================================================
-- ファイル名: 04_select_first_receipt_with_correlated_subquery.sql
-- 役割:
--   相関サブクエリを使って、
--   顧客ごとの最初の購入明細を取得する。
--
-- このSQLの目的:
--   Receipts テーブルから
--     各顧客について最小の seq を持つ行
--   を取り出すこと。
--
-- 学習上の主題:
--   - 相関サブクエリとは何か
--   - 外側クエリの現在行に応じて内側クエリが変わること
--   - 「顧客ごとの最小 seq」と元行の対応
--   - 前回の「集約サブクエリ + JOIN」との発想の違い
--
-- 今回の最重要ポイント:
--   このSQLでは、外側の Receipts の各行 R1 に対して、
--   内側のサブクエリが
--     「その顧客 R1.cust_id の最小 seq は何か」
--   を毎回計算している。
--
--   そして
--     外側行の seq = その顧客の最小 seq
--   である行だけを残す。
--
--   つまりアルゴリズムとしては
--     1. 外側で1行取る
--     2. その行の cust_id を使って内側で最小 seq を求める
--     3. 外側行の seq と比較する
--     4. 一致したら出力する
--   という「各行ごとの判定型」になっている。
--
--   これが相関サブクエリの本質である。
-- ================================================

SELECT
       cust_id,
       -- cust_id:
       --   顧客IDを返す。
       --
       --   最終的に
       --     各顧客の最初の購入明細
       -- を表示したいので、この列をそのまま返している。
       --
       --   この列そのものは外側クエリ R1 の列であり、
       --   サブクエリはその「判定材料」として使われている。

       seq,
       -- seq:
       --   購入明細番号を返す。
       --
       --   ただし WHERE 句によって
       --     その顧客の最小 seq
       -- を持つ行だけが残るため、
       --   最終結果では
       --     各顧客の最初の明細番号
       -- が表示される。
       --
       --   この列が今回の比較対象そのものである。

       price
       -- price:
       --   その購入明細の金額を返す。
       --
       --   相関サブクエリでは最小 seq しか求めていないが、
       --   最終的に欲しいのは
       --     その最小 seq を持つ「元の行」
       -- なので、
       --   外側クエリから price をそのまま返している。
       --
       --   ここが
       --     「集約値そのもの」ではなく
       --     「集約条件を満たす元行」
       -- を取りたい問題であることを示している。

  FROM Receipts R1
  -- Receipts R1:
  --   外側クエリの元表である。
  --
  --   ここでは Receipts の全行を候補として1行ずつ見ていく。
  --
  --   つまり R1 は
  --     「今判定しようとしている購入明細」
  -- を表している。
  --
  --   相関サブクエリの理解では、
  --   まず
  --     外側に現在行 R1 がある
  -- という意識が非常に重要である。

 WHERE seq =
       (
           SELECT MIN(seq)
             FROM Receipts R2
            WHERE R1.cust_id = R2.cust_id
       );
       -- 相関サブクエリ:
       --   ここが今回の主役である。
       --
       --   内側の SELECT は、
       --   Receipts R2 の中から
       --     R1 と同じ cust_id を持つ行だけ
       -- を対象にして、
       --     MIN(seq)
       -- を求めている。
       --
       --   つまり
       --     「今見ている外側行 R1 の顧客について、
       --       その顧客の最小 seq は何か」
       -- を返している。
       --
       -- 相関の意味:
       --   このサブクエリは独立して固定の結果を返すわけではない。
       --
       --   なぜなら WHERE 句で
       --     R1.cust_id = R2.cust_id
       --   と書かれており、
       --   外側クエリの現在行 R1 を参照しているからである。
       --
       --   たとえば外側が
       --     A, seq=2, price=1000
       --   を見ているとき、
       --   内側サブクエリは実質的に
       --
       --     SELECT MIN(seq)
       --       FROM Receipts R2
       --      WHERE 'A' = R2.cust_id
       --
       --   のように評価される。
       --
       --   この結果は 1 になる。
       --
       --   すると外側の
       --     seq = 2
       --   と比較して不一致なので、
       --   この行は落ちる。
       --
       --   一方で外側が
       --     A, seq=1, price=500
       --   を見ているとき、
       --   内側の結果は同じく 1 なので、
       --   外側の seq=1 と一致し、
       --   この行は残る。
       --
       --   つまり
       --     外側行が変わる
       --     -> 内側サブクエリの検索条件が変わる
       --     -> 内側結果が変わる
       --   という依存関係がある。
       --
       --   これが「相関サブクエリ」と呼ばれる理由である。
       --
       -- WHERE R1.cust_id = R2.cust_id:
       --   外側の顧客と同じ顧客だけを
       --   内側の集約対象に限定している。
       --
       --   これにより MIN(seq) は
       --   「表全体の最小」ではなく
       --   「その顧客の最小」
       -- になる。
       --
       -- MIN(seq):
       --   その顧客について最初の購入明細番号を返す。
       --
       -- 外側の WHERE seq = (...) 全体:
       --   外側行 R1 の seq が、
       --   その顧客にとって最小 seq と一致するときだけ残す。
       --
       --   つまり
       --     「その顧客の最初の明細行だけを採用する」
       --   条件である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 外側クエリで Receipts の1行を候補として取る
-- 2. その行の cust_id を使って、
--    内側サブクエリで同じ顧客の MIN(seq) を求める
-- 3. 外側行の seq と内側結果の MIN(seq) を比較する
-- 4. 一致すればその行を残す
-- 5. 不一致ならその行を捨てる
-- 6. 全行についてこれを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each row r1 in Receipts:
--       min_seq_of_customer =
--           minimum seq among rows r2
--           where r2.cust_id == r1.cust_id
--
--       if r1.seq == min_seq_of_customer:
--           output r1
--
-- つまりこれは、
--   「各行を、その顧客グループの最小値と比較して選抜する」
--   相関サブクエリアルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Receipts:
--
--   A 1   500
--   A 2  1000
--   A 3   700
--
--   B 5   100
--   B 6  5000
--   B 7   300
--   B 9   200
--   B 12 1000
--
--   C 10  600
--   C 20  100
--   C 45  200
--   C 70   50
--
--   D 3  2000
--
-- 外側行ごとの判定例:
--
-- 1. R1 = (A, 1, 500)
--   内側:
--     A の MIN(seq) = 1
--   比較:
--     1 = 1
--   -> 残る
--
-- 2. R1 = (A, 2, 1000)
--   内側:
--     A の MIN(seq) = 1
--   比較:
--     2 = 1
--   -> 落ちる
--
-- 3. R1 = (B, 5, 100)
--   内側:
--     B の MIN(seq) = 5
--   比較:
--     5 = 5
--   -> 残る
--
-- 4. R1 = (B, 6, 5000)
--   内側:
--     B の MIN(seq) = 5
--   比較:
--     6 = 5
--   -> 落ちる
--
-- ・・・
--
-- 最終的に残るのは
--
--   A 1   500
--   B 5   100
--   C 10  600
--   D 3  2000
--
-- である。
--
-- これは前の「サブクエリ + JOIN」の解と同じ結果になる。


-- ================================================
-- 相関サブクエリと前回の解との違い
-- ================================================
--
-- 前回のリスト7.2では、
--   まずサブクエリで
--     cust_id ごとの最小 seq 一覧
--   を表として作り、
--   それを元表に JOIN していた。
--
-- つまり発想は
--
--   1. 先に中間表を作る
--   2. その中間表を使って元表から行を回収する
--
-- だった。
--
-- 一方、今回の相関サブクエリ版では、
--   外側の各行を見ながら
--   毎回その顧客の MIN(seq) を計算している。
--
-- つまり発想は
--
--   1. 外側の各行を順に見る
--   2. その行ごとに「自分の顧客の最小 seq」と比較する
--
-- である。
--
-- したがって
--
--   リスト7.2:
--     要約表を先に作ってから JOIN する
--
--   リスト7.3:
--     各行ごとに相関サブクエリで最小値を確認する
--
-- という違いがある。
--
-- 結果は同じでも、
--   アルゴリズムの見え方
-- がかなり異なる。
--
-- 今回はその「相関」の意味を理解するのが主眼である。


-- ================================================
-- 図解: 相関サブクエリの動作
-- ================================================
--
--   外側クエリ:
--     Receipts の1行 R1 を取る
--
--        R1 = (cust_id='B', seq=7, price=300)
--                     |
--                     v
--   内側サブクエリ:
--     同じ顧客 B の行だけを見る
--
--        B 5  100
--        B 6 5000
--        B 7  300
--        B 9  200
--        B 12 1000
--
--     MIN(seq) = 5
--                     |
--                     v
--   外側判定:
--     R1.seq = 7
--     7 = 5 ? いいえ
--                     |
--                     v
--                  行を捨てる
--
--
--   外側クエリ:
--     R1 = (cust_id='B', seq=5, price=100)
--                     |
--                     v
--   内側サブクエリ:
--     同じく B の MIN(seq) = 5
--                     |
--                     v
--   外側判定:
--     5 = 5 ? はい
--                     |
--                     v
--                  行を残す
--
-- つまり、
--   外側1行ごとに内側サブクエリが走り、
--   その行が代表行かどうかを判定している。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   グループごとの代表値を
--   「外側の現在行」と比較して判定する
-- ところにある。
--
-- つまり
--   先に要約表を作ってから JOIN する
-- のではなく、
--   各行が自分で「自分は代表か？」を問い合わせる
-- ような形で書かれている。
--
-- このため相関サブクエリは、
--   「各行に対して別の表・同じ表から基準値を問い合わせる」
--   書き方
-- として理解すると分かりやすい。
--
-- 今回は同じ Receipts 表を
--   外側 R1
--   内側 R2
-- で使っているので、
--   同じ表に対する相関サブクエリ
-- の典型例になっている。
--
-- これは
--   最小値を持つ行
--   最大値を持つ行
--   グループ内順位で代表を選ぶ行
-- などを考えるうえで重要な基本形である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT cust_id, seq, price
--   → 代表として残った購入明細行を返す
--
-- FROM Receipts R1
--   → 外側クエリの候補行集合
--
-- WHERE seq = (SELECT MIN(seq) ... )
--   → その行の seq が、その顧客の最小 seq と一致するか判定する
--
-- WHERE R1.cust_id = R2.cust_id
--   → 内側サブクエリを外側の顧客に相関させる
--
-- ;
--   → SQL文の終了を表す
-- ================================================


-- ================================================
-- 補足1:
--   今回の主キーは (cust_id, seq) なので、
--   顧客ごとの最小 seq は1行に一意に対応する。
--
--   そのため「最初の購入明細」が一意に決まる。
-- ================================================


-- ================================================
-- 補足2:
--   相関サブクエリは読みやすい一方で、
--   外側行ごとに内側計算を考える構造になるため、
--   データ量が多いと性能に注意が必要な場合がある。
-- ================================================


