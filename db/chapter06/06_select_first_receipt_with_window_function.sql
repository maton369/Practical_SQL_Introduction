-- ================================================
-- ファイル名: 06_select_first_receipt_with_window_function.sql
-- 役割:
--   ウィンドウ関数を使って、
--   顧客ごとの最初の購入明細を取得する。
--
-- このSQLの目的:
--   Receipts テーブルの各行に対して、
--   顧客ごとの並び順を付与し、
--   その中で1番目の行だけを取り出すこと。
--
-- 学習上の主題:
--   - ウィンドウ関数の基本
--   - ROW_NUMBER() の意味
--   - PARTITION BY によるグループ分割
--   - ORDER BY によるグループ内順位付け
--   - サブクエリ + JOIN や相関サブクエリとの発想の違い
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   まず各明細行に対して
--     「同じ cust_id の中で何番目の行か」
--   を ROW_NUMBER() で付けている。
--
--   そしてその順位が 1 の行だけを最後に残す。
--
--   つまりアルゴリズムとしては
--     1. cust_id ごとにグループ分けする
--     2. 各グループ内で seq 昇順に並べる
--     3. 1,2,3,... と連番を振る
--     4. 連番が 1 の行だけを抽出する
--   という「順位付けして代表行を選ぶ」形になっている。
--
--   これがウィンドウ関数版の本質である。
-- ================================================

SELECT
       cust_id,
       -- cust_id:
       --   顧客IDを返す。
       --
       --   最終的に
       --     各顧客の最初の購入明細
       -- を表示したいので、
       --   WORK からそのまま返している。

       seq,
       -- seq:
       --   購入明細番号を返す。
       --
       --   ただし外側の WHERE で row_seq = 1 に絞るため、
       --   最終的には
       --     各顧客で最小の seq
       -- を持つ行だけが残る。
       --
       --   つまりこの列は、
       --   ウィンドウ関数による順位付けの結果
       --   「1番目」と判定された明細番号である。

       price
       -- price:
       --   その購入明細の金額を返す。
       --
       --   サブクエリ + JOIN 版や相関サブクエリ版では、
       --   price を取るために
       --   「元表へ戻る」
       --   という発想が必要だった。
       --
       --   しかしウィンドウ関数版では、
       --   元の各行に対してそのまま順位を付けるので、
       --   同じ行の price を自然に一緒に返せる。
       --
       --   これがウィンドウ関数版の大きな利点である。

  FROM
       (
           SELECT
                  cust_id,
                  -- 顧客ID。
                  -- PARTITION BY cust_id の単位になる。

                  seq,
                  -- 購入明細番号。
                  -- グループ内での並び順を決める列であり、
                  -- ORDER BY seq に使われる。

                  price,
                  -- 明細金額。
                  -- 順位付けの対象ではないが、
                  -- 代表行を選んだあとに最終結果で返したいので
                  -- 派生表 WORK の中に残しておく。

                  ROW_NUMBER()
                  OVER (
                      PARTITION BY cust_id
                      ORDER BY seq
                  ) AS row_seq
                  -- ROW_NUMBER() OVER (...):
                  --   ここが今回の主役である。
                  --
                  --   ROW_NUMBER() は、
                  --   各行に 1,2,3,... の連番を振るウィンドウ関数である。
                  --
                  -- PARTITION BY cust_id:
                  --   顧客ごとに独立したグループを作る。
                  --
                  --   つまり
                  --     A の明細群
                  --     B の明細群
                  --     C の明細群
                  --     D の明細群
                  --   を別々に扱う。
                  --
                  --   重要:
                  --   PARTITION BY を使わないと、
                  --   表全体に 1,2,3,... を振ってしまい、
                  --   「顧客ごとの最初の行」は取れない。
                  --
                  -- ORDER BY seq:
                  --   各顧客グループの中で
                  --   seq が小さい順に並べる。
                  --
                  --   つまり
                  --     seq が最小の行に row_seq = 1
                  --   が付く。
                  --
                  --   今回の問題では
                  --     最初の購入明細 = 最小 seq
                  --   とみなしているので、
                  --   この ORDER BY が問題設定そのものを表している。
                  --
                  -- AS row_seq:
                  --   生成した順位に row_seq という名前を付けている。
                  --
                  --   この列が
                  --     各顧客の中で何番目の行か
                  --   を表す。
                  --
                  --   外側クエリでは
                  --     row_seq = 1
                  --   の行だけを残すため、
                  --   代表行選択の鍵になる。
             FROM Receipts
             -- Receipts:
             --   元表である。
             --
             --   ウィンドウ関数版では、
             --   元表の各行を失わずに
             --   「その行がグループ内で何番目か」
             --   を付加できる。
             --
             --   ここが
             --     先に要約してから元表へ戻る
             --   サブクエリ + JOIN 版と大きく違う点である。
       ) WORK
       -- WORK:
       --   派生表である。
       --
       --   ここには
       --     cust_id
       --     seq
       --     price
       --     row_seq
       --   が入っている。
       --
       --   つまり
       --     「各購入明細に順位を付けた中間結果」
       -- である。
       --
       --   外側クエリはこの中間結果から
       --   row_seq = 1
       -- の行だけを選ぶ。

 WHERE WORK.row_seq = 1;
 -- WHERE WORK.row_seq = 1:
 --   各顧客グループの中で
 --   1番目に並んだ行だけを残す。
 --
 --   すなわち
 --     seq が最小の行
 --   だけが最終結果になる。
 --
 --   これにより、
 --   各顧客の最初の購入明細が抽出される。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Receipts の各行を cust_id ごとにグループ分けする
-- 2. 各グループの中で seq 昇順に並べる
-- 3. その順番に 1,2,3,... と ROW_NUMBER を振る
-- 4. 派生表 WORK を作る
-- 5. WORK から row_seq = 1 の行だけを残す
--
-- 概念的には次のような処理に近い。
--
--   for each customer_group in Receipts grouped by cust_id:
--       sort customer_group by seq ascending
--
--       row_seq = 1
--       for each row in customer_group:
--           row.row_seq = row_seq
--           row_seq += 1
--
--   output rows where row_seq == 1
--
-- つまりこれは、
--   「グループ内順位付けにより代表行を選ぶ」
--   ウィンドウ関数アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Receipts:
--
--   A 1   500
--   A 2  1000
--   A 3   700
--
--   B 5   100
--   B 6  5000
--   B 7   300
--   B 9   200
--   B 12 1000
--
--   C 10  600
--   C 20  100
--   C 45  200
--   C 70   50
--
--   D 3  2000
--
-- これに ROW_NUMBER() を適用すると、
--   派生表 WORK は概ね次のようになる。
--
--   A 1   500   1
--   A 2  1000   2
--   A 3   700   3
--
--   B 5   100   1
--   B 6  5000   2
--   B 7   300   3
--   B 9   200   4
--   B 12 1000   5
--
--   C 10  600   1
--   C 20  100   2
--   C 45  200   3
--   C 70   50   4
--
--   D 3  2000   1
--
-- 最後に row_seq = 1 の行だけを取るので、
-- 結果は
--
--   A 1   500
--   B 5   100
--   C 10  600
--   D 3  2000
--
-- になる。
--
-- これは前の
--   サブクエリ + JOIN
--   相関サブクエリ
-- の結果と同じである。


-- ================================================
-- サブクエリ版・相関サブクエリ版との違い
-- ================================================
--
-- これまでの2つの解法では、
--   「最小 seq をまず別に求める」
--   という発想が中心だった。
--
-- リスト7.2:
--   サブクエリで (cust_id, min_seq) を作り、
--   それを元表へ JOIN していた。
--
-- リスト7.3:
--   各行ごとに相関サブクエリで
--   その顧客の MIN(seq) を問い合わせて比較していた。
--
-- 一方、今回のウィンドウ関数版では、
--   最小値そのものを直接比較していない。
--
-- 代わりに
--   顧客ごとに順位を付けて、
--   1番の行を選ぶ
-- という発想になっている。
--
-- つまり
--
--   リスト7.2:
--     最小値を先に作る -> 元表へ戻る
--
--   リスト7.3:
--     各行が自分のグループ最小値を問い合わせる
--
--   リスト7.4:
--     各行に順位を振って、1位の行を残す
--
-- という違いがある。
--
-- ウィンドウ関数版の強みは、
--   行を失わずに、その行へ順位情報を付加できる
-- ところである。
--
-- このため
--   price を取るために元表へ戻る必要がない。
--
-- ここが大きな利点である。


-- ================================================
-- 図解: ウィンドウ関数版
-- ================================================
--
--   Receipts
--      |
--      | PARTITION BY cust_id
--      v
--   顧客ごとにグループ化
--      |
--      | ORDER BY seq
--      v
--   各グループ内で seq 昇順に並べる
--      |
--      | ROW_NUMBER()
--      v
--   各行に 1,2,3,... を付ける
--      |
--      v
--   派生表 WORK
--      |
--      | WHERE row_seq = 1
--      v
--   各顧客の最初の購入明細だけを残す
--
-- つまり、
--   「順位を付けてから代表行を選ぶ」
--   という流れである。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   集約結果を別表として作るのではなく、
--   各元行へ「順位」という補助情報を付加して
--   代表行を選ぶ
-- ところにある。
--
-- つまり
--   行を要約して失う
-- のではなく、
--   行を保ったまま代表判定できる
-- のがウィンドウ関数の大きな特徴である。
--
-- 今回でいえば、
--   顧客ごとの最小 seq 行
-- を取りたいが、
--   そのために
--     JOIN で元表へ戻る
--     相関サブクエリで毎回問い合わせる
--   必要はない。
--
-- 各行に
--   row_seq
-- を付ければよい。
--
-- これがウィンドウ関数版の最も美しい点である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER()
--   → グループ内で1,2,3,...の順位を振る
--
-- OVER (PARTITION BY cust_id ORDER BY seq)
--   → 顧客ごとに分け、seq 昇順で順位付けする
--
-- AS row_seq
--   → その順位に row_seq という名前を付ける
--
-- FROM (...) WORK
--   → 順位付きの中間結果を派生表として扱う
--
-- WHERE WORK.row_seq = 1
--   → 各顧客で1位の行だけを残す
--
-- ;
--   → SQL文の終了を表す
-- ================================================


-- ================================================
-- 補足1:
--   ROW_NUMBER() は同順位を許さず、
--   必ず 1,2,3,... と振る。
--
--   今回は主キーが (cust_id, seq) なので
--   seq は顧客内で一意であり、
--   順位が曖昧にならない。
-- ================================================


-- ================================================
-- 補足2:
--   この書き方では、
--   グループごとの代表行抽出を
--   非常に自然に表現できる。
--
--   そのため、現代SQLでは
--   この種の問題にウィンドウ関数がよく使われる。
-- ================================================


-- ================================================
-- 補足3:
--   もし最初の2件を取りたければ
--   WHERE row_seq <= 2
--   のようにできる。
--
--   これもウィンドウ関数版の拡張性の高さを示している。
-- ================================================