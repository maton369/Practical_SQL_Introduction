-- ================================================
-- ファイル名: 16_select_join_then_group.sql
-- 役割:
--   先に Companies と Shops を結合し、
--   その後で集約する。
--
-- このSQLの目的:
--   「結合を先に行う」場合のアルゴリズムを明示すること。
--
-- 学習上の主題:
--   - JOIN と GROUP BY の順序
--   - 結合後の行集合をそのまま集約する発想
--   - 集約前の行数が大きい場合の考え方
--
-- 今回の最重要ポイント:
--   このSQLでは、
--     1. まず Companies と Shops を co_cd で結合する
--     2. main_flg='Y' の行だけ残す
--     3. その結合結果を会社ごとに GROUP BY する
--     4. emp_nbr を SUM する
--   という順序になっている。
--
--   つまり
--     JOIN -> FILTER -> GROUP BY
--   の流れである。
--
--   このため、
--   集約の入力になるのは
--     「結合済みの詳細行」
--   である。
--
--   これが解2との最大の違いである。
-- ================================================

SELECT
       C.co_cd,
       -- 会社コードを返す。
       --
       -- GROUP BY の単位でもある。
       --
       -- なお district も結果に出しているが、
       -- co_cd が主キーなので会社単位の属性として矛盾しない。

       C.district,
       -- 会社の地区属性を返す。
       --
       -- 集約対象ではないが、
       -- JOIN により会社情報として付与されている。

       SUM(emp_nbr) AS sum_emp
       -- sum_emp:
       --   main_flg='Y' の事業所について、
       --   会社ごとに emp_nbr を合計した値である。
       --
       --   この集約は
       --   「JOIN 後の行集合」
       --   を入力にして行われる点が重要である。

  FROM Companies C
       INNER JOIN Shops S
         ON C.co_cd = S.co_cd
         -- まず Companies と Shops を会社コードで結合する。
         --
         -- これにより、
         --   会社情報 + 事業所情報
         -- を持つ詳細行集合ができる。
         --
         -- つまりこの時点では
         --   会社 001 の行は 3事業所分に増える
         -- など、
         --   1会社 : 複数事業所
         -- の関係がそのまま行数に表れる。

 WHERE main_flg = 'Y'
       -- JOIN 後の詳細行集合から、
       -- 本店フラグが Y の行だけを残す。
       --
       -- つまり
       --   集約の入力行を先に絞り込む
       -- フィルタである。
       --
       -- この条件は S.main_flg に対するものだが、
       -- 結合後の結果に対して適用されていると考えると理解しやすい。

 GROUP BY C.co_cd;
 -- C.co_cd ごとにグループ化し、
 -- SUM(emp_nbr) を計算する。
 --
 -- つまり最終的に
 --   1会社につき1行
 -- の結果にまとめている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Companies と Shops を co_cd で結合する
-- 2. 結合結果から main_flg='Y' の行だけを残す
-- 3. 会社コードごとにグループ化する
-- 4. emp_nbr を SUM する
--
-- 概念的には次のような処理に近い。
--
--   joined_rows = []
--
--   for each company in Companies:
--       for each shop in Shops:
--           if company.co_cd == shop.co_cd:
--               joined_rows.append(company + shop)
--
--   filtered_rows = rows where main_flg == 'Y'
--
--   for each company_code group in filtered_rows:
--       sum_emp = sum(emp_nbr)
--       output company_code, district, sum_emp
--
-- つまりこれは、
--   「詳細行を先に結び付け、その後で集約する」
--   JOIN先行型アルゴリズムである。


-- ================================================
-- 図解: 結合を先に行う
-- ================================================
--
--   Companies ---- co_cd で JOIN ---- Shops
--        |                               |
--        +----------- 詳細行集合 ---------+
--                        |
--                        | main_flg='Y'
--                        v
--                   対象行だけ残す
--                        |
--                        | GROUP BY co_cd
--                        v
--                     SUM(emp_nbr)
--
-- つまり
--   先に詳細レベルで結び付けてから、
--   その詳細行をまとめている。