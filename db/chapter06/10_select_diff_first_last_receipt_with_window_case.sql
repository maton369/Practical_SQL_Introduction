-- リスト7.5との違い
-- ================================================
--
-- リスト7.5では
--   最初の明細表 TMP_MIN
--   最後の明細表 TMP_MAX
-- を別々に作って最後に JOIN していた。
--
-- 一方リスト7.6では、
--   元の各行へ
--     最初側順位
--     最後側順位
--   を同時に付けている。
--
-- つまり
--
--   リスト7.5:
--     代表行表を2本作って最後に合流
--
--   リスト7.6:
--     1本の中間表に最初情報と最後情報を同居させる
--
-- という違いがある。
--
-- そのためウィンドウ関数版の方が、
--   構造が一体化していて簡潔
-- になっている。
--
-- これが今回の大きな比較ポイントである。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   2種類の代表行
--     最初の行
--     最後の行
--   を別々の表として作らず、
--   同じ中間表 WORK の中で識別できるようにしている点にある。
--
--   そしてその識別結果を
--     CASE 式による条件付き集約
--   へつなげている。
--
-- つまり
--   順位付け
--   フィルタ
--   条件付き集約
-- を1本の流れとしてつなげたSQLである。
--
-- これはウィンドウ関数の強みをよく示している。
--
-- 特に
--   元行の情報を失わない
--   代表行を複数種類扱える
--   集約ロジックへ自然に接続できる
-- という点が非常に重要である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER() OVER (PARTITION BY cust_id ORDER BY seq)
--   → 最初側の順位 min_seq を作る
--
-- ROW_NUMBER() OVER (PARTITION BY cust_id ORDER BY seq DESC)
--   → 最後側の順位 max_seq を作る
--
-- CASE WHEN min_seq = 1 THEN price ELSE 0 END
--   → 最初行だけ price を加算対象にする
--
-- CASE WHEN max_seq = 1 THEN price ELSE 0 END
--   → 最後行だけ price を減算対象にする
--
-- WHERE min_seq = 1 OR max_seq = 1
--   → 最初行と最後行だけ残す
--
-- GROUP BY cust_id
--   → 顧客ごとの差分を1行にまとめる
-- ================================================