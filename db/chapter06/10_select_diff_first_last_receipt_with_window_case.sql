-- ================================================
-- ファイル名: 10_select_diff_first_last_receipt_with_window_case.sql
-- 役割:
--   ウィンドウ関数と CASE 式を使って、
--   各顧客の「最初の購入金額 - 最後の購入金額」を求める。
--
-- このSQLの目的:
--   各購入明細行に
--     最初側の順位
--     最後側の順位
--   を同時に付け、
--   CASE 式で必要な行だけを加算・減算することで、
--   顧客ごとの差分を1本の集約クエリで求めること。
--
-- 学習上の主題:
--   - ウィンドウ関数の複数利用
--   - ROW_NUMBER() を昇順・降順の両方で使う意味
--   - CASE 式で条件付き集約をする考え方
--   - 多重サブクエリ版との構造比較
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   まず各行に
--     min_seq = 顧客内で seq 昇順の順位
--     max_seq = 顧客内で seq 降順の順位
--   を付ける。
--
--   すると
--     min_seq = 1 の行は「最初の購入明細」
--     max_seq = 1 の行は「最後の購入明細」
--   と分かる。
--
--   あとは
--     最初の行の price を足し、
--     最後の行の price を引く
--   ように CASE 付き SUM を取れば差分が出る。
--
--   つまりアルゴリズムとしては
--     1. 各行へ最初側順位と最後側順位を付ける
--     2. 最初行 price を加算対象にする
--     3. 最後行 price を減算対象にする
--     4. cust_id ごとに合計して diff を作る
--   という「順位付け + 条件付き集約」になっている。
--
--   これがウィンドウ関数 + CASE 式版の本質である。
-- ================================================

SELECT
       cust_id,
       -- cust_id:
       --   顧客IDを返す。
       --
       --   最終的に cust_id ごとに GROUP BY しているので、
       --   顧客単位の差分結果になる。

       SUM(CASE WHEN min_seq = 1 THEN price ELSE 0 END)
         - SUM(CASE WHEN max_seq = 1 THEN price ELSE 0 END) AS diff
       -- diff:
       --   最初の購入金額 - 最後の購入金額
       --   を表す。
       --
       --   左側の SUM:
       --     min_seq = 1 の行、つまり
       --     各顧客の最初の購入明細の price だけを加算する。
       --
       --   右側の SUM:
       --     max_seq = 1 の行、つまり
       --     各顧客の最後の購入明細の price だけを加算する。
       --
       --   それを引き算しているので、
       --     最初 - 最後
       --   になる。
       --
       -- CASE 式の役割:
       --   条件を満たす行だけに price を与え、
       --   他は 0 にする。
       --
       --   つまり CASE は
       --     「この行は最初価格として数えるか」
       --     「この行は最後価格として数えるか」
       --   を判定するスイッチの役割を持つ。
       --
       -- 顧客 D のように最初と最後が同じ行の場合:
       --   その1行は
       --     min_seq = 1
       --     max_seq = 1
       --   の両方を満たす。
       --
       --   そのため
       --     +price
       --     -price
       --   が同時に入り、差は 0 になる。
       --
       --   これは意図通りである。

  FROM
       (
           SELECT
                  cust_id,
                  -- 顧客ID。
                  -- PARTITION BY cust_id の単位になる。

                  price,
                  -- 各購入明細の金額。
                  -- 最終的に条件付き SUM の対象になる。

                  ROW_NUMBER()
                  OVER (
                      PARTITION BY cust_id
                      ORDER BY seq
                  ) AS min_seq,
                  -- min_seq:
                  --   顧客ごとに seq 昇順で順位を付けたもの。
                  --
                  --   したがって
                  --     min_seq = 1
                  --   の行は
                  --     その顧客の最初の購入明細
                  --   を表す。
                  --
                  --   これは「最小 seq 側の順位」である。

                  ROW_NUMBER()
                  OVER (
                      PARTITION BY cust_id
                      ORDER BY seq DESC
                  ) AS max_seq
                  -- max_seq:
                  --   顧客ごとに seq 降順で順位を付けたもの。
                  --
                  --   したがって
                  --     max_seq = 1
                  --   の行は
                  --     その顧客の最後の購入明細
                  --   を表す。
                  --
                  --   これは「最大 seq 側の順位」である。
                  --
                  --   つまりこの派生表では、
                  --   各行について
                  --     最初側順位
                  --     最後側順位
                  --   を両方持たせている。
             FROM Receipts
             -- Receipts:
             --   元表である。
             --
             --   ウィンドウ関数版では、
             --   元表の各行を失わずに
             --   「最初側順位」「最後側順位」
             --   という2つの補助情報を同時に付加できる。
             --
             --   ここがサブクエリ・パラノイア版との大きな違いである。
       ) WORK
       -- WORK:
       --   各購入明細に
       --     min_seq
       --     max_seq
       -- を付けた中間結果である。
       --
       --   つまり
       --     この行は最初候補か
       --     この行は最後候補か
       --   が分かる状態になっている。
       --
       --   外側クエリはこれを使って CASE 集約を行う。

 WHERE WORK.min_seq = 1
    OR WORK.max_seq = 1
    -- WHERE 句:
    --   最初行または最後行だけを残す。
    --
    --   つまり
    --     顧客ごとの最初の購入明細
    --     顧客ごとの最後の購入明細
    --   だけが外側集約の対象になる。
    --
    --   これにより、
    --   中間の行は CASE 以前に落とせるので、
    --   集約対象を絞り込んでいる。
    --
    --   顧客 D のように最初と最後が同じ1行の場合は、
    --   その1行が条件を満たして残る。

 GROUP BY cust_id;
 -- GROUP BY cust_id:
 --   顧客ごとにまとめて
 --   最初価格と最後価格の差分を計算する。
 --
 --   つまり最終結果は
 --     顧客単位の diff 表
 --   になる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Receipts の各行に対して
--      min_seq = seq 昇順順位
--      max_seq = seq 降順順位
--    を付ける
-- 2. 最初行(min_seq=1) または 最後行(max_seq=1) のみを残す
-- 3. cust_id ごとにグループ化する
-- 4. CASE 式により
--      最初行の price は加算
--      最後行の price は減算
--    する
-- 5. 差分 diff を得る
--
-- 概念的には次のような処理に近い。
--
--   for each customer_group in Receipts grouped by cust_id:
--       mark first row with min_seq = 1
--       mark last  row with max_seq = 1
--
--   for each customer_group:
--       diff = sum(price of rows where min_seq = 1)
--            - sum(price of rows where max_seq = 1)
--       output customer_group.cust_id, diff
--
-- つまりこれは、
--   「2種類の順位を付けて、条件付き集約で差分を作る」
--   ウィンドウ関数アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- 顧客 A:
--   1 500  -> min_seq=1, max_seq=3
--   2 1000 -> min_seq=2, max_seq=2
--   3 700  -> min_seq=3, max_seq=1
--
--   WHERE 後に残るのは
--     1 500 (最初)
--     3 700 (最後)
--
--   diff = 500 - 700 = -200
--
-- 顧客 B:
--   5 100   -> min_seq=1
--   6 5000
--   7 300
--   9 200
--   12 1000 -> max_seq=1
--
--   diff = 100 - 1000 = -900
--
-- 顧客 C:
--   10 600  -> min_seq=1
--   ...
--   70 50   -> max_seq=1
--
--   diff = 600 - 50 = 550
--
-- 顧客 D:
--   3 2000 -> min_seq=1 かつ max_seq=1
--
--   diff = 2000 - 2000 = 0
--
-- したがって結果は
--
--   A  -200
--   B  -900
--   C   550
--   D     0
--
-- になる。
--
-- これはリスト7.5と同じ結果である。


-- ================================================
-- 図解: ウィンドウ関数 + CASE 式版
-- ================================================
--
--   Receipts
--      |
--      | PARTITION BY cust_id
--      v
--   顧客ごとに分割
--      |
--      +------------------------------+
--      | seq 昇順 ROW_NUMBER()        |
--      | -> min_seq                   |
--      +------------------------------+
--      |
--      +------------------------------+
--      | seq 降順 ROW_NUMBER()        |
--      | -> max_seq                   |
--      +------------------------------+
--      |
--      v
--   WORK
--      |
--      | min_seq=1 または max_seq=1 だけ残す
--      v
--   最初行・最後行だけの表
--      |
--      | CASE WHEN min_seq=1 THEN +price
--      | CASE WHEN max_seq=1 THEN -price
--      v
--   cust_id ごとに SUM
--      |
--      v
--   diff を得る
--
-- つまり、
--   「順位付け -> 必要行抽出 -> 条件付き集約」
--   の流れである。


