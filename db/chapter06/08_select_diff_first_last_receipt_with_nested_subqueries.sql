-- ================================================
-- ファイル名: 08_select_diff_first_last_receipt_with_nested_subqueries.sql
-- 役割:
--   サブクエリを多重に使って、
--   各顧客の「最初の購入金額 - 最後の購入金額」を求める。
--
-- このSQLの目的:
--   Receipts から
--     顧客ごとの最初の購入明細
--     顧客ごとの最後の購入明細
--   を別々に作り、
--   それらを cust_id で突き合わせて差分を計算すること。
--
-- 学習上の主題:
--   - サブクエリの入れ子構造
--   - 「最初の行」と「最後の行」を別々に抽出する発想
--   - 2つの中間結果をさらに JOIN する考え方
--   - サブクエリを重ねることで処理が段階化されること
--
-- 今回の最重要ポイント:
--   このSQLは非常に段階的である。
--
--   アルゴリズムとしては
--     1. 顧客ごとの最小 seq 一覧を作る
--     2. それを元表へ JOIN して「最初の購入明細表」TMP_MIN を作る
--     3. 顧客ごとの最大 seq 一覧を作る
--     4. それを元表へ JOIN して「最後の購入明細表」TMP_MAX を作る
--     5. TMP_MIN と TMP_MAX を cust_id で結合する
--     6. 最初の price - 最後の price を計算する
--   という多段処理である。
--
--   つまりこのSQLの本質は、
--   「グループごとの代表行を2種類作ってから最後に組み合わせる」
--   ことにある。
-- ================================================

SELECT
       TMP_MIN.cust_id,
       -- TMP_MIN.cust_id:
       --   顧客IDを返す。
       --
       --   TMP_MIN と TMP_MAX は最終的に cust_id で結び付いているので、
       --   どちらから返しても同じ顧客IDになる。
       --
       --   ここでは「最初の購入明細側」の派生表から顧客IDを返している。

       TMP_MIN.price - TMP_MAX.price AS diff
       -- diff:
       --   最初の購入金額 - 最後の購入金額
       --   を表す差分列である。
       --
       --   ここで重要なのは、
       --   TMP_MIN.price は
       --     その顧客の最初の購入明細の金額
       --   であり、
       --   TMP_MAX.price は
       --     その顧客の最後の購入明細の金額
       --   だという点である。
       --
       --   したがって
       --     最初 - 最後
       --   の差をそのまま計算している。
       --
       --   たとえば顧客 A は
       --     最初: 500
       --     最後: 700
       --   なので
       --     500 - 700 = -200
       --   になる。

  FROM
       (
           SELECT
                  R1.cust_id,
                  -- 最初の購入明細の顧客ID

                  R1.seq,
                  -- 最初の購入明細の seq

                  R1.price
                  -- 最初の購入明細の金額
             FROM Receipts R1
                  INNER JOIN
                  (
                      SELECT
                             cust_id,
                             MIN(seq) AS min_seq
                             -- 顧客ごとの最小 seq
                             --
                             -- つまり最初の購入明細番号である。
                        FROM Receipts
                       GROUP BY cust_id
                       -- cust_id ごとに最小 seq を求める。
                       --
                       -- 概念的には
                       --   A -> 1
                       --   B -> 5
                       --   C -> 10
                       --   D -> 3
                       -- のような中間表になる。
                  ) R2
                    ON R1.cust_id = R2.cust_id
                   AND R1.seq     = R2.min_seq
                   -- R1 を R2 と結び付けることで、
                   -- 元表 Receipts の中から
                   -- 「各顧客の最小 seq を持つ元の行」
                   -- を取り戻している。
                   --
                   -- つまりこの内側全体は
                   --   各顧客の最初の購入明細表
                   -- を作っている。
       ) TMP_MIN
       -- TMP_MIN:
       --   各顧客の最初の購入明細だけを集めた派生表である。
       --
       --   ここには
       --     cust_id
       --     seq (最小)
       --     price (最初の購入金額)
       --   が入る。
       --
       --   つまり「最初の代表行」を表す中間結果である。

       INNER JOIN

       (
           SELECT
                  R3.cust_id,
                  -- 最後の購入明細の顧客ID

                  R3.seq,
                  -- 最後の購入明細の seq

                  R3.price
                  -- 最後の購入明細の金額
             FROM Receipts R3
                  INNER JOIN
                  (
                      SELECT
                             cust_id,
                             MAX(seq) AS min_seq
                             -- 顧客ごとの最大 seq
                             --
                             -- 列名が min_seq になっているが、
                             -- 実際に求めているのは MAX(seq) である。
                             --
                             -- したがって意味としては
                             --   「最後の購入明細番号」
                             -- である。
                             --
                             -- 実務や可読性の観点では
                             --   max_seq
                             -- の方が自然だが、
                             -- 今回は元のSQLを尊重している。
                        FROM Receipts
                       GROUP BY cust_id
                       -- cust_id ごとに最大 seq を求める。
                       --
                       -- 概念的には
                       --   A -> 3
                       --   B -> 12
                       --   C -> 70
                       --   D -> 3
                       -- のような中間表になる。
                  ) R4
                    ON R3.cust_id = R4.cust_id
                   AND R3.seq     = R4.min_seq
                   -- R3 を R4 と結び付けることで、
                   -- 元表 Receipts の中から
                   -- 「各顧客の最大 seq を持つ元の行」
                   -- を取り戻している。
                   --
                   -- つまりこの内側全体は
                   --   各顧客の最後の購入明細表
                   -- を作っている。
       ) TMP_MAX
       -- TMP_MAX:
       --   各顧客の最後の購入明細だけを集めた派生表である。
       --
       --   ここには
       --     cust_id
       --     seq (最大)
       --     price (最後の購入金額)
       --   が入る。
       --
       --   つまり「最後の代表行」を表す中間結果である。

    ON TMP_MIN.cust_id = TMP_MAX.cust_id;
    -- 最後に TMP_MIN と TMP_MAX を cust_id で結び付ける。
    --
    -- これにより、
    --   同じ顧客について
    --     最初の購入明細
    --     最後の購入明細
    --   が横に並ぶ。
    --
    --   その結果、
    --     TMP_MIN.price - TMP_MAX.price
    --   が計算できる。
    --
    --   つまりこの JOIN は
    --     2種類の代表行表を1つに統合する
    --   最後の段階である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Receipts から cust_id ごとの MIN(seq) を求める
-- 2. その MIN(seq) を元表へ JOIN して最初の購入明細表 TMP_MIN を作る
-- 3. Receipts から cust_id ごとの MAX(seq) を求める
-- 4. その MAX(seq) を元表へ JOIN して最後の購入明細表 TMP_MAX を作る
-- 5. TMP_MIN と TMP_MAX を cust_id で結合する
-- 6. price の差分を計算する
--
-- 概念的には次のような処理に近い。
--
--   first_receipt[cust_id] = row with minimum seq per customer
--   last_receipt[cust_id]  = row with maximum seq per customer
--
--   for each cust_id:
--       diff = first_receipt[cust_id].price
--            - last_receipt[cust_id].price
--       output cust_id, diff
--
-- つまりこれは、
--   「2種類の代表行を別々に作って最後に突き合わせる」
--   多段サブクエリアルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- 顧客 A:
--   最初: seq=1, price=500
--   最後: seq=3, price=700
--   diff = 500 - 700 = -200
--
-- 顧客 B:
--   最初: seq=5,  price=100
--   最後: seq=12, price=1000
--   diff = 100 - 1000 = -900
--
-- 顧客 C:
--   最初: seq=10, price=600
--   最後: seq=70, price=50
--   diff = 600 - 50 = 550
--
-- 顧客 D:
--   最初: seq=3, price=2000
--   最後: seq=3, price=2000
--   diff = 0
--
-- したがって最終結果は概ね
--
--   A  -200
--   B  -900
--   C   550
--   D     0
--
-- になる。


-- ================================================
-- 図解: サブクエリ・パラノイア版
-- ================================================
--
--   Receipts
--      |
--      +------------------------------+
--      | 最小 seq を求めるサブクエリ  |
--      +------------------------------+
--      |
--      v
--   顧客ごとの最初の明細表 TMP_MIN
--
--   Receipts
--      |
--      +------------------------------+
--      | 最大 seq を求めるサブクエリ  |
--      +------------------------------+
--      |
--      v
--   顧客ごとの最後の明細表 TMP_MAX
--
--   TMP_MIN --- cust_id で JOIN --- TMP_MAX
--                    |
--                    v
--                diff を計算
--
-- つまり、
--   最初の代表行表
--   最後の代表行表
-- を別々に作り、
--   最後に顧客単位で合流させている。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   集約結果だけではなく、
--   その集約結果に対応する元行の price を使いたい
-- ために、
--   「最初の行」
--   「最後の行」
-- をそれぞれ独立に取り出している点にある。
--
-- つまり
--   MIN(seq) と MAX(seq) を求めるだけでは不十分で、
--   それぞれに対応する元の price が必要
-- なのである。
--
-- そのため、
--   代表行を2種類作る
--   それを最後に JOIN する
-- という非常に段階的な設計になっている。
--
-- これが「サブクエリ・パラノイア」と呼ばれるゆえんであり、
--   サブクエリを多層に重ねた代表例である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- TMP_MIN
--   → 各顧客の最初の購入明細表
--
-- TMP_MAX
--   → 各顧客の最後の購入明細表
--
-- MIN(seq)
--   → 最初の購入明細番号
--
-- MAX(seq)
--   → 最後の購入明細番号
--
-- INNER JOIN ... ON cust_id / seq
--   → 集約結果に対応する元表行を回収する
--
-- TMP_MIN.price - TMP_MAX.price
--   → 最初と最後の金額差を求める
-- ================================================