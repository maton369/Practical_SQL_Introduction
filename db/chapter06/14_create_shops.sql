-- ================================================
-- ファイル名: 14_create_shops.sql
-- 役割:
--   事業所テーブル Shops を作成する。
--
-- このSQLの目的:
--   各会社の各事業所について
--     会社コード
--     事業所ID
--     従業員数
--     本店フラグ
--   を保持する表を定義すること。
--
-- 学習上の主題:
--   - 明細表の役割
--   - 集約対象となる fact table 的な表
--   - main_flg='Y' を条件にした絞り込みと SUM 集約
--
-- 今回の最重要ポイント:
--   後続の比較では、
--   Shops に対して
--     main_flg='Y'
--   の条件をかけ、
--     emp_nbr を会社ごとに SUM する
--   のが共通処理になる。
--
--   ただし
--     JOIN を先にするか
--     SUM を先にするか
--   でクエリの形と実行計画が変わる。
--
--   したがってこの表は、
--   「どの段階で集約するか」を考える中心になる。
-- ================================================

CREATE TABLE Shops
(
    co_cd CHAR(3) NOT NULL,
    -- co_cd:
    --   会社コードである。
    --
    --   Companies.co_cd と結び付く外部キー的な列である。
    --
    --   今回の集約はこの co_cd ごとに行う。

    shop_id CHAR(3) NOT NULL,
    -- shop_id:
    --   事業所IDである。
    --
    --   同一会社内の事業所を区別する。

    emp_nbr INTEGER NOT NULL,
    -- emp_nbr:
    --   その事業所の従業員数である。
    --
    --   後続のSQLでは、
    --   main_flg='Y' の事業所について
    --   これを会社ごとに SUM する。

    main_flg CHAR(1) NOT NULL,
    -- main_flg:
    --   本店フラグである。
    --
    --   'Y' の行だけが集計対象になる。
    --
    --   つまり
    --     WHERE main_flg = 'Y'
    --   が集約前のフィルタ条件になる。

    PRIMARY KEY (co_cd, shop_id)
    -- PRIMARY KEY(co_cd, shop_id):
    --   会社ごとの事業所が一意に決まる複合主キーである。
);