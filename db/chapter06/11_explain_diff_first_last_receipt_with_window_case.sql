-- ================================================
-- ファイル名: 11_explain_diff_first_last_receipt_with_window_case.sql
-- 役割:
--   リスト7.6のウィンドウ関数 + CASE 式版について、
--   MySQL の実行計画を確認する。
--
-- このSQLの目的:
--   DBMS が
--     派生表 WORK の生成
--     2本の ROW_NUMBER() 計算
--     WHERE による絞り込み
--     GROUP BY による集約
--   をどう処理するかを見ること。
--
-- 学習上の主題:
--   - ウィンドウ関数を複数使う場合の EXPLAIN の見方
--   - 派生表(DERIVED)の構造
--   - ORDER BY 昇順・降順の両方が必要なときのコスト感
--   - サブクエリ・パラノイア版との比較
--
-- 今回の最重要ポイント:
--   このSQLでは、
--     min_seq 用の昇順順位
--     max_seq 用の降順順位
--   の2つを同時に計算している。
--
--   そのため実行計画では、
--   派生表 WORK を作る過程で
--   並び替えや temporary 処理が見える可能性がある。
--
--   さらに外側では
--     WHERE min_seq = 1 OR max_seq = 1
--     GROUP BY cust_id
--   があるため、
--   「順位付き中間表を作ってから絞り込み・集約する」
--   構造として読むことが重要である。
-- ================================================

EXPLAIN
SELECT
       cust_id,
       SUM(CASE WHEN min_seq = 1 THEN price ELSE 0 END)
         - SUM(CASE WHEN max_seq = 1 THEN price ELSE 0 END) AS diff
  FROM
       (
           SELECT
                  cust_id,
                  price,
                  ROW_NUMBER()
                  OVER (
                      PARTITION BY cust_id
                      ORDER BY seq
                  ) AS min_seq,
                  ROW_NUMBER()
                  OVER (
                      PARTITION BY cust_id
                      ORDER BY seq DESC
                  ) AS max_seq
             FROM Receipts
       ) WORK
 WHERE WORK.min_seq = 1
    OR WORK.max_seq = 1
 GROUP BY cust_id;

-- ================================================
-- 実行計画で見たいポイント
-- ================================================
--
-- 1. 派生表 WORK の生成
--   このクエリは
--     まず順位付き中間表 WORK を作る
--   構造なので、
--   EXPLAIN では DERIVED が現れやすい。
--
-- 2. 2本の ROW_NUMBER() の計算
--   min_seq は seq 昇順、
--   max_seq は seq 降順である。
--
--   つまり
--     2種類の順序情報
--   を扱うため、
--   filesort や temporary が発生しやすい可能性がある。
--
-- 3. 外側の WHERE
--   WORK の中から
--     最初行
--     最後行
--   だけを残す。
--
--   これは「順位付け後にフィルタ」の段階である。
--
-- 4. 外側の GROUP BY
--   最後に cust_id ごとへまとめて差分を計算する。
--
--   つまり全体構造としては
--     派生表生成
--     -> 必要行抽出
--     -> 顧客ごと集約
--   となる。
--
-- 5. リスト7.5との比較
--   リスト7.5:
--     最初代表表と最後代表表を別々に作って JOIN
--
--   リスト7.6:
--     1つの WORK に両方の順位情報を持たせて集約
--
--   実行計画の見え方も、
--   「2本の派生表 + 最後の結合」
--   と
--   「1本の派生表 + 集約」
--   の違いとして表れやすい。


-- ================================================
-- 読み方の目安
-- ================================================
--
-- もし EXPLAIN で
--   DERIVED
-- が見えたら、
--   まず WORK が作られていると理解できる。
--
-- もし Extra に
--   Using filesort
--   Using temporary
-- が見えたら、
--   ウィンドウ関数や GROUP BY のための処理が行われている
--   可能性が高い。
--
-- 重要なのは、
--   このSQLが
--     先に全行へ最初順位・最後順位を付ける
--   その後で
--     必要行だけ残して集約する
--   という一括処理構造である点である。
--
--   多重サブクエリ版のように
--   代表行表を2本作って JOIN する構造とは
--   実行計画上の見え方がかなり変わる。
