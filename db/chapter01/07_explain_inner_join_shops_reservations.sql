-- ================================================
-- ファイル名: 08_explain_inner_join_shops_reservations.sql
-- 目的:
--   Shops と Reservations の内部結合を、
--   MySQL がどのような順序・方式で実行するか確認する。
--
-- EXPLAIN の役割:
--   実際の検索結果ではなく、
--   DBMS がどのテーブルから読み始め、
--   どのインデックスを使い、
--   何行くらい読む見込みなのかを確認するための命令である。
--
-- このSQLで確認したいこと:
--   1. どちらのテーブルから読み始めるか
--   2. shop_id に対するインデックスが使われるか
--   3. 結合方式が全件走査寄りか、インデックス利用寄りか
--
-- 結合アルゴリズムの概念:
--   INNER JOIN は概念的には
--   「片方のテーブルの各行に対して、
--     もう片方から対応する行を探す」
--   という処理である。
--
-- もしインデックスが無ければ、
--   比較回数が増えて重くなりやすい。
--
-- もし shop_id にインデックスがあれば、
--   対応行を効率的に探せる可能性が高い。
-- ================================================

EXPLAIN
SELECT
    S.shop_name
FROM
    Shops S
    INNER JOIN Reservations R
        ON S.shop_id = R.shop_id;