-- ================================================
-- ファイル名: 09_analyze_tables_shops_reservations.sql
-- 目的:
--   Shops テーブルと Reservations テーブルの統計情報を更新する。
--
-- 背景:
--   データベースのオプティマイザは、
--   実行計画を決めるときに
--   「テーブルに何行あるか」
--   「値の分布がどうなっているか」
--   「どのくらい絞り込めそうか」
--   といった統計情報を参考にする。
--
--   しかし、テーブル作成直後や大量の INSERT / UPDATE / DELETE の後では、
--   統計情報が古かったり不十分だったりして、
--   最適ではない実行計画が選ばれることがある。
--
--   そのため、MySQL では ANALYZE TABLE を使って
--   テーブル統計を更新し、
--   オプティマイザがより適切な判断をしやすい状態にする。
--
-- このファイルで行うこと:
--   1. Shops テーブルの統計情報を更新する
--   2. Reservations テーブルの統計情報を更新する
--
-- 他DBとの対応関係:
--   Oracle      : DBMS_STATS.GATHER_TABLE_STATS(...)
--   PostgreSQL  : ANALYZE テーブル名
--   MySQL       : ANALYZE TABLE テーブル名
--
-- 注意:
--   ANALYZE TABLE は検索結果を返すSQLではなく、
--   統計情報を収集・更新するための管理系SQLである。
-- ================================================


-- ================================================
-- Shops テーブルの統計情報を更新する
-- ================================================
--
-- この文は Shops テーブルを対象に、
-- オプティマイザが利用する統計情報を更新する。
--
-- これにより、たとえば以下のようなSQLに対して
-- 実行計画の精度向上が期待できる。
--
--   SELECT *
--   FROM Shops
--   WHERE shop_id = '00050';
--
-- shop_id が主キーであることや、
-- 行数・分布などの情報をもとに、
-- MySQL がより適切なアクセス方式
-- （全件走査か、インデックス利用か など）
-- を判断しやすくなる。
ANALYZE TABLE Shops;


-- ================================================
-- Reservations テーブルの統計情報を更新する
-- ================================================
--
-- この文は Reservations テーブルを対象に、
-- 統計情報を更新する。
--
-- Reservations は結合時の外側表・内側表の候補になりうるため、
-- 行数や分布の統計が実行計画に影響する。
--
-- たとえば次のような結合SQLでは、
--
--   SELECT S.shop_name
--   FROM Shops S
--   INNER JOIN Reservations R
--     ON S.shop_id = R.shop_id;
--
-- MySQL は
--   - Shops から読み始めるか
--   - Reservations から読み始めるか
--   - どのインデックスを使うか
-- を判断する必要がある。
--
-- そのとき、Reservations の統計情報が適切に更新されていると、
-- より妥当な結合順序やアクセス方式を選びやすくなる。
ANALYZE TABLE Reservations;


-- ================================================
-- 補足1: まとめて書くこともできる
-- ================================================
--
-- MySQL では複数テーブルをまとめて ANALYZE TABLE に渡すこともできる。
-- たとえば次の1文でも同様の意図を表せる。
--
--   ANALYZE TABLE Shops, Reservations;
--
-- ただし、学習用・教材用としては
-- どのテーブルに対して何をしているかが明確になるため、
-- 1テーブルずつ分けて書く方が分かりやすいことが多い。


-- ================================================
-- 補足2: 統計更新後は EXPLAIN で確認する
-- ================================================
--
-- ANALYZE TABLE 自体は統計情報を更新する命令であり、
-- 実際にその統計をもとにどんな実行計画が選ばれたかは、
-- EXPLAIN を使って確認する。
--
-- つまり役割分担は次の通りである。
--
--   ANALYZE TABLE
--     -> 統計情報を更新する
--
--   EXPLAIN SELECT ...
--     -> その統計をもとに、どんな実行計画になったか確認する
--
-- 例:
--
--   EXPLAIN
--   SELECT
--       S.shop_name
--   FROM
--       Shops S
--       INNER JOIN Reservations R
--           ON S.shop_id = R.shop_id;


-- ================================================
-- 補足3: 実行計画の見方との関係
-- ================================================
--
-- 統計情報が更新されると、EXPLAIN に出る次のような項目の見積もり精度が
-- 改善する可能性がある。
--
--   - rows
--       何行くらい読む見込みか
--
--   - filtered
--       読んだ行のうち、どれくらい条件に合う見込みか
--
--   - type
--       ALL, ref, const, eq_ref など、どのアクセス方式を選ぶか
--
-- 特に結合SQLでは、
-- 推定行数がずれると結合順序やアクセス方式の選択も変わりうるため、
-- 統計情報の更新は重要である。