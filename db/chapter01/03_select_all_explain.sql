-- EXPLAIN は、「この SELECT 文をデータベースがどのように実行する予定か」を確認するための命令です。
-- 実際に取得結果そのものを見るというより、
--   1. どのテーブルを読むか
--   2. 全件走査になるか、インデックスが使われるか
--   3. どの順番・方法で検索するか
-- といった「実行計画」を調べる目的で使います。
--
-- 今回の SQL は非常に単純で、
-- Shops テーブルの全ての列を、全ての行について取得する SELECT 文です。
-- その前に EXPLAIN を付けることで、
-- 「Shops テーブルをどのように読みに行くか」を確認できます。
--
-- この SQL 自体は、通常は次のような意味になります。
--   SELECT * FROM Shops;
--   → Shops テーブルの全列・全行を取得する
--
-- ただし今回は EXPLAIN が先頭にあるため、
-- 実データではなく実行計画が返されます。
--
-- なお、WHERE 句が無いため、
-- 一般には Shops テーブル全体を読む計画
-- （フルスキャン / 全件走査）になりやすいです。
-- テーブルサイズが小さければ問題ありませんが、
-- 行数が多い場合はコストが大きくなる可能性があります。
EXPLAIN
SELECT
    *
FROM
    Shops;