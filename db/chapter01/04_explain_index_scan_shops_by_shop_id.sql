-- ================================================
-- ファイル名: 04_explain_index_scan_shops_by_shop_id.sql
-- 目的:
--   Shops テーブルに対して、shop_id を条件に 1件の店舗を検索するSQLの
--   実行計画を確認する。
--
-- このSQLで確認したいこと:
--   - shop_id を条件にした検索で、インデックスが使われるか
--   - 全件走査ではなく、必要な行だけを効率的に探せるか
--   - EXPLAIN の結果として、type や key にどのような値が出るか
--
-- 背景:
--   前回のような WHERE 句なしの SELECT * FROM Shops; では、
--   MySQL はテーブル全体を順番に読む「全件走査」を行いやすい。
--   一方、今回のSQLでは shop_id = '00050' という条件があるため、
--   shop_id にインデックスが存在すれば、そのインデックスを利用して
--   必要な行だけを高速に探しにいける可能性が高い。
--
-- 学習上のポイント:
--   このSQL自体はデータを取得する SELECT 文であるが、
--   先頭に EXPLAIN を付けることで、
--   「実際にデータを取る前に、DBMS がどう実行するつもりか」
--   を確認できる。
-- ================================================

EXPLAIN
SELECT
    *
FROM
    Shops
WHERE
    shop_id = '00050';


-- ================================================
-- このSQLの意味
-- ================================================
--
-- 1. Shops テーブルを検索対象にする
-- 2. 各行のうち、shop_id が '00050' と一致する行を探す
-- 3. 一致した行について、全列(*)を返す
--
-- ただし今回は EXPLAIN を付けているため、
-- 実際の店舗データを返すのではなく、
-- 「どのように検索する予定か」という実行計画が返される。
--
-- 期待される挙動:
--   shop_id が主キーまたはインデックス列であれば、
--   MySQL はそのインデックスを使って目的の行へ直接近づこうとする。
--
-- その場合、前回のような
--   type = ALL
-- ではなく、より効率のよい
--   type = const
--   または type = ref
-- などが出る可能性がある。
--
-- 特に、shop_id が主キーまたは UNIQUE であれば、
-- 1件に確定する検索として非常に効率のよい実行計画になることが多い。


-- ================================================
-- 実行計画で注目する列
-- ================================================
--
-- 1. table
--    どのテーブルを検索しているかを表す。
--    今回は Shops が表示されるはずである。
--
-- 2. type
--    アクセス方式を表す重要な列である。
--    値が ALL なら全件走査、
--    ref / range / const / eq_ref などなら、
--    より効率的な検索が行われている可能性が高い。
--
-- 3. possible_keys
--    利用候補となるインデックスが表示される。
--    ここに shop_id に対応するインデックス名が出るかを確認する。
--
-- 4. key
--    実際に使われたインデックスが表示される。
--    ここが NULL でなければ、インデックスが使われたと判断しやすい。
--
-- 5. rows
--    何行くらい読む見込みかを表す。
--    インデックスが効いていれば、この値はかなり小さくなることが多い。
--
-- 6. filtered
--    読み込んだ行のうち、条件に一致すると見込まれる割合を表す。
--
-- 7. Extra
--    追加情報が表示される。
--    特別な処理がなければ NULL のこともある。


-- ================================================
-- アルゴリズムのイメージ
-- ================================================
--
-- インデックスが無い場合:
--
--   Shops の先頭行から順に
--   「shop_id == '00050' か？」を1行ずつ確認する
--
--   1行目 -> 比較
--   2行目 -> 比較
--   3行目 -> 比較
--   ...
--   50行目付近 -> 一致
--
--   これは線形探索に近く、行数 N に対して
--
--   O(N)
--
--   の処理になりやすい。
--
--
-- インデックスがある場合:
--
--   shop_id 用の索引構造
--   （B+木インデックスなど）を使って、
--   '00050' に対応する位置を効率的に探す
--
--   その後、対応する行だけを取り出す
--
--   そのため、全件をなめるよりかなり効率が良い。
--
--   概念的には、
--   「順番に全部見る」のではなく
--   「索引を手がかりに目的地へ直接近づく」
--   という動きになる。


-- ================================================
-- 処理フローの図解
-- ================================================
--
--   +----------------------------------------------+
--   | EXPLAIN SELECT * FROM Shops                  |
--   | WHERE shop_id = '00050';                     |
--   +----------------------------------------------+
--                      |
--                      v
--   +----------------------------------------------+
