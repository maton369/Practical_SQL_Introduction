--   +----------------------------------------------+
--                      |
--                      v
--   +----------------------------------------------+
--   | shop_id に使えるインデックスがあるか確認     |
--   +----------------------------------------------+
--              |                          |
--          ある                          ない
--              |                          |
--              v                          v
--   +---------------------+   +-----------------------------+
--   | インデックスを辿る  |   | 全行を先頭から順に確認      |
--   +---------------------+   +-----------------------------+
--              |                          |
--              v                          v
--   +---------------------+   +-----------------------------+
--   | 該当行を特定        |   | 条件一致行を見つける        |
--   +---------------------+   +-----------------------------+
--              |                          |
--              +------------+-------------+
--                           |
--                           v
--   +----------------------------------------------+
--   | 実行計画を結果として返す                      |
--   +----------------------------------------------+


-- ================================================
-- 補足
-- ================================================
--
-- 今回は SELECT * なので、一致した行の全列を取得する。
-- 学習用としては分かりやすいが、
-- 実務では必要な列だけに絞る方が望ましいことも多い。
--
-- 例:
--   EXPLAIN
--   SELECT shop_id, shop_name
--   FROM Shops
--   WHERE shop_id = '00050';
--
-- このように書くと、
-- 何を取得したいかがより明確になる。
--
-- また、実際にインデックススキャンになっているかどうかは、
-- EXPLAIN の結果を見て
--   - key が NULL ではないか
--   - type が ALL ではないか
--   - rows が小さくなっているか
-- を確認するのが重要である。