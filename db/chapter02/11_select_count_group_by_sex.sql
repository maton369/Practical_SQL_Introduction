-- ================================================
-- ファイル名: 11_select_count_group_by_sex.sql
-- 役割:
--   Address テーブルに登録されているデータを、
--   sex 列の値ごとにグループ化し、
--   それぞれの人数を数える。
--
-- このSQLの目的:
--   個々の人の情報を1行ずつ表示するのではなく、
--   「男は何人いるか」「女は何人いるか」のように、
--   集計結果として人数を求めることである。
--
-- 学習上の主題:
--   GROUP BY を使って同じ値どうしをまとめ、
--   COUNT(*) を使って各グループの件数を数える方法を理解すること。
--
-- 今回のSQLは、
--   行をそのまま返す SELECT ではなく、
--   複数行をまとめて要約した結果を返す SELECT
-- になっている点が重要である。
-- ================================================

SELECT
    -- SELECT 句の1列目:
    --   sex 列をそのまま出力する。
    --
    --   ここでは「どのグループの集計結果なのか」を表す見出しの役割を持つ。
    --   今回なら、結果には '男' や '女' が出てくる。
    sex,

    -- SELECT 句の2列目:
    --   COUNT(*) は件数を数える集約関数である。
    --
    --   * は「行全体」を意味しており、
    --   COUNT(*) は「そのグループに属している行が何件あるか」を数える。
    --
    --   今回は sex ごとにグループ化した後なので、
    --   男グループの行数、女グループの行数がそれぞれ求められる。
    COUNT(*)
FROM
    -- FROM 句:
    --   集計対象のテーブルを指定する。
    --
    --   今回は Address テーブルに入っている全行を対象にして、
    --   その後で sex ごとにまとめる。
    Address
GROUP BY
    -- GROUP BY 句:
    --   指定した列の値が同じ行どうしを1つのグループとしてまとめる。
    --
    --   今回は sex を指定しているため、
    --   sex = '男' の行は男グループ、
    --   sex = '女' の行は女グループ、
    --   というように分かれる。
    --
    --   その後、各グループに対して COUNT(*) が計算される。
    sex;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を読み込む
-- 2. sex 列の値ごとに行をグループ分けする
-- 3. 各グループについて COUNT(*) を計算する
-- 4. sex と件数の組を結果として返す
--
-- 概念的には次のような処理に近い。
--
--   groups = {}
--
--   for each row in Address:
--       key = row.sex
--       groups[key] += 1
--
--   for each key in groups:
--       output key, groups[key]
--
-- つまりこれは、
--   「個別行をそのまま返す」のではなく、
--   同じ sex を持つ行をまとめて、その個数を数える
-- という集計処理である。


-- ================================================
-- GROUP BY の意味
-- ================================================
--
-- GROUP BY は、
--   「同じ値を持つ行を1つのまとまりとして扱う」
-- ための構文である。
--
-- 今回の
--
--   GROUP BY sex
--
-- は、
--   sex が同じ行をまとめる
-- という意味になる。
--
-- たとえば Address テーブルに
--
--   小川   男
--   前田   女
--   森     男
--   林     男
--   井上   女
--
-- のようなデータがあれば、
--
--   男グループ: 小川, 森, 林, ...
--   女グループ: 前田, 井上, ...
--
-- のように分けられる。
--
-- そして COUNT(*) は、
-- それぞれのグループに何行あるかを数える。


-- ================================================
-- COUNT(*) の意味
-- ================================================
--
-- COUNT(*) は、行数を数える集約関数である。
--
-- 一般に COUNT は「個数を数える」ために使う。
-- 今回の COUNT(*) は、
--   グループの中に何行あるか
-- を数えている。
--
-- ここで * を使っているため、
-- 特定の列の値ではなく、行そのものの件数を数えていると考えればよい。
--
-- 今回は sex ごとに GROUP BY しているので、
-- COUNT(*) は
--   男の人数
--   女の人数
-- を返すことになる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のような sex データがあるとする。
--
--   name    | sex
--   --------+----
--   小川    | 男
--   前田    | 女
--   森      | 男
--   林      | 男
--   井上    | 女
--   佐々木  | 女
--   松本    | 女
--   佐藤    | 女
--   鈴木    | 男
--
-- これを sex ごとにまとめると、
--
--   男グループ: 小川, 森, 林, 鈴木
--   女グループ: 前田, 井上, 佐々木, 松本, 佐藤
--
-- となる。
--
-- したがって COUNT(*) の結果は、
--
--   sex | COUNT(*)
--   ----+---------
--   男  | 4
--   女  | 5
--
-- のようになる。


-- ================================================
-- 図解: GROUP BY と COUNT(*) のイメージ
-- ================================================
--
--   Address テーブル
--        |
--        v
--   +-----------------------------+
--   | sex 列の値ごとに分ける      |
--   +-----------------------------+
--        |
--   +----+-------------------------------+
--   |                                    |
--   v                                    v
--   男グループ                           女グループ
--   |                                    |
--   | 4行                                | 5行
--   v                                    v
-- COUNT(*) = 4                         COUNT(*) = 5
--   |                                    |
--   +----------------+-------------------+
--                    |
--                    v
--           集計結果として返す
--
--
-- 具体的には、
--
--   男 → 4
--   女 → 5
--
-- のような結果になる。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT sex, COUNT(*)
--   → sex ごとの件数を表示したい
--
-- FROM Address
--   → Address テーブルを集計対象にする
--
-- GROUP BY sex
--   → sex が同じ行どうしをまとめる
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   GROUP BY を使う場合、
--   SELECT 句には
--     - グループ化に使った列
--     - 集約関数の結果
--   を書くのが基本である。
--
--   今回なら
--     sex
--     COUNT(*)
--   がそれに当たる。
-- ================================================


-- ================================================
-- 補足2:
--   このSQLには WHERE 句が無いので、
--   Address テーブルの全行が集計対象になる。
--
--   もし特定条件に絞ってから男女別に集計したい場合は、
--   たとえば次のように WHERE を先に付ける。
--
--     SELECT sex, COUNT(*)
--     FROM Address
--     WHERE address = '東京都'
--     GROUP BY sex;
--
--   この場合は、東京都の人だけを対象に男女別人数を数える。
-- ================================================


