-- ファイル名: 28_delete_all_from_address.sql
-- 役割:
--   Address テーブルに入っている全データを削除する。
--
-- このSQLの目的:
--   Address テーブルの中身を空にしたいときに、
--   DELETE 文で全行を削除する方法を理解すること。
--
-- 学習上の主題:
--   DELETE FROM 表名;
--   という最も基本的な削除構文を理解すること。
--
-- 今回の最重要ポイント:
--   WHERE 句を書かない DELETE は、
--   表の中の「すべての行」が削除対象になる。
--
--   つまりこのSQLは、
--   Address テーブルそのものを消すのではなく、
--   Address テーブルに入っている全レコードを消す。
--
--   ここは非常に重要である。
--   DELETE は「表定義の削除」ではなく「行データの削除」である。
-- ================================================

DELETE FROM
    -- DELETE FROM:
    --   指定した表から行を削除するための構文である。
    --
    --   SELECT が「読む」処理、
    --   INSERT が「追加する」処理だったのに対し、
    --   DELETE は「既存の行を消す」処理である。
    Address;
    -- Address:
    --   削除対象となる表である。
    --
    --   今回は Address テーブルの全行が対象になる。
    --
    -- ; の前に WHERE が無いため、
    -- 条件による絞り込みは一切行われない。
    --
    -- したがって、Address に入っている全レコードが削除される。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを削除対象として指定する
-- 2. WHERE 句が無いので、全行を削除候補とみなす
-- 3. Address に存在するすべての行を削除する
-- 4. 結果として、表の中身は空になる
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       delete row
--
-- つまりこれは、
--   「Address テーブルの全件削除」
--   を行うSQLである。


-- ================================================
-- DELETE とは何か
-- ================================================
--
-- DELETE は、
--   表に存在する既存の行を削除するための構文である。
--
-- 一般形は次のようになる。
--
--   DELETE FROM 表名
--   WHERE 条件;
--
-- 今回は WHERE が無いので、
--
--   DELETE FROM Address;
--
-- という形になっている。
--
-- この場合、
--   条件に一致する行
-- ではなく、
--   条件指定が無いので全行
-- が削除対象になる。
--
-- したがって、
--   全削除
-- を意味する DELETE 文である。


-- ================================================
-- 「表が消える」のではない点に注意
-- ================================================
--
-- このSQLを実行しても、
-- Address テーブルそのものは消えない。
--
-- 消えるのは
--   表の中に入っているデータ行
-- だけである。
--
-- つまり実行後も、
--   Address という表定義
--   name, phone_nbr, address, sex, age という列構成
--   PRIMARY KEY などの制約
-- は残る。
--
-- 空の表として残るだけである。
--
-- もし表そのものを削除したいなら、
-- DELETE ではなく DROP TABLE を使う。
--
-- ここは混同しやすいので非常に重要である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば実行前の Address が次のような状態だとする。
--
--   name    | phone_nbr       | address    | sex | age
