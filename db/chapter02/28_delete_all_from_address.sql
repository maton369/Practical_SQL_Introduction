--   小川    | 080-3333-XXXX   | 東京都     | 男  | 30
--   前田    | 090-0000-XXXX   | 東京都     | 女  | 21
--   森      | 090-2984-XXXX   | 東京都     | 男  | 45
--   林      | 080-3333-XXXX   | 福島県     | 男  | 32
--   井上    | NULL            | 福島県     | 女  | 55
--   佐々木  | 080-5848-XXXX   | 千葉県     | 女  | 19
--   松本    | NULL            | 千葉県     | 女  | 20
--   佐藤    | 090-1922-XXXX   | 三重県     | 女  | 25
--   鈴木    | 090-0001-XXXX   | 和歌山県   | 男  | 32
--
-- これに対して
--
--   DELETE FROM Address;
--
-- を実行すると、実行後は次のようになる。
--
--   Address テーブル
--   行数 = 0
--
-- つまり表は存在するが、中のデータが空になる。


-- ================================================
-- 図解: 全件 DELETE のイメージ
-- ================================================
--
--   Address テーブル
--   +----------------------------------+
--   | 小川                             |
--   | 前田                             |
--   | 森                               |
--   | 林                               |
--   | 井上                             |
--   | 佐々木                           |
--   | 松本                             |
--   | 佐藤                             |
--   | 鈴木                             |
--   +----------------------------------+
--                 |
--                 v
--        DELETE FROM Address;
--                 |
--                 v
--   +----------------------------------+
--   |   全行が削除される               |
--   +----------------------------------+
--                 |
--                 v
--   Address テーブル
--   +----------------------------------+
--   |   行が 0 件                      |
--   +----------------------------------+
--
-- つまり、
--   WHERE が無い DELETE は
--   全件削除
-- である。


-- ================================================
-- DELETE と SELECT の違い
-- ================================================
--
-- たとえば
--
--   SELECT * FROM Address;
--
-- は、Address の全行を読むだけであり、
-- データは変わらない。
--
-- 一方、
--
--   DELETE FROM Address;
--
-- は、Address の全行を実際に消す。
--
-- つまり DELETE は、
--   結果を見るだけの文ではなく、
--   表の状態を変更する更新系SQL
-- である。
--
-- このため、実行には特に注意が必要である。


-- ================================================
-- 実務上の注意
-- ================================================
--
-- WHERE なしの DELETE は非常に強力であり、
-- 誤って実行すると全データを消してしまう。
--
-- そのため実務では、
--
-- 1. 先に SELECT で対象確認する
-- 2. 必要ならトランザクション内で実行する
-- 3. 本当に全件削除かを再確認する
--
-- といった運用が重要になる。
--
-- 教材としては簡潔だが、
-- 実際の運用ではかなり注意すべき文である。


-- ================================================
-- 補足1:
--   DELETE は行削除であり、表定義削除ではない。
--
--   表ごと消す:
--     DROP TABLE Address;
--
--   行だけ消す:
--     DELETE FROM Address;
-- ================================================


