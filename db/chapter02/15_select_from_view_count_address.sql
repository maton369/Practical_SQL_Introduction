-- ================================================
-- ファイル名: 15_select_from_view_count_address.sql
-- 役割:
--   作成済みの CountAddress ビューを参照し、
--   都道府県別人数の集計結果を取得する。
--
-- このSQLの目的:
--   直前に CREATE VIEW で定義した CountAddress を、
--   通常の表と同じように SELECT して利用すること。
--
-- 学習上の主題:
--   ビューは作成したあと、
--   実表とほぼ同じ感覚で SELECT できることを理解すること。
--
-- 今回の重要点:
--   この SQL 自体は単純な SELECT 文に見えるが、
--   実際には CountAddress ビューの背後にある
--   集計 SELECT 文が使われている。
--
--   つまり、
--   ビューを参照することで
--   「都道府県別人数の集計結果」を
--   あたかも1つの表のように取り出している。
-- ================================================

SELECT
    -- v_address 列:
    --   CountAddress ビューで定義した都道府県名の列である。
    --
    --   元になっているのは Address テーブルの address 列だが、
    --   ビュー作成時に v_address という名前を付けているため、
    --   ここではその名前で参照する。
    v_address,

    -- cnt 列:
    --   CountAddress ビューで定義した件数列である。
    --
    --   元になっているのは COUNT(*) の結果であり、
    --   その都道府県に属する人数を表す。
    cnt
FROM
    -- CountAddress ビュー:
    --   CREATE VIEW で定義した仮想表である。
    --
    --   見た目としては通常のテーブルのように FROM 句で使えるが、
    --   実際には Address テーブルを集計する SELECT 文の結果を表している。
    CountAddress;


-- ================================================
-- このSQLの意味
-- ================================================
--
-- この SQL は一見すると
--
--   ある表 CountAddress から
--   v_address と cnt を取り出している
--
-- ように見える。
--
-- しかし CountAddress は実表ではなくビューである。
--
-- そのため、概念的には
--
--   SELECT address, COUNT(*)
--     FROM Address
--    GROUP BY address
--
-- という元の集計結果を、
-- CountAddress という名前経由で参照していると考えられる。
--
-- つまり今回の SQL は、
--   「ビューを使って集計結果を簡単に再利用する」
-- 例になっている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. CountAddress ビューを参照する
-- 2. ビュー定義の中身に相当する集計結果を得る
-- 3. その結果から v_address と cnt を取り出す
-- 4. そのまま表示する
--
-- 概念的には次のように考えられる。
--
--   rows = result of:
--       SELECT address, COUNT(*)
--       FROM Address
--       GROUP BY address
--
--   for each row in rows:
--       output row.v_address, row.cnt
--
-- つまり、
--   ビューを表として読むことで、
--   元の複雑さを隠しながら集計結果だけを使っている。


-- ================================================
-- ビューを使う利点
-- ================================================
--
-- このようにビューを使うと、
-- 毎回次のような長い SQL を書かなくても済む。
--
--   SELECT address, COUNT(*)
--   FROM Address
--   GROUP BY address;
--
-- 代わりに、
--
--   SELECT v_address, cnt
--   FROM CountAddress;
--
-- と短く書ける。
--
-- したがってビューは、
--   - 再利用しやすい
--   - 読みやすい
--   - 元の集計ロジックを隠蔽できる
-- という利点を持つ。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- CountAddress ビューが次のような内容を持つとする。
--
--   v_address  | cnt
--   -----------+----
--   東京都     | 3
--   福島県     | 2
--   千葉県     | 2
--   三重県     | 1
--   和歌山県   | 1
--
-- このとき今回の SQL
--
--   SELECT v_address, cnt
--   FROM CountAddress;
--
-- は、そのまま次のような結果を返す。
--
--   v_address  | cnt
--   -----------+----
--   東京都     | 3
--   福島県     | 2
--   千葉県     | 2
--   三重県     | 1
--   和歌山県   | 1


-- ================================================
-- 図解: ビュー利用のイメージ
-- ================================================
--
--   CountAddress ビュー
--        |
--        v
--   +-------------------------------+
--   | v_address と cnt を持つ       |
--   | 仮想表として見える            |
--   +-------------------------------+
--        |
--        v
--   SELECT v_address, cnt
--   FROM CountAddress;
--        |
--        v
--   集計済みの都道府県別人数を取得
--
--
-- 内部的な考え方:
--
--   Address テーブル
--        |
--        v
--   GROUP BY address
--        |
--        v
--   COUNT(*) で人数集計
--        |
--        v
--   CountAddress ビュー
--        |
--        v
--   それを表のように SELECT


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT v_address, cnt
--   → ビューの中から表示したい列を指定する
--
-- FROM CountAddress
--   → CountAddress ビューを参照する
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   ビューを使う側から見ると、
--   CountAddress は通常の表のように扱える。
--
--   これにより、
--   元の GROUP BY や COUNT(*) を毎回意識せずに済む。
-- ================================================


-- ================================================
-- 補足2:
--   ただし、ビューの背後には元の SELECT 文があるため、
--   ビューの内容は Address テーブルの状態に依存する。
--
--   つまり、Address のデータが変われば、
--   CountAddress から見える集計結果も変わりうる。
-- ================================================


-- ================================================
-- 補足3:
--   ORDER BY が無いため、結果の並び順は保証されない。
--
--   都道府県名順で見たい場合は、たとえば次のように書ける。
--
--     SELECT v_address, cnt
--     FROM CountAddress
--     ORDER BY v_address;
