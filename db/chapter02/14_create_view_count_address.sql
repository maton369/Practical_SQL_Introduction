-- ================================================
-- ファイル名: 14_create_view_count_address.sql
-- 役割:
--   Address テーブルを都道府県ごとに集計した結果を、
--   CountAddress というビューとして定義する。
--
-- このSQLの目的:
--   毎回
--
--     SELECT address, COUNT(*)
--       FROM Address
--      GROUP BY address;
--
--   を直接書かなくても、
--   あたかも1つの表のように再利用できるようにすること。
--
-- 学習上の主題:
--   CREATE VIEW によって
--   「SELECT 文の結果に名前を付けて保存する」
--   という考え方を理解すること。
--
-- ビューとは:
--   ビューは、実表のように見える「仮想的な表」である。
--   中身としてデータを別に持つというより、
--   「どの SELECT 文を使って見せるか」を定義している。
--
-- 今回の CountAddress ビューは、
--   Address テーブルを address ごとに GROUP BY し、
--   各都道府県の人数を COUNT(*) で集計した結果
-- を仮想表として扱えるようにする。
-- ================================================

CREATE VIEW CountAddress (v_address, cnt)
AS
SELECT
    -- address 列:
    --   元の Address テーブルの address 列である。
    --
    --   今回は都道府県ごとにグループ化しているので、
    --   結果としては
    --     東京都
    --     福島県
    --     千葉県
    --   のように、都道府県名が1行ずつ並ぶ。
    --
    --   CREATE VIEW の直後で
    --     (v_address, cnt)
    --   と列名を指定しているため、
    --   この SELECT の address 列は
    --   ビュー上では v_address という列名で見える。
    address,

    -- COUNT(*) 列:
    --   その都道府県グループに属する行数を数える。
    --
    --   つまり、
    --   「その都道府県に登録されている人数」
    --   を表す。
    --
    --   ビュー上では、この列は cnt という名前で見える。
    COUNT(*)
FROM
    -- 集計元の実表:
    --   Address テーブルを元データとして使う。
    Address
GROUP BY
    -- GROUP BY address:
    --   address 列の値が同じ行どうしを1つのグループにまとめる。
    --
    --   その結果、都道府県ごとのグループが作られ、
    --   各グループに対して COUNT(*) が計算される。
    address;


-- ================================================
-- CREATE VIEW の構文の意味
-- ================================================
--
-- 一般形は次のようになる。
--
--   CREATE VIEW ビュー名 (列名1, 列名2, ...)
--   AS
--   SELECT ...;
--
-- 今回は
--
--   CREATE VIEW CountAddress (v_address, cnt)
--   AS
--   SELECT address, COUNT(*)
--     FROM Address
--    GROUP BY address;
--
-- である。
--
-- 各部分の意味:
--
--   CREATE VIEW
--     → 新しいビューを定義する
--
--   CountAddress
--     → ビューの名前
--
--   (v_address, cnt)
--     → ビューとして見せるときの列名
--
--   AS
--     → これ以降に、このビューの中身となる SELECT 文を書く
--
--   SELECT ...
--     → ビューが参照されたときに使われる定義本体
--
-- つまり今回のSQLは、
--   「都道府県別人数の集計結果に CountAddress という名前を付ける」
-- ための定義文である。


-- ================================================
-- ビュー上の列名を指定している意味
-- ================================================
--
-- CREATE VIEW CountAddress (v_address, cnt)
--
-- の丸括弧部分では、
-- ビューに見せる列名を指定している。
--
-- ここで指定しているのは
--
--   v_address
--   cnt
--
-- である。
--
-- したがって、このビューを後で SELECT するときは
--
--   SELECT v_address, cnt
--   FROM CountAddress;
--
-- のように書ける。
--
-- もし列名をここで指定しなければ、
-- 元の SELECT 文の列名や式の見え方に依存する。
--
-- 今回は
--   集計結果の列を分かりやすく使うために
--   明示的に名前を付けている
-- と理解すればよい。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを集計元として使う
-- 2. address ごとに GROUP BY する
-- 3. 各グループの人数を COUNT(*) で数える
-- 4. その SELECT 文全体に CountAddress というビュー名を付ける
-- 5. 後で CountAddress を表のように参照できるようにする
--
-- 概念的には次のような考え方に近い。
--
--   define CountAddress as:
--       for each address group in Address:
--           output address, count(rows in that group)
--
-- つまり、
--   「集計結果に名前を付けて再利用可能にする」
-- のがビュー作成である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のような住所分布があるとする。
--
--   東京都   → 3人
--   福島県   → 2人
--   千葉県   → 2人
--   三重県   → 1人
--   和歌山県 → 1人
--
-- すると、このビュー CountAddress は
-- 次のような表に見える。
--
--   v_address  | cnt
--   -----------+----
--   東京都     | 3
--   福島県     | 2
--   千葉県     | 2
--   三重県     | 1
--   和歌山県   | 1
--
-- ただし重要なのは、
-- これは物理的に手入力した表ではなく、
-- Address から計算される結果に名前を付けたものだという点である。


-- ================================================
-- 図解: ビュー作成のイメージ
-- ================================================
--
--   Address テーブル
--        |
--        v
--   +-------------------------------+
--   | address ごとに GROUP BY する  |
--   +-------------------------------+
--        |
--        v
--   +-------------------------------+
--   | COUNT(*) で人数を数える       |
--   +-------------------------------+
--        |
--        v
--   +-------------------------------+
--   | 集計結果                      |
--   | 東京都   3                    |
--   | 福島県   2                    |
--   | 千葉県   2                    |
--   | 三重県   1                    |
--   | 和歌山県 1                    |
--   +-------------------------------+
--        |
--        v
--   +-------------------------------+
--   | CountAddress という名前で定義 |
--   +-------------------------------+
--
-- この結果、後で CountAddress を
-- 1つの表のように参照できる。


-- ================================================
-- 補足1:
--   ビューは「SELECT 文に名前を付けたもの」と考えると分かりやすい。
--
--   複雑な SELECT 文を毎回書かずに済むため、
--   再利用性や可読性の向上に役立つ。
-- ================================================


-- ================================================
-- 補足2:
--   今回のビューは集計結果を返すビューなので、
--   元の Address テーブルの1行1人の粒度ではなく、
--   1行1都道府県の粒度になっている。
--
--   つまり、ビューを使うと
--   「見たい粒度に加工した結果」を仮想表として扱える。
-- ================================================


-- ================================================
-- 補足3:
--   ビューを使う側は、
--   中で GROUP BY や COUNT(*) をしていることを毎回意識しなくても、
--   CountAddress という表を参照する感覚で使える。
--   これがビューの大きな利点の1つである。
