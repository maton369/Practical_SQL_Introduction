-- ================================================
-- ファイル名: 12_select_address_group_by_address_having_count_eq_1.sql
-- 役割:
--   Address テーブルを都道府県ごとに集計し、
--   その中から「人数が1人だけの都道府県」だけを選び出す。
--
-- このSQLの目的:
--   個々の人をそのまま表示するのではなく、
--   address 列の値ごとに人数を数えた上で、
--   その人数がちょうど1である都道府県だけを取り出すことである。
--
-- 学習上の主題:
--   GROUP BY でグループ化した結果に対して、
--   HAVING で条件をかける方法を理解すること。
--
-- 今回のSQLで重要なのは、
--   WHERE ではなく HAVING を使っている
-- という点である。
--
-- WHERE は「元の行」に対して条件をかけるが、
-- HAVING は「GROUP BY の結果できたグループ」に対して条件をかける。
-- ================================================

SELECT
    -- SELECT 句の1列目:
    --   address 列を出力する。
    --
    --   ここでは「どの都道府県の集計結果なのか」を表す。
    address,

    -- SELECT 句の2列目:
    --   COUNT(*) は、そのグループに属する行数を数える集約関数である。
    --
    --   今回は address ごとにグループ化しているため、
    --   「その都道府県に住んでいる人数」を表す。
    COUNT(*)
FROM
    -- FROM 句:
    --   集計対象のテーブルを指定する。
    --
    --   今回は Address テーブルの全行を対象にする。
    Address
GROUP BY
    -- GROUP BY 句:
    --   address 列の値が同じ行どうしを1つのグループとしてまとめる。
    --
    --   たとえば、
    --     東京都の行
    --     福島県の行
    --     千葉県の行
    --   のように、都道府県ごとのグループが作られる。
    address
HAVING
    -- HAVING 句:
    --   GROUP BY のあとにできた各グループに対して条件をかける。
    --
    --   ここが今回の最重要ポイントである。
    --
    --   COUNT(*) = 1
    --   は、
    --   「そのグループに属する行数が1件である」
    --   という意味である。
    --
    --   つまり、
    --   その都道府県に住んでいる人が1人だけのグループだけを残す。
    --
    --   もし 2人以上いる都道府県であれば、この条件を満たさないので除外される。
    COUNT(*) = 1;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を読む
-- 2. address 列の値ごとに行をグループ化する
-- 3. 各グループについて COUNT(*) を計算する
-- 4. COUNT(*) = 1 を満たすグループだけを残す
-- 5. 残ったグループについて address と件数を表示する
--
-- 概念的には次のような処理に近い。
--
--   groups = {}
--
--   for each row in Address:
--       key = row.address
--       groups[key] += 1
--
--   for each key in groups:
--       if groups[key] == 1:
--           output key, groups[key]
--
-- つまりこれは、
--   「都道府県ごとに人数を数え、その人数が1人だけの都道府県を選ぶ」
-- という集計＋絞り込みの処理である。


-- ================================================
-- GROUP BY と HAVING の違い
-- ================================================
--
-- GROUP BY:
--   同じ値を持つ行どうしをまとめるための構文である。
--
-- HAVING:
--   まとめた後のグループに対して条件をかけるための構文である。
--
-- 今回は
--
--   GROUP BY address
--
-- により都道府県ごとのグループを作り、
--
--   HAVING COUNT(*) = 1
--
-- により、そのグループの件数が1件のものだけを残している。
--
-- ここで注意すべき点は、
-- COUNT(*) のような集約結果は GROUP BY の後で初めて計算されるため、
-- WHERE ではなく HAVING を使う必要があることである。


-- ================================================
-- なぜ WHERE ではなく HAVING なのか
-- ================================================
--
-- 初学者が混同しやすい点として、
--
--   WHERE COUNT(*) = 1
--
-- のように書きたくなることがある。
--
-- しかし WHERE は、GROUP BY の前に各行へ条件をかける句である。
-- その時点では COUNT(*) のような集約結果はまだ存在しない。
--
-- 一方 HAVING は、GROUP BY の後にグループ単位で条件をかける句であるため、
-- COUNT(*) のような集約関数を使うことができる。
--
-- したがって今回のような
--   「グループごとの件数で絞り込む」
-- 場合は HAVING を使う。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のような住所データがあるとする。
--
--   name    | address
--   --------+----------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県
--
-- これを address ごとにまとめると、
--
--   東京都   → 3人
--   福島県   → 2人
--   千葉県   → 2人
--   三重県   → 1人
--   和歌山県 → 1人
--
-- となる。
--
-- ここで HAVING COUNT(*) = 1 を適用すると、
--   1人だけの都道府県
-- だけが残る。
--
-- したがって結果は次のようになる。
--
--   address    | COUNT(*)
--   -----------+---------
--   三重県     | 1
--   和歌山県   | 1


-- ================================================
-- 図解: GROUP BY と HAVING のイメージ
-- ================================================
--
--   Address テーブル
--        |
--        v
--   +-------------------------------+
--   | address ごとにグループ化する  |
--   +-------------------------------+
--        |
--   +----+----------------------------------------------+
--   |                                                   |
--   v                                                   v
-- 東京都グループ                                      三重県グループ
-- 3人                                                 1人
--   |                                                   |
--   v                                                   v
-- COUNT(*) = 3                                        COUNT(*) = 1
--   |                                                   |
--   v                                                   v
-- HAVINGで除外                                         HAVINGで残す
--
-- 同様に、
--   福島県 = 2人 → 除外
--   千葉県 = 2人 → 除外
--   和歌山県 = 1人 → 残す
--
-- 最終的に
--   1人だけの都道府県
-- だけが結果として返される。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT address, COUNT(*)
--   → 都道府県名と、その都道府県の人数を表示する
--
-- FROM Address
--   → Address テーブルを集計対象にする
--
-- GROUP BY address
--   → address が同じ行どうしをまとめる
--
-- HAVING COUNT(*) = 1
--   → まとめたグループのうち、人数が1人のものだけを残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   HAVING は GROUP BY と組み合わせて使うことが多い。
--
--   特に、
--     COUNT(*)
--     SUM(...)
--     AVG(...)
--     MAX(...)
--     MIN(...)
--   のような集約関数の結果で絞り込みたいときに使う。
-- ================================================


-- ================================================
-- 補足2:
--   このSQLには WHERE 句が無いので、
--   Address テーブルの全行が集計対象になる。
--
--   もし特定条件に絞ってから都道府県ごとに数えたい場合は、
--   たとえば次のように書く。
--
--     SELECT address, COUNT(*)
--     FROM Address
--     WHERE sex = '女'
--     GROUP BY address
--     HAVING COUNT(*) = 1;
--
--   この場合は、女性だけを対象に都道府県ごとに集計し、
--   その中で1人だけの都道府県を求めることになる。
-- ================================================


