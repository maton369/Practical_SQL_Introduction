-- ================================================
-- INTERSECT との関係
-- ================================================
--
-- INTERSECT は「両方にあるもの」を残した。
-- EXCEPT は「前側にだけあるもの」を残す。
--
-- したがって、
--
--   INTERSECT
--     → 共通部分
--
--   EXCEPT
--     → 前側から共通部分を引いた残り
--
-- と考えると理解しやすい。


-- ================================================
-- EXCEPT に必要な条件
-- ================================================
--
-- EXCEPT を使うには、
-- 前後の SELECT 文が「同じ形の結果」を返す必要がある。
--
-- 具体的には、少なくとも次の点が重要である。
--
-- 1. 列数が同じであること
-- 2. 列の並び順が対応していること
-- 3. 対応する列どうしの型が極端に矛盾しないこと
--
-- 今回は
--   Address
--   Address2
-- が同じ構造を持っているため、
--
--   SELECT * FROM Address
--   EXCEPT
--   SELECT * FROM Address2
--
-- という書き方が成立している。


-- ================================================
-- 重要: 何が「同じ行」とみなされるのか
-- ================================================
--
-- 今回 SELECT * を使っているため、
-- 比較対象は name だけではない。
--
--   name
--   phone_nbr
--   address
--   sex
--   age
--
-- の5列すべてが一致して初めて、
-- 「同じ行」とみなされる。
--
-- たとえ name が同じでも、
-- phone_nbr や age が違えば別行として扱われる。
--
-- つまり今回の EXCEPT は
--   name の差集合
-- ではなく、
--   行全体の差集合
-- を求めている。


