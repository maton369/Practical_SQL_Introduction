--
-- このとき、
--   小川
--   林
-- の行は両方の表に存在しているので、
-- EXCEPT の結果からは除外される。
--
-- 一方で Address にだけ存在する
--
--   前田
--   森
--   井上
--   佐々木
--   松本
--   佐藤
--   鈴木
