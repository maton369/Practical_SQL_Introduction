-- ================================================
-- ファイル名: 22_select_except_address_minus_address2.sql
-- 役割:
--   Address テーブルには存在するが、
--   Address2 テーブルには存在しない行だけを取り出す。
--
-- このSQLの目的:
--   2つの表を比べたときに、
--   「Address 側にだけあるレコード」
--   を見つけること。
--
-- 学習上の主題:
--   EXCEPT を使って、
--   2つの SELECT 結果の差集合を求める方法を理解すること。
--
-- 今回の最重要ポイント:
--   EXCEPT は、
--   前側の SELECT 結果から
--   後側の SELECT 結果に含まれる行を取り除く構文である。
--
--   したがって今回のSQLは、
--     Address にある行
--   から
--     Address2 にもある行
--   を引き算している。
--
--   つまり、
--   「Address にだけ存在する行」
--   が最終結果に残る。
-- ================================================

SELECT
    -- * は「そのテーブルの全列」を意味する。
    --
    -- 今回 Address テーブルには
    --   name
    --   phone_nbr
    --   address
    --   sex
    --   age
    -- の5列があるため、
    -- SELECT * はその5列すべてを取り出す。
    --
    -- つまり、この SELECT の1行は
    -- 「1人分の完全なレコード全体」
    -- を表す。
    *
FROM
    -- 1つ目の SELECT の対象:
    --   Address テーブルから全行・全列を取得する。
    --
    --   EXCEPT では、
    --   この前側の SELECT 結果が「引かれる側」になる。
    Address

EXCEPT

-- EXCEPT:
--   前後2つの SELECT 結果の差集合を求める。
--
--   差集合とは、
--   「前側にはあるが、後側にはないものだけを残す」
--   操作である。
--
--   そのため今回の意味は、
--
--     Address にある
--     かつ
--     Address2 にはない
--
--   行だけを返すことになる。
--
--   逆に、
--   両方に共通している行は除外される。
--
--   これは
--     UNION   = 和集合
--     INTERSECT = 積集合
-- に対して、
--     EXCEPT  = 差集合
-- という関係にある。

SELECT
    -- こちらも * により Address2 の全列を取り出す。
    --
    -- EXCEPT を使うためには、
    -- 前後の SELECT が返す列数と列順が対応している必要がある。
    --
    -- 今回は Address と Address2 が
    --   name, phone_nbr, address, sex, age
    -- という同じ構造を持つため、
    -- SELECT * 同士で EXCEPT できる。
    *
FROM
    -- 2つ目の SELECT の対象:
    --   Address2 テーブルから全行・全列を取得する。
    --
    --   EXCEPT では、
    --   この後側の SELECT 結果が「引く側」になる。
    Address2;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を取得する
-- 2. Address2 テーブルの全行を取得する
-- 3. Address の各行について、
--    同じ行が Address2 に存在するかを確認する
-- 4. Address2 にも存在する行は除外する
-- 5. Address2 に存在しない行だけを残す
-- 6. 残った行を結果として返す
--
-- 概念的には次のような処理に近い。
--
--   result1 = all rows from Address
--   result2 = all rows from Address2
--
--   diff = {}
--
--   for each row in result1:
--       if row is not in result2:
--           add row to diff
--
--   output diff
--
-- つまりこれは、
--   「Address を基準にして、
--     Address2 にある共通行を引き去る」
-- という集合演算である。


-- ================================================
-- EXCEPT とは何か
-- ================================================
--
-- EXCEPT は、
--   2つの SELECT 文の結果の差集合を求める構文である。
--
-- 集合論でいう差集合とは、
--
--   A - B
--
-- のように、
--   「A には属するが、B には属さない要素」
-- を集めた集合のことである。
--
-- 今回で言えば、
--
--   SELECT * FROM Address
--
-- から
--
--   SELECT * FROM Address2
--
-- を引いているので、
--
--   「Address にあるが Address2 にはない行」
--
-- を返している。


-- ================================================
-- なぜ「差集合」なのか
-- ================================================
--
-- 以前学んだ集合演算との違いを整理すると次のようになる。
--
--   UNION
--     → 和集合
--       どちらか一方にあれば結果に含まれる
--
--   INTERSECT
--     → 積集合
--       両方にあるものだけ結果に含まれる
--
--   EXCEPT
--     → 差集合
--       前側にはあるが後側にはないものだけ結果に含まれる
--
-- 今回はこのうち EXCEPT を使っているため、
-- 「共通部分」ではなく、
-- 「Address にだけ残る部分」
-- を求めている。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Address に次のような行があるとする。
--
--   小川, 080-3333-XXXX, 東京都,   男, 30
