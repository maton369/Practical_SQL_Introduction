-- ================================================
-- ファイル名: 20_select_union_address_and_address2.sql
-- 役割:
--   Address テーブルと Address2 テーブルの内容をまとめて取得し、
--   重複行を除いた「和集合」を求める。
--
-- このSQLの目的:
--   2つの表に入っているレコードを1つにまとめて見たいときに、
--   UNION を使って結果を結合する方法を理解すること。
--
-- 学習上の主題:
--   UNION は、2つ以上の SELECT 文の結果を縦方向に結合し、
--   さらに重複する行を取り除くための構文である。
--
-- 今回の最重要ポイント:
--   UNION は単なる「行を足す」だけではなく、
--   最終結果を集合として扱い、
--   同じ行が両方に存在すれば1回だけ残す。
--
--   したがって今回のSQLは、
--   Address と Address2 のレコード全体の
--   「和集合」を求めるSQLである。
-- ================================================

SELECT
    -- * は「そのテーブルの全列」を意味する。
    --
    -- 今回 Address テーブルには
    --   name
    --   phone_nbr
    --   address
    --   sex
    --   age
    -- の5列があるため、
    -- SELECT * はその5列すべてを取り出す。
    *
FROM
    -- 1つ目の SELECT の対象テーブル:
    --   Address テーブルから全行・全列を取得する。
    Address

UNION

-- UNION:
--   前の SELECT 結果と、次の SELECT 結果を結合する。
--
--   ただし UNION は「和集合」なので、
--   まったく同じ行が両方に存在した場合は1行にまとめられる。
--
--   この点が UNION ALL との違いである。
--   UNION ALL なら重複行もそのまま残る。

SELECT
    -- こちらも * により Address2 の全列を取り出す。
    --
    -- UNION を使うためには、
    -- 前後の SELECT が返す列数と列の並びが対応している必要がある。
    --
    -- 今回は Address と Address2 が
    --   name, phone_nbr, address, sex, age
    -- という同じ構造を持っているため、
    -- SELECT * 同士で UNION できる。
    *
FROM
    -- 2つ目の SELECT の対象テーブル:
    --   Address2 テーブルから全行・全列を取得する。
    Address2;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を取得する
-- 2. Address2 テーブルの全行を取得する
-- 3. 2つの結果を縦方向に結合する
-- 4. まったく同じ行があれば重複を取り除く
-- 5. 重複除去後の結果を返す
--
-- 概念的には次のような処理に近い。
--
--   result1 = all rows from Address
--   result2 = all rows from Address2
--
--   merged = result1 + result2
--
--   remove duplicate rows from merged
--
--   output merged
--
-- つまりこれは、
--   「2つの表を1つにまとめる」
-- ただし
--   「同じ行は1回だけ残す」
-- という集合演算である。


-- ================================================
-- UNION とは何か
-- ================================================
--
-- UNION は、
--   2つ以上の SELECT 文の結果を結合して、
--   1つの結果として返す構文である。
--
-- 名前のとおり、集合論でいう「和集合」に対応する。
--
-- 今回は
--
--   SELECT * FROM Address
--
-- と
--
--   SELECT * FROM Address2
--
-- の結果を合わせている。
--
-- そのため、意味としては
--   「Address にある行」
--   または
--   「Address2 にある行」
-- を集める操作である。
--
-- ただし UNION は集合として扱うため、
-- 両方に同じ行がある場合は1回だけ残る。


-- ================================================
-- なぜ「和集合」なのか
-- ================================================
--
-- 以前学んだ OR や IN は、
-- 1つの表の中で条件を広げることで和集合的な結果を作っていた。
--
-- 今回の UNION は、
-- 2つの SELECT 結果そのものを和集合としてまとめる。
--
-- つまり今回は、
--   条件の和集合
-- ではなく、
--   結果表どうしの和集合
-- を取っている。
--
-- この違いが重要である。


-- ================================================
-- UNION に必要な条件
-- ================================================
--
-- UNION を使うには、
-- 前後の SELECT 文が「同じ形の結果」を返す必要がある。
--
-- 具体的には、少なくとも次の点が重要である。
--
-- 1. 列数が同じであること
-- 2. 列の並び順が対応していること
-- 3. 対応する列どうしの型が極端に矛盾しないこと
--
-- 今回は
--
--   Address
--   Address2
--
-- が同じ構造のテーブルとして作られているので、
--
--   SELECT * FROM Address
--   UNION
--   SELECT * FROM Address2
--
-- という形が自然に成立している。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Address に次のような行があるとする。
--
--   小川, 080-3333-XXXX, 東京都,   男, 30
--   前田, 090-0000-XXXX, 東京都,   女, 21
--   森,   090-2984-XXXX, 東京都,   男, 45
--   林,   080-3333-XXXX, 福島県,   男, 32
--   ...
--
-- Address2 に次のような行があるとする。
--
--   小川, 080-3333-XXXX, 東京都,   男, 30
--   林,   080-3333-XXXX, 福島県,   男, 32
--   武田, NULL,            福島県,   男, 18
--   斉藤, 080-2367-XXXX, 千葉県,   女, 19
--   上野, NULL,            千葉県,   女, 20
--   広田, 090-0205-XXXX, 三重県,   男, 25
--
-- このとき
--   小川
--   林
-- の行は両方の表に存在している。
--
-- UNION は重複行を除くため、
-- これらは最終結果で1回ずつしか現れない。
--
-- 一方、
--   前田
--   森
--   武田
--   斉藤
--   上野
--   広田
-- のように片方にしかない行は、そのまま結果に含まれる。


-- ================================================
-- 図解: UNION のイメージ
-- ================================================
--
--   Address の結果
--   +----------------------------------+
--   | 小川                             |
--   | 前田                             |
--   | 森                               |
--   | 林                               |
--   | ...                              |
--   +----------------------------------+
--
--               UNION
--
--   Address2 の結果
--   +----------------------------------+
--   | 小川                             |
--   | 林                               |
--   | 武田                             |
--   | 斉藤                             |
--   | 上野                             |
--   | 広田                             |
--   +----------------------------------+
--
--               ↓ 結合して重複除去
--
--   UNION の結果
--   +----------------------------------+
--   | 小川    ← 両方にあっても1回      |
--   | 前田                             |
--   | 森                               |
--   | 林      ← 両方にあっても1回      |
--   | 武田                             |
--   | 斉藤                             |
--   | 上野                             |
--   | 広田                             |
--   | ...                              |
--   +----------------------------------+


-- ================================================
-- UNION と UNION ALL の違い
-- ================================================
--
-- UNION:
--   結果を結合したあと、重複行を除く。
--
-- UNION ALL:
--   結果を結合するだけで、重複行を除かない。
--
-- たとえば小川の行が Address と Address2 の両方にある場合、
--
--   UNION
--     → 小川は1回だけ出る
--
--   UNION ALL
--     → 小川は2回出る
--
-- 今回は「和集合」を求める題材なので UNION を使っている。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT * FROM Address
--   → Address テーブルの全列・全行を取得する
--
-- UNION
--   → 前後2つの SELECT 結果を結合し、重複を除く
--
-- SELECT * FROM Address2
--   → Address2 テーブルの全列・全行を取得する
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   UNION は結果を集合として扱うので、
--   重複除去が入る分、UNION ALL より処理が重くなることがある。
--
--   もし重複をそのまま残してよいなら、
--   UNION ALL の方が素直で速い場合がある。
-- ================================================


