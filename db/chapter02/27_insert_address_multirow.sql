-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを追加先として指定する
-- 2. 挿入対象列を name, phone_nbr, address, sex, age の順で決める
-- 3. VALUES に並んだ各値組を、列順に対応づける
-- 4. 各値組ごとに1行ずつ新しい行を作る
-- 5. それら複数行を1回の INSERT 文でまとめて追加する
--
-- 概念的には次のような処理に近い。
--
--   rows = [
--       {'name':'小川',   'phone_nbr':'080-3333-XXXX', 'address':'東京都',   'sex':'男', 'age':30},
--       {'name':'前田',   'phone_nbr':'090-0000-XXXX', 'address':'東京都',   'sex':'女', 'age':21},
--       {'name':'森',     'phone_nbr':'090-2984-XXXX', 'address':'東京都',   'sex':'男', 'age':45},
--       {'name':'林',     'phone_nbr':'080-3333-XXXX', 'address':'福島県',   'sex':'男', 'age':32},
--       {'name':'井上',   'phone_nbr':NULL,            'address':'福島県',   'sex':'女', 'age':55},
--       {'name':'佐々木', 'phone_nbr':'080-5848-XXXX', 'address':'千葉県',   'sex':'女', 'age':19},
--       {'name':'松本',   'phone_nbr':NULL,            'address':'千葉県',   'sex':'女', 'age':20},
--       {'name':'佐藤',   'phone_nbr':'090-1922-XXXX', 'address':'三重県',   'sex':'女', 'age':25},
--       {'name':'鈴木',   'phone_nbr':'090-0001-XXXX', 'address':'和歌山県', 'sex':'男', 'age':32}
--   ]
--
--   insert all rows into Address
--
-- つまりこれは、
--   「複数の新規行をまとめて一括登録する」
--   処理である。


-- ================================================
-- multirow insert とは何か
-- ================================================
--
-- multirow insert とは、
--   1つの INSERT 文で複数行を追加する書き方
-- である。
--
-- 一般形は次のようになる。
--
--   INSERT INTO 表名 (列1, 列2, ...)
--   VALUES
--       (値1-1, 値1-2, ...),
--       (値2-1, 値2-2, ...),
--       (値3-1, 値3-2, ...);
--
-- このとき重要なのは、
--
--   1組の丸括弧 = 1行分
--
-- という対応である。
--
-- 今回はその丸括弧が 9組あるため、
-- 9行が一度に追加される。


-- ================================================
-- 1行 INSERT との違い
-- ================================================
--
-- 1行 INSERT は次のような形だった。
--
--   INSERT INTO Address (name, phone_nbr, address, sex, age)
--   VALUES ('小川', '080-3333-XXXX', '東京都', '男', 30);
--
-- これは 1行しか追加しない。
--
-- 一方、今回の multirow insert は、
-- VALUES の後ろに複数の値組を並べている。
--
-- したがって、
--   同じ書式を何度も別文で書く代わりに、
--   1つの文の中へまとめて書いている
-- と理解できる。
--
-- つまり multirow insert は、
--   「単一行 INSERT の繰り返しを1文に圧縮した形」
-- と考えると分かりやすい。


-- ================================================
-- multirow insert の利点
-- ================================================
--
-- multirow insert の主な利点は次の通りである。
--
-- 1. SQL 文の本数を減らせる
--    → 9件なら 9回の INSERT を 1回にまとめられる
--
-- 2. 可読性が上がる
--    → 同じ表・同じ列構成への追加が1か所にまとまる
--
-- 3. DB とのやり取り回数を減らせる
--    → 実務では性能面で有利になることが多い
--
-- 4. 初期データ投入に向いている
--    → サンプルデータやマスタデータを一括登録しやすい
--
-- したがって、複数件をまとめて登録したい場面では
-- 非常に便利な書き方である。


-- ================================================
-- 各 VALUES 組の意味
-- ================================================
--
-- 今回の VALUES には 9組のデータがある。
--
-- それぞれが 1人分の住所録レコードに対応している。
--
-- 対応関係はすべて共通で、
--
--   1番目 -> name
--   2番目 -> phone_nbr
--   3番目 -> address
--   4番目 -> sex
--   5番目 -> age
--
-- である。
--
-- したがって、たとえば
--
--   ('林', '080-3333-XXXX', '福島県', '男', 32)
--
-- は
--
--   name      = '林'
--   phone_nbr = '080-3333-XXXX'
--   address   = '福島県'
--   sex       = '男'
--   age       = 32
--
-- という1行を表している。


-- ================================================
-- NULL を含む行の意味
-- ================================================
--
-- 今回は 井上 さんと 松本 さんの phone_nbr が NULL になっている。
--
-- ここでの NULL は、
--   文字列 'NULL'
-- ではなく、
--   値が未設定であることを表す特殊値
-- である。
--
-- したがって、
--
--   ('井上', NULL, '福島県', '女', 55)
--
-- は
--   「井上さんの電話番号は登録されていない」
-- という意味になる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- この SQL を実行すると、Address テーブルに次の9行が追加される。
--
--   小川     | 080-3333-XXXX | 東京都     | 男 | 30
--   前田     | 090-0000-XXXX | 東京都     | 女 | 21
--   森       | 090-2984-XXXX | 東京都     | 男 | 45
--   林       | 080-3333-XXXX | 福島県     | 男 | 32
--   井上     | NULL          | 福島県     | 女 | 55
--   佐々木   | 080-5848-XXXX | 千葉県     | 女 | 19
--   松本     | NULL          | 千葉県     | 女 | 20
--   佐藤     | 090-1922-XXXX | 三重県     | 女 | 25
--   鈴木     | 090-0001-XXXX | 和歌山県   | 男 | 32
--
-- つまり、1回の INSERT 実行で、
-- Address テーブルへ 9件のサンプルデータが一括登録される。


-- ================================================
-- 図解: multirow insert のイメージ
-- ================================================
--
--   INSERT INTO Address (name, phone_nbr, address, sex, age)
--   VALUES
--       ( ... ),   ← 1行目
--       ( ... ),   ← 2行目
--       ( ... ),   ← 3行目
--       ...
--       ( ... );   ← 9行目
--
--             |
--             v
--   +----------------------------------+
--   | 1組の丸括弧ごとに 1行を作る      |
--   +----------------------------------+
--             |
--             v
--   +----------------------------------+
--   | それらをまとめて Address へ追加  |
--   +----------------------------------+
--
--
-- 単一行 INSERT との比較:
--
--   1行 INSERT:
--     1文 -> 1行
--
--   multirow insert:
--     1文 -> 複数行


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- INSERT INTO Address (name, phone_nbr, address, sex, age)
--   → Address テーブルへ、指定した列順でデータを追加する
--
-- VALUES (...)
--   → 1組の値 = 1行分のデータ
--
-- VALUES (...), (...), (...)
--   → 複数組の値 = 複数行分のデータ
--
-- NULL
--   → 値未設定を表す特殊値
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   multirow insert では、すべての値組が
--   同じ列数・同じ列順で書かれていなければならない。
--
--   どこか1組でも値の数がずれるとエラーになる。
-- ================================================


-- ================================================
-- 補足2:
--   name は PRIMARY KEY なので、
--   すでに同じ name が Address テーブルに存在している場合、
--   その行は重複エラーになる。
--
--   つまりこの SQL は、空表や初期データ投入の場面を想定すると理解しやすい。
-- ================================================


-- ================================================
-- 補足3:
--   多くの DBMS では multirow insert は非常に一般的だが、
--   方言差が少しあることもある。
--
--   ただし今回のような
--
--     INSERT INTO ... VALUES (...), (...), ...
--
--   は主要なDBで広く使われる代表的な書き方である。
