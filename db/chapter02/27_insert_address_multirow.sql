--   Address テーブルに複数行を一度に追加する。
--
-- このSQLの目的:
--   9件のレコードを、1行ずつ別々に INSERT するのではなく、
--   1つの INSERT 文でまとめて登録する方法を理解すること。
--
-- 学習上の主題:
--   multirow insert
--   すなわち
--   「VALUES に複数組の値を書くことで、複数行を一括追加する方法」
--   を理解すること。
--
-- 今回の最重要ポイント:
--   1行追加では VALUES の後ろに 1組だけ書いた。
--
--   今回は
--     (値1, ...)
--     (値2, ...)
--     ...
--   のように複数組をカンマで並べる。
--
--   つまり
--   「1組の丸括弧 = 1行分」
--   という考え方をそのまま拡張したものが multirow insert である。
-- ================================================

INSERT INTO Address
(
    -- どの列へ値を入れるかを明示している。
    --
    -- 後ろの各 VALUES 組は、すべてこの列順に対応している。
    --
