-- ================================================
-- ファイル名: 03_select_all_from_address.sql
-- 役割:
--   Address テーブルの内容を一覧表示する。
--
-- SELECT とは:
--   テーブルからデータを取り出すためのSQL構文である。
--
-- 基本構文:
--
--   SELECT 列名1, 列名2, ...
--   FROM テーブル名;
--
-- このSQLの意味:
--   Address テーブルから
--   name, phone_nbr, address, sex, age
--   の5列を取り出して表示する。
--
-- FROM の意味:
--   「どのテーブルからデータを取ってくるか」を指定する句である。
--
-- 今回は WHERE 句が無いので、
--   Address テーブルに存在するすべての行が対象になる。
--
-- つまりこのSQLは、
--   Address テーブルの全行について、
--   指定した5列をそのまま表示する
-- という処理である。
-- ================================================

SELECT
    -- 1列目として name 列を取り出す
    name,

    -- 2列目として phone_nbr 列を取り出す
    phone_nbr,

    -- 3列目として address 列を取り出す
    address,

    -- 4列目として sex 列を取り出す
    sex,

    -- 5列目として age 列を取り出す
    age
FROM
    -- データ取得元のテーブルを指定する
    Address;


-- ================================================
-- このSQLの構文上のポイント
-- ================================================
--
-- 1. SELECT 句
--    取り出したい列をカンマ区切りで並べる。
--
--    今回は
--      name, phone_nbr, address, sex, age
--    を指定している。
--
-- 2. FROM 句
--    どのテーブルを対象にするかを指定する。
--
--    今回は Address テーブルを対象にしている。
--
-- 3. セミコロン ;
--    SQL文の終わりを表す。
--    特に複数のSQLを続けて書くときに重要である。
--
-- 4. なぜ * を使っていないのか
--    すべての列を取り出すだけなら
--
--      SELECT * FROM Address;
--
--    と書くこともできる。
--
--    しかし今回は列を明示しているため、
--    「どの列を取り出しているのか」が分かりやすい。
--    学習用にはこちらの方が適している。


