-- ================================================
-- ファイル名: 19_select_address_with_case_district.sql
-- 役割:
--   Address テーブルの各行について、
--   address 列の値をもとに「地方区分」を新しく作って表示する。
--
-- このSQLの目的:
--   住所として入っている都道府県名を、そのまま表示するだけでなく、
--   それを
--     - 関東
--     - 東北
--     - 中部
--     - 関西
--   のような、より大きな分類に変換して見せること。
--
-- 学習上の主題:
--   CASE 式を使って、
--   「条件によって返す値を切り替える」
--   方法を理解すること。
--
-- 今回の最重要ポイント:
--   CASE 式は、SQL の中で
--   if / elif / else のような条件分岐を表現するための構文である。
--
--   つまり今回のSQLは、
--   各行の address を見て、
--   その値に応じて district 列の値を切り替えている。
-- ================================================

SELECT
    -- name 列:
    --   元の Address テーブルに入っている名前をそのまま表示する。
    name,

    -- address 列:
    --   元の Address テーブルに入っている都道府県名をそのまま表示する。
    address,

    CASE
        -- CASE 式:
        --   条件に応じて返す値を切り替えるための式である。
        --
        --   一般形は次のようになる。
        --
        --     CASE
        --         WHEN 条件1 THEN 値1
        --         WHEN 条件2 THEN 値2
        --         ...
        --         ELSE 値n
        --     END
        --
        --   上から順番に条件を評価し、
        --   最初に真になった WHEN の THEN の値を返す。
        --
        --   どの WHEN にも当てはまらない場合は ELSE の値を返す。

        WHEN address = '東京都' THEN '関東'
        -- もし address が '東京都' なら、
        -- district として '関東' を返す。

        WHEN address = '千葉県' THEN '関東'
        -- もし address が '千葉県' なら、
        -- district として '関東' を返す。
        --
        -- つまり今回は
        --   東京都
        --   千葉県
        -- の両方を関東地方として扱っている。

        WHEN address = '福島県' THEN '東北'
        -- もし address が '福島県' なら、
        -- district として '東北' を返す。

        WHEN address = '三重県' THEN '中部'
        -- もし address が '三重県' なら、
        -- district として '中部' を返す。

        WHEN address = '和歌山県' THEN '関西'
        -- もし address が '和歌山県' なら、
        -- district として '関西' を返す。

        ELSE NULL
        -- ELSE:
        --   上のどの WHEN 条件にも当てはまらなかった場合に返す値である。
        --
        --   今回は NULL を返している。
        --   つまり、想定していない都道府県名が入っていた場合は、
        --   district 列を未設定扱いにする。
        --
        --   例:
        --     大阪府
        --     北海道
        --   のような値がもし入っていた場合、
        --   今回の CASE では該当 WHEN が無いため NULL になる。

    END AS district
    -- END:
    --   CASE 式の終わりを表す。
    --
    -- AS district:
    --   CASE 式の結果列に district という名前を付ける。
    --
    --   つまり、SELECT の結果には
    --     name
    --     address
    --     district
    --   の3列が現れる。

FROM
    -- FROM 句:
    --   元データとして Address テーブルを参照する。
    Address;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を読む
-- 2. 各行について name と address を取り出す
-- 3. 各行について CASE 式を評価する
-- 4. address の値に応じて district の値を決める
-- 5. name, address, district の3列を結果として返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address == '東京都':
--           district = '関東'
--       elif row.address == '千葉県':
--           district = '関東'
--       elif row.address == '福島県':
--           district = '東北'
--       elif row.address == '三重県':
--           district = '中部'
--       elif row.address == '和歌山県':
--           district = '関西'
--       else:
--           district = NULL
--
--       output row.name, row.address, district
--
-- つまりこれは、
--   「各行の値を見て、新しい分類結果を計算して付け加える」
-- という処理である。


-- ================================================
-- CASE 式とは何か
-- ================================================
--
-- CASE 式は、SQL の中で条件分岐を行うための式である。
--
-- プログラミング言語でいうと、
--   if
--   else if
--   else
-- に相当する。
--
-- 今回は address の値によって district を決めているため、
-- CASE 式は
--   「都道府県 → 地方」
-- の変換表として働いている。
--
-- 重要なのは、CASE は文ではなく「式」である点である。
-- そのため、SELECT 句の中に書いて、
-- 1つの列の値を計算する用途で使える。


-- ================================================
-- WHEN / THEN / ELSE の意味
-- ================================================
--
-- WHEN 条件 THEN 値
--   → 条件が真なら、その値を返す
--
-- ELSE 値
--   → どの WHEN にも一致しなかったときに返す
--
-- END
--   → CASE 式の終わり
--
-- 今回の例では、
--
--   WHEN address = '東京都' THEN '関東'
--
-- は
--   「address が東京都なら、district は関東」
-- という対応関係を表している。
--
-- 複数の WHEN がある場合は、
-- 上から順に評価されて、
-- 最初に一致したものが採用される。


-- ================================================
-- 上から順番に評価されるという意味
-- ================================================
--
-- CASE 式の WHEN は上から順番に判定される。
--
-- 今回の条件は互いに重ならないので大きな問題は起きにくいが、
-- 一般には順序が意味を持つ。
--
-- たとえば、もしもっと広い条件を先に書いてしまうと、
-- 後ろの細かい条件に到達しないことがある。
--
-- 今回のように
--   address = '東京都'
--   address = '千葉県'
-- のような完全一致条件では、
-- 各条件が独立しているため読みやすい。


-- ================================================
-- ELSE NULL の意味
-- ================================================
--
-- ELSE NULL は、
-- 「どの条件にも当てはまらなければ district を NULL にする」
-- という意味である。
--
-- これは
--   未分類
--   未対応
--   不明
-- のような状態を表すための書き方である。
--
-- もし必ず何か文字列を入れたいなら、たとえば
--
--   ELSE '未分類'
--
-- のようにも書ける。
--
-- 今回は NULL を使っているため、
-- 「この CASE では地方区分を決められなかった」
-- ことを表している。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | address
--   --------+----------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県
--
-- このとき CASE 式により district は次のように決まる。
--
--   東京都     → 関東
--   千葉県     → 関東
--   福島県     → 東北
--   三重県     → 中部
--   和歌山県   → 関西
--
-- したがって結果は概ね次のようになる。
--
--   name    | address    | district
--   --------+------------+---------
--   小川    | 東京都     | 関東
--   前田    | 東京都     | 関東
--   森      | 東京都     | 関東
--   林      | 福島県     | 東北
--   井上    | 福島県     | 東北
--   佐々木  | 千葉県     | 関東
--   松本    | 千葉県     | 関東
--   佐藤    | 三重県     | 中部
--   鈴木    | 和歌山県   | 関西


-- ================================================
-- 図解: CASE 式のイメージ
-- ================================================
--
--   各行の address を読む
--        |
--        v
--   +------------------------------+
--   | 東京都 か？                  |
--   +------------------------------+
--        | yes
--        v
--      関東
--
--        no
--        |
--        v
--   +------------------------------+
--   | 千葉県 か？                  |
--   +------------------------------+
--        | yes
--        v
--      関東
--
--        no
--        |
--        v
--   +------------------------------+
--   | 福島県 か？                  |
--   +------------------------------+
--        | yes
--        v
--      東北
--
--        no
--        |
--        v
--   +------------------------------+
--   | 三重県 か？                  |
--   +------------------------------+
--        | yes
--        v
--      中部
--
--        no
--        |
--        v
--   +------------------------------+
--   | 和歌山県 か？                |
--   +------------------------------+
--        | yes
--        v
--      関西
--
--        no
--        |
--        v
--      NULL
--
-- つまり CASE 式は、
-- 条件の分岐表をそのまま SQL の中に書いたものと考えられる。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, address, CASE ... END AS district
--   → 元の列に加えて、CASE で計算した新しい列 district を返す
--
-- WHEN 条件 THEN 値
--   → 条件が真ならその値を返す
--
-- ELSE NULL
--   → どの条件にも一致しなければ NULL を返す
--
-- END
--   → CASE 式の終了
--
-- AS district
--   → CASE の結果列に district という名前を付ける
--
-- FROM Address
--   → Address テーブルを元データとする
--
-- ;
--   → SQL文の終了を表す


