-- ================================================
-- ファイル名: 01_create_address_table.sql
-- 役割:
--   Address テーブルを作成する。
--
-- このSQLの目的:
--   住所録のようなデータを保存するための表を定義する。
--
-- CREATE TABLE とは:
--   新しいテーブル（表）をデータベース内に作るためのSQL構文である。
--
-- 構文の基本形:
--
--   CREATE TABLE テーブル名 (
--       列名 データ型 制約,
--       列名 データ型 制約,
--       ...
--   );
--
-- 今回は Address という名前のテーブルを作る。
--
-- Address という名前の意味:
--   住所録・連絡先情報をまとめた表であることを表している。
--
-- 各列の意味:
--   name      : 名前
--   phone_nbr : 電話番号
--   address   : 住所（都道府県）
--   sex       : 性別
--   age       : 年齢
--
-- PRIMARY KEY (name) の意味:
--   name 列を主キーにする、という宣言である。
--   主キーとは、各行を一意に識別するための列であり、
--   同じ値を重複して入れることができない。
--
-- つまり今回のテーブルでは、
--   「同じ name を2回登録してはいけない」
-- というルールになる。
-- ================================================

CREATE TABLE Address
(
    -- name 列:
    --   名前を表す列である。
    --   VARCHAR(32) は「最大32文字程度の可変長文字列」を表す。
    --   NOT NULL は「NULL（値なし）を禁止する」という制約である。
    --   したがって、name には必ず何らかの文字列を入れなければならない。
    name       VARCHAR(32) NOT NULL,

    -- phone_nbr 列:
    --   電話番号を表す列である。
    --   VARCHAR(32) なので文字列として保存する。
    --   電話番号は数値に見えるが、
    --   ハイフンや 'XXXX' のような文字を含むため、
    --   INTEGER ではなく VARCHAR を使うのが自然である。
    --
    --   この列には NOT NULL が付いていないため、
    --   電話番号が不明な場合は NULL を入れることができる。
    phone_nbr  VARCHAR(32),

    -- address 列:
    --   住所情報を表す列である。
    --   今回のサンプルでは東京都、福島県などの都道府県名が入る。
    --   VARCHAR(32) は文字列型であり、
    --   NOT NULL により値の省略を禁止している。
    address    VARCHAR(32) NOT NULL,

    -- sex 列:
    --   性別を表す列である。
    --   CHAR(4) は「固定長文字列」である。
    --   VARCHAR と違って、あらかじめ決まった長さで扱う型である。
    --
    --   今回は '男' や '女' のような短い値を入れている。
    --   NOT NULL により必須項目としている。
    sex        CHAR(4) NOT NULL,

    -- age 列:
    --   年齢を表す列である。
    --   INTEGER は整数型であり、
    --   30, 21, 45 のような数値をそのまま扱う。
    --   NOT NULL により、年齢は必ず入力が必要である。
    age        INTEGER NOT NULL,

    -- PRIMARY KEY (name):
    --   name 列を主キーとして指定する。
    --   列定義の後に、表全体に対する制約として書いている。
    --
    --   PRIMARY KEY の構文は次のように書ける。
    --
    --     PRIMARY KEY (列名)
    --
    --   今回は name を主キーにしているため、
    --   Address テーブルでは name が各行を識別する鍵になる。
    PRIMARY KEY (name)
);