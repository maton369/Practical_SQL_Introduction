-- ================================================
-- ファイル名: 07_select_address_where_tokyo_and_age_ge_30.sql
-- 役割:
--   Address テーブルの中から、
--   「住所が東京都であり、かつ年齢が30歳以上である人」
--   だけを検索する。
--
-- このSQLの目的:
--   Address テーブル全体をそのまま表示するのではなく、
--   複数の条件を同時に満たす人だけに対象を絞り込んで、
--   その人たちの
--     - name
--     - address
--     - age
--   を表示する。
--
-- 学習上の主題:
--   AND を使って、複数条件を同時に満たす行だけを選ぶ方法を理解すること。
--
-- 今回の条件は2つある。
--
--   1. address = '東京都'
--   2. age >= 30
--
-- AND は、
--   「左の条件も真、右の条件も真」
-- のときだけ全体を真にする演算子である。
--
-- そのため今回のSQLは、
--   東京都に住んでいる
--   かつ
--   30歳以上である
-- 人だけを結果に残す。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は
    --     - name
    --     - address
    --     - age
    --   の3列を取り出す。
    --
    --   したがって、
    --   phone_nbr や sex は表示されない。
    name,
    address,
    age
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを検索対象にしている。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込む条件を書く部分である。
    --
    --   今回は条件が1つではなく2つある。
    --   それらを AND で結び、
    --   「両方を満たす行だけ」を結果に残す。

    -- 第1条件:
    --   address = '東京都'
    --
    --   これは
    --   「address 列の値が '東京都' と等しい」
    --   という意味である。
    --
    --   = は等価比較演算子であり、
    --   文字列 '東京都' と完全一致する行だけがこの条件を満たす。
    --
    --   '東京都' は文字列なのでシングルクォートで囲む。
    address = '東京都'

    AND

    -- AND:
    --   複数条件を同時に満たす必要があることを表す論理演算子である。
    --
    --   今回は
    --     address = '東京都'
    --   と
    --     age >= 30
    --   の両方が真である行だけが採用される。
    --
    --   どちらか一方だけ真では不十分であり、
    --   両方とも真でなければ結果に残らない。

    -- 第2条件:
    --   age >= 30
    --
    --   これは
    --   「age 列の値が 30 以上である」
    --   という意味である。
    --
    --   >= は「以上」を表す比較演算子であり、
    --   30, 31, 32, ... は条件を満たす。
    --
    --   一方で 29, 21, 19 などは条件を満たさない。
    --
    --   30 は数値なので、文字列のようにクォートしない。
    age >= 30;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について address 列が '東京都' かどうかを確認する
-- 3. 同じ行について age 列が 30 以上かどうかを確認する
-- 4. 2つの条件を両方とも満たす行だけを残す
-- 5. 残った行から name, address, age の3列だけを取り出す
-- 6. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address == '東京都' and row.age >= 30:
--           output row.name, row.address, row.age
--
-- つまりこれは、
--   「全行を見ながら、複数条件の共通部分だけを通す」
-- という検索である。


-- ================================================
-- AND の意味
-- ================================================
--
-- AND は論理積を表す演算子である。
-- 集合の考え方で言うと、
-- 「条件Aを満たす集合」と「条件Bを満たす集合」の
-- 共通部分を選ぶ働きをする。
--
-- 今回の条件を集合で考えると、
--
--   条件A: address = '東京都'
--          → 東京都に住んでいる人の集合
--
--   条件B: age >= 30
--          → 30歳以上の人の集合
--
-- AND を使うことで、
--
--   条件A ∩ 条件B
--
-- つまり
--   「東京都に住んでいて、なおかつ30歳以上の人」
-- だけを選ぶことになる。


-- ================================================
-- 図解: 集合としての AND
-- ================================================
--
--   +-----------------------------+
--   | 条件A: 東京都              |
--   |                             |
--   |    +-------------------+    |
--   |    | 共通部分          |    |
--   |    | 東京都 かつ       |    |
--   |    | 30歳以上          |    |
--   |    +-------------------+    |
--   |                             |
--   +-----------------------------+
--
--             と
--
--   +-----------------------------+
--   | 条件B: 30歳以上            |
--   +-----------------------------+
--
-- AND はこの「共通部分」だけを結果として残す。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | address    | age
--   --------+------------+----
--   小川    | 東京都     | 30
--   前田    | 東京都     | 21
--   森      | 東京都     | 45
--   林      | 福島県     | 32
--   井上    | 福島県     | 55
--   佐々木  | 千葉県     | 19
--   松本    | 千葉県     | 20
--   佐藤    | 三重県     | 25
--   鈴木    | 和歌山県   | 32
--
-- まず
--   address = '東京都'
-- を満たすのは
--   - 小川
--   - 前田
--   - 森
--
-- 次に
--   age >= 30
-- を満たすのは
--   - 小川
--   - 森
--   - 林
--   - 井上
--   - 鈴木
--
-- この2つの条件を両方満たすのは
--   - 小川
--   - 森
-- のみである。
--
-- したがって結果は次のようになる。
--
--   name    | address    | age
--   --------+------------+----
--   小川    | 東京都     | 30
--   森      | 東京都     | 45


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, address, age
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- WHERE address = '東京都'
--   → 東京都の行だけを候補にする
--
-- AND age >= 30
--   → その中でも 30歳以上の行だけを残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   AND は「どちらも必要」である。
--
--   したがって、
--   東京都でも年齢が 21 の前田さんは除外される。
--
--   また、
--   32歳でも住所が福島県の林さんは除外される。
--
--   つまり、
--   片方だけ条件を満たしても結果には残らない。
-- ================================================


-- ================================================
-- 補足2:
--   OR との違い
--
--   今回は AND なので、
--   「東京都で、しかも30歳以上」
--   という共通部分を選ぶ。
--
--   もし OR を使うと、
--   「東京都である、または30歳以上である」
--   行を選ぶことになる。
--
--   つまり OR は条件の和集合寄り、
--   AND は条件の共通部分寄りの動きをする。
-- ================================================


-- ================================================
-- 補足3:
--   ORDER BY が無いため、結果の並び順は保証されない。
--
--   名前順や年齢順で並べたい場合は、たとえば次のように書く。
--
--     SELECT name, address, age
--     FROM Address
--     WHERE address = '東京都'
--       AND age >= 30
--     ORDER BY age;
--
--   今回の主題は「AND による複数条件の絞り込み」であるため、
--   並び替えまでは行っていない。
-- ================================================