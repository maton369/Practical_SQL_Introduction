-- ================================================
-- ファイル名: 04_select_address_where_chiba.sql
-- 役割:
--   Address テーブルの中から、
--   address 列が '千葉県' である行だけを検索する。
--
-- このSQLの目的:
--   Address テーブル全体を表示するのではなく、
--   条件に一致する人だけを絞り込んで表示する。
--
-- 今回の条件:
--   address = '千葉県'
--
-- つまり、
--   「住所が千葉県である人だけを対象にする」
-- SQLである。
--
-- 学習上の主題:
--   WHERE 句の構文と役割を理解すること。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は name 列と address 列だけを取り出す。
    --   したがって、phone_nbr, sex, age は表示されない。
    name,
    address
FROM
    -- FROM 句:
    --   データを取り出す元のテーブルを指定する。
    --
    --   今回は Address テーブルからデータを取得する。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込むための条件を書く部分である。
    --
    --   WHERE 句が無ければ、Address テーブルの全行が対象になる。
    --   WHERE 句があることで、
    --   条件に一致した行だけを結果に残すことができる。
    --
    --   address = '千葉県'
    --   は、
    --   「address 列の値が '千葉県' と等しい行だけを選ぶ」
    --   という意味である。
    --
    --   = は等価比較を表す演算子である。
    --
    --   '千葉県' は文字列リテラルなので、
    --   シングルクォートで囲んで書く。
    address = '千葉県';


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について address 列の値を確認する
-- 3. address が '千葉県' の行だけを残す
-- 4. 残った行から name 列と address 列だけを取り出す
-- 5. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address == '千葉県':
--           output row.name, row.address
--
-- つまりこれは、
--   「条件付きで全行を調べ、その条件に一致した行だけ返す」
-- という検索である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, address
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを対象にする
--
-- WHERE address = '千葉県'
--   → address 列が '千葉県' の行だけを残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Address テーブルに次のようなデータがあるとする。
--
--   name    | address
--   --------+--------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県
--
-- このとき、
--   WHERE address = '千葉県'
-- に一致するのは
--   - 佐々木
--   - 松本
-- の2行だけである。
--
-- したがって結果は次のようになる。
--
--   name    | address
--   --------+--------
--   佐々木  | 千葉県
--   松本    | 千葉県


-- ================================================
-- 補足1:
--   文字列比較では、比較する値をシングルクォートで囲む。
--
--   例:
--     address = '千葉県'
--
--   数値比較であれば、通常はクォートしない。
--
--   例:
--     age = 20
-- ================================================


-- ================================================
-- 補足2:
--   WHERE 句が無い場合との違い
--
--   次のSQL:
--
--     SELECT name, address
--     FROM Address;
--
--   は全行を返す。
--
--   一方、今回のSQLは
--   WHERE 句によって千葉県の行だけに絞り込んでいる。
--
--   つまり WHERE 句は、
--   「どの行を結果に残すか」を決めるフィルタである。
-- ================================================