-- ================================================
-- ファイル名: 25_select_dense_rank_over_order_by_age_desc.sql
-- 役割:
--   Address テーブルの各行について、
--   age 列を基準に順位を付けて表示する。
--
-- このSQLの目的:
--   ただ年齢順に並べるだけでなく、
--   「年齢が高い人は何位か」を各行に明示すること。
--
-- 学習上の主題:
--   DENSE_RANK() OVER(...) を使って、
--   抜け番のないランキングを計算する方法を理解すること。
--
-- 今回の最重要ポイント:
--   以前の RANK() は同順位があると次の順位が飛んだ。
--
--   それに対して DENSE_RANK() は、
--   同順位を許しつつ、
--   次の順位を飛ばさない。
--
--   つまり今回は、
--   「順位に欠番が出ないランキング」
--   を各行へ付与している。
-- ================================================

SELECT
    -- name 列:
    --   元の Address テーブルに入っている名前をそのまま表示する。
    --
    --   ランキング結果が「誰の順位か」を分かるようにするために必要である。
    name,

    -- age 列:
    --   ランキングの基準となる年齢をそのまま表示する。
    --
    --   今回の順位は age の大きさで決まるため、
    --   順位と一緒に age を表示すると結果の意味が分かりやすい。
    age,

    DENSE_RANK() OVER
    (
        ORDER BY age DESC
    ) AS dense_rnk
    -- DENSE_RANK() OVER(...) :
    --   これがウィンドウ関数によるランキング計算である。
    --
    -- DENSE_RANK() とは:
    --   順位を返すウィンドウ関数であり、
    --   同じ値には同じ順位を付ける。
    --
    --   ただし RANK() と違って、
    --   同順位の次の順位番号を飛ばさない。
    --
    -- OVER(...) とは:
    --   この関数を、どの順序を基準に計算するか指定する部分である。
    --
    -- ORDER BY age DESC とは:
    --   age の大きい順に並べたときの順番を基準に順位を決める、
    --   という意味である。
    --
    -- DESC を使っているため、
    --   年齢が高い人ほど上位になる。
    --
    -- AS dense_rnk:
    --   計算した順位列に dense_rnk という名前を付ける。
    --
    --   したがってこの列には、
    --   1位, 2位, 3位, ...
    -- に相当する数値が入るが、
    -- 同順位があっても抜け番は発生しない。
FROM
    -- FROM 句:
    --   元データとして Address テーブルを参照する。
    Address;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を読む
-- 2. age 列の値を基準に、年齢の高い順で並ぶ順序を考える
-- 3. その順序に従って各行へ順位を付ける
-- 4. 同じ age の行には同じ順位を付ける
-- 5. その次の順位は飛ばさず、連続した番号を付ける
-- 6. name, age, 順位(dense_rnk) を各行ごとに返す
--
-- 概念的には次のような処理に近い。
--
--   rows = all rows from Address
--
--   sort rows by age descending
--
--   assign dense rank to each row based on sorted order
--
--   output row.name, row.age, row.dense_rank
--
-- つまりこれは、
--   「年齢でソートした順番を使って、抜け番なしの順位列を計算する」
--   という処理である。


-- ================================================
-- DENSE_RANK() とは何か
-- ================================================
--
-- DENSE_RANK() は、順位を返すウィンドウ関数である。
--
-- 今回のように
--
--   DENSE_RANK() OVER(ORDER BY age DESC)
--
-- と書くと、
--   age が高い順に並べたときの順位
-- を各行に返す。
--
-- たとえば age が
--
--   55, 45, 32, 32, 30, 25, 21, 20, 19
--
-- なら、
--
--   55 → 1位
--   45 → 2位
--   32 → 3位
--   32 → 3位
--   30 → 4位
--   25 → 5位
--   21 → 6位
--   20 → 7位
--   19 → 8位
--
-- のような順位になる。
--
-- ここで重要なのは、
-- 同順位があっても、その次の順位が飛ばないことである。
--
-- たとえば 32歳が2人いても、
-- 2人とも 3位 になり、
-- 次は 4位 になる。
--
-- これが DENSE_RANK() の最大の特徴である。


-- ================================================
-- OVER(ORDER BY age DESC) の意味
-- ================================================
--
-- OVER(...) は、
-- ウィンドウ関数が
-- どの順序・どの区切りで計算されるか
-- を指定する。
--
-- 今回は PARTITION BY が無いので、
-- Address テーブル全体を1つの集合として順位付けしている。
--
-- ORDER BY age DESC によって、
--   年齢が高い順
-- が順位の基準になる。
--
-- つまり今回は、
--   テーブル全体を対象にした年齢ランキング
-- を作っている。


-- ================================================
-- ORDER BY 句との違い
-- ================================================
--
-- 次のSQLは単純な並び替えである。
--
--   SELECT name, age
--     FROM Address
--    ORDER BY age DESC;
--
-- これは
--   「年齢が高い順に表示する」
-- だけであり、
-- 順位列は作られない。
--
-- 一方、今回のSQLは
--
--   DENSE_RANK() OVER(ORDER BY age DESC)
--
-- を使っているため、
--   「年齢が高い順を基準に順位を計算して返す」
-- 処理になっている。
--
-- つまり、
--
--   ORDER BY
--     → 並び順を決める
--
--   DENSE_RANK() OVER(ORDER BY ...)
--     → その並び順を使って順位列を作る
--
-- という違いがある。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | age
--   --------+----
--   小川    | 30
--   前田    | 21
--   森      | 45
--   林      | 32
--   井上    | 55
--   佐々木  | 19
--   松本    | 20
--   佐藤    | 25
--   鈴木    | 32
--
-- age の高い順に並べると、
--
--   井上    55
--   森      45
--   林      32
--   鈴木    32
--   小川    30
--   佐藤    25
--   前田    21
--   松本    20
--   佐々木  19
--
-- となる。
--
-- ここに DENSE_RANK() を適用すると、
--
--   name    | age | dense_rnk
--   --------+-----+----------
--   井上    | 55  | 1
--   森      | 45  | 2
--   林      | 32  | 3
--   鈴木    | 32  | 3
--   小川    | 30  | 4
--   佐藤    | 25  | 5
--   前田    | 21  | 6
--   松本    | 20  | 7
--   佐々木  | 19  | 8
--
-- のようになる。
--
-- ここで
--   林
--   鈴木
-- は同じ 32歳なので、
-- 同順位の 3位 になる。
--
-- そして次の 小川 は 4位 になる。
-- ここが RANK() と違う点である。


-- ================================================
-- 図解: DENSE_RANK のイメージ
-- ================================================
--
--   Address の各行
--        |
--        v
--   +------------------------------+
--   | age の大きい順を考える       |
--   | ORDER BY age DESC            |
--   +------------------------------+
--        |
--        v
--   55, 45, 32, 32, 30, 25, 21, 20, 19
--        |
--        v
--   +------------------------------+
--   | 順位を付ける                 |
--   | DENSE_RANK()                 |
--   +------------------------------+
--        |
--        v
--   1, 2, 3, 3, 4, 5, 6, 7, 8
--
--
-- 同順位がある場合のイメージ:
--
--   55  → 1位
--   45  → 2位
--   32  → 3位
--   32  → 3位
--   30  → 4位
--
-- つまり、
--   同じ値には同じ順位を付ける
--   ただし次の順位は飛ばない
-- という仕組みになっている。


-- ================================================
-- RANK() との違い
-- ================================================
--
-- 前回学んだ RANK() は、
-- 同順位があると次の順位が飛ぶ。
--
-- 例:
--   1, 2, 3, 3, 5
--
-- 一方、今回の DENSE_RANK() は、
-- 同順位があっても次の順位が飛ばない。
--
-- 例:
--   1, 2, 3, 3, 4
--
-- したがって、
--
--   RANK()
--     → 競技順位のようなイメージ
--
--   DENSE_RANK()
--     → 順位を詰めて連番にしたいイメージ
--
-- と考えると理解しやすい。


-- ================================================
-- DENSE_RANK(), RANK(), ROW_NUMBER() の違い
-- ================================================
--
-- 今回は DENSE_RANK() を使っている。
--
-- 似た関数として次がある。
--
-- 1. RANK()
--    同順位を許す。
--    次の順位は飛ぶ。
--
--    例:
--      1, 2, 3, 3, 5
--
-- 2. DENSE_RANK()
--    同順位を許す。
--    次の順位は飛ばない。
--
--    例:
--      1, 2, 3, 3, 4
--
-- 3. ROW_NUMBER()
--    同じ値でも必ず別番号を振る。
--
--    例:
--      1, 2, 3, 4, 5
--
-- 今回は
--   「同点は同順位にしたい」
--   しかし
--   「順位番号は詰めたい」
-- 場合に対応する DENSE_RANK() を使っている。


-- ================================================
-- GROUP BY との違い
-- ================================================
--
-- 今回の DENSE_RANK() は、
-- 各行に順位を付けるが、
-- 元の行は消えない。
--
-- つまり結果の粒度は
--   1行1人
-- のままである。
--
-- 一方、GROUP BY は行をまとめてしまう。
--
-- したがって DENSE_RANK() は、
--   明細を残したまま比較・順位付けしたい
-- ときに向いている。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, age,
--        DENSE_RANK() OVER(ORDER BY age DESC) AS dense_rnk
--   → name, age と、年齢順位 dense_rnk を表示する
--
-- DENSE_RANK()
--   → 抜け番なしの順位を計算するウィンドウ関数
--
-- OVER(...)
--   → どの順序を基準に順位を付けるかを指定する
--
-- ORDER BY age DESC
--   → 年齢が高い順に順位を付ける
--
-- AS dense_rnk
--   → 計算した順位列に dense_rnk という名前を付ける
--
-- FROM Address
--   → Address テーブルを元データとする
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   今回の SELECT 文には、文全体の ORDER BY は無い。
--
--   そのため、
--   dense_rnk の計算基準は age DESC だが、
--   表示順自体は必ずしも dense_rnk 順になるとは限らない。
--
--   見やすくしたい場合は、たとえば次のように書ける。
--
--     SELECT name,
--            age,
--            DENSE_RANK() OVER(ORDER BY age DESC) AS dense_rnk
--       FROM Address
--      ORDER BY dense_rnk, name;
-- ================================================


-- ================================================
-- 補足2:
--   PARTITION BY を加えると、
--   グループごとのランキングもできる。
--
--   例:
--
--     SELECT name,
--            sex,
--            age,
--            DENSE_RANK() OVER(PARTITION BY sex ORDER BY age DESC) AS dense_rnk
--       FROM Address;
--
--   この場合は、男女それぞれの中で年齢順位を付ける。
-- ================================================


