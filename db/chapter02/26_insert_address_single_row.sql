-- ================================================
-- ファイル名: 26_insert_address_single_row.sql
-- 役割:
--   Address テーブルに 1 行だけデータを追加する。
--
-- このSQLの目的:
--   まずは INSERT 文の基本形として、
--   1件のレコードを表へ登録する方法を理解すること。
--
-- 学習上の主題:
--   INSERT INTO ... VALUES ...
--   という最も基本的な追加構文を理解すること。
--
-- 今回の重要点:
--   1行追加では、VALUES の後ろに
--   1組の値の並びだけを書く。
--
--   つまり
--   「1回の INSERT 文で 1行だけ登録する」
--   という基本形である。
-- ================================================

INSERT INTO Address
(
    -- ここでは、どの列へ値を入れるかを明示している。
    --
    -- この列順は、後ろの VALUES の値の順番と対応していなければならない。
    --
    -- 対応関係:
    --   1番目 -> name
    --   2番目 -> phone_nbr
    --   3番目 -> address
    --   4番目 -> sex
    --   5番目 -> age
    name,
    phone_nbr,
    address,
    sex,
    age
)
VALUES
(
    -- VALUES の中には、実際に挿入したい値を書く。
    --
    -- ここでは 1組だけ書いているため、
    -- 追加される行も 1行だけである。
    --
    -- 各値の意味:
    --   '小川'             -> name
    --   '080-3333-XXXX'   -> phone_nbr
    --   '東京都'           -> address
    --   '男'               -> sex
    --   30                 -> age
    --
    -- 文字列はシングルクォートで囲む。
    -- 数値はクォートしない。
    '小川',
    '080-3333-XXXX',
    '東京都',
    '男',
    30
);


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを追加先として指定する
-- 2. 値を入れる対象列を name, phone_nbr, address, sex, age の順で決める
-- 3. VALUES の中の1組の値を、その列順に対応させる
-- 4. その対応関係に従って 1行を新規追加する
--
-- 概念的には次のような処理に近い。
--
--   new_row = {
--       name: '小川',
--       phone_nbr: '080-3333-XXXX',
--       address: '東京都',
--       sex: '男',
--       age: 30
--   }
--
--   insert new_row into Address
--
-- つまりこれは、
--   「1件のデータを 1行として登録する」
--   という最小単位の INSERT である。


-- ================================================
-- INSERT INTO の意味
-- ================================================
--
-- INSERT INTO は、
--   表に新しい行を追加するための構文である。
--
-- 今回の
--
--   INSERT INTO Address (...)
--
-- は、
--   Address テーブルへデータを入れる
-- という意味になる。
--
-- SELECT が「読む」処理だったのに対し、
-- INSERT は「書き込む」処理である。


-- ================================================
-- 列名を明示する意味
-- ================================================
--
-- 今回は
--
--   INSERT INTO Address (name, phone_nbr, address, sex, age)
--
-- のように列名を明示している。
--
-- この書き方の利点:
--
-- 1. どの列に何を入れるかが分かりやすい
-- 2. VALUES の順序ミスを防ぎやすい
-- 3. 表定義の列順に依存しにくい
--
-- もし列名を書かない場合は、
-- 表定義の列順に完全に依存するため、
-- 教材としても実務としても、列名明示の方が安全であることが多い。


-- ================================================
-- VALUES の意味
-- ================================================
--
-- VALUES は、
--   実際に挿入する値の並び
-- を書く部分である。
--
-- 今回は 1組だけなので、
-- 追加される行も 1行だけである。
--
-- 一般に、
--   1組の丸括弧 = 1行分
-- と考えると分かりやすい。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- もし Address テーブルが空なら、
-- 実行後は次の1行が追加される。
--
--   name   | phone_nbr       | address | sex | age
--   -------+-----------------+---------+-----+----
--   小川   | 080-3333-XXXX   | 東京都  | 男  | 30
--
-- もしすでにデータが入っているなら、
-- その末尾に新しい1行が追加されると考えればよい。
--
-- ただし、name は PRIMARY KEY なので、
-- すでに '小川' が存在している場合は重複エラーになる。


-- ================================================
-- 補足1:
--   文字列と数値の書き方の違い
--
--   '小川' や '東京都' は文字列なのでシングルクォートが必要である。
--   30 は整数なのでクォートしない。
-- ================================================


-- ================================================
-- 補足2:
--   NULL を入れたい場合は、文字列 'NULL' ではなく、
--   クォートなしの NULL と書く必要がある。
--
--   例:
--     INSERT INTO Address (name, phone_nbr, address, sex, age)
--     VALUES ('井上', NULL, '福島県', '女', 55);
-- ================================================


-- ================================================
-- 補足3:
--   この SQL は 1行追加なので、
--   多数のレコードを入れる場合は同じ INSERT を何度も書くことになる。
--
--   次の multirow insert は、
--   これを 1つの INSERT 文にまとめて効率よく書く方法である。
