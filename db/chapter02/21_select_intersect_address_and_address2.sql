-- ================================================
-- ファイル名: 21_select_intersect_address_and_address2.sql
-- 役割:
--   Address テーブルと Address2 テーブルの両方に共通して存在する行だけを取り出す。
--
-- このSQLの目的:
--   2つの表をまとめるのではなく、
--   「両方にまったく同じ形で存在するレコードだけ」を選び出すこと。
--
-- 学習上の主題:
--   INTERSECT を使って、
--   2つの SELECT 結果の積集合を求める方法を理解すること。
--
-- 今回の最重要ポイント:
--   INTERSECT は、
--   前後2つの SELECT 結果のうち
--   「両方に共通する行だけ」を返す構文である。
--
--   つまり今回は、
--   Address にもあり、
--   Address2 にもある行だけが最終結果に残る。
-- ================================================

SELECT
    -- * は「そのテーブルの全列」を意味する。
    --
    -- 今回 Address テーブルには
    --   name
    --   phone_nbr
    --   address
    --   sex
    --   age
    -- の5列があるため、
    -- SELECT * はその5列すべてを取り出す。
    --
    -- したがって、この SELECT の1行は
    -- 「1人分の完全なレコード全体」
    -- を表す。
    *
FROM
    -- 1つ目の SELECT の対象:
    --   Address テーブルから全行・全列を取得する。
    Address

INTERSECT

-- INTERSECT:
--   前後2つの SELECT 結果の積集合を求める。
--
--   積集合とは、
--   「両方の結果に共通して存在するものだけを残す」
--   操作である。
--
--   そのため、
--   Address にだけある行は除外される。
--   Address2 にだけある行も除外される。
--   両方にある行だけが残る。
--
--   これは UNION が「和集合」だったのと対になる考え方である。

SELECT
    -- こちらも * により Address2 の全列を取り出す。
    --
    -- INTERSECT を使うためには、
    -- 前後の SELECT が返す列数と列の並びが対応している必要がある。
    --
    -- 今回は Address と Address2 が
    --   name, phone_nbr, address, sex, age
    -- という同じ構造を持つため、
    -- SELECT * 同士で INTERSECT できる。
    *
FROM
    -- 2つ目の SELECT の対象:
    --   Address2 テーブルから全行・全列を取得する。
    Address2;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を取得する
-- 2. Address2 テーブルの全行を取得する
-- 3. 2つの結果を比較する
-- 4. 両方に存在する行だけを残す
-- 5. 共通している行だけを結果として返す
--
-- 概念的には次のような処理に近い。
--
--   result1 = all rows from Address
--   result2 = all rows from Address2
--
--   common = rows that exist in both result1 and result2
--
--   output common
--
-- つまりこれは、
--   「2つの表を見比べて、共通している完全一致行だけを取り出す」
-- という集合演算である。


-- ================================================
-- INTERSECT とは何か
-- ================================================
--
-- INTERSECT は、
--   2つ以上の SELECT 文の結果の「積集合」を求める構文である。
--
-- 集合論でいう積集合とは、
--   A と B の両方に属する要素だけを集めた集合
-- のことである。
--
-- 今回で言えば、
--
--   SELECT * FROM Address
--
-- と
--
--   SELECT * FROM Address2
--
-- の両方に含まれている行だけが残る。
--
-- つまり意味としては、
--   「Address にある行」
--   かつ
--   「Address2 にもある行」
-- を選ぶ操作である。


-- ================================================
-- なぜ「積集合」なのか
-- ================================================
--
-- 前に学んだ UNION は和集合であり、
-- どちらか一方にあれば結果に含まれた。
--
-- 今回の INTERSECT はその逆で、
-- 両方に存在しないと結果に含まれない。
--
-- つまり
--
--   UNION
--     → Address または Address2 にある行
--
--   INTERSECT
--     → Address かつ Address2 にある行
--
-- という違いになる。
--
-- したがって INTERSECT は、
--   「共通部分を取り出す」
-- と理解すると分かりやすい。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Address に次のような行があるとする。
--
--   小川, 080-3333-XXXX, 東京都,   男, 30
--   前田, 090-0000-XXXX, 東京都,   女, 21
--   森,   090-2984-XXXX, 東京都,   男, 45
--   林,   080-3333-XXXX, 福島県,   男, 32
--   ...
--
-- Address2 に次のような行があるとする。
--
--   小川, 080-3333-XXXX, 東京都,   男, 30
--   林,   080-3333-XXXX, 福島県,   男, 32
--   武田, NULL,            福島県,   男, 18
--   斉藤, 080-2367-XXXX, 千葉県,   女, 19
--   上野, NULL,            千葉県,   女, 20
--   広田, 090-0205-XXXX, 三重県,   男, 25
--
-- このとき、
--   小川
--   林
-- の行は両方の表に存在している。
--
-- 一方で
--   前田
--   森
-- は Address にしかなく、
--   武田
--   斉藤
--   上野
--   広田
-- は Address2 にしかない。
--
-- したがって INTERSECT の結果は、
-- 共通する次の行だけになる。
--
--   小川, 080-3333-XXXX, 東京都, 男, 30
--   林,   080-3333-XXXX, 福島県, 男, 32


-- ================================================
-- 図解: INTERSECT のイメージ
-- ================================================
--
--   Address の結果
--   +----------------------------------+
--   | 小川                             |
--   | 前田                             |
--   | 森                               |
--   | 林                               |
--   | ...                              |
--   +----------------------------------+
--
--             INTERSECT
--
--   Address2 の結果
--   +----------------------------------+
--   | 小川                             |
--   | 林                               |
--   | 武田                             |
--   | 斉藤                             |
--   | 上野                             |
--   | 広田                             |
--   +----------------------------------+
--
--             ↓ 共通している行だけ残す
--
--   INTERSECT の結果
--   +----------------------------------+
--   | 小川                             |
--   | 林                               |
--   +----------------------------------+
--
--
-- 集合として見ると:
--
--   Address の行集合
--      ∩
--   Address2 の行集合
--
--   =
--   両方に含まれる行だけ


-- ================================================
-- INTERSECT と他の集合演算との違い
-- ================================================
--
-- UNION:
--   和集合
--   どちらか一方にあれば結果に含まれる
--
-- INTERSECT:
--   積集合
--   両方にあるものだけ結果に含まれる
--
-- EXCEPT
--   差集合
--   片方にあって、もう片方には無いものを返す
--
-- 今回はこのうち INTERSECT を使っているため、
-- 「共通している行の抽出」が目的である。


-- ================================================
-- INTERSECT に必要な条件
-- ================================================
--
-- INTERSECT を使うには、
-- 前後の SELECT 文が「同じ形の結果」を返す必要がある。
--
-- 具体的には、少なくとも次の点が重要である。
--
-- 1. 列数が同じであること
-- 2. 列の並び順が対応していること
-- 3. 対応する列どうしの型が極端に矛盾しないこと
--
-- 今回は
--   Address
--   Address2
-- が同じ構造を持っているため、
--
--   SELECT * FROM Address
--   INTERSECT
--   SELECT * FROM Address2
--
-- という書き方が成立している。


-- ================================================
-- 重要: 何が「同じ行」とみなされるのか
-- ================================================
--
-- 今回 SELECT * を使っているため、
-- 比較対象は name だけではない。
--
--   name
--   phone_nbr
--   address
--   sex
--   age
--
-- の5列すべてが一致して初めて、
-- 「同じ行」とみなされる。
--
-- したがって、
-- たとえ name が同じでも、
-- phone_nbr や age が違えば別行として扱われる。
--
-- ここは非常に重要である。
--
-- 今回の INTERSECT は
--   name の共通部分
-- ではなく、
--   行全体の共通部分
-- を求めている。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT * FROM Address
--   → Address テーブルの全列・全行を取得する
--
-- INTERSECT
--   → 前後2つの SELECT 結果の共通部分だけを残す
--
-- SELECT * FROM Address2
--   → Address2 テーブルの全列・全行を取得する
--
-- ;
--   → SQL文の終了を表す


