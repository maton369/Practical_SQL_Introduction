-- ================================================
-- ファイル名: 30_update_address_phone_by_name.sql
-- 役割:
--   Address テーブルに入っている既存レコードのうち、
--   佐々木さんの phone_nbr を新しい値に更新する。
--
-- このSQLの目的:
--   INSERT のように新しい行を追加するのではなく、
--   すでに存在している行の一部の列値だけを書き換える方法を理解すること。
--
-- 学習上の主題:
--   UPDATE ... SET ... WHERE ...
--   という、最も基本的な更新構文を理解すること。
--
-- 今回の最重要ポイント:
--   UPDATE は
--   「新しい行を作る」のではなく、
--   「既存の行を探して、その中の値を書き換える」
--   処理である。
--
--   つまり今回は、
--   Address テーブルの中から
--   name = '佐々木'
--   の行を見つけ、
--   その行の phone_nbr を新しい電話番号へ変更している。
-- ================================================

UPDATE
    -- UPDATE:
    --   既存の表の中にある行を更新するための構文である。
    --
    --   INSERT は新規追加、
    --   DELETE は削除だったのに対し、
    --   UPDATE は「中身を書き換える」処理である。
    Address
SET
    -- SET:
    --   更新したい列と、その新しい値を指定する部分である。
    --
    --   今回は phone_nbr 列だけを更新対象にしている。
    --
    --   = の左側:
    --     更新する列名
    --
    --   = の右側:
    --     更新後に入れたい新しい値
    phone_nbr = '080-5849-XXXX'
WHERE
    -- WHERE:
    --   どの行を更新対象にするかを絞り込む条件を書く部分である。
    --
    --   今回は
    --     name = '佐々木'
    --   なので、name 列が 佐々木 の行だけが更新対象になる。
    --
    --   逆に言うと、
    --   WHERE が無ければ Address テーブルの全行が更新対象になるため、
    --   UPDATE では WHERE の有無が非常に重要である。
    name = '佐々木';
    -- '佐々木' は文字列なのでシングルクォートで囲む。
    --
    -- この条件に一致する行だけが更新される。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを更新対象として指定する
-- 2. 各行について name = '佐々木' かどうかを判定する
-- 3. 条件に一致した行だけを更新候補とする
-- 4. その行の phone_nbr を '080-5849-XXXX' に書き換える
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.name == '佐々木':
--           row.phone_nbr = '080-5849-XXXX'
--
-- つまりこれは、
--   「佐々木さんの行を見つけて、電話番号だけ変更する」
--   条件付き UPDATE である。


-- ================================================
-- UPDATE とは何か
-- ================================================
--
-- UPDATE は、
--   表にすでに存在している行の値を変更するための構文である。
--
-- 一般形は次のようになる。
--
--   UPDATE 表名
--      SET 列名 = 新しい値
--    WHERE 条件;
--
-- 今回はその最も基本的な形であり、
--
--   UPDATE Address
--      SET phone_nbr = '080-5849-XXXX'
--    WHERE name = '佐々木';
--
-- となっている。
--
-- したがって意味としては、
--   Address の中で佐々木さんの行を探し、
--   phone_nbr だけ新しい値に変更する
-- という処理になる。


-- ================================================
-- INSERT や DELETE との違い
-- ================================================
--
-- INSERT:
--   新しい行を追加する
--
-- DELETE:
--   既存の行を削除する
--
-- UPDATE:
--   既存の行の中身を書き換える
--
-- 今回のSQLでは、
-- 佐々木さんという行そのものは消えないし、新しくも作られない。
-- その行の phone_nbr の値だけが変わる。
--
-- ここが UPDATE の本質である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば実行前の Address テーブルに次の行があるとする。
--
--   name    | phone_nbr       | address | sex | age
--   --------+-----------------+---------+-----+----
--   佐々木  | 080-5848-XXXX   | 千葉県  | 女  | 19
--
-- ここで
--
--   UPDATE Address
--      SET phone_nbr = '080-5849-XXXX'
--    WHERE name = '佐々木';
--
-- を実行すると、実行後は次のようになる。
--
--   name    | phone_nbr       | address | sex | age
--   --------+-----------------+---------+-----+----
--   佐々木  | 080-5849-XXXX   | 千葉県  | 女  | 19
--
-- つまり、
--   phone_nbr だけが更新され、
--   他の列はそのまま残る。
--
-- ここで重要なのは、
--   address, sex, age は変更されない
-- という点である。
--
-- UPDATE は、SET で指定した列だけを書き換える。


-- ================================================
-- 図解: 単一列 UPDATE のイメージ
-- ================================================
--
--   Address テーブル
--   +-----------------------------------------------+
--   | 小川    | 080-3333-XXXX | 東京都   | 男 | 30 |
--   | 前田    | 090-0000-XXXX | 東京都   | 女 | 21 |
--   | 佐々木  | 080-5848-XXXX | 千葉県   | 女 | 19 |
--   | 松本    | NULL          | 千葉県   | 女 | 20 |
--   +-----------------------------------------------+
--                    |
--                    v
--         WHERE name = '佐々木'
--                    |
--         +----------+----------+
--         |                     |
--         v                     v
--      一致する               一致しない
--      (佐々木)               (その他)
--         |                     |
--         v                     v
--   phone_nbr を更新           そのまま
--
-- 更新後:
--
--   佐々木  | 080-5849-XXXX | 千葉県 | 女 | 19
--
-- つまり、
--   対象行を見つけて
--   指定列だけを書き換える
-- というアルゴリズムである。


-- ================================================
-- WHERE が重要である理由
-- ================================================
--
-- 今回は
--
--   WHERE name = '佐々木'
--
-- があるため、1人分だけ更新される。
--
-- もし WHERE が無かった場合、
--
--   UPDATE Address
--      SET phone_nbr = '080-5849-XXXX';
--
-- となり、Address テーブルの全行の phone_nbr が
-- 同じ値に書き換わってしまう。
--
-- したがって UPDATE 文では、
--   WHERE の有無が更新範囲を決める
-- という点が最重要である。


-- ================================================
-- 実務上の注意
-- ================================================
--
-- UPDATE はデータを直接変更する更新系SQLである。
-- そのため、実務では実行前に対象確認を行うことが重要である。
--
-- たとえば今回なら、先に次の SELECT を実行して
-- 更新対象を確認できる。
--
--   SELECT *
--     FROM Address
--    WHERE name = '佐々木';
--
-- これで本当に更新したい行が1件だけ出ることを確認してから
-- UPDATE を実行すると安全である。


-- ================================================
-- 補足1:
--   name が PRIMARY KEY である前提なら、
--   WHERE name = '佐々木' で最大1行だけが更新される。
--
--   もし name が一意でない設計なら、
--   同じ名前の複数行が一度に更新される可能性がある。
-- ================================================


-- ================================================
-- 補足2:
--   変更後の確認は次のような SELECT で行える。
--
--     SELECT *
--       FROM Address
--      WHERE name = '佐々木';
-- ================================================


