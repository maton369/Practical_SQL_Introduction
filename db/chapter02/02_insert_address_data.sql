-- ================================================
-- ファイル名: 02_insert_address_data.sql
-- 役割:
--   Address テーブルにサンプルデータを登録する。
--
-- INSERT INTO とは:
--   テーブルに新しい行を追加するためのSQL構文である。
--
-- 基本構文:
--
--   INSERT INTO テーブル名
--   VALUES(値1, 値2, 値3, ...);
--
-- この書き方では、
--   テーブル定義で並んでいる列の順番どおりに値を指定する必要がある。
--
-- 今回の Address テーブルの列順は次の通りである。
--
--   1. name
--   2. phone_nbr
--   3. address
--   4. sex
--   5. age
--
-- したがって、VALUES の中もこの順番で書かれている。
--
-- NULL の意味:
--   値が存在しないことを表す特別な値である。
--   空文字列 '' とは異なる。
--
-- 今回は phone_nbr 列に NOT NULL が無いので、
--   phone_nbr に NULL を入れることができる。
-- ================================================

-- 1行目を追加する。
-- name='小川', phone_nbr='080-3333-XXXX', address='東京都', sex='男', age=30
INSERT INTO Address VALUES('小川', '080-3333-XXXX', '東京都',   '男', 30);

-- 2行目を追加する。
INSERT INTO Address VALUES('前田', '090-0000-XXXX', '東京都',   '女', 21);

-- 3行目を追加する。
INSERT INTO Address VALUES('森',   '090-2984-XXXX', '東京都',   '男', 45);

-- 4行目を追加する。
INSERT INTO Address VALUES('林',   '080-3333-XXXX', '福島県',   '男', 32);

-- 5行目を追加する。
-- phone_nbr に NULL を入れている。
-- これは「電話番号が未登録・不明」であることを表す。
INSERT INTO Address VALUES('井上', NULL,            '福島県',   '女', 55);

-- 6行目を追加する。
INSERT INTO Address VALUES('佐々木', '080-5848-XXXX', '千葉県', '女', 19);

-- 7行目を追加する。
-- ここでも phone_nbr は NULL である。
INSERT INTO Address VALUES('松本', NULL,            '千葉県',   '女', 20);

-- 8行目を追加する。
INSERT INTO Address VALUES('佐藤', '090-1922-XXXX', '三重県',   '女', 25);

-- 9行目を追加する。
INSERT INTO Address VALUES('鈴木', '090-0001-XXXX', '和歌山県', '男', 32);


-- ================================================
-- 補足: なぜ列名を書いていないのか
-- ================================================
--
-- 今回は次の書き方を使っている。
--
--   INSERT INTO Address VALUES(...);
--
-- この書き方は短いが、
-- テーブルの列順に強く依存する。
--
-- より明示的に書く場合は、次のように列名を書くこともできる。
--
--   INSERT INTO Address(name, phone_nbr, address, sex, age)
--   VALUES('小川', '080-3333-XXXX', '東京都', '男', 30);
--
-- この書き方の方が、
-- 「どの値がどの列に入るのか」が読み手に分かりやすい。
--
-- 教材としては両方覚えておくとよい。