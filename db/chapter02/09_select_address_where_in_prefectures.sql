-- ================================================
-- ファイル名: 09_select_address_where_in_prefectures.sql
-- 役割:
--   Address テーブルの中から、
--   address 列の値が
--     - '東京都'
--     - '福島県'
--     - '千葉県'
--   のいずれかに当てはまる行だけを検索する。
--
-- このSQLの目的:
--   Address テーブル全体をそのまま表示するのではなく、
--   住所が特定の複数都県に属する人だけを対象に絞り込んで、
--   その人たちの
--     - name
--     - address
--   を表示する。
--
-- 学習上の主題:
--   IN を使って、
--   「ある列の値が、候補の一覧の中に含まれているか」
--   を判定する方法を理解すること。
--
-- 今回の条件:
--   address IN ('東京都', '福島県', '千葉県')
--
-- これは
--   「address 列の値が、
--     東京都・福島県・千葉県のいずれかであれば採用する」
-- という意味である。
--
-- OR との関係:
--   この IN は、意味としては次の OR とほぼ同じである。
--
--     address = '東京都'
--     OR address = '福島県'
--     OR address = '千葉県'
--
-- ただし、同じ列に対して複数の候補を並べる場合は、
-- OR を何度も書くより IN を使った方が短く、読みやすい。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は name 列と address 列だけを取り出す。
    --   したがって、phone_nbr, sex, age は表示されない。
    name,
    address
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを検索対象にしている。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込む条件を書く部分である。
    --
    --   今回は単純な = や <> ではなく、
    --   IN を使って「候補の一覧に含まれるかどうか」を判定している。
    --
    --   address IN ('東京都', '福島県', '千葉県')
    --   は、
    --   「address 列の値が、この3つの文字列のどれかと一致すれば真」
    --   という意味である。
    --
    --   つまり、
    --     address = '東京都'
    --     または
    --     address = '福島県'
    --     または
    --     address = '千葉県'
    --   のどれかを満たせば結果に残る。
    --
    -- IN の右側の丸括弧 (...) には、
    --   比較対象となる候補値をカンマ区切りで並べる。
    --
    -- ここでは文字列を並べているので、
    -- 各値をシングルクォートで囲っている。
    address IN ('東京都', '福島県', '千葉県');


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について address 列の値を確認する
-- 3. address の値が
--      '東京都'
--      '福島県'
--      '千葉県'
--    のいずれかに一致するかを判定する
-- 4. 一致した行だけを残す
-- 5. 残った行から name 列と address 列だけを取り出す
-- 6. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address in {'東京都', '福島県', '千葉県'}:
--           output row.name, row.address
--
-- つまりこれは、
--   「全行を見ながら、住所が候補一覧に含まれる行だけを通す」
-- という検索である。


-- ================================================
-- IN の意味
-- ================================================
--
-- IN は、
--   「左辺の値が、右辺の候補集合の中に含まれているか」
-- を調べるための構文である。
--
-- 一般形は次のようになる。
--
--   列名 IN (値1, 値2, 値3, ...)
--
-- 今回で言えば、
--
--   address IN ('東京都', '福島県', '千葉県')
--
-- なので、
--   address が東京都なら真
--   address が福島県なら真
--   address が千葉県なら真
--   それ以外なら偽
-- となる。
--
-- したがって、
--   - 東京都
--   - 福島県
--   - 千葉県
-- の行は結果に残り、
-- それ以外の
--   - 三重県
--   - 和歌山県
-- などの行は結果から除外される。


-- ================================================
-- OR で書いた場合との対応
-- ================================================
--
-- 今回のSQLは、意味としては次のように OR で書いたものと同じである。
--
--   SELECT name, address
--   FROM Address
--   WHERE address = '東京都'
--      OR address = '福島県'
--      OR address = '千葉県';
--
-- このように、同じ列に対して
-- 「A または B または C」
-- を書きたいときは、
-- IN を使うとすっきり書ける。
--
-- つまり IN は、
--   OR を簡潔に書くための便利な構文
-- と理解すると分かりやすい。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | address
--   --------+----------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県
--
-- このとき、
--   address IN ('東京都', '福島県', '千葉県')
-- に一致するのは
--   - 小川
--   - 前田
--   - 森
--   - 林
--   - 井上
--   - 佐々木
--   - 松本
-- である。
--
-- 一方で
--   - 佐藤   (三重県)
--   - 鈴木   (和歌山県)
-- は候補一覧に含まれていないため、結果に含まれない。
--
-- したがって結果は次のようになる。
--
--   name    | address
--   --------+----------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県


-- ================================================
-- 図解: IN のイメージ
-- ================================================
--
--   address の値
--        |
--        v
--   +------------------------------+
--   | 候補一覧に含まれるか確認する |
--   | ('東京都', '福島県', '千葉県') |
--   +------------------------------+
--        |
--   +----+-------------------------------+
--   |                                    |
--   v                                    v
-- 含まれる                             含まれない
--   |                                    |
--   v                                    v
-- 結果に残す                           結果から除外する
--
--
-- 具体例:
--
--   '東京都'   → 含まれる → 残す
--   '福島県'   → 含まれる → 残す
--   '千葉県'   → 含まれる → 残す
--   '三重県'   → 含まれない → 除外
--   '和歌山県' → 含まれない → 除外


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, address
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- WHERE address IN ('東京都', '福島県', '千葉県')
--   → address 列が指定した候補のどれかに一致する行だけを残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   IN は「候補一覧の中にあるか」を調べるので、
--   同じ列に対して複数の候補を指定する場面で便利である。
--
--   たとえば都道府県のように、
--   候補値をいくつか並べたいときに向いている。
-- ================================================


-- ================================================
-- 補足2:
--   IN の右側は丸括弧で囲み、候補をカンマ区切りで並べる。
--
--   例:
--     IN ('東京都', '福島県', '千葉県')
--
--   文字列なので各値にシングルクォートが必要である。
-- ================================================


-- ================================================
-- 補足3:
--   ORDER BY が無いため、結果の並び順は保証されない。
--
--   名前順で並べたい場合は、たとえば次のように書く。
--
--     SELECT name, address
--     FROM Address
--     WHERE address IN ('東京都', '福島県', '千葉県')
--     ORDER BY name;
--
--   今回の主題は「IN による複数候補の絞り込み」であるため、
--   並び替えまでは行っていない。
-- ================================================