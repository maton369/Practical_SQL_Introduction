-- ================================================
-- ファイル名: 31_update_address_multiple_columns_by_name.sql
-- 役割:
--   Address テーブルの中で、
--   佐々木さんの複数列を 1つの UPDATE 文でまとめて更新する。
--
-- このSQLの目的:
--   1列だけでなく、複数列を同時に書き換える方法を理解すること。
--
-- 学習上の主題:
--   1つの UPDATE 文に複数の列更新を書く方法として、
--
--   1. SET に列をカンマ区切りで並べる方法
--   2. 列リストと値リストを括弧でまとめる方法
--
--   の2つを理解すること。
--
-- 今回の最重要ポイント:
--   UPDATE は 1列だけでなく、
--   複数列を同時に変更できる。
--
--   つまり今回は、
--   佐々木さんの行を対象に
--     - phone_nbr
--     - age
--   の2列を、1回の UPDATE 文でまとめて変更している。
-- ================================================


-- =========================================================
-- 1. SET に列をカンマ区切りで並べる方法
-- =========================================================
--
-- この書き方は、多くのSQLで最も基本的で分かりやすい複数列更新である。
-- 各列に対して
--   列名 = 新しい値
-- を個別に書き、それらをカンマで区切る。
-- =========================================================

UPDATE
    -- 更新対象の表
    Address
SET
    -- 1つ目の更新:
    --   phone_nbr 列を新しい電話番号に書き換える
    phone_nbr = '080-5848-XXXX',

    -- 2つ目の更新:
    --   age 列を 20 に書き換える
    --
    -- ここでは数値なのでシングルクォートは不要である。
    age = 20
WHERE
    -- 更新対象を佐々木さんの行に限定する
    name = '佐々木';


-- ================================================
-- この1つ目の UPDATE の処理の流れ
-- ================================================
--
-- 1. Address テーブルを更新対象とする
-- 2. 各行について name = '佐々木' かどうかを判定する
-- 3. 一致した行だけを更新対象とする
-- 4. その行の phone_nbr を '080-5848-XXXX' に書き換える
-- 5. 同じ行の age を 20 に書き換える
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.name == '佐々木':
--           row.phone_nbr = '080-5848-XXXX'
--           row.age = 20
--
-- つまりこれは、
--   「1人の行に対して、複数の属性を同時に更新する」
--   処理である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば更新前が次のようだとする。
--
--   name    | phone_nbr       | address | sex | age
--   --------+-----------------+---------+-----+----
--   佐々木  | 080-5849-XXXX   | 千葉県  | 女  | 19
--
-- この UPDATE 実行後は次のようになる。
--
--   name    | phone_nbr       | address | sex | age
--   --------+-----------------+---------+-----+----
--   佐々木  | 080-5848-XXXX   | 千葉県  | 女  | 20
--
-- つまり、
--   phone_nbr と age が同時に変わる。
--   address や sex は変わらない。
--
-- ここでも、SET に書いた列だけが更新される点が重要である。


-- ================================================
-- 図解: カンマ区切りで複数列更新
-- ================================================
--
--   対象行:
--   +-----------------------------------------------+
--   | 佐々木 | 080-5849-XXXX | 千葉県 | 女 | 19   |
--   +-----------------------------------------------+
--                    |
--                    v
--   SET phone_nbr = '080-5848-XXXX',
--       age       = 20
--                    |
--                    v
--   +-----------------------------------------------+
--   | 佐々木 | 080-5848-XXXX | 千葉県 | 女 | 20   |
--   +-----------------------------------------------+
--
-- つまり、
--   1回の UPDATE で
--   2つの列を書き換えている。


-- =========================================================
-- 2. 列を括弧で囲むことによるリスト表現
-- =========================================================
--
-- この書き方は、
--   更新する列の並び
-- と
--   新しい値の並び
-- を、リストとして対応づける表現である。
--
-- 見方としては、
--   (phone_nbr, age)
-- に
--   ('080-5848-XXXX', 20)
-- を対応させて一括代入する
-- と考えると分かりやすい。
-- =========================================================

UPDATE
    -- 更新対象の表
    Address
SET
    -- 左側:
    --   更新したい列のリスト
    --
    -- 右側:
    --   それぞれの列へ入れる新しい値のリスト
    --
    -- 対応関係:
    --   phone_nbr <- '080-5848-XXXX'
    --   age       <- 20
    (phone_nbr, age) = ('080-5848-XXXX', 20)
WHERE
    -- 更新対象を佐々木さんの行に限定する
    name = '佐々木';


-- ================================================
-- この2つ目の UPDATE の処理の流れ
-- ================================================
--
-- 1. Address テーブルを更新対象とする
-- 2. 各行について name = '佐々木' かどうかを判定する
-- 3. 一致した行だけを更新対象とする
-- 4. 列リスト (phone_nbr, age) と値リスト ('080-5848-XXXX', 20) を対応づける
-- 5. その対応に従って複数列を同時に書き換える
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.name == '佐々木':
--           (row.phone_nbr, row.age) = ('080-5848-XXXX', 20)
--
-- つまりこれは、
--   「複数列への代入をリストとしてまとめて書いた UPDATE」
--   である。


-- ================================================
-- 2つの書き方の意味の違い
-- ================================================
--
-- 1つ目:
--
--   SET phone_nbr = '080-5848-XXXX',
--       age = 20
--
-- これは
--   各列への代入を個別に並べた形
-- である。
--
-- 2つ目:
--
--   SET (phone_nbr, age) = ('080-5848-XXXX', 20)
--
-- これは
--   列リストと値リストを対応づけた形
-- である。
--
-- どちらも意味としては同じであり、
-- 最終的には
--   phone_nbr を '080-5848-XXXX' にする
--   age を 20 にする
-- という更新を行う。
--
-- つまり違うのは主に書き方であり、
-- アルゴリズムとしては同じである。


-- ================================================
-- なぜ複数列を1文でまとめるのか
-- ================================================
--
-- もし 1列ずつ別の UPDATE 文で書くと、たとえば次のようになる。
--
--   UPDATE Address
--      SET phone_nbr = '080-5848-XXXX'
--    WHERE name = '佐々木';
--
--   UPDATE Address
--      SET age = 20
--    WHERE name = '佐々木';
--
-- これでも意味は通るが、
--
-- 1. SQL文が複数本になる
-- 2. 同じ WHERE を繰り返し書く必要がある
-- 3. 1回でまとめて変更したい意図が見えにくい
--
-- という点がある。
--
-- 一方、1つの UPDATE 文にまとめれば、
--
--   「この行の複数属性をまとめて修正したい」
--
-- という意図が明確になり、SQLも簡潔になる。
--
-- したがって複数列更新は実務でも非常によく使われる。


-- ================================================
-- 実務上の注意
-- ================================================
--
-- UPDATE で複数列を更新する場合も、
-- 最も重要なのは WHERE 条件である。
--
-- もし WHERE を書かなければ、
--
--   UPDATE Address
--      SET phone_nbr = '080-5848-XXXX',
--          age = 20;
--
-- となり、Address テーブルの全行の phone_nbr と age が
-- 同じ値に書き換わってしまう。
--
-- したがって、
--   「複数列を更新する」ことよりも
--   「どの行を更新するかを正しく限定する」
--   ことの方がさらに重要である。


-- ================================================
-- 実行前の確認方法
-- ================================================
--
-- 実務では UPDATE 前に、対象行を SELECT で確認することが多い。
--
-- 今回なら次のように書ける。
--
--   SELECT *
--     FROM Address
--    WHERE name = '佐々木';
--
-- この結果を見て、
-- 本当に更新したい行が選ばれていることを確認してから
-- UPDATE を実行するのが安全である。


-- ================================================
-- 図解: 複数列 UPDATE の全体イメージ
-- ================================================
--
--   Address テーブル
--   +-----------------------------------------------+
--   | 小川    | 080-3333-XXXX | 東京都 | 男 | 30 |
--   | 佐々木  | 080-5849-XXXX | 千葉県 | 女 | 19 |
--   | 松本    | NULL          | 千葉県 | 女 | 20 |
--   +-----------------------------------------------+
--                    |
--                    v
--         WHERE name = '佐々木'
--                    |
--         +----------+----------+
--         |                     |
--         v                     v
--      一致する               一致しない
--      (佐々木)               (その他)
--         |                     |
--         v                     v
--   phone_nbr, age を更新      そのまま
--
-- 更新後:
--
--   佐々木 | 080-5848-XXXX | 千葉県 | 女 | 20
--
-- つまり、
--   条件で対象行を決めて
--   その行の複数列を一括更新している。


