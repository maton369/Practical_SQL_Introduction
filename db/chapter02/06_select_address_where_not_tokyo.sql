-- ================================================
-- ファイル名: 06_select_address_where_not_tokyo.sql
-- 役割:
--   Address テーブルの中から、
--   address 列の値が '東京都' ではない行だけを検索する。
--
-- このSQLの目的:
--   住所録全体をそのまま表示するのではなく、
--   「東京都以外に住んでいる人だけ」を対象に絞り込んで、
--   その人たちの
--     - name
--     - address
--   を表示する。
--
-- 学習上の主題:
--   WHERE 句で「等しくない」を表す比較演算子
--   <> を使う方法を理解すること。
--
-- 前回までの
--   address = '千葉県'
--   age >= 30
-- とは異なり、
-- 今回は「東京都ではない」という否定条件で絞り込んでいる。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は name 列と address 列だけを取り出す。
    --   したがって、phone_nbr, sex, age は表示されない。
    name,
    address
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを検索対象にしている。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込む条件を書く部分である。
    --
    --   WHERE 句が無い場合は Address テーブルの全行が対象になる。
    --   今回は WHERE 句があるため、
    --   条件に一致する行だけが結果として残る。
    --
    --   address <> '東京都'
    --   は、
    --   「address 列の値が '東京都' と等しくない行だけを選ぶ」
    --   という意味である。
    --
    --   <> は「等しくない」を表す比較演算子である。
    --   SQL では not equal の意味でよく使われる。
    --
    --   したがって、
    --     address が '福島県', '千葉県', '三重県', '和歌山県'
    --   などであれば条件に一致する。
    --
    --   一方で、
    --     address が '東京都'
    --   である行は条件に一致しないため、結果から除外される。
    --
    --   '東京都' は文字列リテラルなので、
    --   シングルクォートで囲んで書く。
    address <> '東京都';


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について address 列の値を確認する
-- 3. address が '東京都' ではない行だけを残す
-- 4. 残った行から name 列と address 列だけを取り出す
-- 5. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address != '東京都':
--           output row.name, row.address
--
-- つまりこれは、
--   「全行を見ながら、東京都ではない行だけを通す」
-- という否定条件付きの検索である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, address
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- WHERE address <> '東京都'
--   → address 列が '東京都' ではない行だけを結果に残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 比較演算子 <> の意味
-- ================================================
--
-- <> は「等しくない」を表す比較演算子である。
--
-- 具体例:
--   '千葉県' <> '東京都'   → 真
--   '福島県' <> '東京都'   → 真
--   '東京都' <> '東京都'   → 偽
--
-- したがって今回の条件は、
--   「東京都を除外する」
-- という意味になる。
--
-- なお、DBMS によっては != でも
-- 「等しくない」を表せることがあるが、
-- SQL の標準的な書き方としては <> を覚えておくとよい。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | address
--   --------+----------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県
--
-- このとき、
--   WHERE address <> '東京都'
-- に一致するのは
--   - 林
--   - 井上
--   - 佐々木
--   - 松本
--   - 佐藤
--   - 鈴木
-- である。
--
-- したがって結果は次のようになる。
--
--   name    | address
--   --------+----------
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県


-- ================================================
-- 補足1:
--   今回は「東京都ではない」という条件なので、
--   東京都以外のすべての住所が結果に含まれる。
--
--   つまり特定の1県だけを取り出すのではなく、
--   ある値を除外する検索になっている。
-- ================================================


-- ================================================
-- 補足2:
--   = と <> の違い
--
--   address = '東京都'
--     → 東京都だけを選ぶ
--
--   address <> '東京都'
--     → 東京都以外を選ぶ
--
--   このように、
--   = は一致条件、
--   <> は不一致条件である。
-- ================================================


-- ================================================
-- 補足3:
--   WHERE 句が無い場合との違い
--
--   次のSQL:
--
--     SELECT name, address
--     FROM Address;
--
--   は全行を返す。
--
--   一方、今回のSQL:
--
--     SELECT name, address
--     FROM Address
--     WHERE address <> '東京都';
--
--   は東京都の行を除外して返す。
--
--   つまり WHERE 句は、
--   「どの行を結果から除外するか」
--   も指定できるフィルタである。
-- ================================================


-- ================================================
-- 補足4:
--   ORDER BY が無いため、結果の並び順は保証されない。
--
--   名前順で並べたい場合は、たとえば次のように書く。
--
--     SELECT name, address
--     FROM Address
--     WHERE address <> '東京都'
--     ORDER BY name;
--
--   今回の主題は「等しくない条件で絞り込むこと」なので、
--   並び替えまでは行っていない。
-- ================================================