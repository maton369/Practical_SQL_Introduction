-- 補足2:
--   PARTITION BY を加えると、
--   グループごとのランキングもできる。
--
--   例:
--
--     SELECT name,
--            sex,
--            age,
--            RANK() OVER(PARTITION BY sex ORDER BY age DESC) AS rnk
--       FROM Address;
--
--   この場合は、男女それぞれの中で年齢順位を付ける。
-- ================================================


