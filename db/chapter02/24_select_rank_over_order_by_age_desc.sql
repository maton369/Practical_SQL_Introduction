-- ================================================
-- ファイル名: 24_select_rank_over_order_by_age_desc.sql
-- 役割:
--   Address テーブルの各行について、
--   age 列を基準に順位を付けて表示する。
--
-- このSQLの目的:
--   ただ年齢順に並べるだけでなく、
--   「年齢が高い人は何位か」
--   を各行に対して明示的に付けて表示すること。
--
-- 学習上の主題:
--   ウィンドウ関数 RANK() OVER(...) を使って、
--   ランキングを計算する方法を理解すること。
--
-- 今回の最重要ポイント:
--   ORDER BY age DESC だけなら
--   「並び順を変える」だけである。
--
--   一方、
--   RANK() OVER(ORDER BY age DESC)
--   は
--   「その並び順に基づいた順位そのものを各行に計算して付ける」
--   処理である。
--
--   つまり今回は、
--   ソートそのものではなく、
--   ソート順を基準にした順位付けが主題である。
-- ================================================

SELECT
    -- name 列:
    --   元の Address テーブルに入っている名前をそのまま表示する。
    --
    --   ランキング結果が「誰の順位か」を分かるようにするために必要である。
    name,

    -- age 列:
    --   ランキングの基準となる年齢をそのまま表示する。
    --
    --   今回の順位は age の大きさで決まるため、
    --   順位と一緒に age を表示すると結果の意味が分かりやすい。
    age,

    RANK() OVER
    (
        ORDER BY age DESC
    ) AS rnk
    -- RANK() OVER(...) :
    --   これがウィンドウ関数によるランキング計算である。
    --
    -- RANK() とは:
    --   並び順に基づいて順位を返す関数である。
    --
    -- OVER(...) とは:
    --   この関数をウィンドウ関数として使うための指定である。
    --
    -- ORDER BY age DESC とは:
    --   age の大きい順に並べたときの順番を基準に順位を決める、
    --   という意味である。
    --
    -- DESC を使っているため、
    --   年齢が高い人ほど上位になる。
    --
    -- AS rnk:
    --   計算した順位列に rnk という名前を付ける。
    --
    -- したがってこの列には、
    --   1位, 2位, 3位, ...
    -- に相当する数値が入る。
FROM
    -- FROM 句:
    --   元データとして Address テーブルを参照する。
    Address;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を読む
-- 2. age 列の値を基準に、年齢の高い順で並ぶ順序を考える
-- 3. その順序に従って各行へ順位を付ける
-- 4. name, age, 順位(rnk) を各行ごとに返す
--
-- 概念的には次のような処理に近い。
--
--   rows = all rows from Address
--
--   sort rows by age descending
--
--   assign rank to each row based on sorted order
--
--   output row.name, row.age, row.rank
--
-- つまりこれは、
--   「年齢でソートした順番を使って順位列を計算する」
--   という処理である。
--
-- ここで重要なのは、
--   ORDER BY age DESC 単独なら表示順を変えるだけ
-- だが、
--   RANK() OVER(ORDER BY age DESC)
--   では順位そのものが列として計算される
-- 点である。


-- ================================================
-- RANK() とは何か
-- ================================================
--
-- RANK() は、順位を返すウィンドウ関数である。
--
-- 今回のように
--
--   RANK() OVER(ORDER BY age DESC)
--
-- と書くと、
--   age が高い順に並べたときの順位
-- を各行に返す。
--
-- たとえば age が
--
--   55, 45, 32, 32, 30, 25, 21, 20, 19
--
-- なら、
--
--   55 → 1位
--   45 → 2位
--   32 → 3位
--   32 → 3位
--   30 → 5位
--   25 → 6位
--   21 → 7位
--   20 → 8位
--   19 → 9位
--
-- のような順位になる。
--
-- ここで重要なのは、
-- 同順位があると、その次の順位が飛ぶことである。
--
-- たとえば 32歳が2人いると、
-- 2人とも 3位 になり、
-- 次は 4位 ではなく 5位 になる。
--
-- これが RANK() の特徴である。


-- ================================================
-- OVER(ORDER BY age DESC) の意味
-- ================================================
--
-- OVER(...) は、
-- ウィンドウ関数が
-- どの順序・どの区切りで計算されるか
-- を指定する。
--
-- 今回は PARTITION BY が無いので、
-- Address テーブル全体を1つの集合として順位付けしている。
--
-- ORDER BY age DESC によって、
--   年齢が高い順
-- が順位の基準になる。
--
-- つまり今回は、
--   テーブル全体を対象にした年齢ランキング
-- を作っている。


-- ================================================
-- ORDER BY 句との違い
-- ================================================
--
-- 次のSQLは以前学んだ単純な並び替えである。
--
--   SELECT name, age
--     FROM Address
--    ORDER BY age DESC;
--
-- これは
--   「年齢が高い順に表示する」
-- だけであり、
-- 順位列は作られない。
--
-- 一方、今回のSQLは
--
--   RANK() OVER(ORDER BY age DESC)
--
-- を使っているため、
--   「年齢が高い順を基準に順位を計算して返す」
-- 処理になっている。
--
-- つまり、
--
--   ORDER BY
--     → 並び順を決める
--
--   RANK() OVER(ORDER BY ...)
--     → その並び順を使って順位列を作る
--
-- という違いがある。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | age
--   --------+----
--   小川    | 30
--   前田    | 21
--   森      | 45
--   林      | 32
--   井上    | 55
--   佐々木  | 19
--   松本    | 20
--   佐藤    | 25
--   鈴木    | 32
--
-- age の高い順に並べると、
--
--   井上    55
--   森      45
--   林      32
--   鈴木    32
--   小川    30
--   佐藤    25
--   前田    21
--   松本    20
--   佐々木  19
--
-- となる。
--
-- ここに RANK() を適用すると、
--
--   name    | age | rnk
--   --------+-----+----
--   井上    | 55  | 1
--   森      | 45  | 2
--   林      | 32  | 3
--   鈴木    | 32  | 3
--   小川    | 30  | 5
--   佐藤    | 25  | 6
--   前田    | 21  | 7
--   松本    | 20  | 8
--   佐々木  | 19  | 9
--
-- のようになる。
--
-- ここで
--   林
--   鈴木
-- は同じ 32歳なので、
-- 同順位の 3位 になる。
--
-- そして次の 小川 は 4位 ではなく 5位 になる。
-- これが RANK() の特徴である。


-- ================================================
-- 図解: ランキングのイメージ
-- ================================================
--
--   Address の各行
--        |
--        v
--   +------------------------------+
--   | age の大きい順を考える       |
--   | ORDER BY age DESC            |
--   +------------------------------+
--        |
--        v
--   55, 45, 32, 32, 30, 25, 21, 20, 19
--        |
--        v
--   +------------------------------+
--   | 順位を付ける                 |
--   | RANK()                       |
--   +------------------------------+
--        |
--        v
--   1, 2, 3, 3, 5, 6, 7, 8, 9
--
--
-- 同順位がある場合のイメージ:
--
--   55  → 1位
--   45  → 2位
--   32  → 3位
--   32  → 3位
--   30  → 5位
--
-- つまり、
--   同じ値には同じ順位を付ける
--   そのぶん次の順位は飛ぶ
-- という仕組みになっている。


-- ================================================
-- RANK() と GROUP BY の違い
-- ================================================
--
-- 今回の RANK() は、
-- 各行に順位を付けるが、
-- 元の行は消えない。
--
-- つまり結果の粒度は
--   1行1人
-- のままである。
--
-- 一方、GROUP BY は行をまとめてしまう。
--
-- したがって RANK() は、
--   明細を残したまま比較・順位付けしたい
-- ときに向いている。


-- ================================================
-- RANK(), DENSE_RANK(), ROW_NUMBER() の違い
-- ================================================
--
-- 今回は RANK() を使っている。
--
-- 似た関数として次がある。
--
-- 1. RANK()
--    同順位を許す。
--    同順位の次の順位は飛ぶ。
--
--    例:
--      1, 2, 3, 3, 5
--
-- 2. DENSE_RANK()
--    同順位を許す。
--    ただし次の順位は飛ばない。
--
--    例:
--      1, 2, 3, 3, 4
--
-- 3. ROW_NUMBER()
--    同じ値でも必ず別番号を振る。
--
--    例:
--      1, 2, 3, 4, 5
--
-- 今回は「同点は同順位にしたい」
-- かつ
-- 「競技順位のように次順位を飛ばしたい」
-- 場合に対応する RANK() を使っている。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, age,
--        RANK() OVER(ORDER BY age DESC) AS rnk
--   → name, age と、年齢順位 rnk を表示する
--
-- RANK()
--   → 順位を計算するウィンドウ関数
--
-- OVER(...)
--   → どの順序を基準に順位を付けるかを指定する
--
-- ORDER BY age DESC
--   → 年齢が高い順に順位を付ける
--
-- AS rnk
--   → 計算した順位列に rnk という名前を付ける
--
-- FROM Address
--   → Address テーブルを元データとする
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   今回の SELECT 文には、文全体の ORDER BY は無い。
--
--   そのため、
--   rnk の計算基準は age DESC だが、
--   表示順自体は必ずしも rnk 順になるとは限らない。
--
--   見やすくしたい場合は、たとえば次のように書ける。
--
--     SELECT name,
--            age,
--            RANK() OVER(ORDER BY age DESC) AS rnk
--       FROM Address
--      ORDER BY rnk, name;
-- ================================================


