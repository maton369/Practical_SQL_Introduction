-- ================================================
-- ファイル名: 10_select_address_where_phone_is_null.sql
-- 役割:
--   Address テーブルの中から、
--   phone_nbr 列が NULL である行だけを検索する。
--
-- このSQLの目的:
--   電話番号が登録されている人ではなく、
--   「電話番号が未登録である人」だけを取り出して、
--   その人たちの
--     - name
--     - phone_nbr
--   を表示する。
--
-- 学習上の主題:
--   NULL を判定するときは
--     = NULL
--   とは書かず、
--     IS NULL
--   と書く必要があることを理解すること。
--
-- 今回のSQLは、
--   「意図したデータを選択できる SELECT 文」
-- という題材として、
-- NULL を正しく扱うための基本構文を示している。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は
    --     - name
    --     - phone_nbr
    --   の2列を取り出す。
    --
    --   したがって、
    --   address, sex, age は表示されない。
    --
    --   「誰の電話番号が未登録なのか」を確認したいので、
    --   name と phone_nbr を並べている。
    name,
    phone_nbr
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを検索対象にしている。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込む条件を書く部分である。
    --
    --   今回の条件は
    --     phone_nbr IS NULL
    --   である。
    --
    --   これは
    --   「phone_nbr 列の値が NULL である行だけを選ぶ」
    --   という意味である。
    --
    --   NULL は「空文字」ではなく、
    --   「値が存在しない」「値が未設定」「値が不明」
    --   といった状態を表す特別な値である。
    --
    --   そのため、通常の = や <> で比較してはいけない。
    --   SQL では NULL を判定するとき、
    --
    --     IS NULL
    --     IS NOT NULL
    --
    --   を使う。
    --
    --   したがって今回の条件は、
    --   「電話番号が入っていない人だけを取り出す」
    --   条件になっている。
    phone_nbr IS NULL;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について phone_nbr 列の値を確認する
-- 3. phone_nbr が NULL である行だけを残す
-- 4. 残った行から name 列と phone_nbr 列だけを取り出す
-- 5. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.phone_nbr is NULL:
--           output row.name, row.phone_nbr
--
-- つまりこれは、
--   「全行を見ながら、電話番号が未登録の行だけを通す」
-- という NULL 条件付き検索である。


-- ================================================
-- NULL とは何か
-- ================================================
--
-- NULL は、単なる空文字列 '' とは異なる。
--
-- たとえば次の3つは意味が違う。
--
--   1. '090-1234-XXXX'
--      → 電話番号が登録されている
--
--   2. ''
--      → 空文字列
--         「長さ0の文字列」が入っている状態
--
--   3. NULL
--      → 値が入っていない状態
--         未登録・不明・未設定などを表す
--
-- 今回は 3 の NULL を判定したいので、
-- = や <> ではなく IS NULL を使っている。


-- ================================================
-- なぜ = NULL ではいけないのか
-- ================================================
--
-- 初学者がよく書いてしまう誤りとして、
--
--   WHERE phone_nbr = NULL
--
-- という書き方がある。
--
-- しかし SQL では、NULL は通常の値ではないため、
-- = NULL では正しく判定できない。
--
-- そのため、NULL を調べたいときは必ず
--
--   IS NULL
--
-- を使う必要がある。
--
-- 逆に、NULL ではない行を取り出したいなら
--
--   IS NOT NULL
--
-- を使う。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | phone_nbr
--   --------+-----------------
--   小川    | 080-3333-XXXX
--   前田    | 090-0000-XXXX
--   森      | 090-2984-XXXX
--   林      | 080-3333-XXXX
--   井上    | NULL
--   佐々木  | 080-5848-XXXX
--   松本    | NULL
--   佐藤    | 090-1922-XXXX
--   鈴木    | 090-0001-XXXX
--
-- このとき、
--   WHERE phone_nbr IS NULL
-- に一致するのは
--   - 井上
--   - 松本
-- である。
--
-- したがって結果は次のようになる。
--
--   name    | phone_nbr
--   --------+-----------
--   井上    | NULL
--   松本    | NULL


-- ================================================
-- 図解: IS NULL のイメージ
-- ================================================
--
--   Address の各行を順に見る
--        |
--        v
--   +-----------------------------+
--   | phone_nbr が NULL か？      |
--   +-----------------------------+
--        |
--   +----+------------------+
--   |                       |
--   v                       v
--   はい                    いいえ
--   |                       |
--   v                       v
-- 結果に残す              結果から除外する
--
--
-- 具体例:
--
--   '080-3333-XXXX' → NULL ではない → 除外
--   '090-0000-XXXX' → NULL ではない → 除外
--   NULL            → NULL である   → 残す


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, phone_nbr
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- WHERE phone_nbr IS NULL
--   → phone_nbr 列が NULL の行だけを残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   IS NULL は「NULL である」行を選ぶ構文である。
--
--   逆に、電話番号が登録されている人だけを取り出したいなら、
--   次のように書く。
--
--     SELECT name, phone_nbr
--     FROM Address
--     WHERE phone_nbr IS NOT NULL;
-- ================================================


-- ================================================
-- 補足2:
--   今回は phone_nbr 列に NOT NULL 制約が付いていないため、
--   NULL が入りうる。
--
--   そのため、
--   IS NULL による検索が意味を持つ。
--
--   もし列定義が NOT NULL であれば、
--   その列に NULL は入れられないので、
--   IS NULL の結果は常に空になるはずである。
-- ================================================


-- ================================================
-- 補足3:
--   ORDER BY が無いため、結果の並び順は保証されない。
--
--   名前順で見たい場合は、たとえば次のように書く。
--
--     SELECT name, phone_nbr
--     FROM Address
--     WHERE phone_nbr IS NULL
--     ORDER BY name;
--
--   今回の主題は「NULL を正しく判定すること」なので、
--   並び替えまでは行っていない。
-- ================================================