-- ================================================
-- ファイル名: 08_select_address_where_tokyo_or_age_ge_30.sql
-- 役割:
--   Address テーブルの中から、
--   「住所が東京都である人」または
--   「年齢が30歳以上である人」
--   のどちらか一方でも満たす人を検索する。
--
-- このSQLの目的:
--   Address テーブル全体をそのまま表示するのではなく、
--   複数の条件のうち少なくとも1つを満たす人だけに対象を絞り込んで、
--   その人たちの
--     - name
--     - address
--     - age
--   を表示する。
--
-- 学習上の主題:
--   OR を使って、複数条件のうち
--   「どちらか一方でも満たす行」を選ぶ方法を理解すること。
--
-- 今回の条件は2つある。
--
--   1. address = '東京都'
--   2. age >= 30
--
-- OR は、
--   「左の条件が真」または「右の条件が真」
-- のどちらかであれば、全体を真にする論理演算子である。
--
-- そのため今回のSQLは、
--   東京都に住んでいる人
--   または
--   30歳以上の人
-- を結果に残す。
--
-- AND との違い:
--   AND は共通部分を選ぶが、
--   OR は和集合を選ぶ。
--   つまり OR の方が、一般には結果件数が多くなりやすい。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は
    --     - name
    --     - address
    --     - age
    --   の3列を取り出す。
    --
    --   したがって、
    --   phone_nbr や sex は表示されない。
    name,
    address,
    age
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを検索対象にしている。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込む条件を書く部分である。
    --
    --   今回は条件が2つあり、
    --   それらを OR で結んでいる。
    --   OR を使うことで、
    --   「少なくともどちらか一方を満たす行」を結果に残す。

    -- 第1条件:
    --   address = '東京都'
    --
    --   これは
    --   「address 列の値が '東京都' と等しい」
    --   という意味である。
    --
    --   = は等価比較演算子であり、
    --   文字列 '東京都' と完全一致する行がこの条件を満たす。
    --
    --   '東京都' は文字列なのでシングルクォートで囲む。
    address = '東京都'

    OR

    -- OR:
    --   論理和を表す演算子である。
    --
    --   今回は
    --     address = '東京都'
    --   または
    --     age >= 30
    --   のどちらか一方、あるいは両方を満たす行が採用される。
    --
    --   AND と違って、
    --   両方を満たす必要はない。
    --   片方だけ真でも結果に残る。

    -- 第2条件:
    --   age >= 30
    --
    --   これは
    --   「age 列の値が 30 以上である」
    --   という意味である。
    --
    --   >= は「以上」を表す比較演算子であり、
    --   30, 31, 32, ... は条件を満たす。
    --
    --   30 は数値なので、文字列のようにクォートしない。
    age >= 30;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について address 列が '東京都' かどうかを確認する
-- 3. 同じ行について age 列が 30 以上かどうかを確認する
-- 4. 2つの条件のうち、どちらか一方でも満たす行を残す
-- 5. 残った行から name, address, age の3列だけを取り出す
-- 6. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address == '東京都' or row.age >= 30:
--           output row.name, row.address, row.age
--
-- つまりこれは、
--   「全行を見ながら、複数条件の和集合に属する行を通す」
-- という検索である。


-- ================================================
-- OR の意味
-- ================================================
--
-- OR は論理和を表す演算子である。
-- 集合の考え方で言うと、
-- 「条件Aを満たす集合」と「条件Bを満たす集合」の
-- 和集合を選ぶ働きをする。
--
-- 今回の条件を集合で考えると、
--
--   条件A: address = '東京都'
--          → 東京都に住んでいる人の集合
--
--   条件B: age >= 30
--          → 30歳以上の人の集合
--
-- OR を使うことで、
--
--   条件A ∪ 条件B
--
-- つまり
--   「東京都に住んでいる人」
--   または
--   「30歳以上の人」
-- のどちらかに入る人を選ぶことになる。
--
-- このとき、
--   - 東京都で30歳以上の人
--   - 東京都だが30歳未満の人
--   - 東京都ではないが30歳以上の人
-- は、すべて結果に含まれる。


-- ================================================
-- 図解: 集合としての OR
-- ================================================
--
--   条件A: 東京都に住んでいる人
--   +-------------------------------+
--   | 小川, 前田, 森                |
--   +-------------------------------+
--
--   条件B: 30歳以上の人
--   +-------------------------------+
--   | 小川, 森, 林, 井上, 鈴木      |
--   +-------------------------------+
--
--   OR が選ぶもの
--   +-------------------------------+
--   | 条件A または 条件B            |
--   | 小川, 前田, 森, 林, 井上, 鈴木|
--   +-------------------------------+
--
-- AND では共通部分だけだったが、
-- OR では両集合を合わせた範囲全体が対象になる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | address    | age
--   --------+------------+----
--   小川    | 東京都     | 30
--   前田    | 東京都     | 21
--   森      | 東京都     | 45
--   林      | 福島県     | 32
--   井上    | 福島県     | 55
--   佐々木  | 千葉県     | 19
--   松本    | 千葉県     | 20
--   佐藤    | 三重県     | 25
--   鈴木    | 和歌山県   | 32
--
-- まず
--   address = '東京都'
-- を満たすのは
--   - 小川
--   - 前田
--   - 森
--
-- 次に
--   age >= 30
-- を満たすのは
--   - 小川
--   - 森
--   - 林
--   - 井上
--   - 鈴木
--
-- この2つの条件のどちらか一方でも満たすのは
--   - 小川
--   - 前田
--   - 森
--   - 林
--   - 井上
--   - 鈴木
-- である。
--
-- 一方で
--   - 佐々木 (千葉県, 19)
--   - 松本   (千葉県, 20)
--   - 佐藤   (三重県, 25)
-- は
--   東京都でもなく、30歳以上でもない
-- ため結果に含まれない。
--
-- したがって結果は次のようになる。
--
--   name    | address    | age
--   --------+------------+----
--   小川    | 東京都     | 30
--   前田    | 東京都     | 21
--   森      | 東京都     | 45
--   林      | 福島県     | 32
--   井上    | 福島県     | 55
--   鈴木    | 和歌山県   | 32


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, address, age
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- WHERE address = '東京都'
--   → 東京都の行を条件Aとして選ぶ
--
-- OR age >= 30
--   → さらに 30歳以上の行を条件Bとして選ぶ
--
--   このとき OR なので、
--   条件A または 条件B のどちらかを満たせば結果に残る
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   OR は「どちらか一方でよい」条件である。
--
--   したがって、
--   東京都で年齢が21の前田さんは、
--   age >= 30 は満たさないが
--   address = '東京都' を満たすので結果に残る。
--
--   また、
--   和歌山県で32歳の鈴木さんは、
--   address = '東京都' は満たさないが
--   age >= 30 を満たすので結果に残る。
-- ================================================


-- ================================================
-- 補足2:
--   AND との違い
--
--   今回は OR なので、
--   「東京都である、または30歳以上である」
--   行を選ぶ。
--
--   もし AND を使うと、
--   「東京都であり、かつ30歳以上である」
--   行だけを選ぶことになる。
--
--   つまり、
--   OR は条件の和集合、
--   AND は条件の共通部分
--   と考えると理解しやすい。
-- ================================================


