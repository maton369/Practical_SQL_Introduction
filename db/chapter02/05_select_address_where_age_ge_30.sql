-- ================================================
-- ファイル名: 05_select_address_where_age_ge_30.sql
-- 役割:
--   Address テーブルの中から、
--   age 列の値が 30 以上である行だけを検索する。
--
-- このSQLの目的:
--   住所録全体をそのまま表示するのではなく、
--   「年齢が30歳以上の人だけ」を対象に絞り込んで、
--   その人たちの
--     - name
--     - age
--   を表示する。
--
-- 学習上の主題:
--   WHERE 句で数値条件を使って検索する方法を理解すること。
--
-- 前回の address = '千葉県' は文字列比較だったが、
-- 今回は age >= 30 という数値比較である点が重要である。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は name 列と age 列だけを取り出す。
    --   したがって、phone_nbr, address, sex は表示されない。
    name,
    age
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを検索対象にしている。
    Address
WHERE
    -- WHERE 句:
    --   行を絞り込む条件を書く部分である。
    --
    --   WHERE 句が無い場合は Address テーブルの全行が対象になる。
    --   しかし今回は WHERE 句があるため、
    --   条件を満たした行だけが結果として残る。
    --
    --   age >= 30
    --   は、
    --   「age 列の値が 30 以上である行だけを選ぶ」
    --   という意味である。
    --
    --   >= は比較演算子であり、
    --   「左辺が右辺以上かどうか」を判定する。
    --
    --   つまり、
    --     age が 30, 31, 32, ...
    --   のような値なら条件に一致する。
    --
    --   一方で、
    --     age が 29, 21, 19
    --   のような値なら条件に一致しないため、結果から除外される。
    --
    --   なお、30 は数値なのでシングルクォートでは囲まない。
    --   ここが文字列比較との大きな違いである。
    age >= 30;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを検索対象として読む
-- 2. 各行について age 列の値を確認する
-- 3. age が 30 以上である行だけを残す
-- 4. 残った行から name 列と age 列だけを取り出す
-- 5. その結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.age >= 30:
--           output row.name, row.age
--
-- つまりこれは、
--   「全行を見ながら、年齢条件を満たす行だけを通す」
-- という条件付き検索である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, age
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- WHERE age >= 30
--   → age 列が 30 以上の行だけを結果に残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 比較演算子 >= の意味
-- ================================================
--
-- >= は「以上」を表す比較演算子である。
--
-- 具体例:
--   30 >= 30  → 真
--   32 >= 30  → 真
--   45 >= 30  → 真
--   29 >= 30  → 偽
--   21 >= 30  → 偽
--
-- したがって今回の条件は、
--   「30歳ちょうど」も含む
-- という点に注意する。
--
-- もし「30歳より大きい」を意味したいなら、
--   age > 30
-- と書く必要がある。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | age
--   --------+----
--   小川    | 30
--   前田    | 21
--   森      | 45
--   林      | 32
--   井上    | 55
--   佐々木  | 19
--   松本    | 20
--   佐藤    | 25
--   鈴木    | 32
--
-- このとき、
--   WHERE age >= 30
-- に一致するのは
--   - 小川   (30)
--   - 森     (45)
--   - 林     (32)
--   - 井上   (55)
--   - 鈴木   (32)
-- である。
--
-- したがって結果は次のようになる。
--
--   name    | age
--   --------+----
--   小川    | 30
--   森      | 45
--   林      | 32
--   井上    | 55
--   鈴木    | 32


-- ================================================
-- 補足1:
--   数値比較では、比較値を通常クォートしない。
--
--   正しい例:
--     age >= 30
--
--   文字列比較の例:
--     address = '千葉県'
--
--   この違いを意識することが重要である。
-- ================================================


-- ================================================
-- 補足2:
--   WHERE 句が無い場合との違い
--
--   次のSQL:
--
--     SELECT name, age
--     FROM Address;
--
--   は全員分の name と age を返す。
--
--   一方、今回のSQL:
--
--     SELECT name, age
--     FROM Address
--     WHERE age >= 30;
--
--   は 30歳以上の人だけを返す。
--
--   つまり WHERE 句は、
--   「どの行を結果に含めるか」を決めるフィルタである。
-- ================================================


-- ================================================
-- 補足3:
--   ORDER BY が無いため、結果の並び順は保証されない。
--
--   年齢順や名前順に並べたい場合は、たとえば次のように書く。
--
--     SELECT name, age
--     FROM Address
--     WHERE age >= 30
--     ORDER BY age;
--
--   今回のSQLは「条件で絞ること」が主題なので、
--   並び替えまでは行っていない。
-- ================================================