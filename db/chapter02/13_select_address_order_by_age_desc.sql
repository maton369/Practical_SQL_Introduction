-- ================================================
-- ファイル名: 13_select_address_order_by_age_desc.sql
-- 役割:
--   Address テーブルのレコードを、
--   age 列の値が大きい順に並べて表示する。
--
-- このSQLの目的:
--   Address テーブルに入っているデータをそのまま表示するだけでなく、
--   年齢が高い人から順に並べ替えて確認できるようにすること。
--
-- 学習上の主題:
--   ORDER BY を使って結果を並べ替える方法、
--   そして DESC を使って降順にする方法を理解すること。
--
-- 今回の重要点:
--   これまでの WHERE は
--   「どの行を残すか」を決める構文だった。
--
--   それに対して ORDER BY は
--   「残った結果をどの順番で表示するか」を決める構文である。
--
--   つまり ORDER BY は、
--   行の選別ではなく、出力順の制御を担当する。
-- ================================================

SELECT
    -- SELECT 句:
    --   結果として表示したい列を指定する部分である。
    --
    --   今回は Address テーブルの主要な列をすべて明示している。
    --   具体的には、
    --     - name
    --     - phone_nbr
    --     - address
    --     - sex
    --     - age
    --   の5列を取り出す。
    --
    --   SELECT * と書くこともできるが、
    --   ここではどの列を表示したいのかが明確になるように、
    --   列名を1つずつ書いている。
    name,
    phone_nbr,
    address,
    sex,
    age
FROM
    -- FROM 句:
    --   どのテーブルからデータを取り出すかを指定する部分である。
    --
    --   今回は Address テーブルを対象にしている。
    Address
ORDER BY
    -- ORDER BY 句:
    --   SELECT の結果を、指定した列の値に基づいて並べ替えるための構文である。
    --
    --   今回は age 列を基準に並べ替えるので、
    --   「年齢順に並べる」ことになる。
    age

    DESC;
    -- DESC:
    --   descending の略であり、「降順」を意味する。
    --
    --   降順とは、大きい値から小さい値へ並べる順序である。
    --   今回 age は年齢なので、
    --   年齢が高い人から順に表示される。
    --
    --   たとえば age が
    --     55, 45, 32, 30, 25, 21, 20, 19
    --   のように大きい順で並ぶ。
    --
    --   逆に、小さい順に並べたいなら ASC を使う。
    --   ただし ASC は省略可能であり、
    --   ORDER BY age;
    --   と書いても通常は昇順になる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルから全行を読み込む
-- 2. 各行について、SELECT で指定した列を取り出す
-- 3. 取り出した結果全体を、age 列の値を基準に並べ替える
-- 4. 年齢が高い順に結果を表示する
--
-- 概念的には次のような処理に近い。
--
--   rows = all rows from Address
--   sort rows by row.age in descending order
--   output rows
--
-- つまり今回は、
--   「条件で絞り込む」のではなく、
--   「取得した結果を年齢順に整列してから表示する」
-- という処理である。


-- ================================================
-- ORDER BY の意味
-- ================================================
--
-- ORDER BY は、
--   SELECT した結果を並べ替えるための構文である。
--
-- 一般形は次のようになる。
--
--   ORDER BY 列名 ASC
--   ORDER BY 列名 DESC
--
-- ASC:
--   ascending の略で、昇順を意味する。
--   小さい値から大きい値へ並べる。
--
-- DESC:
--   descending の略で、降順を意味する。
--   大きい値から小さい値へ並べる。
--
-- 今回は
--
--   ORDER BY age DESC
--
-- なので、
--   age の大きい行ほど先に表示される。


-- ================================================
-- DESC の意味
-- ================================================
--
-- DESC は「大きい順」を表す。
--
-- age に対して使うと、
--   高齢の人から先に並ぶ。
--
-- 例:
--
--   age が
--     19, 20, 21, 25, 30, 32, 45, 55
--   という値を持っていたとき、
--
--   ORDER BY age DESC
--
--   の結果は
--
--     55, 45, 32, 30, 25, 21, 20, 19
--
--   の順になる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | phone_nbr       | address    | sex | age
--   --------+-----------------+------------+-----+----
--   小川    | 080-3333-XXXX   | 東京都     | 男  | 30
--   前田    | 090-0000-XXXX   | 東京都     | 女  | 21
--   森      | 090-2984-XXXX   | 東京都     | 男  | 45
--   林      | 080-3333-XXXX   | 福島県     | 男  | 32
--   井上    | NULL            | 福島県     | 女  | 55
--   佐々木  | 080-5848-XXXX   | 千葉県     | 女  | 19
--   松本    | NULL            | 千葉県     | 女  | 20
--   佐藤    | 090-1922-XXXX   | 三重県     | 女  | 25
--   鈴木    | 090-0001-XXXX   | 和歌山県   | 男  | 32
--
-- これに対して
--
--   ORDER BY age DESC
--
-- を適用すると、
-- 年齢の高い順に並び替えられるため、結果は概ね次のようになる。
--
--   name    | phone_nbr       | address    | sex | age
--   --------+-----------------+------------+-----+----
--   井上    | NULL            | 福島県     | 女  | 55
--   森      | 090-2984-XXXX   | 東京都     | 男  | 45
--   林      | 080-3333-XXXX   | 福島県     | 男  | 32
--   鈴木    | 090-0001-XXXX   | 和歌山県   | 男  | 32
--   小川    | 080-3333-XXXX   | 東京都     | 男  | 30
--   佐藤    | 090-1922-XXXX   | 三重県     | 女  | 25
--   前田    | 090-0000-XXXX   | 東京都     | 女  | 21
--   松本    | NULL            | 千葉県     | 女  | 20
--   佐々木  | 080-5848-XXXX   | 千葉県     | 女  | 19
--
-- ただし、同じ age を持つ行どうし
-- （今回なら 32歳の 林 と 鈴木）は、
-- age だけでは順序が一意に決まらないため、
-- 並び順が固定されない場合がある。


-- ================================================
-- 図解: ORDER BY のイメージ
-- ================================================
--
--   Address テーブルの全行
--        |
--        v
--   +-------------------------------+
--   | age 列の値を取り出して比較する|
--   +-------------------------------+
--        |
--        v
--   +-------------------------------+
--   | 大きい age を先に並べる       |
--   | (DESC = 降順)                 |
--   +-------------------------------+
--        |
--        v
--   年齢が高い順の結果
--
--
-- 具体例:
--
--   並べ替え前:
--     小川 30
--     前田 21
--     森   45
--     林   32
--     井上 55
--
--   並べ替え後:
--     井上 55
--     森   45
--     林   32
--     小川 30
--     前田 21


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, phone_nbr, address, sex, age
--   → 表示したい列を指定する
--
-- FROM Address
--   → Address テーブルを検索対象にする
--
-- ORDER BY age DESC
--   → age 列の値が大きい順に結果を並べる
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   ORDER BY は WHERE と違い、
--   行を減らすのではなく結果の順番を変える。
--
--   つまり、
--   返される行数は変わらず、
--   表示順だけが変わる。
-- ================================================


-- ================================================
-- 補足2:
--   昇順にしたい場合は ASC を使う。
--
--   例:
--
--     SELECT name, phone_nbr, address, sex, age
--     FROM Address
--     ORDER BY age ASC;
--
--   あるいは ASC は省略できるので、
--
--     ORDER BY age;
--
--   と書いても通常は昇順になる。
-- ================================================


-- ================================================
-- 補足3:
--   同じ age の人が複数いる場合、
--   age だけでは順序が一意に決まらない。
--
--   その場合、さらに別の列を指定して順序を安定させられる。
--
--   例:
--
--     SELECT name, phone_nbr, address, sex, age
--     FROM Address
--     ORDER BY age DESC, name ASC;
--
--   これなら、
--   まず age の高い順に並べ、
--   同じ年齢の中では name の昇順に並べる。
-- ================================================