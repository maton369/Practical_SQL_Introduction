-- 実行後は概ね次のようになる。
--
--   name    | phone_nbr       | address    | sex | age
--   --------+-----------------+------------+-----+----
--   小川    | 080-3333-XXXX   | 東京都     | 男  | 30
--   前田    | 090-0000-XXXX   | 東京都     | 女  | 21
--   森      | 090-2984-XXXX   | 東京都     | 男  | 45
--   林      | 080-3333-XXXX   | 福島県     | 男  | 32
--   井上    | NULL            | 福島県     | 女  | 55
--   佐藤    | 090-1922-XXXX   | 三重県     | 女  | 25
--   鈴木    | 090-0001-XXXX   | 和歌山県   | 男  | 32
--
-- つまり
--   佐々木
--   松本
-- の2行だけが消える。


-- ================================================
-- 図解: 条件付き DELETE のイメージ
-- ================================================
--
--   Address テーブル
--   +----------------------------------+
--   | 小川  -> 東京都                  |
--   | 前田  -> 東京都                  |
--   | 森    -> 東京都                  |
--   | 林    -> 福島県                  |
--   | 井上  -> 福島県                  |
--   | 佐々木-> 千葉県                  |
--   | 松本  -> 千葉県                  |
--   | 佐藤  -> 三重県                  |
--   | 鈴木  -> 和歌山県                |
--   +----------------------------------+
--                 |
--                 v
--      WHERE address = '千葉県'
--                 |
--      +----------+----------+
--      |                     |
--      v                     v
--   一致する               一致しない
--   (千葉県)               (その他)
--      |                     |
--      v                     v
--    削除する               残す
--
-- 結果:
--   千葉県の行だけが消える
--
-- つまり、
--   DELETE + WHERE は
--   「削除版の条件検索」
-- と考えると理解しやすい。


-- ================================================
-- DELETE と SELECT の関係
-- ================================================
--
-- 今回の DELETE は、
-- 次の SELECT とほぼ同じ条件判定を使っていると考えられる。
--
--   SELECT *
--     FROM Address
--    WHERE address = '千葉県';
--
-- この SELECT は、
--   削除されるはずの行を「見る」だけ
-- である。
--
-- 一方、
--
--   DELETE FROM Address
--    WHERE address = '千葉県';
--
-- は、
--   その条件に一致した行を実際に消す。
--
-- したがって実務では、
-- 先に SELECT で対象確認してから DELETE することが多い。
--
-- これは非常に重要な運用上の考え方である。


-- ================================================
-- 実務上の注意
-- ================================================
--
-- DELETE は表の状態を変更する更新系SQLなので、
-- 実行前に対象確認が重要である。
--
-- 特に WHERE 付き DELETE では、
-- 条件が想定どおりかどうかを確認するために、
-- 先に同じ WHERE 条件で SELECT する習慣が有用である。
--
-- 例:
--
--   SELECT *
--     FROM Address
--    WHERE address = '千葉県';
--
-- これで対象行を確認してから、
--
--   DELETE FROM Address
--    WHERE address = '千葉県';
--
-- を実行すると安全である。


