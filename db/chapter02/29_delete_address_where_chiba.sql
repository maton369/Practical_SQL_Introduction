-- ================================================
-- ファイル名: 29_delete_address_where_chiba.sql
-- 役割:
--   Address テーブルの中から、
--   address が '千葉県' である行だけを削除する。
--
-- このSQLの目的:
--   全件削除ではなく、
--   条件に一致する一部のレコードだけを削除する方法を理解すること。
--
-- 学習上の主題:
--   DELETE 文に WHERE 句を付けて、
--   削除対象を限定する方法を理解すること。
--
-- 今回の最重要ポイント:
--   DELETE に WHERE を付けると、
--   条件に一致した行だけが削除される。
--
--   つまり今回は、
--   Address テーブル全体を空にするのではなく、
--   千葉県の人の行だけを取り除く。
--
--   したがって、
--   DELETE の本質は
--   「どの行を消すかを条件で決める」
--   ところにある。
-- ================================================

DELETE FROM
    -- DELETE FROM:
    --   指定した表から既存の行を削除する構文である。
    Address
WHERE
    -- WHERE 句:
    --   削除対象を絞り込む条件を書く部分である。
    --
    --   今回は
    --     address = '千葉県'
    --   という条件を付けているため、
    --   address 列の値が千葉県である行だけが削除対象になる。
    --
    --   逆に、
    --   東京都, 福島県, 三重県, 和歌山県 などの行は削除されない。
    address = '千葉県';
    -- '千葉県' は文字列なのでシングルクォートで囲む。
    --
    -- この条件に一致する行だけが消される。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルを削除対象として指定する
-- 2. 各行について address = '千葉県' かどうかを判定する
-- 3. 条件を満たす行だけを削除候補とする
-- 4. 条件に一致した行だけを削除する
-- 5. 一致しない行はそのまま残す
--
-- 概念的には次のような処理に近い。
--
--   for each row in Address:
--       if row.address == '千葉県':
--           delete row
--
-- つまりこれは、
--   「Address の中から千葉県の行だけを削除する」
--   条件付きDELETEである。


-- ================================================
-- DELETE + WHERE の意味
-- ================================================
--
-- 一般形は次のようになる。
--
--   DELETE FROM 表名
--   WHERE 条件;
--
-- この形では、
--   WHERE 条件を満たす行だけが削除される。
--
-- 今回は
--
--   DELETE FROM Address
--   WHERE address = '千葉県';
--
-- なので、
--   Address の各行のうち
--   address が '千葉県' のものだけ
-- が削除される。
--
-- したがってこれは、
--   全削除ではなく部分削除
-- である。


-- ================================================
-- 全件 DELETE との違い
-- ================================================
--
-- 前の
--
--   DELETE FROM Address;
--
-- は WHERE が無いため、全件削除だった。
--
-- 一方、今回の
--
--   DELETE FROM Address
--   WHERE address = '千葉県';
--
-- は WHERE があるため、
-- 条件に一致する行だけを消す。
--
-- 整理すると、
--
--   WHERE なし
--     → 全件削除
--
--   WHERE あり
--     → 条件一致行だけ削除
--
-- という違いになる。
--
-- ここは DELETE 文の最も基本的で重要な使い分けである。


-- ================================================
-- この WHERE 条件の意味
-- ================================================
--
--   address = '千葉県'
--
-- は、
--   address 列の値が文字列 '千葉県' と完全一致する
-- 行を選ぶ条件である。
--
-- したがって、
--
--   佐々木 | 千葉県
--   松本   | 千葉県
--
-- のような行は削除対象になる。
--
-- 一方で、
--
--   小川   | 東京都
--   林     | 福島県
--   佐藤   | 三重県
--
-- のような行は条件を満たさないので残る。
--
-- つまり今回は、
--   都道府県が千葉県の人だけ消す
-- という意味である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
