--   松本    | NULL            | 千葉県     | 女  | 20
--   佐藤    | 090-1922-XXXX   | 三重県     | 女  | 25
--   鈴木    | 090-0001-XXXX   | 和歌山県   | 男  | 32
--
-- ここで
--
--   DELETE FROM Address
--   WHERE address = '千葉県';
--
-- を実行すると、
