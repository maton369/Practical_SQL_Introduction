-- ================================================
-- ファイル名: 18_select_address_name_in_subquery.sql
-- 役割:
--   Address テーブルの name のうち、
--   Address2 テーブルにも存在する name だけを選び出す。
--
-- このSQLの目的:
--   2つのテーブル
--     - Address
--     - Address2
--   を、name 列を使って比較し、
--   「両方の表に共通して現れる名前」
--   を取り出すこと。
--
-- 学習上の主題:
--   IN の中でサブクエリを使う方法を理解すること。
--
-- 今回の最重要ポイント:
--   WHERE name IN (SELECT name FROM Address2)
--
--   の丸括弧の中にある SELECT は、
--   外側の SELECT の一部として動く「サブクエリ」である。
--
--   つまりこのSQLは、
--   1つの SELECT 文の中に、
--   もう1つの SELECT 文が埋め込まれている構造になっている。
-- ================================================

SELECT
    -- 外側の SELECT 句:
    --   最終的に表示したい列を指定する。
    --
    --   今回は Address テーブルから name だけを返す。
    name
FROM
    -- 外側の FROM 句:
    --   外側クエリの検索対象テーブルを指定する。
    --
    --   今回、最終的に結果として返すのは Address 側の name であるため、
    --   外側の対象表は Address になっている。
    Address
WHERE
    -- 外側の WHERE 句:
    --   Address の各行について、
    --   その name が特定の集合に含まれているかどうかを判定する。
    --
    --   その「特定の集合」を作っているのが、
    --   IN の中に書かれたサブクエリである。
    name IN
    (
        -- ========================================
        -- ここから内側の SELECT = サブクエリ
        -- ========================================
        --
        -- サブクエリとは:
        --   別の SQL 文の中に埋め込まれた SELECT 文である。
        --
        -- 今回のサブクエリは、
        --   Address2 テーブルから name を取り出して、
        --   その一覧を外側クエリへ渡す役割を持つ。
        --
        -- つまり、
        --   SELECT name
        --   FROM Address2
        --
        -- の結果が、
        --   IN (...)
        -- の候補集合になる。
        --
        -- このサブクエリが返す名前一覧は概念的には次のようになる。
        --
        --   {'小川', '林', '武田', '斉藤', '上野', '広田'}
        --
        -- 外側クエリでは、
        --   Address.name がこの集合に含まれていれば採用、
        --   含まれていなければ除外
        -- となる。
        SELECT
            -- サブクエリの SELECT 句:
            --   Address2 から name 列だけを取り出す。
            --
            --   ここで返すのは最終結果ではなく、
            --   外側クエリが比較に使うための候補一覧である。
            name
        FROM
            -- サブクエリの FROM 句:
            --   サブクエリ側の対象テーブルを指定する。
            --
            --   ここでは Address2 テーブルから name を集める。
            Address2
    );


-- ================================================
-- このSQL全体の処理の流れ
-- ================================================
--
-- このSQLは、理解しやすく分けると次の2段階で動く。
--
-- 1. まずサブクエリを実行する
--
--      SELECT name
--        FROM Address2
--
--    これにより、Address2 に存在する name の一覧を得る。
--
-- 2. 次に外側クエリを実行する
--
--      SELECT name
--        FROM Address
--       WHERE name IN (その一覧)
--
--    これにより、Address の name のうち、
--    Address2 にも存在するものだけが残る。
--
-- 概念的には次のような擬似コードに近い。
--
--   sub_result = all names from Address2
--
--   for each row in Address:
--       if row.name is in sub_result:
--           output row.name
--
-- つまり、
--   サブクエリで比較対象集合を作り、
--   外側クエリでその集合への所属判定をしている。


-- ================================================
-- サブクエリとは何か
-- ================================================
--
-- サブクエリとは、
--   SQL 文の中に埋め込まれた別の SELECT 文
-- のことである。
--
-- 今回のように
--
--   WHERE name IN (SELECT name FROM Address2)
--
-- と書いた場合、
--
--   - 外側クエリ
--       SELECT name FROM Address WHERE ...
--
--   - 内側クエリ
--       SELECT name FROM Address2
--
-- の2つの SELECT が存在する。
--
-- 内側クエリは外側クエリに材料を渡す役割を持つ。
--
-- 今回の材料は
--   「Address2 に存在する name の一覧」
-- である。


-- ================================================
-- IN とサブクエリの関係
-- ================================================
--
-- 以前学んだ IN の基本形は、
--
--   address IN ('東京都', '福島県', '千葉県')
--
-- のように、候補値を自分で直接並べる形だった。
--
-- 今回はそれが次の形に変わっている。
--
--   name IN (SELECT name FROM Address2)
--
-- つまり、
--   候補を手で書く代わりに、
--   別の SELECT 文の結果を候補集合として使っている。
--
-- これが「IN の中でサブクエリを使う」という意味である。
--
-- したがって今回のSQLは、
--   固定の候補一覧に属するか
-- ではなく、
--   別表から動的に作られた候補一覧に属するか
-- を判定している。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Address テーブルの name が次のようだとする。
--
--   小川
--   前田
--   森
--   林
--   井上
--   佐々木
--   松本
--   佐藤
--   鈴木
--
-- 一方、Address2 テーブルの name は次のようである。
--
--   小川
--   林
--   武田
--   斉藤
--   上野
--   広田
--
-- サブクエリ
--
--   SELECT name
--   FROM Address2
--
-- は、この Address2 の name 一覧を返す。
--
-- すると外側クエリは、
--   Address の各 name がこの一覧に含まれるかを判定する。
--
-- 共通して存在するのは
--
--   小川
--   林
--
-- なので、最終結果は次のようになる。
--
--   name
--   ----
--   小川
--   林


-- ================================================
-- 図解: サブクエリのイメージ
-- ================================================
--
--   外側クエリ
--
--     SELECT name
--       FROM Address
--      WHERE name IN ( ... );
--
--                     |
--                     v
--            +------------------+
--            | サブクエリ       |
--            | SELECT name      |
--            | FROM Address2    |
--            +------------------+
--                     |
--                     v
--        Address2 にある name の一覧を作る
--                     |
--                     v
--     その一覧に Address.name が含まれるか判定する
--                     |
--                     v
--         含まれる name だけを結果に残す
--
--
-- 集合として見ると:
--
--   Address の name 集合
--     {小川, 前田, 森, 林, 井上, 佐々木, 松本, 佐藤, 鈴木}
--
--   Address2 の name 集合
--     {小川, 林, 武田, 斉藤, 上野, 広田}
--
--   共通部分
--     {小川, 林}
--
-- 今回の SQL は、この共通部分を
-- IN + サブクエリによって取り出していると考えられる。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name
--   → 最終的に表示したい列を指定する
--
-- FROM Address
--   → 外側クエリの対象表を Address にする
--
-- WHERE name IN ( ... )
--   → name が候補集合に含まれる行だけを残す
--
-- (SELECT name FROM Address2)
--   → 候補集合を作るサブクエリ
--
-- ;
--   → SQL文の終了を表す


