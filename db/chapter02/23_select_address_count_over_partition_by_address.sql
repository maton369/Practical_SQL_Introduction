-- ================================================
-- ファイル名: 23_select_address_count_over_partition_by_address.sql
-- 役割:
--   Address テーブルの各行について、
--   その行が属する住所（address）グループの人数を
--   ウィンドウ関数で求めて表示する。
--
-- このSQLの目的:
--   address ごとの人数を知りたいが、
--   GROUP BY のように行を1件にまとめてしまうのではなく、
--   元の行を残したまま
--   「この住所には何人いるか」
--   という集計値を各行に付けて見たい。
--
-- 学習上の主題:
--   ウィンドウ関数
--
--     COUNT(*) OVER(PARTITION BY address)
--
--   の意味を理解すること。
--
-- 今回の最重要ポイント:
--   GROUP BY は行をまとめるが、
--   ウィンドウ関数は行をまとめずに
--   集計結果だけを各行へ付け加える。
--
--   つまり今回は、
--   「住所別人数を出す」
--   という点では GROUP BY に似ているが、
--   結果の粒度は 1行1住所 ではなく
--   1行1元レコード のままである。
-- ================================================

SELECT
    -- address 列:
    --   元の Address テーブルの address 列をそのまま表示する。
    --
    --   つまり、この行がどの都道府県に属しているかを表す。
    address,

    COUNT(*) OVER
    (
        PARTITION BY address
    )
    -- COUNT(*) OVER (...) :
    --   これがウィンドウ関数である。
    --
    --   COUNT(*) 自体は「件数を数える集約関数」だが、
    --   今回は GROUP BY と一緒に使っているのではなく、
    --   OVER(...) と一緒に使っている。
    --
    --   これにより、
    --   行を1件にまとめるのではなく、
    --   各行に対して
    --   「自分が属しているグループの件数」
    --   を計算して返すことができる。
    --
    -- PARTITION BY address :
    --   ウィンドウ関数の計算単位を
    --   address ごとに分ける指定である。
    --
    --   つまり、
    --     東京都の行どうし
    --     福島県の行どうし
    --     千葉県の行どうし
    --   を別々のグループとして扱い、
    --   そのグループ内で COUNT(*) を計算する。
    --
    --   結果として、
    --   各行には
    --   「自分と同じ address を持つ行が全部で何件あるか」
    --   が表示される。
FROM
    -- FROM 句:
    --   元データとして Address テーブルを参照する。
    Address;


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Address テーブルの全行を読む
-- 2. 各行を address の値ごとにグループ分けして考える
-- 3. 各グループ内の件数を COUNT(*) で求める
-- 4. その件数を、グループに属する各行それぞれへ付ける
-- 5. address と件数を各行ごとに返す
--
-- 概念的には次のような処理に近い。
--
--   groups = {}
--
--   for each row in Address:
--       groups[row.address] += 1
--
--   for each row in Address:
--       output row.address, groups[row.address]
--
-- つまりこれは、
--   「住所ごとの人数を数える」
--   ただし
--   「1住所1行にまとめず、元の各行にその人数を付ける」
--   という処理である。


-- ================================================
-- ウィンドウ関数とは何か
-- ================================================
--
-- ウィンドウ関数とは、
--   ある行に注目しながら、
--   その行が属する範囲（ウィンドウ）に対して集計を行い、
--   結果を各行へ返す関数である。
--
-- 今回のウィンドウ関数は
--
--   COUNT(*) OVER(PARTITION BY address)
--
-- である。
--
-- これを自然言語で言うと、
--
--   「同じ address を持つ行の集団の人数を数えて、
--     その人数を各行に表示する」
--
-- という意味になる。
--
-- 重要なのは、
--   GROUP BY のように行を1つにまとめない
-- ことである。
--
-- したがってウィンドウ関数は、
--   集計値を知りたい
--   しかし元の明細行も残したい
-- という場面で非常に有用である。


-- ================================================
-- PARTITION BY の意味
-- ================================================
--
-- PARTITION BY は、
--   ウィンドウ関数の計算対象を
--   どの単位で区切るか
-- を指定する。
--
-- 今回の
--
--   PARTITION BY address
--
-- は、
--   address が同じ行どうしを1つの区切りとして扱う
-- という意味である。
--
-- つまり、
--
--   東京都グループ
--   福島県グループ
--   千葉県グループ
--   三重県グループ
--   和歌山県グループ
--
-- のように分かれ、
-- COUNT(*) はそれぞれの区切りの中で計算される。
--
-- その結果、
--   東京都の各行には 3
--   福島県の各行には 2
--   千葉県の各行には 2
--   三重県の各行には 1
--   和歌山県の各行には 1
-- のような値が付く。


-- ================================================
-- GROUP BY との違い
-- ================================================
--
-- このSQLと似た集計として、
-- 以前学んだ次のような GROUP BY がある。
--
--   SELECT address, COUNT(*)
--     FROM Address
--    GROUP BY address;
--
-- この GROUP BY の結果は、
--   1住所につき1行
-- になる。
--
-- たとえば
--
--   東京都   3
--   福島県   2
--   千葉県   2
--   三重県   1
--   和歌山県 1
--
-- のように、要約表になる。
--
-- 一方、今回のウィンドウ関数は、
-- 元の行を消さずに各行へ人数を付ける。
--
-- したがって結果は、
--   東京都の人が3人いるなら、
--   東京都の各行すべてに 3 が付く
-- 形になる。
--
-- 整理すると次の違いがある。
--
--   GROUP BY
--     → 行をまとめる
--     → 結果の粒度がグループ単位になる
--
--   ウィンドウ関数
--     → 行をまとめない
--     → 結果の粒度は元の行のまま
--     → 集計値だけを付加する


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Address テーブルに次のようなデータがあるとする。
--
--   name    | address
--   --------+----------
--   小川    | 東京都
--   前田    | 東京都
--   森      | 東京都
--   林      | 福島県
--   井上    | 福島県
--   佐々木  | 千葉県
--   松本    | 千葉県
--   佐藤    | 三重県
--   鈴木    | 和歌山県
--
-- 住所ごとの人数は
--
--   東京都   → 3
--   福島県   → 2
--   千葉県   → 2
--   三重県   → 1
--   和歌山県 → 1
--
-- である。
--
-- 今回のSQLでは、その人数が各行に付くので、
-- 結果は概ね次のようになる。
--
--   address    | COUNT(*) OVER(PARTITION BY address)
--   -----------+------------------------------------
--   東京都     | 3
--   東京都     | 3
--   東京都     | 3
--   福島県     | 2
--   福島県     | 2
--   千葉県     | 2
--   千葉県     | 2
--   三重県     | 1
--   和歌山県   | 1
--
-- ここで重要なのは、
-- GROUP BY なら東京都は1行しか出ないが、
-- 今回は東京都の行が3件そのまま残っていることである。


-- ================================================
-- 図解: ウィンドウ関数のイメージ
-- ================================================
--
--   Address テーブルの各行
--        |
--        v
--   +-------------------------------+
--   | address ごとに区切って考える |
--   | (PARTITION BY address)        |
--   +-------------------------------+
--        |
--   +----+-------------------------------------------+
--   |                                                |
--   v                                                v
-- 東京都の区切り                                     福島県の区切り
--   小川                                               林
--   前田                                               井上
--   森
--   件数 = 3                                          件数 = 2
--        |                                                |
--        v                                                v
-- 各行に 3 を付ける                                   各行に 2 を付ける
--
-- 同様に
--   千葉県 → 各行に 2
--   三重県 → 各行に 1
--   和歌山県 → 各行に 1
--
-- つまり、
--   グループの件数を求める
--   しかしグループを1行に潰さない
-- というのがウィンドウ関数の特徴である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT address,
--        COUNT(*) OVER(PARTITION BY address)
--   → address と、その address に属する行数を各行ごとに表示する
--
-- COUNT(*)
--   → 件数を数える集約関数
--
-- OVER(...)
--   → この COUNT をウィンドウ関数として使う
--
-- PARTITION BY address
--   → address ごとに区切って件数を計算する
--
-- FROM Address
--   → Address テーブルを元データとする
--
-- ;
--   → SQL文の終了を表す


