-- ================================================
-- ファイル名: 25_select_threeelements_case_in_where.sql
-- 役割:
--   3組ある (date, flg) ペアのうち、
--   最初に date が一致したペアの flg を CASE 式で取り出し、
--   それが 'T' である行を返す。
--
-- このSQLの目的:
--   UNION 版や OR 版と同じ題材を、
--   WHERE 句の CASE 式で表現すること。
--
-- 学習上の主題:
--   WHERE 句内の CASE による条件分岐と、
--   それを UNION 版と比較したときのパフォーマンス上の考え方を理解すること。
--
-- 今回の最重要ポイント:
--   CASE 版は、各行に対して
--   「最初に date が一致した場所の flg を返す」
--   という逐次分岐を行う。
--
--   つまり、
--   検索条件を索引可能な複数枝へ分割する UNION 版とは違い、
--   行ごとの式評価に寄った書き方である。
--
--   このため、一般には UNION 版のような
--   各複合インデックスの個別利用が見えにくくなりやすい。
-- ================================================

SELECT
    `key`, name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
    CASE
        WHEN date_1 = '2013-11-01' THEN flg_1
        WHEN date_2 = '2013-11-01' THEN flg_2
        WHEN date_3 = '2013-11-01' THEN flg_3
        ELSE NULL
    END = 'T';
    -- この CASE 式は、
    --   まず date_1 が一致するかを調べる
    --   一致したら flg_1 を返す
    --
    --   そうでなければ date_2 を調べる
    --   一致したら flg_2 を返す
    --
    --   そうでなければ date_3 を調べる
    --   一致したら flg_3 を返す
    --
    --   どれも一致しなければ NULL を返す
    -- という意味である。
    --
    -- その返り値が 'T' なら行を採用する。
    --
    -- つまり概念的には
    --
    --   if date_1 matches:
    --       return flg_1
    --   elif date_2 matches:
    --       return flg_2
    --   elif date_3 matches:
    --       return flg_3
    --   else:
    --       return NULL
    --
    -- を行い、
    -- その結果が 'T' かどうかを判定している。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. ThreeElements の各行を候補として見る
-- 2. 各行で CASE 式を上から順に評価する
-- 3. 最初に date_X='2013-11-01' を満たした場所の flg_X を返す
-- 4. 返り値が 'T' なら、その行を結果へ返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in ThreeElements:
--       value = NULL
--
--       if row.date_1 == '2013-11-01':
--           value = row.flg_1
--       elif row.date_2 == '2013-11-01':
--           value = row.flg_2
--       elif row.date_3 == '2013-11-01':
--           value = row.flg_3
--
--       if value == 'T':
--           output row
--
-- つまり、
--   行ごとに CASE 評価して真偽判定する
-- 形である。


-- ================================================
-- UNION 版との違い
-- ================================================
--
-- UNION 版は
--   各列ペア条件を独立した SELECT に分けることで、
--   各枝が
--     IDX_1
--     IDX_2
--     IDX_3
-- を個別に使いやすい形だった。
--
-- 一方、この CASE 版は
--   各行の中で順に条件を評価する式
-- になっている。
--
-- そのため、
--   「第1枝は IDX_1」
--   「第2枝は IDX_2」
--   「第3枝は IDX_3」
-- のような索引利用の分離は、SQL の見た目からは消えている。
--
-- したがってこのケースでは、
--   可読性や条件の表現力はある
-- が
--   パフォーマンス面では UNION 版の方が有利になりやすい
-- と考えるのが自然である。


-- ================================================
-- パフォーマンス比較の観点
-- ================================================
--
-- この CASE 版は、論理的には
--   「各行に対して式を評価している」
-- 形である。
--
-- 一方、UNION 版は
--   「索引可能な条件検索を3本に分けている」
-- 形であった。
--
-- この違いは重要で、
--   CASE 版は行単位の評価寄り
--   UNION 版は索引探索寄り
-- と言える。
--
-- そのため今回のように
--   似た列ペアが複数あり、
--   それぞれに専用複合インデックスがある
-- 場面では、
--   UNION 版の方が索引活用の面で有利になりやすい。
--
-- もちろん実際の実行方式は EXPLAIN で確認する必要があるが、
-- 構造上の比較としては
--   CASE 版は UNION 版ほど索引利用の意図が明確ではない
-- と整理できる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- 今回のデータでは、
--   key='1'
--   key='2'
--   key='5'
-- の3行が条件一致する。
--
-- ただし CASE 版には注意点がある。
--
-- この式は
--   「最初に一致した date の flg を見る」
-- ため、
--   1行の中で複数の date が一致するようなデータがある場合、
--   先に書かれた WHEN が優先される。
--
-- 今回のサンプルではその問題は起きないが、
-- 論理設計としては OR / UNION より微妙な差が出る可能性がある。
--
-- その意味でも、
--   索引利用
--   条件意味の明確さ
-- の両面で UNION 版の方が扱いやすいケースが多い。


-- ================================================
