-- ================================================
-- ファイル名: 27_create_nonaggtbl.sql
-- 役割:
--   非集約テーブル NonAggTbl を作成する。
--
-- このSQLの目的:
--   id ごとに複数種類の data_type を持つ縦持ちデータを用意し、
--   後続で CASE + 集約関数を使って横持ちへ変換できるようにすること。
--
-- 学習上の主題:
--   1人(id)に対して
--     data_type='A'
--     data_type='B'
--     data_type='C'
--   の複数行を持つ表をどう扱うかを理解すること。
--
-- 今回の最重要ポイント:
--   この表は「1人 = 1行」ではない。
--
--   実際には
--     1人 × 1データ種別 = 1行
-- の構造になっている。
--
--   そのため、
--   最終的に欲しい
--     id ごとに 1 行
-- の形へ変換するには、
--   条件分岐しながら集約する必要がある。
-- ================================================

CREATE TABLE NonAggTbl
(
    id VARCHAR(32) NOT NULL,
    -- id:
    --   対象者を表す識別子である。
    --
    --   例:
    --     Jim
    --     Ken
    --     Beth
    --
    --   後続の GROUP BY では、この id ごとに1行へまとめる。

    data_type CHAR(1) NOT NULL,
    -- data_type:
    --   データ種別を表す列である。
    --
    --   今回は
    --     'A'
    --     'B'
    --     'C'
    -- の3種類がある。
    --
    --   どの列を最終結果へ採用するかは、
    --   この data_type によって決まる。

    data_1 INTEGER,
    data_2 INTEGER,
    data_3 INTEGER,
    data_4 INTEGER,
    data_5 INTEGER,
    data_6 INTEGER
    -- data_1 ～ data_6:
    --   数値データ列である。
    --
    --   ただし重要なのは、
    --   各列が常に意味を持つわけではない点である。
    --
    --   今回の最終SQLでは、
    --     data_type='A' の行から data_1, data_2 を使う
    --     data_type='B' の行から data_3, data_4, data_5 を使う
    --     data_type='C' の行から data_6 を使う
    -- という対応になっている。
    --
    --   つまり、1行の全列を使うのではなく、
    --   data_type に応じて意味のある列だけを抜き出している。
);