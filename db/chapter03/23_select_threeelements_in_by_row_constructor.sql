-- ================================================
-- ファイル名: 23_select_threeelements_in_by_row_constructor.sql
-- 役割:
--   3組ある (date, flg) ペアのうち、
--   いずれかが指定タプルと一致する行を IN で取得する。
--
-- このSQLの目的:
--   OR 版や UNION 版と同じ意味の検索を、
--   行コンストラクタの IN 比較で表現すること。
--
-- 学習上の主題:
--   (値1, 値2) IN ((列1, 列2), (列3, 列4), ...)
--   のような書き方と、
--   それを UNION 版と比較した際のパフォーマンス上の考え方を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは見た目としては非常に簡潔で、
--   「('2013-11-01','T') に一致する列ペアが
--    3組のうちどこかにあるか」
--   を素直に表している。
--
--   ただしパフォーマンス上は、
--   UNION 版のように
--   各複合インデックス条件を独立した SELECT に分けているわけではない。
--
--   そのため、この書き方が索引利用の面でどう扱われるかは
--   実行計画を見て確認する必要がある。
-- ================================================

SELECT
    `key`, name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
    ('2013-11-01', 'T')
    IN (
        (date_1, flg_1),
        (date_2, flg_2),
        (date_3, flg_3)
    );
    -- この条件は、
    --   ('2013-11-01', 'T')
    -- という2要素の組が、
    --   (date_1, flg_1)
    --   (date_2, flg_2)
    --   (date_3, flg_3)
    -- のどれかと一致するかを調べている。
    --
    -- つまり意味としては
    --   pair1 matches
    --   or pair2 matches
    --   or pair3 matches
    -- とほぼ同じである。
    --
    -- ただし見た目は
    --   「3組の中にこのタプルが含まれるか」
    -- という非常にコンパクトな書き方になっている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 各行について
--      (date_1, flg_1)
--      (date_2, flg_2)
--      (date_3, flg_3)
--    の3組を取り出す
-- 2. それぞれが
--      ('2013-11-01', 'T')
--    と一致するかを判定する
-- 3. どれか1組でも一致すれば、その行を結果として返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in ThreeElements:
--       pair1 = (row.date_1, row.flg_1)
--       pair2 = (row.date_2, row.flg_2)
--       pair3 = (row.date_3, row.flg_3)
--
--       if ('2013-11-01', 'T') in [pair1, pair2, pair3]:
--           output row
--
-- つまり、
--   OR を「タプル集合に対する IN」として書き換えた
-- 形である。


-- ================================================
-- UNION 版との違い
-- ================================================
--
-- UNION 版は、
--   第1ペア条件を1本目
--   第2ペア条件を2本目
--   第3ペア条件を3本目
-- の SELECT に分けていた。
--
-- そのため各枝が
--   IDX_1
--   IDX_2
--   IDX_3
-- をそれぞれ使いやすかった。
--
-- 一方、この IN 版は
--   3組の列ペアを1本の条件式の中に畳み込んでいる。
--
-- したがって意味としては同じでも、
--   「各枝が専用インデックスにきれいに乗る」
-- という UNION 版の強みは表面上は見えにくい。
--
-- このため、読みやすさは高いが、
-- パフォーマンス面では UNION 版の方が有利になることがある。


-- ================================================
-- パフォーマンス比較の観点
-- ================================================
--
-- MySQL は行コンストラクタ比較や複数値比較をサポートしているが、
-- 今回のように
--   右辺が複数の「列ペア」
-- で構成される形では、
--   UNION 版のような「各列ペア専用インデックス検索」
-- と同じ見え方になるとは限らない。
--
-- したがってこの題材では、
--   可読性:
--     IN 版が非常に高い
--
--   索引利用の明確さ:
--     UNION 版が高い
--
-- と整理すると分かりやすい。
--
-- 最終的には EXPLAIN で、
--   全表走査になるか
--   何らかのインデックス最適化が起きるか
-- を見るべきである。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- 条件一致する行は UNION 版や OR 版と同じく、
--   key='1'
--   key='2'
--   key='5'
-- の3行である。
--
-- したがって意味的には同じ結果を返す。
--
-- 差は
--   条件の表現方法
--   実行計画の選ばれ方
-- にある。


