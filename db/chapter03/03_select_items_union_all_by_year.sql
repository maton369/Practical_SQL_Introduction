-- ================================================
-- ファイル名: 03_select_items_union_all_by_year.sql
-- 役割:
--   年によって参照する価格列を切り替えながら、
--   Items テーブルから商品名・年・価格を取得する。
--
-- このSQLの目的:
--   2001年以前は税抜価格、
--   2002年以降は税込価格を使って、
--   1つの結果表として表示すること。
--
-- 学習上の主題:
--   UNION ALL を使った条件分岐的な書き方を理解すること。
--
-- 今回の最重要ポイント:
--   1本の SELECT の中で CASE を使わず、
--   条件ごとに別々の SELECT を書き、
--   それらを UNION ALL で縦に結合している。
--
--   つまり今回は、
--   「条件分岐を集合演算で表現する」
--   という考え方が中心である。
-- ================================================

SELECT
    item_name,
    -- item_name:
    --   商品名を表示する。
    --
    --   たとえば
    --     カップ
    --     スプーン
    --     ナイフ
    --   が入る。

    year,
    -- year:
    --   その価格がどの年のものかを表示する。

    price_tax_ex AS price
    -- price_tax_ex AS price:
    --   2001年以前については税抜価格を採用する。
    --
    --   AS price を付けることで、
    --   結果列名を統一している。
    --
    --   つまりこの SELECT の結果では
    --   「税抜価格」という名前ではなく
    --   単に price という共通列名で見せている。
FROM
    Items
WHERE
    year <= 2001
    -- year <= 2001:
    --   2000年と2001年の行だけを取り出す。
    --
    --   これが前半部分の条件分岐に相当する。
    --
    --   この範囲では税抜価格を採用する。

UNION ALL

SELECT
    item_name,
    -- item_name:
    --   後半部分でも同じく商品名を表示する。

    year,
    -- year:
    --   後半部分でも同じく年を表示する。

    price_tax_in AS price
    -- price_tax_in AS price:
    --   2002年以降については税込価格を採用する。
    --
    --   こちらも列名を price にそろえているため、
    --   前半の SELECT と後半の SELECT を
    --   同じ形の結果として UNION ALL できる。
FROM
    Items
WHERE
    year >= 2002;
    -- year >= 2002:
    --   2002年と2003年の行だけを取り出す。
    --
    --   この範囲では税込価格を採用する。
    --
    --   したがって全体として
    --     2000, 2001 -> 税抜
    --     2002, 2003 -> 税込
    -- という切り替えになる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Items テーブルから year <= 2001 の行を取り出す
-- 2. その行については price_tax_ex を price として返す
-- 3. Items テーブルから year >= 2002 の行を取り出す
-- 4. その行については price_tax_in を price として返す
-- 5. 2つの結果を UNION ALL で縦に結合する
-- 6. 最終的に item_name, year, price の3列を返す
--
-- 概念的には次のような処理に近い。
--
--   result = []
--
--   for each row in Items:
--       if row.year <= 2001:
--           result.append(row.item_name, row.year, row.price_tax_ex)
--
--   for each row in Items:
--       if row.year >= 2002:
--           result.append(row.item_name, row.year, row.price_tax_in)
--
--   output result
--
-- つまりこれは、
--   条件ごとに別々の結果集合を作り、
--   それを結合して1つの表にしている
-- というアルゴリズムである。


-- ================================================
-- なぜ「条件分岐」なのか
-- ================================================
--
-- 今回の SQL は CASE 式ではなく UNION ALL を使っているが、
-- やっていることは実質的に条件分岐である。
--
-- 前半:
--   year <= 2001 の行に対して税抜価格を使う
--
-- 後半:
--   year >= 2002 の行に対して税込価格を使う
--
-- つまり
--
--   if year <= 2001:
--       price = price_tax_ex
--   else if year >= 2002:
--       price = price_tax_in
--
-- のような分岐を、
-- 2本の SELECT と UNION ALL で表現している。
--
-- したがってこれは
--   CASE による列値分岐
-- ではなく
--   SELECT を分けることによる集合レベルの分岐
-- と考えると分かりやすい。


-- ================================================
-- UNION ALL の意味
-- ================================================
--
-- UNION ALL は、
--   2つの SELECT 結果を縦方向にそのまま結合する構文である。
--
-- UNION と違って、重複除去は行わない。
--
-- 今回は
--
--   前半: 2000年・2001年
--   後半: 2002年・2003年
--
-- で年の範囲が重なっていないため、
-- 重複除去の必要が無い。
--
-- そのため UNION よりも UNION ALL の方が素直であり、
-- 「条件ごとに作った結果をそのまま連結する」
-- 意図が明確になる。
--
-- 今回のように条件範囲が互いに排他的なら、
-- UNION ALL は非常に自然な選択である。


-- ================================================
-- 列名をそろえている意味
-- ================================================
--
-- 前半の SELECT では
--   price_tax_ex AS price
--
-- 後半の SELECT では
--   price_tax_in AS price
--
-- としている。
--
-- これは、
--   元の列は違っていても
--   最終結果では同じ意味の列 price として扱いたい
-- ためである。
--
-- もし列名をそろえなければ、
-- 「前半は税抜価格、後半は税込価格」という実装都合が前面に出る。
--
-- しかし今回欲しい最終結果は
--   item_name, year, price
-- なので、
-- price という共通名に統一している。
--
-- ここは UNION ALL で「同じ形の結果」を作る上でも重要である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Items に次のようなデータがある。
--
--   カップ   2000  税抜 500  税込 525
--   カップ   2001  税抜 520  税込 546
--   カップ   2002  税抜 600  税込 630
--   カップ   2003  税抜 600  税込 630
--
--   スプーン 2000  税抜 500  税込 525
--   スプーン 2001  税抜 500  税込 525
--   スプーン 2002  税抜 500  税込 525
--   スプーン 2003  税抜 500  税込 525
--
--   ナイフ   2000  税抜 600  税込 630
--   ナイフ   2001  税抜 550  税込 577
--   ナイフ   2002  税抜 550  税込 577
--   ナイフ   2003  税抜 400  税込 420
--
-- このとき前半 SELECT は
--   2000年, 2001年の行を取り、
--   税抜価格を price として返す。
--
-- 後半 SELECT は
--   2002年, 2003年の行を取り、
--   税込価格を price として返す。
--
-- したがって最終結果は概ね次のようになる。
--
--   item_name | year | price
--   ----------+------+------
--   カップ    | 2000 | 500
--   カップ    | 2001 | 520
--   スプーン  | 2000 | 500
--   スプーン  | 2001 | 500
--   ナイフ    | 2000 | 600
--   ナイフ    | 2001 | 550
--   カップ    | 2002 | 630
--   カップ    | 2003 | 630
--   スプーン  | 2002 | 525
--   スプーン  | 2003 | 525
--   ナイフ    | 2002 | 577
--   ナイフ    | 2003 | 420
--
-- ここで重要なのは、
--   2001年以前は税抜
--   2002年以降は税込
-- というルールが price 列に反映されていることである。


-- ================================================
-- 図解: UNION ALL による条件分岐
-- ================================================
--
--   Items テーブル
--        |
--        v
--   +--------------------------------------+
--   | year <= 2001 の行を取り出す          |
--   +--------------------------------------+
--        |
--        v
--   +--------------------------------------+
--   | price_tax_ex を price として返す     |
--   +--------------------------------------+
--        |
--        v
--   前半結果
--
--
--   Items テーブル
--        |
--        v
--   +--------------------------------------+
--   | year >= 2002 の行を取り出す          |
--   +--------------------------------------+
--        |
--        v
--   +--------------------------------------+
--   | price_tax_in を price として返す     |
--   +--------------------------------------+
--        |
--        v
--   後半結果
--
--
--   前半結果
--      +
--   後半結果
--      |
--      v
--   UNION ALL
--      |
--      v
--   最終結果 item_name, year, price
--
-- つまり、
--   条件ごとに別の列を使って結果集合を作り、
--   それを最後に合成している。


-- ================================================
-- CASE 式との比較
-- ================================================
--
-- 同じことは CASE 式でも表現できる。
--
-- たとえば概念的には、
--
--   SELECT item_name,
--          year,
--          CASE
--              WHEN year <= 2001 THEN price_tax_ex
--              WHEN year >= 2002 THEN price_tax_in
--          END AS price
--     FROM Items;
--
-- のようにも書ける。
--
-- しかし今回の教材では、
--   条件ごとに SELECT 自体を分けて
--   UNION ALL で結合する
-- という表現を学ぶことが主題である。
--
-- したがって今回のポイントは、
--   列値の分岐を CASE で書く代わりに、
--   結果集合の分岐として表現している
-- と理解するとよい。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT item_name, year, price_tax_ex AS price
--   → 前半では item_name, year と税抜価格を price として返す
--
-- WHERE year <= 2001
--   → 2000年, 2001年の行だけを対象にする
--
-- UNION ALL
--   → 前半結果と後半結果をそのまま縦結合する
--
-- SELECT item_name, year, price_tax_in AS price
--   → 後半では item_name, year と税込価格を price として返す
--
-- WHERE year >= 2002
--   → 2002年, 2003年の行だけを対象にする
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   今回は year <= 2001 と year >= 2002 が重ならないため、
--   UNION ALL を使っても同一行が重複する心配がない。
-- ================================================


-- ================================================
-- 補足2:
--   条件範囲が重なっていた場合、
--   同じ行が前半と後半の両方に出る可能性がある。
--
--   その場合、UNION ALL では重複したまま出る。
--   ここは UNION との違いとして重要である。
-- ================================================


