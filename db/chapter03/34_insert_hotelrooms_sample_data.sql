-- ================================================
-- ファイル名: 34_insert_hotelrooms_sample_data.sql
-- 役割:
--   HotelRooms テーブルへ部屋ごとの稼働区間サンプルを投入する。
--
-- このSQLの目的:
--   複数区間を合計して総稼働日数を出す処理を試せるようにすること。
--
-- 学習上の主題:
--   同じ部屋が複数の稼働期間を持つこと、
--   それらの期間長を合計することで総稼働日数を計算できることを理解すること。
--
-- 今回の最重要ポイント:
--   各部屋は次のような構造になっている。
--
--   - 101号室: 連続している区間もあるが、途中に空白日がある
--   - 202号室: 区間が連続してつながっている
--   - 303号室: 1本の長い区間だけを持つ
--
--   後続SQLでは、これらの区間を部屋ごとに足し合わせて
--   総稼働日数を求める。
-- ================================================

INSERT INTO HotelRooms VALUES(101, '2008-02-01', '2008-02-06');
INSERT INTO HotelRooms VALUES(101, '2008-02-06', '2008-02-08');
INSERT INTO HotelRooms VALUES(101, '2008-02-10', '2008-02-13');

INSERT INTO HotelRooms VALUES(202, '2008-02-05', '2008-02-08');
INSERT INTO HotelRooms VALUES(202, '2008-02-08', '2008-02-11');
INSERT INTO HotelRooms VALUES(202, '2008-02-11', '2008-02-12');

INSERT INTO HotelRooms VALUES(303, '2008-02-03', '2008-02-17');


-- ================================================
-- データの見方
-- ================================================
--
-- 1. 101号室
--    2008-02-01 ～ 2008-02-06
--    2008-02-06 ～ 2008-02-08
--    2008-02-10 ～ 2008-02-13
--
--    日付差で長さを出すと
--      5日
--      2日
--      3日
--    合計 10日
--
--    ただし 2008-02-08 ～ 2008-02-10 の間に空白がある。
--    今回のSQLは「連続性」ではなく「総稼働日数」を見るので、
--    この空白があっても単純に合計する。
--
-- 2. 202号室
--    2008-02-05 ～ 2008-02-08  -> 3日
--    2008-02-08 ～ 2008-02-11  -> 3日
--    2008-02-11 ～ 2008-02-12  -> 1日
--    合計 7日
--
-- 3. 303号室
--    2008-02-03 ～ 2008-02-17  -> 14日
--
-- したがって後続SQLでは、
--   101号室 -> 10日
--   202号室 -> 7日
--   303号室 -> 14日
-- となる。
--
-- HAVING >= 10 の条件では
--   101号室
--   303号室
-- が抽出される。