-- ================================================
-- ファイル名: 08_select_population_union_pivot.sql
-- 役割:
--   Population の縦持ちデータを、
--   UNION を使って都道府県ごとの男女別人口の横持ち形式へ変換する。
--
-- このSQLの目的:
--   男性行と女性行を別々に取り出し、
--   それぞれを仮想的な列
--     pop_men
--     pop_wom
--   に割り当てたうえで、
--   最後に prefecture ごとにまとめること。
--
-- 学習上の主題:
--   UNION を使った条件分岐的な書き方、
--   NULL を使った列の埋め分け、
--   最後に GROUP BY + SUM で横持ち化する考え方を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは単純な集計ではなく、
--   「男性行」と「女性行」を別々の SELECT で処理し、
--   それを UNION で結合してから再集約している。
--
--   つまりアルゴリズムとしては、
--     1. 男性集合を作る
--     2. 女性集合を作る
--     3. UNION で縦に結合する
--     4. 県ごとに合算して横持ち化する
--   という多段階処理になっている。
--
--   ここが主にパフォーマンス上の比較ポイントでもある。
-- ================================================

SELECT
    prefecture,
    -- prefecture:
    --   都道府県ごとに結果を1行へまとめるためのキーである。

    SUM(pop_men) AS pop_men,
    -- SUM(pop_men):
    --   TMP 表の中にある pop_men 列を都道府県ごとに合計する。
    --
    --   男性行では pop_men に値が入り、
    --   女性行では pop_men は NULL である。
    --
    --   そのため都道府県ごとに SUM すると、
    --   男性人口だけが残る。

    SUM(pop_wom) AS pop_wom
    -- SUM(pop_wom):
    --   TMP 表の中にある pop_wom 列を都道府県ごとに合計する。
    --
    --   女性行では pop_wom に値が入り、
    --   男性行では pop_wom は NULL である。
    --
    --   そのため都道府県ごとに SUM すると、
    --   女性人口だけが残る。

FROM
(
    SELECT
        prefecture,
        pop AS pop_men,
        NULL AS pop_wom
        -- 男性側SELECT:
        --   男性行だけを取り出し、
        --   その人口を pop_men 列へ入れる。
        --
        --   一方、女性人口列 pop_wom には NULL を入れる。
        --
        --   つまりこの SELECT は
        --     男性人口だけを持つレコード集合
        --   を作っている。
    FROM
        Population
    WHERE
        sex = '1'
        -- sex = '1':
        --   男性だけを対象にする。
        --
        --   ここが最初の条件分岐である。
        --
        --   元の Population を
        --     男性側
        --     女性側
        --   に分けている。

    UNION

    SELECT
        prefecture,
        NULL AS pop_men,
        pop AS pop_wom
        -- 女性側SELECT:
        --   女性行だけを取り出し、
        --   その人口を pop_wom 列へ入れる。
        --
        --   一方、男性人口列 pop_men には NULL を入れる。
        --
        --   つまりこの SELECT は
        --     女性人口だけを持つレコード集合
        --   を作っている。
    FROM
        Population
    WHERE
        sex = '2'
        -- sex = '2':
        --   女性だけを対象にする。
        --
        --   これが2つ目の条件分岐である。

) TMP
-- TMP:
--   UNION で結合された中間結果に付けた別名である。
--
--   この中間表では、各都道府県について
--     男性行
--     女性行
--   が別々に存在し、
--   どちらか一方の人口列だけに値が入っている。

GROUP BY
    prefecture;
    -- GROUP BY prefecture:
    --   最後に都道府県ごとに1行へまとめる。
    --
    --   SUM(pop_men), SUM(pop_wom) によって、
    --   男性人口列と女性人口列が完成する。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Population から sex='1' の男性行だけを取り出す
-- 2. 男性行では pop を pop_men に入れ、pop_wom は NULL にする
-- 3. Population から sex='2' の女性行だけを取り出す
-- 4. 女性行では pop を pop_wom に入れ、pop_men は NULL にする
-- 5. 2つの結果を UNION で縦方向に結合する
-- 6. その中間結果を prefecture ごとに GROUP BY する
-- 7. SUM(pop_men), SUM(pop_wom) により横持ち形式へ変換する
--
-- 概念的には次のような処理に近い。
--
--   tmp = []
--
--   for each row in Population:
--       if row.sex == '1':
--           tmp.append(row.prefecture, row.pop, NULL)
--
--   for each row in Population:
--       if row.sex == '2':
--           tmp.append(row.prefecture, NULL, row.pop)
--
--   group tmp by prefecture
--   pop_men = sum(pop_men)
--   pop_wom = sum(pop_wom)
--
--   output prefecture, pop_men, pop_wom
--
-- つまりこれは、
--   縦持ちデータを UNION によって仮想的に分岐し、
--   最後に再集約して横持ちへ変換している。
--
-- ここがアルゴリズムの本質である。


-- ================================================
-- UNION による条件分岐の意味
-- ================================================
--
-- このSQLでは CASE を使わずに、
-- 条件ごとに SELECT 自体を分けている。
--
-- 男性側:
--   sex = '1'
--   pop -> pop_men
--   pop_wom = NULL
--
-- 女性側:
--   sex = '2'
--   pop -> pop_wom
--   pop_men = NULL
--
-- つまり
--   「男性なら pop_men に入れる」
--   「女性なら pop_wom に入れる」
-- という分岐を、
-- 2本の SELECT と UNION で表現している。
--
-- これは条件分岐としては分かりやすいが、
-- 同じ Population 表を
--   男性用に1回
--   女性用に1回
-- 読む構造になりやすい。
--
-- したがって、パフォーマンス比較の観点では、
--   1本の SELECT の中で CASE を使う方法
-- よりも、参照回数や処理段階が増える可能性がある。
--
-- 今回の主題はまさにここである。


-- ================================================
-- パフォーマンス上の比較ポイント
-- ================================================
--
-- この UNION 版は、構造的に次のようなコスト要因を持つ。
--
-- 1. Population を複数回参照しやすい
--    - 男性集合用に読む
--    - 女性集合用に読む
--
-- 2. 中間結果 TMP を作る
--    - UNION 後の結果を derived table として扱う
--
-- 3. その TMP に対してさらに GROUP BY する
--    - 再集約処理が必要になる
--
-- つまり、
--   「分けて、結合して、さらにまとめ直す」
-- 構造になっている。
--
-- 一方、CASE を使う代表的な書き方は概念的に次のようになる。
--
--   SELECT prefecture,
--          SUM(CASE WHEN sex='1' THEN pop ELSE 0 END) AS pop_men,
--          SUM(CASE WHEN sex='2' THEN pop ELSE 0 END) AS pop_wom
--     FROM Population
--    GROUP BY prefecture;
--
-- こちらは
--   Population を 1 回読み、
--   その1回の走査の中で
--   男性人口・女性人口を条件分岐で積み上げる
-- 形になりやすい。
--
-- したがって一般には、
--   同じ表を条件別に分けて UNION するより、
--   1本の SELECT の中で CASE + 集約する方が
--   実行計画が単純になりやすい
-- と考えるのが自然である。
--
-- ただし、実際の優劣は
--   インデックス
--   行数
--   オプティマイザ
--   統計情報
-- に依存するため、
-- 最終的には EXPLAIN で比較すべきである。
--
-- しかしアルゴリズム構造だけを見れば、
-- 今回の UNION 版は
--   多段階
--   多回参照
--   中間表あり
-- なので、
--   CASE 集約版より重くなりやすい
-- と理解してよい。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- Population の元データは縦持ちである。
--
--   prefecture | sex | pop
--   -----------+-----+----
--   徳島       | 1   |  60
--   徳島       | 2   |  40
--   香川       | 1   |  90
--   香川       | 2   | 100
--   愛媛       | 1   | 100
--   愛媛       | 2   |  50
--   高知       | 1   | 100
--   高知       | 2   | 100
--   福岡       | 1   |  20
--   福岡       | 2   | 200
--
-- 男性側SELECTの結果は概ね次のようになる。
--
--   prefecture | pop_men | pop_wom
--   -----------+---------+--------
--   徳島       |  60     | NULL
--   香川       |  90     | NULL
--   愛媛       | 100     | NULL
--   高知       | 100     | NULL
--   福岡       |  20     | NULL
--
-- 女性側SELECTの結果は概ね次のようになる。
--
--   prefecture | pop_men | pop_wom
--   -----------+---------+--------
--   徳島       | NULL    |  40
--   香川       | NULL    | 100
--   愛媛       | NULL    |  50
--   高知       | NULL    | 100
--   福岡       | NULL    | 200
--
-- UNION 後の TMP は、男性行と女性行が縦に並んだ状態になる。
--
-- これを prefecture ごとに GROUP BY して SUM すると、
-- 最終結果は次のようになる。
--
--   prefecture | pop_men | pop_wom
--   -----------+---------+--------
--   徳島       |  60     |  40
--   香川       |  90     | 100
--   愛媛       | 100     |  50
--   高知       | 100     | 100
--   福岡       |  20     | 200
--
-- つまりこれは、
--   縦持ちの性別人口表を
--   横持ちの男女別人口表へ変換している。


-- ================================================
-- 図解: UNION 版のアルゴリズム
-- ================================================
--
--   Population
--        |
--        +-------------------------------+
--        |                               |
--        v                               v
--   sex='1' の行を抽出               sex='2' の行を抽出
--        |                               |
--        v                               v
--   pop -> pop_men                    pop -> pop_wom
--   pop_wom = NULL                    pop_men = NULL
--        |                               |
--        +---------------+---------------+
--                        |
--                        v
--                     UNION
--                        |
--                        v
--                    中間表 TMP
--                        |
--                        v
--              GROUP BY prefecture
--                        |
--                        v
--         SUM(pop_men), SUM(pop_wom)
--                        |
--                        v
--                最終結果(横持ち表)
--
-- つまり、
--   分ける
--   結合する
--   まとめ直す
-- という3段階構造になっている。


-- ================================================
-- CASE 集約版との比較イメージ
-- ================================================
--
-- CASE 集約版の代表形は概念的に次の通りである。
--
--   Population
--        |
--        v
--   1回だけ走査
--        |
--        v
--   行ごとに
--     男性なら pop_men に加算
--     女性なら pop_wom に加算
--        |
--        v
--   GROUP BY prefecture
--        |
--        v
--   最終結果
--
-- こちらは
--   分ける → 結合する
-- の段階が無い。
--
-- そのため、アルゴリズム構造としては
-- CASE 集約版の方が単純になりやすい。
--
-- これが、UNION 条件分岐のパフォーマンス比較で重要な観点である。


-- ================================================
-- UNION と UNION ALL の点について
-- ================================================
--
-- 今回のSQLは UNION を使っている。
--
-- ただし、
--   男性側の行と女性側の行は
--   pop_men / pop_wom の入り方が違うため、
--   実質的に同一行が重複することは起こりにくい。
--
-- したがって意味的には UNION ALL でも同じ結果になりやすい。
--
-- この場合、
--   重複除去の可能性を持つ UNION より、
--   単純連結の UNION ALL の方が軽くなる余地がある。
--
-- ただし今回の本質的な論点は、
--   UNION か UNION ALL か
-- よりも、
--   そもそも SELECT を分ける構造そのものが多段階である
-- 点にある。
--
-- つまり本当の比較対象は
--   UNION 系の分割方式
-- vs
--   CASE 集約の単一走査方式
-- である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT prefecture, SUM(pop_men), SUM(pop_wom)
--   → 最終的に都道府県ごとの男女人口を返す
--
-- FROM ( ... ) TMP
--   → UNION で作った中間表を TMP として参照する
--
-- SELECT prefecture, pop AS pop_men, NULL AS pop_wom
--   → 男性行だけを pop_men 側へ入れる
--
-- WHERE sex = '1'
--   → 男性だけを対象にする
--
-- UNION
--   → 男性側結果と女性側結果を結合する
--
-- SELECT prefecture, NULL AS pop_men, pop AS pop_wom
--   → 女性行だけを pop_wom 側へ入れる
--
-- WHERE sex = '2'
--   → 女性だけを対象にする
--
-- GROUP BY prefecture
--   → 最後に都道府県ごとに1行へまとめる
--
-- SUM(pop_men), SUM(pop_wom)
--   → 片側だけに値がある列を集計して横持ち人口列を完成させる


