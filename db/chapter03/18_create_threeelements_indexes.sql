-- ================================================
-- ファイル名: 18_create_threeelements_indexes.sql
-- 役割:
--   各 (date_X, flg_X) ペアに対応する複合インデックスを作成する。
--
-- このSQLの目的:
--   UNION で各列ペアを個別に検索するときに、
--   それぞれの条件が対応インデックスを使いやすくすること。
--
-- 学習上の主題:
--   OR 条件を1本にまとめた検索よりも、
--   「列ペアごとに分けた検索 + UNION」が有利になりやすい
--   背景を理解すること。
--
-- 今回の最重要ポイント:
--   このケースでは
--     (date_1, flg_1)
--     (date_2, flg_2)
--     (date_3, flg_3)
-- ごとに別々の複合インデックスがある。
--
--   そのため、
--   各条件を別SELECTに分けると、
--   それぞれの SELECT が対応するインデックスを個別に使いやすい。
--
--   これが、このようなケースで UNION が有効になりやすい理由である。
-- ================================================

CREATE INDEX IDX_1 ON ThreeElements (date_1, flg_1);
CREATE INDEX IDX_2 ON ThreeElements (date_2, flg_2);
CREATE INDEX IDX_3 ON ThreeElements (date_3, flg_3);

-- IDX_1:
--   date_1, flg_1 条件用の複合インデックス
--
-- IDX_2:
--   date_2, flg_2 条件用の複合インデックス
--
-- IDX_3:
--   date_3, flg_3 条件用の複合インデックス
--
-- これらにより、たとえば
--   WHERE date_1='2013-11-01' AND flg_1='T'
-- のような条件が、それぞれ専用の索引で処理されやすくなる。