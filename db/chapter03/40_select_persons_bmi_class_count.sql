-- ================================================
-- ファイル名: 40_select_persons_bmi_class_count.sql
-- 役割:
--   Persons テーブルの各人物について BMI を計算し、
--   その BMI 区分ごとの人数を集計する。
--
-- このSQLの目的:
--   身長と体重から BMI を求め、
--     やせ
--     標準
--     肥満
-- のどれに属するかを判定して、
--   各区分に何人いるかを数えること。
--
-- 学習上の主題:
--   計算式を使った分類、
--   CASE 式による区分化、
--   GROUP BY による集計を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは age のような元からある列を区分しているのではなく、
--   まず
--     BMI = 体重(kg) / 身長(m)^2
--   を計算し、
--   その「計算結果」を使って分類している。
--
--   つまりアルゴリズムとしては
--     1. height と weight から BMI を計算する
--     2. BMI の値に応じて区分ラベルを付ける
--     3. 同じ区分ラベルの人数を数える
--   という
--   「計算 → 分類 → 集計」
--   の流れになっている。
-- ================================================

SELECT
    CASE
        WHEN weight / POWER(height / 100, 2) < 18.5 THEN 'やせ'
        -- BMI < 18.5:
        --   BMI が 18.5 未満なら「やせ」に分類する。
        --
        -- ここで BMI は
        --   weight / POWER(height / 100, 2)
        -- で求めている。
        --
        --   height は cm 単位なので、
        --   まず / 100 で m 単位へ直している。
        --
        --   その後 POWER(height / 100, 2) によって
        --   身長(m)の2乗を計算している。
        --
        --   つまり式全体は
        --     BMI = 体重 / 身長^2
        -- をそのままSQLで書いたものである。
        --
        -- 例:
        --   もし身長 160cm, 体重 55kg なら
        --
        --   BMI = 55 / (1.60^2)
        --       = 55 / 2.56
        --       ≒ 21.48
        --
        -- となるため、「やせ」ではなく「標準」に入る。

        WHEN 18.5 <= weight / POWER(height / 100, 2)
         AND weight / POWER(height / 100, 2) < 25 THEN '標準'
        -- 18.5 <= BMI < 25:
        --   BMI が 18.5 以上 25 未満なら「標準」に分類する。
        --
        --   下限は含み、上限は含まない区間である。
        --
        --   つまり
        --     BMI = 18.5 は標準
        --     BMI = 24.999... も標準
        --     BMI = 25 は標準ではなく肥満
        -- になる。
        --
        -- このように区間を
        --   [18.5, 25)
        -- で切っている点が重要である。

        WHEN 25 <= weight / POWER(height / 100, 2) THEN '肥満'
        -- BMI >= 25:
        --   BMI が 25 以上なら「肥満」に分類する。
        --
        --   たとえば Bill は
        --     身長 177cm
        --     体重 120kg
        -- なので、
        --
        --   BMI = 120 / (1.77^2)
        --       = 120 / 3.1329
        --       ≒ 38.30
        --
        -- となり、明らかにこの区分へ入る。

        ELSE NULL
        -- ELSE NULL:
        --   上のどの条件にも入らない場合は NULL を返す。
        --
        -- 今回の3区分は理論上すべての実数 BMI をカバーしているため、
        -- 通常の正しい height, weight データでは NULL はほぼ出ない。
        --
        -- ただし、異常値や NULL を含む設計へ拡張された場合に備えた
        -- 安全側の書き方になっている。
    END AS bmi,
    -- AS bmi:
    --   CASE 式の結果列に bmi という名前を付ける。
    --
    --   ただしここでの bmi は数値の BMI 値そのものではなく、
    --   その BMI 値から導いた分類ラベル
    --     やせ
    --     標準
    --     肥満
    -- を表している。
    --
    --   つまり「BMI値」ではなく「BMI区分名」の列である。

    COUNT(*)
    -- COUNT(*):
    --   各 BMI 区分に属する人数を数える。
    --
    --   ここでのグループは、
    --   下の GROUP BY でまとめられた
    --     やせ
    --     標準
    --     肥満
    -- の各区分である。
    --
    --   したがってこれは、
    --   各 BMI パーティションの人数を数えていることになる。

FROM
    Persons
    -- FROM Persons:
    --   元データの人物表である。
    --
    --   この時点では各人物は
    --     name
    --     age
    --     height
    --     weight
    -- を持つだけで、
    --   BMI 区分はまだ付いていない。
    --
    --   この表全体に対して、
    --   身長・体重から BMI を計算し、
    --   その結果で分類している。

GROUP BY
    CASE
        WHEN weight / POWER(height / 100, 2) < 18.5 THEN 'やせ'
        WHEN 18.5 <= weight / POWER(height / 100, 2)
         AND weight / POWER(height / 100, 2) < 25 THEN '標準'
        WHEN 25 <= weight / POWER(height / 100, 2) THEN '肥満'
        ELSE NULL
    END;
    -- GROUP BY CASE ... END:
    --   BMI 計算結果に応じた区分ラベルごとに行をまとめる。
    --
    --   つまり各人物は
    --     やせグループ
    --     標準グループ
    --     肥満グループ
    -- のいずれかに振り分けられる。
    --
    --   その後 COUNT(*) により、
    --   各グループに何人いるかが求まる。
    --
    --   ここで SELECT 句と GROUP BY 句に同じ CASE 式が書かれているのは、
    --   表示用の区分ラベルと実際の集計単位を一致させるためである。
    --
    --   つまり、
    --   「何を表示するか」と「何でグループ化するか」を
    --   同じ分類ロジックにしている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Persons テーブルの各行を1つずつ見る
-- 2. 各人物について
--      BMI = weight / (height[m]^2)
--    を計算する
-- 3. 計算した BMI を CASE 式で
--      やせ
--      標準
--      肥満
--    のいずれかに分類する
-- 4. 同じ分類ラベルを持つ人物同士をグループ化する
-- 5. 各グループの人数を COUNT(*) で数える
-- 6. BMI 区分と人数を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = {
--       'やせ': 0,
--       '標準': 0,
--       '肥満': 0
--   }
--
--   for each row in Persons:
--       bmi_value = row.weight / ((row.height / 100) ** 2)
--
--       if bmi_value < 18.5:
--           groups['やせ'] += 1
--       elif bmi_value < 25:
--           groups['標準'] += 1
--       else:
--           groups['肥満'] += 1
--
--   output groups
--
-- つまりこれは、
--   「計算値をしきい値で区切り、
--     その区分ごとの件数を数える」
--   分類集計アルゴリズムである。


-- ================================================
-- BMI 計算式の意味
-- ================================================
--
-- BMI は一般に次の式で定義される。
--
--   BMI = 体重(kg) / 身長(m)^2
--
-- 今回のデータでは height が cm 単位なので、
--   height / 100
-- で m に変換している。
--
-- そのため SQL の式は
--
--   weight / POWER(height / 100, 2)
--
-- となる。
--
-- ここを順に分解すると、
--
-- 1. height / 100
--    → 身長を cm から m に変換する
--
-- 2. POWER(height / 100, 2)
--    → 身長(m)の2乗を計算する
--
-- 3. weight / ...
--    → 体重をその2乗値で割って BMI を求める
--
-- つまり今回のSQLは、
--   BMI の定義式そのものを
--   何度も CASE の中で書いている。
--
-- そのためロジック上は正しいが、
--   同じ式が繰り返し登場する
-- 点が特徴でもある。


