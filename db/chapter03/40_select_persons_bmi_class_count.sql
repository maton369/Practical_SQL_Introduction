-- したがって全体としては
--   やせ
--   標準
--   肥満
-- のいずれかに各人物が割り当てられる。


-- ================================================
-- 図解: BMI 区分パーティション
-- ================================================
--
--   Persons 全体
--      |
--      v
--   各行について BMI を計算
--      |
--      v
--   BMI 値を CASE で区分
--      |
--      +------------------------------------------+
--      | BMI < 18.5           -> やせ             |
--      | 18.5 <= BMI < 25     -> 標準             |
--      | 25 <= BMI            -> 肥満             |
--      +------------------------------------------+
--      |
--      v
--   論理的に3つのパーティションへ分割
--      |
--      +-------------------+-------------------+-------------------+
--      |                   |                   |
--      v                   v                   v
--    やせ               標準                肥満
--      |                   |                   |
--      v                   v                   v
--   COUNT(*)            COUNT(*)            COUNT(*)
--      |                   |                   |
--      +-------------------+-------------------+
--                          |
--                          v
--                   最終結果を返す
--
-- つまり、
--   人物全体を BMI 区分という基準で分割し、
--   その分割単位ごとに人数を集計している。


-- ================================================
-- なぜ CASE を2回書くのか
-- ================================================
--
-- このSQLでは、
--   SELECT 句
--   GROUP BY 句
-- の両方に同じ CASE 式が書かれている。
--
-- これは、
--   表示したい BMI 区分
-- と
--   実際に集計したい BMI 区分
-- を一致させるためである。
--
-- もし
--   SELECT では別の分類
--   GROUP BY では別の分類
-- になってしまうと、
--   出力ラベルと集計単位が食い違う。
--
-- したがって同じ CASE を繰り返すこと自体には意味がある。
--
-- ただし可読性の面では、
--   同じ BMI 計算式が何度も出てくる
-- ため、
--   サブクエリや CTE で先に bmi_value や bmi_class を作る
-- 書き方の方が見やすいことも多い。


-- ================================================
-- パーティションとして見るとどうなるか
-- ================================================
--
-- 今回も age 区分と同様に、
--   元の Persons 全体を
--   BMI 区分ごとの部分集合に分けている
-- と考えることができる。
--
-- つまり論理的には
--
--   やせパーティション
--   標準パーティション
--   肥満パーティション
--
-- を作って、その人数を数えている。
--
-- ここでいうパーティションは、
--   テーブルの物理パーティション機能
-- ではなく、
--   分類による論理的な分割単位
-- のことである。
--
-- したがって今回のSQLは、
--   「BMI による人物集合のパーティション化」
--   ＋
--   「各パーティションの件数集計」
-- と捉えると分かりやすい。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   height と weight という2つの元データから
--   BMI という新しい指標を計算し、
--   その計算結果を使って分類している
-- 点にある。
--
-- つまり、
--   元からある列の値をそのまま区切る
-- のではなく、
--   計算によって作られた派生指標を分類軸にしている。
--
-- これは分析SQLで非常に重要な発想であり、
--   売上単価を計算して価格帯分類する
--   利益率を計算して健全度分類する
--   BMI を計算して体重区分する
-- のような処理の基本になる。
--
-- 今回はその典型例として、
--   計算列 BMI → 区分ラベル → 集計
-- の流れを実現している。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT CASE ... END AS bmi, COUNT(*)
--   → BMI 区分ごとの人数を返す
--
-- weight / POWER(height / 100, 2)
--   → BMI を計算する
--
-- WHEN ... THEN 'やせ'
--   → BMI < 18.5 をやせに分類する
--
-- WHEN ... THEN '標準'
--   → 18.5 <= BMI < 25 を標準に分類する
--
-- WHEN ... THEN '肥満'
--   → BMI >= 25 を肥満に分類する
--
-- FROM Persons
--   → 元データは人物表
--
-- GROUP BY CASE ... END
--   → 同じ BMI 区分ごとにグループ化する
--
-- COUNT(*)
--   → 各 BMI 区分に属する人数を数える
--
-- ;
--   → SQL文の終了を表す


