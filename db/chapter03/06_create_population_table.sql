-- ================================================
-- ファイル名: 06_create_population_table.sql
-- 役割:
--   都道府県ごとの男女別人口を管理する Population テーブルを作成する。
--
-- このSQLの目的:
--   後続の UNION による条件分岐や集約処理を試すための、
--   最小限の人口データ表を定義すること。
--
-- 学習上の主題:
--   CREATE TABLE による表定義、
--   主キー (prefecture, sex) の意味、
--   1つの都道府県について男女を別行で持つ設計を理解すること。
--
-- 今回の重要点:
--   この表は
--     1都道府県 = 1行
--   ではなく、
--     1都道府県 × 1性別 = 1行
--   の構造になっている。
--
--   そのため、後で
--     男性人口列
--     女性人口列
--   に横持ち変換したいときに、
--   UNION や CASE、集約を使う余地が生まれる。
-- ================================================

CREATE TABLE Population
(
    prefecture VARCHAR(32),
    -- prefecture:
    --   都道府県名を表す列である。
    --
    --   例:
    --     徳島
    --     香川
    --     愛媛
    --     高知
    --     福岡

    sex CHAR(1),
    -- sex:
    --   性別区分を表す列である。
    --
    --   今回のデータでは
    --     '1' = 男性
    --     '2' = 女性
    --   として扱っている。
    --
    --   数値ではなく文字として保存しているため、
    --   比較時には
    --     sex = '1'
    --     sex = '2'
    --   のようにクォート付きで書く。

    pop INTEGER,
    -- pop:
    --   その都道府県・その性別に対応する人口を表す整数列である。
    --
    --   例:
    --     徳島, 男性, 60
    --     徳島, 女性, 40

    CONSTRAINT pk_pop PRIMARY KEY(prefecture, sex)
    -- PRIMARY KEY(prefecture, sex):
    --   複合主キーである。
    --
    --   これは
    --     同じ都道府県・同じ性別
    --   の組み合わせを 2 回登録できないことを意味する。
    --
    --   したがって、
    --     徳島, '1'
    --     徳島, '2'
    --   は両方登録できるが、
    --     徳島, '1'
    --   を 2 回は登録できない。
);