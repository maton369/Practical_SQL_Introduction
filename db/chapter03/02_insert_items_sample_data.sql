-- ================================================
-- データの見方
-- ================================================
--
-- この表には 3 種類の商品があり、
-- 各商品について 2000年〜2003年の 4 年分の価格が入っている。
--
-- 商品ごとに整理すると次のようになる。
--
-- 1. カップ (item_id = 100)
--    2000: 税抜 500 / 税込 525
--    2001: 税抜 520 / 税込 546
--    2002: 税抜 600 / 税込 630
--    2003: 税抜 600 / 税込 630
--
-- 2. スプーン (item_id = 101)
--    2000: 税抜 500 / 税込 525
--    2001: 税抜 500 / 税込 525
--    2002: 税抜 500 / 税込 525
--    2003: 税抜 500 / 税込 525
--
-- 3. ナイフ (item_id = 102)
--    2000: 税抜 600 / 税込 630
--    2001: 税抜 550 / 税込 577
--    2002: 税抜 550 / 税込 577
--    2003: 税抜 400 / 税込 420
--
-- つまりこのデータは、
--   商品ごとの価格変動
--   税抜価格と税込価格の両方
-- を持つ教材用データになっている。


-- ================================================
-- 後続SQLとの関係
-- ================================================
--
-- 後で使う UNION ALL の SQL では、
--
--   2001年以前 -> 税抜価格 price_tax_ex を使う
--   2002年以降 -> 税込価格 price_tax_in を使う
--
-- という条件分岐を行う。
--
-- そのため、このデータは
--   同じ商品でも年によってどの価格列を採用するか変える
-- という題材にちょうどよい構成になっている。