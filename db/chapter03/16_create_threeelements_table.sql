-- ================================================
-- ファイル名: 16_create_threeelements_table.sql
-- 役割:
--   3組の (date, flg) を1行の中に持つ ThreeElements テーブルを作成する。
--
-- このSQLの目的:
--   後続で
--     (date_1, flg_1)
--     (date_2, flg_2)
--     (date_3, flg_3)
-- のいずれかが条件に一致する行を検索するためのサンプル表を定義すること。
--
-- 学習上の主題:
--   複数の「似た意味の列群」を1行に持つテーブルに対して、
--   UNION が有効になりやすいケースを理解すること。
--
-- 今回の最重要ポイント:
--   この表は正規化された「縦持ち」ではなく、
--   3組の属性が横並びに入っている。
--
--   そのため、
--     date_1 と flg_1 の組
--     date_2 と flg_2 の組
--     date_3 と flg_3 の組
-- をそれぞれ独立に検索したいとき、
--   OR で1本に書く方法と、
--   UNION で3本に分ける方法の比較が重要になる。
-- ================================================

CREATE TABLE ThreeElements
(
    `key` CHAR(8),
    -- key:
    --   1行を識別する主キー列である。
    --
    --   key はSQLの文脈によっては予約語的に見えることがあるため、
    --   実務ではバッククォートで囲む書き方もよく使われる。
    --
    --   今回は教材の表記に合わせて key という列名を使っている。

    name VARCHAR(32),
    -- name:
    --   行のラベルとなる名前列である。
    --
    --   例:
    --     a
    --     b
    --     c
    --   などが入る。

    date_1 DATE,
    flg_1  CHAR(1),
    -- date_1, flg_1:
    --   1つ目の要素ペアである。
    --
    --   たとえば
    --     date_1 = '2013-11-01'
    --     flg_1  = 'T'
    -- のように、日付とフラグの組として使う。

    date_2 DATE,
    flg_2  CHAR(1),
    -- date_2, flg_2:
    --   2つ目の要素ペアである。

    date_3 DATE,
    flg_3  CHAR(1),
    -- date_3, flg_3:
    --   3つ目の要素ペアである。

    PRIMARY KEY(`key`)
    -- PRIMARY KEY(key):
    --   各行を一意に識別する主キーである。
    --
    --   今回の UNION 検索では、
    --   同じ行が複数条件に同時一致する可能性も理屈上はありうるが、
    --   最終的に返る行は key によって一意に識別される。
);