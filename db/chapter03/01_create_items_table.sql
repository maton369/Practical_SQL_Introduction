-- ================================================
-- ファイル名: 01_create_items_table.sql
-- 役割:
--   商品テーブル Items を作成する。
--
-- このSQLの目的:
--   年ごとの商品価格を管理するための表を用意すること。
--
-- 学習上の主題:
--   CREATE TABLE による表定義、
--   各列の意味、
--   複合主キー (item_id, year) の意味を理解すること。
--
-- 今回の重要点:
--   この表は「商品マスタ」ではなく、
--   「商品 × 年」の価格履歴表になっている。
--
--   つまり 1 商品 1 行ではなく、
--   同じ商品でも年が違えば別行として管理する。
-- ================================================

CREATE TABLE Items
(
    item_id INTEGER NOT NULL,
    -- item_id:
    --   商品を識別するためのIDである。
    --
    --   たとえば
    --     100 = カップ
    --     101 = スプーン
    --     102 = ナイフ
    --   のように商品種別を表す。
    --
    --   NOT NULL が付いているため、必ず値が必要である。

    year INTEGER NOT NULL,
    -- year:
    --   その価格がどの年のものかを表す。
    --
    --   このテーブルでは同じ商品でも年ごとに価格を持つため、
    --   year は非常に重要な列である。
    --
    --   たとえば item_id = 100 でも、
    --   2000年と2001年では別行になる。

    item_name CHAR(32) NOT NULL,
    -- item_name:
    --   商品名を表す文字列列である。
    --
    --   今回は
    --     カップ
    --     スプーン
    --     ナイフ
    --   などが入る。
    --
    --   CHAR(32) は固定長文字列型である。
    --   教材では単純化のためこの型を使っている。

    price_tax_ex INTEGER NOT NULL,
    -- price_tax_ex:
    --   税抜価格を表す整数列である。
    --
    --   例:
    --     500
    --     520
    --     600
    --
    --   今回の UNION ALL の条件分岐では、
    --   2001年以前にこの列が使われる。

    price_tax_in INTEGER NOT NULL,
    -- price_tax_in:
    --   税込価格を表す整数列である。
    --
    --   例:
    --     525
    --     546
    --     630
    --
    --   今回の UNION ALL の条件分岐では、
    --   2002年以降にこの列が使われる。

    PRIMARY KEY (item_id, year)
    -- PRIMARY KEY (item_id, year):
    --   複合主キーである。
    --
    --   これは
    --     item_id だけ
    --   でもなく
    --     year だけ
    --   でもなく、
    --   2列の組み合わせで一意であることを意味する。
    --
    --   したがって、
    --     同じ商品IDで同じ年
    --   の行は 2 回登録できない。
    --
    --   一方で、
    --     同じ item_id でも year が違えば登録可能
    --   であり、
    --     同じ year でも item_id が違えば登録可能
    --   である。
    --
    --   この設計により、
    --   商品ごとの年次価格履歴を自然に表現できる。
);