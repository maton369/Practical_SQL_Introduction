-- ================================================
-- ファイル名: 41_select_persons_rank_by_age_class.sql
-- 役割:
--   Persons テーブルの各人物について年齢区分を作り、
--   その区分ごとの中で年齢順位を付ける。
--
-- このSQLの目的:
--   人物を
--     子供
--     成人
--     老人
-- の年齢区分に分けたうえで、
--   各区分の中で age の小さい順に順位を求めること。
--
-- 学習上の主題:
--   CASE 式による分類、
--   ウィンドウ関数 RANK()、
--   そして主に PARTITION BY に式を入れる意味を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは GROUP BY と違って、
--   元の各人物の行を消さない。
--
--   その代わり、
--   PARTITION BY によって
--     子供パーティション
--     成人パーティション
--     老人パーティション
-- を論理的に作り、
--   そのパーティションの中だけで順位計算を行っている。
--
--   つまりアルゴリズムとしては
--     1. 各人物を年齢区分に分類する
--     2. 同じ年齢区分ごとにパーティションを作る
--     3. 各パーティションの中で age の昇順に順位を付ける
--     4. ただし元の各行は残したまま返す
--   という流れである。
-- ================================================

SELECT
    name,
    -- name:
    --   人物名をそのまま返す。
    --
    --   今回は GROUP BY ではないので、
    --   各人物の行は消えずにそのまま残る。
    --   ここが集約SQLとの大きな違いである。

    age,
    -- age:
    --   各人物の年齢である。
    --
    --   今回は
    --     1. 年齢区分を作るため
    --     2. パーティション内順位を付けるため
    -- に使っている。

    CASE
        WHEN age < 20 THEN '子供'
        -- age < 20:
        --   20歳未満は「子供」に分類する。

        WHEN age BETWEEN 20 AND 69 THEN '成人'
        -- age BETWEEN 20 AND 69:
        --   20歳以上69歳以下は「成人」に分類する。
        --
        --   BETWEEN は両端を含むので、
        --     20歳
        --     69歳
        -- も成人に含まれる。

        WHEN age >= 70 THEN '老人'
        -- age >= 70:
        --   70歳以上は「老人」に分類する。

        ELSE NULL
        -- ELSE NULL:
        --   上のどの条件にも当てはまらない場合は NULL を返す。
        --
        --   今回の条件は通常の年齢をほぼすべてカバーしているため、
        --   サンプルデータでは通常ここには来ない。
    END AS age_class,
    -- AS age_class:
    --   CASE 式の結果に age_class という列名を付ける。
    --
    --   この列は
    --     子供
    --     成人
    --     老人
    -- のような分類ラベルを表す。
    --
    --   重要なのは、
    --   この分類ラベルが後ろの PARTITION BY にも使われていることだ。
    --
    --   つまりこの age_class は単なる表示用ではなく、
    --   ウィンドウ関数の計算単位を決める意味も持っている。

    RANK() OVER (
        PARTITION BY
            CASE
                WHEN age < 20 THEN '子供'
                WHEN age BETWEEN 20 AND 69 THEN '成人'
                WHEN age >= 70 THEN '老人'
                ELSE NULL
            END
        -- PARTITION BY CASE ... END:
        --   ここが今回の主題である。
        --
        --   PARTITION BY は、
        --   ウィンドウ関数の計算対象を
        --   「どの単位で区切るか」
        --   を指定する。
        --
        --   今回は列名そのものではなく CASE 式を入れている。
        --   つまり
        --     age を見て
        --     子供 / 成人 / 老人
        --   に分類した結果を、そのままパーティションキーにしている。
        --
        --   これにより Persons 全体は、
        --     子供パーティション
        --     成人パーティション
        --     老人パーティション
        -- に論理的に分割される。
        --
        --   とても重要なのは、
        --   PARTITION BY は GROUP BY と違って
        --   行を1行に潰さない点である。
        --
        --   GROUP BY:
        --     グループごとに1行へまとめる
        --
        --   PARTITION BY:
        --     グループ分けはするが各行は残したまま
        --     そのグループ内でウィンドウ関数を計算する
        --
        --   つまり今回の SQL は、
        --   「年齢区分ごとに人物をまとめる」が、
        --   「各人物の行はそのまま残す」
        --   という処理になっている。
        --
        --   これが PARTITION BY の本質である。

        ORDER BY age
        -- ORDER BY age:
        --   各パーティションの中で、年齢の小さい順に並べて順位を付ける。
        --
        --   つまり
        --     成人パーティションの中だけで age 昇順
        --     老人パーティションの中だけで age 昇順
        --     子供パーティションの中だけで age 昇順
        -- を行う。
        --
        --   ここでの ORDER BY は
        --   全体の表示順ではなく、
        --   ウィンドウ関数が順位を決めるための並び順である点に注意する。
    ) AS age_rank_in_class
    -- RANK() OVER (...):
    --   指定したパーティションの中で順位を付けるウィンドウ関数である。
    --
    --   今回は
    --     同じ age_class の中で
    --     age の小さい順に
    --   順位を付けている。
    --
    --   たとえば成人パーティションだけを見たとき、
    --     21歳
    --     25歳
    --     30歳
    --     30歳
    --     39歳
    --     54歳
    -- の順に並ぶ。
    --
    --   このとき RANK() は同順位を許すので、
    --   同じ年齢があれば同じ順位になり、
    --   次の順位は飛ぶ。
    --
    --   例:
    --     21歳 -> 1位
    --     25歳 -> 2位
    --     30歳 -> 3位
    --     30歳 -> 3位
    --     39歳 -> 5位
    --
    --   つまり RANK() は
    --   「同値なら同順位、次順位は飛ぶ」
    --   ルールである。
    --
    -- AS age_rank_in_class:
    --   「年齢区分内の順位」という意味の列名である。

FROM
    Persons
    -- FROM Persons:
    --   元データの人物表である。
    --
    --   この表全体を、
    --   PARTITION BY の CASE 式によって
    --   論理的に複数パーティションへ分ける。

ORDER BY
    age_class,
    age_rank_in_class;
    -- ORDER BY age_class, age_rank_in_class:
    --   最終結果の表示順を整えるための並び替えである。
    --
    --   まず age_class ごとにまとめ、
    --   その中で age_rank_in_class の小さい順に並べる。
    --
    --   これにより、
    --   子供グループ
    --   成人グループ
    --   老人グループ
    -- の順にまとまりやすく、
    --   各グループ内の順位も見やすくなる。
    --
    --   ここでの ORDER BY は、
    --   さきほどの OVER 句の中の ORDER BY と役割が違う。
    --
    --   OVER(...) の ORDER BY:
    --     順位計算の基準を決める
    --
    --   最後の ORDER BY:
    --     結果の表示順を決める
    --
    --   この2つは意味が異なるので、混同しないことが大切である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Persons テーブルの各人物を1行ずつ読む
-- 2. 各人物の age から
--      子供
--      成人
--      老人
--    の age_class を計算する
-- 3. 同じ age_class を持つ人物同士でパーティションを作る
-- 4. 各パーティションの中で age 昇順に並べる
-- 5. RANK() によって順位を付ける
-- 6. 各人物の行を残したまま、
--      name
--      age
--      age_class
--      age_rank_in_class
--    を返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in Persons:
--       row.age_class = classify(row.age)
--
--   partitions = split rows by age_class
--
--   for each partition:
--       sort by age ascending
--       assign rank with ties
--
--   output all rows
--
-- つまりこれは、
--   「区分ごとに分けて、その区分内だけで順位付けし、
--     ただし元の行は残す」
--   ウィンドウ関数アルゴリズムである。


-- ================================================
-- PARTITION BY の意味
-- ================================================
--
-- PARTITION BY は、
--   ウィンドウ関数の計算範囲を分けるための指定である。
--
-- たとえば今回、
--   PARTITION BY が無ければ
--   Persons 全体で年齢順位を付けることになる。
--
-- その場合は
--   12歳, 21歳, 25歳, 30歳, ...
-- のように全人物を1本に並べて順位付けする。
--
-- しかし今回やりたいのは、
--   子供の中での順位
--   成人の中での順位
--   老人の中での順位
-- である。
--
-- そこで PARTITION BY CASE ... END を使って、
--   年齢区分ごとに計算範囲を分けている。
--
-- つまり今回の PARTITION BY は、
--   「順位を付けるリングを3つに分ける」
--   ような役割を持っている。
--
--   子供リングの中では子供だけで順位を付ける
--   成人リングの中では成人だけで順位を付ける
--   老人リングの中では老人だけで順位を付ける
--
-- こう考えると分かりやすい。


-- ================================================
-- GROUP BY と PARTITION BY の違い
-- ================================================
--
-- 今回の理解で最も大事なのはここである。
--
-- 1. GROUP BY
--   - 行をグループ化する
--   - 各グループを1行へ集約する
--   - 例: 子供 1人, 成人 6人, 老人 2人
--
-- 2. PARTITION BY
--   - 行をグループのように分ける
--   - ただし各行は残す
--   - その分けた単位ごとにウィンドウ関数を計算する
--
-- 今回の SQL では、
--   Darwin の行も
--   Anderson の行も
--   Bates の行も
-- 消えない。
--
-- その代わり、それぞれが
--   子供パーティション
--   成人パーティション
--   老人パーティション
-- のどこかに所属し、
-- その所属先の中で順位が付いている。
--
-- つまり
--   GROUP BY は集約して潰す
--   PARTITION BY は分けるが残す
-- という違いがある。
--
-- ここが PARTITION BY の核心である。


-- ================================================
-- このSQLでの実データイメージ
-- ================================================
--
-- Persons のサンプル年齢は次の通りである。
--
--   Anderson  30
--   Adela     21
--   Bates     87
--   Becky     54
--   Bill      39
--   Chris     90
--   Darwin    12
--   Dawson    25
--   Donald    30
--
-- これを年齢区分で分けると、
--
--   子供:
--     Darwin 12
--
--   成人:
--     Adela    21
--     Dawson   25
--     Anderson 30
--     Donald   30
--     Bill     39
--     Becky    54
--
--   老人:
--     Bates 87
--     Chris 90
--
-- となる。
--
-- ここで各パーティション内で RANK() を付けると、
--
--   子供:
--     Darwin 12 -> 1位
--
--   成人:
--     Adela    21 -> 1位
--     Dawson   25 -> 2位
--     Anderson 30 -> 3位
--     Donald   30 -> 3位
--     Bill     39 -> 5位
--     Becky    54 -> 6位
--
--   老人:
--     Bates 87 -> 1位
--     Chris 90 -> 2位
--
-- のようになる。
--
-- つまり age_rank_in_class は、
--   「その人物が、自分の年齢区分の中で何番目に若いか」
-- を表している。


-- ================================================
-- 図解: PARTITION BY に式を入れた順位付け
-- ================================================
--
--   Persons 全体
--      |
--      v
--   CASE で age_class を作る
--      |
--      +-----------------------------------------+
--      | age < 20           -> 子供              |
--      | 20 <= age <= 69    -> 成人              |
--      | age >= 70          -> 老人              |
--      +-----------------------------------------+
--      |
--      v
--   PARTITION BY age_class
--      |
--      +-------------------+-------------------+-------------------+
--      |                   |                   |
--      v                   v                   v
--    子供               成人                老人
--      |                   |                   |
--      v                   v                   v
--   ORDER BY age        ORDER BY age        ORDER BY age
--      |                   |                   |
--      v                   v                   v
--   RANK() 計算         RANK() 計算         RANK() 計算
--      |                   |                   |
--      +-------------------+-------------------+
--                          |
--                          v
--                   各行を残したまま返す
--
-- つまり、
--   年齢区分ごとに別々の順位表を作っている
-- が、
--   最後には全人物の行を返している。


-- ================================================
-- ORDER BY が2か所ある意味
-- ================================================
--
-- このSQLには ORDER BY が2か所ある。
--
-- 1. OVER(...) の中の ORDER BY age
--   → パーティション内順位を決めるための並び順
--
-- 2. 最後の ORDER BY age_class, age_rank_in_class
--   → 結果セット全体の表示順を整えるための並び順
--
-- この2つはまったく役割が異なる。
--
-- たとえば最後の ORDER BY を消しても、
--   RANK() 自体の計算ルールは変わらない。
--
-- 一方、OVER(...) の ORDER BY age を変えると、
--   順位そのものが変わる。
--
-- したがって
--   「どの順で順位を決めるか」
-- と
--   「結果をどう並べて見せるか」
-- は別物である。
--
-- ここも非常に重要な学習ポイントである。


-- ================================================
-- なぜ PARTITION BY に式を入れるのか
-- ================================================
--
-- PARTITION BY には、列名だけでなく式も入れられる。
--
-- 今回はその典型例で、
--   age という生データから
--   年齢区分ラベルを CASE で作り、
--   その式結果をそのままパーティションキーにしている。
--
-- つまり
--   先に age_class 列をテーブルに持っていなくても、
--   その場で計算した区分でパーティション分けできる
-- ということだ。
--
-- これは非常に柔軟で、
--   月別
--   価格帯別
--   売上ランク別
-- など、計算で作った分類キーごとに
--   パーティション化 + ウィンドウ計算
-- を行える。
--
-- 今回はその基本例である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT name, age, CASE ... END AS age_class
--   → 各人物の名前・年齢・年齢区分を返す
--
-- RANK() OVER (...)
--   → 指定したパーティション内で順位を付ける
--
-- PARTITION BY CASE ... END
--   → 年齢区分ごとに計算範囲を分ける
--
-- ORDER BY age
--   → 各パーティション内で年齢昇順に順位を付ける
--
-- AS age_rank_in_class
--   → 年齢区分内順位という列名を付ける
--
-- FROM Persons
--   → 元データは人物表
--
-- ORDER BY age_class, age_rank_in_class
--   → 最終表示を年齢区分順・順位順で見やすく並べる
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   このSQLでは CASE 式が
--     SELECT 句
--     PARTITION BY 句
-- の両方に書かれている。
--
--   実務では可読性のために、
--   先に age_class を作る CTE やサブクエリを使うことも多い。
-- ================================================


