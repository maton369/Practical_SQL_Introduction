-- ================================================
-- ファイル名: 39_select_persons_age_partition_count.sql
-- 役割:
--   Persons テーブルの人物を年齢帯ごとに区分し、
--   その区分ごとの人数を集計する。
--
-- このSQLの目的:
--   各人物を
--     子供
--     成人
--     老人
-- のどれかに分類し、
--   各分類に何人属するかを数えること。
--
-- 学習上の主題:
--   CASE 式による区分、
--   GROUP BY による分類単位の集約、
--   そして主に「パーティション的な考え方」
--   を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLはウィンドウ関数の PARTITION BY を使ってはいないが、
--   発想としては
--     「表全体を条件に応じて複数の箱へ分ける」
--   という意味で、
--   非常にパーティション的である。
--
--   つまり今回は、
--   Persons 全体を
--     子供パーティション
--     成人パーティション
--     老人パーティション
--   に分割し、
--   その各パーティションの人数を数えている
--   と理解すると分かりやすい。
--
--   ここでいう「パーティション」は
--   物理的なテーブルパーティション機能ではなく、
--   論理的なグループ分割の意味である。
-- ================================================

SELECT
    CASE
        WHEN age < 20 THEN '子供'
        -- age < 20:
        --   年齢が 20 未満なら「子供」に分類する。
        --
        --   例:
        --     Darwin = 12歳
        --   はこの条件に入る。
        --
        --   ここで行われているのは、
        --   元の数値 age をそのまま使うのではなく、
        --   年齢帯ラベルへ変換する処理である。

        WHEN age BETWEEN 20 AND 69 THEN '成人'
        -- age BETWEEN 20 AND 69:
        --   20歳以上69歳以下なら「成人」に分類する。
        --
        --   BETWEEN は両端を含むので、
        --     20
        --     69
        -- も成人に含まれる。
        --
        --   例:
        --     Adela    = 21歳
        --     Anderson = 30歳
        --     Becky    = 54歳
        --   などはここに入る。

        WHEN age >= 70 THEN '老人'
        -- age >= 70:
        --   70歳以上なら「老人」に分類する。
        --
        --   例:
        --     Bates = 87歳
        --     Chris = 90歳
        --   はこの条件に入る。

        ELSE NULL
        -- ELSE NULL:
        --   上のどの条件にも一致しない場合は NULL を返す。
        --
        --   今回の条件は
        --     age < 20
        --     20～69
        --     70以上
        -- で全年齢を論理上すべてカバーしているため、
        --   通常の正の年齢データでは NULL にはなりにくい。
        --
        --   ただし CASE 式としては、
        --   「分類不能な値が来たときの逃げ道」を残している。
    END AS age_class,
    -- AS age_class:
    --   CASE 式の結果列に age_class という名前を付ける。
    --
    --   最終結果では
    --     子供
    --     成人
    --     老人
    -- のような分類ラベルがこの列に入る。
    --
    --   つまりこの age_class 列は、
    --   age という生データから作られた
    --   「年齢帯パーティション名」
    -- と考えると分かりやすい。

    COUNT(*)
    -- COUNT(*):
    --   各グループに含まれる人数を数える。
    --
    --   ここでのグループとは、
    --   下の GROUP BY で分けられた
    --     子供
    --     成人
    --     老人
    -- の各年齢区分である。
    --
    --   したがって、
    --   これは各年齢パーティションの人数を数えていることになる。

FROM
    Persons
    -- FROM Persons:
    --   元データの人物表である。
    --
    --   この時点では1人1行であり、
    --   年齢区分はまだ付いていない。
    --
    --   この表全体をこれから CASE で区分し、
    --   論理的に複数のパーティションへ分ける。

GROUP BY
    CASE
        WHEN age < 20 THEN '子供'
        WHEN age BETWEEN 20 AND 69 THEN '成人'
        WHEN age >= 70 THEN '老人'
        ELSE NULL
    END;
    -- GROUP BY CASE ... END:
    --   SELECT 句で作った年齢区分と同じ式を使って、
    --   行をグループ化している。
    --
    --   つまり各人物は
    --     子供グループ
    --     成人グループ
    --     老人グループ
    -- のいずれかに振り分けられる。
    --
    --   ここが今回の「パーティション的な発想」の中心である。
    --
    --   表全体を
    --     年齢区分ごとの部分集合
    --   に分割し、
    --   その分割単位ごとに COUNT(*) を実行している。
    --
    --   したがってこの GROUP BY は、
    --   論理的には
    --     PARTITION BY age_class
    -- のような感覚で
    --   人々を年齢帯パーティションへ入れている
    -- と理解するとよい。
    --
    --   ただし厳密には、
    --   これはウィンドウ関数の PARTITION BY ではなく、
    --   GROUP BY による集約グループ化である。
    --
    --   両者の違いは、
    --     GROUP BY は1グループ1行に潰す
    --     PARTITION BY は行を残したままパーティション単位で計算する
    -- ところにある。
    --
    --   今回は結果として
    --     子供
    --     成人
    --     老人
    -- ごとに1行ずつ返したいので、
    --   GROUP BY が適切である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Persons テーブルの各行を1つずつ見る
-- 2. 各人物の age を CASE 式で
--      子供
--      成人
--      老人
--    のいずれかへ分類する
-- 3. 同じ分類ラベルを持つ人物同士をまとめる
-- 4. 各分類グループの人数を COUNT(*) で数える
-- 5. 年齢区分と人数の組を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = {
--       '子供': 0,
--       '成人': 0,
--       '老人': 0
--   }
--
--   for each row in Persons:
--       if row.age < 20:
--           groups['子供'] += 1
--       elif 20 <= row.age <= 69:
--           groups['成人'] += 1
--       else:
--           groups['老人'] += 1
--
--   output groups
--
-- つまりこれは、
--   「人物全体を年齢帯ごとのパーティションへ振り分け、
--     各パーティションの件数を数える」
--   アルゴリズムである。


-- ================================================
-- パーティションとして見るとどうなるか
-- ================================================
--
-- 今回の人物データは次の通りである。
--
--   Anderson  30
--   Adela     21
--   Bates     87
--   Becky     54
--   Bill      39
--   Chris     90
--   Darwin    12
--   Dawson    25
--   Donald    30
--
-- これを年齢帯でパーティション分割すると、
--
--   子供パーティション:
--     Darwin
--
--   成人パーティション:
--     Anderson
--     Adela
--     Becky
--     Bill
--     Dawson
--     Donald
--
--   老人パーティション:
--     Bates
--     Chris
--
-- となる。
--
-- つまり元の Persons という1つの集合を、
--   年齢条件に基づいて
--   3つの部分集合へ切っている。
--
-- これが今回の「パーティション」の意味である。
--
-- したがって人数は、
--
--   子供 -> 1
--   成人 -> 6
--   老人 -> 2
--
-- となる。


-- ================================================
-- 図解: 年齢によるパーティション分割
-- ================================================
--
--   Persons 全体
--      |
--      v
--   各行の age を見る
--      |
--      +-----------------------------------------+
--      | age < 20           -> 子供              |
--      | 20 <= age <= 69    -> 成人              |
--      | age >= 70          -> 老人              |
--      +-----------------------------------------+
--      |
--      v
--   論理的に3つのパーティションへ分割
--      |
--      +-------------------+-------------------+-------------------+
--      |                   |                   |
--      v                   v                   v
--    子供               成人                老人
--      |                   |                   |
--      v                   v                   v
--   COUNT(*)            COUNT(*)            COUNT(*)
--      |                   |                   |
--      +-------------------+-------------------+
--                          |
--                          v
--                   最終結果を返す
--
-- つまり、
--   「条件によって表全体を分割し、
--     分割後の各集合ごとに人数を集計する」
-- のが今回の処理である。


-- ================================================
-- GROUP BY と PARTITION BY の違い
-- ================================================
--
-- 今回の主題が「パーティション」なので、
-- GROUP BY と PARTITION BY の違いを整理しておくとよい。
--
-- 1. GROUP BY
--   - 行をグループ化する
--   - 各グループを1行にまとめる
--   - 今回のSQLはこちら
--
-- 2. PARTITION BY
--   - 行をパーティションに分ける
--   - ただし元の行は消さない
--   - ウィンドウ関数と一緒に使う
--
-- たとえば今回、
--   年齢区分ごとの人数を出したいだけ
-- なら GROUP BY が適している。
--
-- もし
--   各人物の行を残したまま
--   その人物が属する年齢区分の総人数も横に出したい
-- なら、
--   COUNT(*) OVER (PARTITION BY age_class)
-- のような発想になる。
--
-- つまり今回は
--   パーティション的な分類
-- をしているが、
--   実装手段は GROUP BY
-- である。
--
-- ここはとても大事である。


-- ================================================
-- なぜ同じ CASE 式を2回書くのか
-- ================================================
--
-- このSQLでは
--   SELECT 句
--   GROUP BY 句
-- の両方に同じ CASE 式が書かれている。
--
-- これは、
--   出力したい分類ラベル
-- と
--   実際にグループ分けする条件
-- を一致させるためである。
--
-- つまり
--   表示上の区分
-- と
--   集計上の区分
-- を同じロジックにしている。
--
-- もしここがずれていると、
--   表示ラベルと集計単位が食い違う
-- ため、不正な結果になる。
--
-- したがって同じ CASE を書くこと自体に意味がある。
--
-- 実務では可読性のために
--   サブクエリや CTE で age_class を先に作る
-- 書き方もよく行われる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- データを年齢区分で分けると、
--
--   子供:
--     Darwin
--     → 1人
--
--   成人:
--     Anderson
--     Adela
--     Becky
--     Bill
--     Dawson
--     Donald
--     → 6人
--
--   老人:
--     Bates
--     Chris
--     → 2人
--
-- したがって最終結果は概ね次のようになる。
--
--   age_class | COUNT(*)
--   ----------+---------
--   子供      | 1
--   成人      | 6
--   老人      | 2
--
-- つまり最終結果は、
--   人物一覧
-- ではなく
--   年齢帯パーティションごとの人数分布
-- である。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   age という連続値
-- を
--   子供 / 成人 / 老人
-- という離散的な区分ラベルへ写像し、
--   その区分ラベルで集計している
-- ところにある。
--
-- つまり、
--   生データのまま集計しているのではなく、
--   分類関数を通してから集計している
-- のである。
--
-- この考え方は非常に重要で、
--   売上を価格帯で分ける
--   顧客を年齢帯で分ける
--   商品をカテゴリ帯で分ける
-- など、多くの分析SQLの基本になる。
--
-- 今回はそれを
--   CASE による年齢パーティション化
-- として行っている。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT CASE ... END AS age_class, COUNT(*)
--   → 年齢区分ごとの人数を返す
--
-- CASE
--   → age を子供/成人/老人へ分類する
--
-- WHEN age < 20 THEN '子供'
--   → 20歳未満を子供に分類する
--
-- WHEN age BETWEEN 20 AND 69 THEN '成人'
--   → 20歳以上69歳以下を成人に分類する
--
-- WHEN age >= 70 THEN '老人'
--   → 70歳以上を老人に分類する
--
-- FROM Persons
--   → 元データは人物表
--
-- GROUP BY CASE ... END
--   → 同じ年齢区分ごとにグループ化する
--
-- COUNT(*)
--   → 各年齢区分に属する人数を数える
--
-- ;
--   → SQL文の終了を表す


