-- ================================================
-- ファイル名: 11_create_employees_table.sql
-- 役割:
--   社員がどのチームを兼務しているかを表す Employees テーブルを作成する。
--
-- このSQLの目的:
--   1人の社員が複数チームに所属できる構造を持つ表を定義し、
--   後続の「兼務数に応じた条件分岐」を試せるようにすること。
--
-- 学習上の主題:
--   CREATE TABLE による表定義、
--   「1社員 = 1行」ではなく「1社員 × 1所属チーム = 1行」であること、
--   複合主キー (emp_id, team_id) の意味を理解すること。
--
-- 今回の最重要ポイント:
--   この表は社員マスタそのものではなく、
--   社員とチーム所属の対応表である。
--
--   つまり同じ社員でも、
--   所属チームが複数あれば複数行を持つ。
--
--   この構造があるからこそ、
--   後で COUNT(*) によって
--     1つだけ所属
--     2つ兼務
--     3つ以上兼務
--   を判定できる。
-- ================================================

CREATE TABLE Employees
(
    emp_id CHAR(3) NOT NULL,
    -- emp_id:
    --   社員を識別するIDである。
    --
    --   例:
    --     '201'
    --     '202'
    --     '204'
    --
    --   ただしこの表では、emp_id だけでは主キーになっていない。
    --   なぜなら同じ社員が複数チームに所属できるため、
    --   emp_id 単独では1行を一意に決められないからである。

    team_id INTEGER NOT NULL,
    -- team_id:
    --   チームを識別する番号である。
    --
    --   例:
    --     1 = 商品企画
    --     2 = 開発
    --     3 = 営業
    --     4 = 管理
    --
    --   同じ社員でも team_id が違えば別行になる。

    emp_name CHAR(16) NOT NULL,
    -- emp_name:
    --   社員名を表す列である。
    --
    --   例:
    --     Joe
    --     Jim
    --     Carl
    --     Bree
    --     Kim
    --
    --   教材では名前で GROUP BY しているが、
    --   実務では同名の可能性があるため、
    --   厳密には emp_id ベースで集計する方が安全なことが多い。

    team CHAR(16) NOT NULL,
    -- team:
    --   チーム名を表す列である。
    --
    --   例:
    --     商品企画
    --     開発
    --     営業
    --     管理

    PRIMARY KEY(emp_id, team_id)
    -- PRIMARY KEY(emp_id, team_id):
    --   複合主キーである。
    --
    --   これは
    --     同じ社員が同じチームに二重登録されること
    --   を防ぐ。
    --
    --   一方で、
    --     同じ emp_id でも team_id が違えば別行として登録できる。
    --
    --   つまりこの設計は
    --     「1人の社員が複数チームを兼務できる」
    --   ことを自然に表現している。
);