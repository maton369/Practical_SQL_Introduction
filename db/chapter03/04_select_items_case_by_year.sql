-- ================================================
-- ファイル名: 04_select_items_case_by_year.sql
-- 役割:
--   Items テーブルから商品名・年・価格を取得する際に、
--   年によって参照する価格列を CASE 式で切り替える。
--
-- このSQLの目的:
--   2001年以前は税抜価格 price_tax_ex を使い、
--   2002年以降は税込価格 price_tax_in を使って、
--   1本の SELECT 文で price 列を作ること。
--
-- 学習上の主題:
--   SELECT 句における CASE 式による条件分岐を理解すること。
--
-- 今回の最重要ポイント:
--   先ほどの UNION ALL 版は、
--     1. 前半SELECT
--     2. 後半SELECT
--   を別々に書いて最後に結合していた。
--
--   一方、今回の CASE 版は、
--   1本の SELECT の中で
--   「price に何を入れるか」
--   だけを条件で切り替えている。
--
--   つまり今回は、
--   集合を分けてから結合するのではなく、
--   各行ごとに price の値を決めている。
-- ================================================

SELECT
    item_name,
    -- item_name:
    --   商品名をそのまま表示する。
    --
    --   例:
    --     カップ
    --     スプーン
    --     ナイフ

    year,
    -- year:
    --   その価格がどの年のものかを表す。
    --
    --   今回の CASE 式では、
    --   この year の値を見て
    --   どちらの価格列を使うかを決める。

    CASE
        -- CASE 式:
        --   SQL の式の中で条件分岐を行うための構文である。
        --
        --   MySQL にはストアドプログラム用の CASE statement と、
        --   式として使う CASE operator があるが、
        --   今回 SELECT 句の中で使っているのは
        --   値を返す CASE 式の方である。  [oai_citation:0‡MySQL Developer Zone](https://dev.mysql.com/doc/refman/8.0/ja/flow-control-functions.html?utm_source=chatgpt.com)
        --
        --   つまり今回は、
        --   各行について year を見て
        --   price に入れる値を切り替えている。

        WHEN year <= 2001 THEN price_tax_ex
        -- year <= 2001:
        --   2000年と2001年の行では税抜価格を使う。
        --
        -- THEN price_tax_ex:
        --   条件が真なら、price_tax_ex の値を返す。
        --
        -- したがって
        --   2000年, 2001年 -> 税抜価格
        -- となる。

        WHEN year >= 2002 THEN price_tax_in
        -- year >= 2002:
        --   2002年と2003年の行では税込価格を使う。
        --
        -- THEN price_tax_in:
        --   条件が真なら、price_tax_in の値を返す。
        --
        -- したがって
        --   2002年, 2003年 -> 税込価格
        -- となる。

    END AS price
    -- END:
    --   CASE 式の終わりを表す。
    --
    -- AS price:
    --   CASE 式の結果列に price という名前を付ける。
    --
    --   元の列は
    --     price_tax_ex
    --     price_tax_in
    --   という2本だが、
    --   最終結果ではそれを1本の列 price として見せる。

FROM
    Items;
    -- FROM Items:
    --   Items テーブルを元データとして参照する。
    --
    --   今回は WHERE 句が無いため、
    --   Items の全行が対象になる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Items テーブルの全行を読む
-- 2. 各行について year の値を確認する
-- 3. year <= 2001 なら price_tax_ex を選ぶ
-- 4. year >= 2002 なら price_tax_in を選ぶ
-- 5. 選ばれた値を price 列として返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in Items:
--       if row.year <= 2001:
--           price = row.price_tax_ex
--       elif row.year >= 2002:
--           price = row.price_tax_in
--
--       output row.item_name, row.year, price
--
-- つまりこれは、
--   「各行を1回ずつ見て、その行の year に応じて price を決める」
--   という列値分岐である。


-- ================================================
-- UNION ALL 版との違い
-- ================================================
--
-- 先ほどの UNION ALL 版は概念的に次のような構造だった。
--
--   前半:
--     year <= 2001 の行を取り、
--     price_tax_ex を price として返す
--
--   後半:
--     year >= 2002 の行を取り、
--     price_tax_in を price として返す
--
--   その後:
--     UNION ALL で2つの結果を縦結合する
--
-- これは
--   「集合を前半・後半に分けてから結合する」
-- 書き方である。
--
-- 一方、今回の CASE 版は
--   「行は最初から全部対象にして、
--     各行の price だけを条件で切り替える」
-- 書き方である。
--
-- つまり整理すると、
--
--   UNION ALL 版
--     → SELECT を2本に分ける
--     → 結果集合レベルの分岐
--
--   CASE 版
--     → SELECT は1本
--     → 列値レベルの分岐
--
-- という違いがある。


-- ================================================
-- パフォーマンス比較の考え方
-- ================================================
--
-- ここが今回の中心である。
--
-- 一般論として、この CASE 版は
--   同じテーブル Items に対する 1 本の SELECT
-- であるのに対し、
-- 先ほどの UNION ALL 版は
--   Items を読む 2 つの query block
-- から構成される。MySQL の UNION / set operations は
-- 複数の query block を組み合わせる構文である。  [oai_citation:1‡MySQL Developer Zone](https://dev.mysql.com/doc/en/union.html?utm_source=chatgpt.com)
--
-- そのため、
--   Items を同じ条件分岐のために 2 回読む形になりやすい UNION ALL 版より、
--   1 回の走査の中で列値だけを切り替える CASE 版の方が、
--   単純で有利になりやすい
-- と考えるのが自然である。これは実行計画から推測される一般的な見方である。  [oai_citation:2‡MySQL Developer Zone](https://dev.mysql.com/doc/en/union.html?utm_source=chatgpt.com)
--
-- ただし、実際の速さは
--   インデックス
--   行数
--   統計情報
--   最適化
-- などに依存するため、
--   必ず CASE 版が速い
-- と断定はできない。
--
-- したがって実務では、
--   EXPLAIN
-- で実行計画を確認して比較するのが正しい。EXPLAIN は
-- SELECT を含む文の実行方法を確認するための仕組みである。  [oai_citation:3‡MySQL Developer Zone](https://dev.mysql.com/doc/en/explain-output.html?utm_source=chatgpt.com)
--
-- 今回のように
--   同じ表
--   同じ出力列
--   年での単純な切り替え
-- という条件なら、
--   可読性・保守性・単純さの面でも
--   CASE 版の方が自然
-- であることが多い。これは上の構造差からの妥当な推論である。  [oai_citation:4‡MySQL Developer Zone](https://dev.mysql.com/doc/en/union.html?utm_source=chatgpt.com)


