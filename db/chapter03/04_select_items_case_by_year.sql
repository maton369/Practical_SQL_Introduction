-- ================================================
-- 図解: CASE 版と UNION ALL 版の比較
-- ================================================
--
-- 1. CASE 版
--
--   Items を 1 回読む
--        |
--        v
--   各行の year を判定
--        |
--        +------------------------------+
--        | year <= 2001 なら 税抜価格   |
--        | year >= 2002 なら 税込価格   |
--        +------------------------------+
--        |
--        v
--   item_name, year, price を返す
--
--
-- 2. UNION ALL 版
--
--   Items を読む(前半)
--        |
--        v
--   year <= 2001 の行だけ
--        |
--        v
--   税抜価格を返す
--
--   Items を読む(後半)
--        |
--        v
--   year >= 2002 の行だけ
--        |
--        v
--   税込価格を返す
--
--   前半結果
--      +
--   後半結果
--      |
--      v
--   UNION ALL で結合
--
-- つまり、
--   CASE 版 = 1本の SELECT の中で値分岐
--   UNION ALL 版 = 2本の SELECT に分けて結果結合
-- という違いである。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT item_name, year, CASE ... END AS price
--   → item_name, year と、CASE で選んだ価格を返す
--
-- CASE
--   → 条件分岐で返す値を切り替える
--
-- WHEN year <= 2001 THEN price_tax_ex
--   → 2001年以前は税抜価格を返す
--
-- WHEN year >= 2002 THEN price_tax_in
--   → 2002年以降は税込価格を返す
--
-- END AS price
--   → CASE の結果列を price という名前で返す
--
-- FROM Items
--   → Items テーブルを元データとする
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- EXPLAIN で比較すべき理由
-- ================================================
--
-- MySQL の EXPLAIN は、
--   SELECT がどのように実行されるか
-- を確認するための仕組みである。  [oai_citation:5‡MySQL Developer Zone](https://dev.mysql.com/doc/en/explain-output.html?utm_source=chatgpt.com)
--
-- そのため、今回のように
--   CASE 版
--   UNION ALL 版
-- のどちらが有利かを判断したい場合は、
-- それぞれに EXPLAIN を付けて比較するのが正しい。
--
-- 例:
--
--   EXPLAIN
--   SELECT item_name, year,
--          CASE WHEN year <= 2001 THEN price_tax_ex
--               WHEN year >= 2002 THEN price_tax_in END AS price
--     FROM Items;
--
--   EXPLAIN
--   SELECT item_name, year, price_tax_ex AS price
--     FROM Items
--    WHERE year <= 2001
--   UNION ALL
--   SELECT item_name, year, price_tax_in AS price
--     FROM Items
--    WHERE year >= 2002;
--
-- この比較により、
--   何回テーブルを読むか
--   どんなアクセス方法か
-- などを確認できる。EXPLAIN は各テーブルをどう読むかを表示する。  [oai_citation:6‡MySQL Developer Zone](https://dev.mysql.com/doc/en/explain-output.html?utm_source=chatgpt.com)


