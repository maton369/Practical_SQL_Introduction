-- ================================================
-- ファイル名: 38_select_persons_group_by_initial.sql
-- 役割:
--   人物名の先頭文字ごとに、何人存在するかを集計する。
--
-- このSQLの目的:
--   Persons テーブルを、名前の「頭文字」という基準でグループ分けし、
--   各グループの人数を数えること。
--
-- 学習上の主題:
--   SUBSTRING による文字の切り出し、
--   GROUP BY による分類、
--   COUNT(*) による件数集計を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLの中心は「カット」である。
--
--   ここでいうカットとは、
--   元の列 name をそのまま使うのではなく、
--   name から
--     先頭1文字
--   だけを切り出して、
--   それを新しい分類キーとして使うことを指す。
--
--   つまり、
--     Anderson と Adela は別人
--   だが、
--     どちらも 'A' で始まる
--   ので、
--   集計上は同じグループに「カット」されて入る。
--
--   この発想は非常に重要で、
--   もとのデータをそのまま集計するのではなく、
--   一部を切り出して新しい分類軸を作る
--   という処理になっている。
-- ================================================

SELECT
    SUBSTRING(name, 1, 1) AS label,
    -- SUBSTRING(name, 1, 1):
    --   name 列の文字列から、
    --   1文字目から長さ1文字分だけを切り出す。
    --
    --   具体例:
    --     Anderson -> 'A'
    --     Adela    -> 'A'
    --     Bates    -> 'B'
    --     Chris    -> 'C'
    --     Donald   -> 'D'
    --
    --   つまりこの部分が
    --     頭文字によるカット
    -- を実現している。
    --
    --   name 全体ではなく、
    --   その一部だけを切り出して分類キーに変換している点が重要である。
    --
    -- AS label:
    --   切り出した先頭文字に label という列名を付けている。
    --
    --   最終結果では
    --     A
    --     B
    --     C
    --     D
    -- などがこの label 列に表示される。

    COUNT(*)
    -- COUNT(*):
    --   各グループに含まれる行数、つまり人数を数える。
    --
    --   ここでの「各グループ」は、
    --   GROUP BY でまとめられた
    --   頭文字ごとのグループである。
    --
    --   したがって、
    --   A グループに属する人数、
    --   B グループに属する人数、
    --   …を数えることになる。

FROM
    Persons
    -- FROM Persons:
    --   元データの人物表である。
    --
    --   この時点では1人1行で保持されている。

GROUP BY
    SUBSTRING(name, 1, 1);
    -- GROUP BY SUBSTRING(name, 1, 1):
    --   先頭1文字ごとに行をグループ化する。
    --
    --   つまり、
    --     'A' で始まる人を1グループ
    --     'B' で始まる人を1グループ
    --     'C' で始まる人を1グループ
    --     'D' で始まる人を1グループ
    -- のようにまとめる。
    --
    --   ここがこのSQLの本体であり、
    --   「カットした結果を分類キーとして集約する」
    --   処理になっている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Persons テーブルの各行を1つずつ見る
-- 2. 各 name から先頭1文字を切り出す
-- 3. 同じ先頭文字を持つ行どうしをグループ化する
-- 4. 各グループの人数を COUNT(*) で数える
-- 5. 頭文字と人数の組を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = {}
--
--   for each row in Persons:
--       label = first character of row.name
--       groups[label] += 1
--
--   output groups
--
-- つまりこれは、
--   「名前から頭文字をカットして取り出し、
--     そのカット結果ごとに人数を集計する」
--   アルゴリズムである。


-- ================================================
-- 「カット」とは何か
-- ================================================
--
-- 今回の主題である「カット」は、
--   文字列の一部を切り出して
--   それを新しい分類軸として使う
-- ことを意味している。
--
-- 今回は
--   SUBSTRING(name, 1, 1)
-- によって
--   名前の先頭1文字
-- を切り出している。
--
-- これは単なる表示加工ではなく、
--   GROUP BY のキーそのもの
-- になっている点が重要である。
--
-- つまりこのSQLでは、
--   元の name
-- ではなく
--   カット後の label
-- によって集計単位が決まる。
--
-- ここがアルゴリズム上の本質である。
--
-- たとえば
--   Anderson
--   Adela
-- は別の name を持つが、
-- どちらも先頭をカットすると
--   'A'
-- になる。
--
-- したがって集計では同じ箱に入る。
--
-- 逆に
--   Bates
--   Becky
--   Bill
-- は全員 B で始まるので、
--   'B'
-- グループとして3人まとめて数えられる。
--
-- つまりカットとは、
--   「個別の文字列を、その一部の特徴でまとめ直す」
--   ための前処理
-- である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- データを頭文字でカットすると、
--
--   Anderson -> A
--   Adela    -> A
--   Bates    -> B
--   Becky    -> B
--   Bill     -> B
--   Chris    -> C
--   Darwin   -> D
--   Dawson   -> D
--   Donald   -> D
--
-- となる。
--
-- これを人数集計すると、
--
--   label | COUNT(*)
--   ------+---------
--   A     | 2
--   B     | 3
--   C     | 1
--   D     | 3
--
-- になる。
--
-- つまり最終結果は、
--   人名一覧
-- ではなく
--   頭文字ごとの人数分布
-- である。


-- ================================================
-- 図解: 文字列カットによる集計
-- ================================================
--
--   Persons
--      |
--      v
--   各 name を見る
--      |
--      v
--   SUBSTRING(name, 1, 1)
--      |
--      +----------------------------------+
--      | Anderson -> A                    |
--      | Adela    -> A                    |
--      | Bates    -> B                    |
--      | Becky    -> B                    |
--      | Bill     -> B                    |
--      | Chris    -> C                    |
--      | Darwin   -> D                    |
--      | Dawson   -> D                    |
--      | Donald   -> D                    |
--      +----------------------------------+
--      |
--      v
--   GROUP BY 先頭文字
--      |
--      v
--   COUNT(*) で人数を数える
--      |
--      v
--   A -> 2
--   B -> 3
--   C -> 1
--   D -> 3
--
-- つまり、
--   元の名前を
--   先頭文字でカットし、
--   そのカット結果ごとに箱へ入れて数えている。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   もとの name 全体を集計するのではなく、
--   name の一部を切り出して新しい分類キーを作る
-- 点にある。
--
-- これは集計SQLの重要な考え方で、
--   既存列をそのまま使うだけでなく、
--   式で加工した値を GROUP BY に使える
-- ことを示している。
--
-- 今回の例では単純に1文字だけだが、
-- 同じ発想で
--   年月だけを切り出す
--   市外局番だけを切り出す
--   先頭3桁だけを切り出す
-- などもできる。
--
-- つまりこのSQLは、
--   「加工した値で分類する」
-- という集計の基本パターンを学ぶ良い例である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT SUBSTRING(name, 1, 1) AS label, COUNT(*)
--   → 先頭文字ごとの人数を返す
--
-- SUBSTRING(name, 1, 1)
--   → name の1文字目から1文字だけ切り出す
--
-- AS label
--   → 切り出した結果に label という列名を付ける
--
-- COUNT(*)
--   → 各グループに属する人数を数える
--
-- FROM Persons
--   → 元データは人物表
--
-- GROUP BY SUBSTRING(name, 1, 1)
--   → 先頭文字ごとにグループ化する
--
-- ;
--   → SQL文の終了を表す


-- ================================================
