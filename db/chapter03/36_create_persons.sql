-- ================================================
-- ファイル名: 36_create_persons.sql
-- 役割:
--   人物情報を管理する Persons テーブルを作成する。
--
-- このSQLの目的:
--   名前・年齢・身長・体重を持つ人物データを用意し、
--   後続で「名前の先頭文字ごとに人数を集計する」処理を試せるようにすること。
--
-- 学習上の主題:
--   CREATE TABLE による基本的な表定義と、
--   集計元となる単純なマスタ表の構造を理解すること。
--
-- 今回の最重要ポイント:
--   後続のSQLでは、
--   name 全体ではなく
--   name の「先頭1文字」を取り出して集計キーにする。
--
--   つまりこの表では
--     1人 = 1行
--   だが、
--   集計時には
--     名前の頭文字 = 1グループ
--   という別の見方をする。
-- ================================================

CREATE TABLE Persons
(
    name VARCHAR(8) NOT NULL,
    -- name:
    --   人物名を表す列である。
    --
    --   例:
    --     Anderson
    --     Adela
    --     Bates
    --   などが入る。
    --
    --   後続SQLでは、この列の全体ではなく
    --   先頭1文字だけを取り出して集計に使う。

    age INTEGER NOT NULL,
    -- age:
    --   年齢を表す列である。

    height FLOAT NOT NULL,
    -- height:
    --   身長を表す列である。
    --
    --   今回の頭文字集計SQLでは直接は使わないが、
    --   人物情報テーブルとしての例示のために含まれている。

    weight FLOAT NOT NULL,
    -- weight:
    --   体重を表す列である。
    --
    --   これも今回の頭文字集計SQLでは直接は使わない。

    PRIMARY KEY (name)
    -- PRIMARY KEY(name):
    --   名前を主キーとしている。
    --
    --   したがって、同じ名前を2回登録することはできない。
    --
    --   今回の題材では、
    --   各行が一意の人物を表す前提を与えている。
);