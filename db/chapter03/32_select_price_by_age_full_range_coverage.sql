-- ================================================
-- ファイル名: 32_select_price_by_age_full_range_coverage.sql
-- 役割:
--   複数の年齢範囲レコードを合計して、
--   製品ごとに 0～100 の全範囲をカバーしているかを判定する。
--
-- このSQLの目的:
--   1つの製品が複数行の範囲データを持つとき、
--   それらをまとめて見て
--   「全体の範囲を埋めている製品」を抽出すること。
--
-- 学習上の主題:
--   GROUP BY と HAVING、
--   範囲長の計算、
--   複数行で1つの連続区間をカバーしているかを判定する考え方を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは
--     どの行が条件一致するか
--   を見るのではなく、
--     同じ product_id の複数行全体をまとめて評価する
--   ところが中心である。
--
--   つまりアルゴリズムとしては
--     1. 製品ごとに範囲行を集める
--     2. 各行の範囲長を計算する
--     3. それを合計する
--     4. 合計が 101 なら 0～100 を全部カバーしているとみなす
--   という集約型の判定になっている。
--
--   ここで 101 という値は、
--     0歳から100歳まで
--   を両端含みで数えた総数である。
-- ================================================

SELECT
    product_id
    -- product_id:
    --   最終的に返したい製品IDである。
    --
    --   条件を満たす製品だけが1行ずつ返る。

FROM
    PriceByAge
    -- FROM PriceByAge:
    --   元データの年齢別価格表である。
    --
    --   この表では、1製品が複数の年齢範囲行を持つ。
    --   そのため、この後の GROUP BY で
    --   同じ製品の範囲群をまとめて扱う。

GROUP BY
    product_id
    -- GROUP BY product_id:
    --   製品ごとに複数行を1つのグループにまとめる。
    --
    --   これにより、
    --   たとえば製品1なら
    --     0～50 の行
    --     51～100 の行
    --   をひとまとまりとして評価できる。
    --
    --   つまり、この GROUP BY が
    --   「複数レコードで1つの範囲をカバーする」
    --   という発想の土台である。

HAVING
    SUM(high_age - low_age + 1) = 101;
    -- high_age - low_age + 1:
    --   各行の範囲長を求めている。
    --
    --   なぜ +1 が必要か:
    --   範囲は両端含みで考えているためである。
    --
    --   たとえば
    --     0～50
    --   は
    --     50 - 0 + 1 = 51
    --   となる。
    --
    --   もし +1 が無いと
    --     50 - 0 = 50
    --   になってしまい、
    --   両端含みの件数と合わない。
    --
    -- SUM(...):
    --   製品ごとの各範囲長を合計する。
    --
    --   たとえば製品1なら
    --     (50 - 0 + 1)   = 51
    --     (100 - 51 + 1) = 50
    --   で、
    --     51 + 50 = 101
    -- となる。
    --
    -- = 101:
    --   0歳から100歳までの人数を両端含みで数えると
    --     100 - 0 + 1 = 101
    -- である。
    --
    --   したがって、
    --   範囲長の合計が 101 なら
    --   「少なくとも総量としては 0～100 を全部埋めている」
    --   と判定している。
    --
    --   つまりこの HAVING 句は、
    --   製品ごとの範囲総延長が
    --   0～100 の全長と一致するかを調べている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. PriceByAge を product_id ごとにグループ化する
-- 2. 各行について範囲長
--      high_age - low_age + 1
--    を計算する
-- 3. 同じ製品の範囲長を SUM で合計する
-- 4. 合計が 101 である製品だけを残す
-- 5. 条件を満たす product_id を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = group PriceByAge by product_id
--
--   for each product group:
--       total_length = 0
--
--       for each range row in group:
--           total_length += (high_age - low_age + 1)
--
--       if total_length == 101:
--           output product_id
--
-- つまりこれは、
--   「範囲の本数」ではなく
--   「範囲長の合計」で全体カバーを判定する
-- 集約アルゴリズムである。


-- ================================================
-- なぜ 101 になるのか
-- ================================================
--
-- 0歳から100歳までを両端含みで数えると、
--
--   100 - 0 + 1 = 101
--
-- である。
--
-- ここで重要なのは、
--   年齢範囲を離散値の集合として見ている
-- という点である。
--
-- たとえば
--   0～50
-- は
--   0, 1, 2, ..., 50
-- の51個の年齢を含む。
--
-- 同様に
--   51～100
-- は50個の年齢を含む。
--
-- したがって製品1では
--
--   51 + 50 = 101
--
-- となり、
--   0～100 全体をちょうど埋める
-- とみなせる。
--
-- この 101 という値が、
--   完全カバー判定の基準値
-- になっている。


-- ================================================
-- 製品ごとの判定イメージ
-- ================================================
--
-- 1. 製品1
--   0～50   → 51
--   51～100 → 50
--   合計    → 101
--   → 条件を満たす
--
-- 2. 製品2
--   0～100  → 101
--   合計    → 101
--   → 条件を満たす
--
-- 3. 製品3
--   0～20   → 21
--   31～70  → 40
--   71～100 → 30
--   合計    → 91
--   → 条件を満たさない
--
-- 4. 製品4
--   0～99   → 100
--   合計    → 100
--   → 条件を満たさない
--
-- したがって返るのは
--   製品1
--   製品2
-- である。


-- ================================================
-- 図解: 範囲長合計による完全カバー判定
-- ================================================
--
--   PriceByAge
--      |
--      v
--   GROUP BY product_id
--      |
--      v
--   各製品ごとに範囲群をまとめる
--      |
--      v
--   各行で範囲長を計算
--      |
--      v
--   high_age - low_age + 1
--      |
--      v
--   SUM で合計
--      |
--      v
--   合計が 101 か判定
--      |
--      +----------------------------+
--      | yes                        | no
--      v                            v
--   製品を返す                  製品を除外する
--
-- つまり、
--   複数の範囲行を長さとして足し合わせ、
--   全体長と一致するかを見ている。


-- ================================================
-- このSQLの本質と注意点
-- ================================================
--
-- このSQLの本質は、
--   範囲が何本あるか
-- ではなく、
--   範囲長の合計が全体長に一致するか
-- を見る点にある。
--
-- したがって、これは
--   「複数レコードで1つの範囲をカバーする」
-- ことを確認するシンプルな方法として有効である。
--
-- ただし注意点もある。
--
-- このSQLは
--   合計長が101であること
-- だけを見ている。
--
-- そのため理論上は、
--   範囲同士が重複していても
--   別の場所に穴があるのに合計だけ101になる
-- ようなデータがあれば、
--   正しく「完全カバー」とは言えない可能性がある。
--
-- 今回のサンプルではそのような複雑な重複は無いので、
-- この方法で十分に正しく判定できる。
--
-- つまりこのSQLは、
--   サンプルの前提では正しい
-- が、
--   一般の範囲検証としては「重複や穴の位置」まで見ていない
-- という点に注意が必要である。
--
-- それでも教材としては、
--   複数行を集約して全体範囲を評価する発想
-- を学ぶには非常に良い例である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT product_id
--   → 条件を満たした製品IDだけを返す
--
-- FROM PriceByAge
--   → 元データは年齢帯価格表
--
-- GROUP BY product_id
--   → 製品ごとに範囲行をまとめる
--
-- SUM(high_age - low_age + 1)
--   → 各範囲の長さを合計する
--
-- HAVING ... = 101
--   → 0～100 の全体長をちょうど埋める製品だけ残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
