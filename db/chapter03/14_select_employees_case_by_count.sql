-- ================================================
-- ファイル名: 14_select_employees_case_by_count.sql
-- 役割:
--   社員ごとの所属チーム数に応じて、
--   表示する team 列の内容を CASE 式で切り替える。
--
-- このSQLの目的:
--   先ほどの UNION 版と同じ結果を、
--   1本の GROUP BY + CASE 式で表現すること。
--
-- 学習上の主題:
--   SELECT 句の中で CASE 式を使い、
--   集約結果に応じて返す値を切り替える方法を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは UNION のように
--     1. COUNT(*) = 1 の集合
--     2. COUNT(*) = 2 の集合
--     3. COUNT(*) >= 3 の集合
--   を別々に作って結合するのではなく、
--
--   まず emp_name ごとに 1 回だけ集約し、
--   その集約結果に対して CASE 式で
--   表示内容を分岐させている。
--
--   つまりアルゴリズムとしては
--     「集約を1回で済ませ、その結果を分岐する」
--   形であり、
--   UNION 版より構造が単純になりやすい。
-- ================================================

SELECT
    emp_name,
    -- emp_name:
    --   社員名を表示する。
    --
    --   最後に GROUP BY emp_name を行うため、
    --   結果は社員ごとに1行になる。
    --
    --   教材では emp_name で集約しているが、
    --   実務では同名社員の可能性があるため、
    --   より厳密には emp_id ベースの集約の方が安全な場合が多い。

    CASE
        -- CASE 式:
        --   集約後の値に応じて、返す team 列の内容を切り替える。
        --
        --   今回の CASE 式は、
        --   各社員グループについて COUNT(*) を見て、
        --   1所属か、2兼務か、3つ以上兼務かを判定している。
        --
        --   重要なのは、
        --   この CASE は「各行」に対してではなく、
        --   GROUP BY によって集約された「各社員グループ」に対して
        --   評価されるという点である。

        WHEN COUNT(*) = 1 THEN MAX(team)
        -- COUNT(*) = 1:
        --   その社員の所属行数が1であることを意味する。
        --
        --   この表では
        --     1行 = 1所属チーム
        -- なので、
        --     COUNT(*) = 1
        -- は
        --     1チームだけ所属
        -- を表す。
        --
        -- THEN MAX(team):
        --   単独所属なら、実際のチーム名をそのまま返す。
        --
        --   COUNT(*) = 1 であれば team は1種類しか存在しないため、
        --   MAX(team) はその唯一のチーム名を返す。
        --
        --   つまり
        --     Jim  -> 開発
        --     Carl -> 営業
        -- のような結果になる。
        --
        --   ここで MAX(team) は
        --   「最大のチーム名を求めたい」のではなく、
        --   GROUP BY した結果から team を1つ取り出すために使っている。

        WHEN COUNT(*) = 2 THEN '2つを兼務'
        -- COUNT(*) = 2:
        --   その社員が2チーム所属していることを意味する。
        --
        -- THEN '2つを兼務':
        --   実際のチーム名ではなく、
        --   兼務状態を表す固定ラベルを返す。
        --
        --   たとえば
        --     Kim -> 2つを兼務
        -- となる。

        WHEN COUNT(*) >= 3 THEN '3つ以上を兼務'
        -- COUNT(*) >= 3:
        --   その社員が3チーム以上に所属していることを意味する。
        --
        -- THEN '3つ以上を兼務':
        --   3チーム以上兼務している社員に対する固定ラベルを返す。
        --
        --   たとえば
        --     Joe  -> 3つ以上を兼務
        --     Bree -> 3つ以上を兼務
        -- となる。

    END AS team
    -- END AS team:
    --   CASE 式の結果列に team という名前を付ける。
    --
    --   ここでの team 列は、元の Employees.team 列そのものではなく、
    --   「実チーム名または兼務状態ラベル」を表す表示用列になっている。

FROM
    Employees
    -- FROM Employees:
    --   元データの表である。
    --
    --   この表は
    --     1社員 × 1所属チーム = 1行
    -- の構造を持つため、
    --   社員ごとの行数 COUNT(*) を取ることで、
    --   兼務数を求められる。

GROUP BY
    emp_name;
    -- GROUP BY emp_name:
    --   社員ごとに所属行をまとめる。
    --
    --   これにより、
    --   各社員について
    --     COUNT(*)
    --     MAX(team)
    --   を計算できる。
    --
    --   そして、その集約結果に対して CASE 式を適用する。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Employees を emp_name ごとにグループ化する
-- 2. 各社員グループについて COUNT(*) を計算する
-- 3. COUNT(*) の値に応じて CASE 式で表示内容を切り替える
--    - 1 なら MAX(team)
--    - 2 なら '2つを兼務'
--    - 3以上なら '3つ以上を兼務'
-- 4. 各社員について1行の結果を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = group Employees by emp_name
--
--   for each employee group in groups:
--       if count(group) == 1:
--           team_label = actual team name
--       elif count(group) == 2:
--           team_label = '2つを兼務'
--       else:
--           team_label = '3つ以上を兼務'
--
--       output emp_name, team_label
--
-- つまりこれは、
--   「社員ごとの所属数を1回だけ集計し、
--     その結果を CASE 式で分類ラベルへ変換する」
--   アルゴリズムである。
--
-- ここが UNION 版との最大の違いである。


-- ================================================
-- UNION 版との違い
-- ================================================
--
-- 先ほどの UNION 版は、
--   COUNT(*) = 1 用の SELECT
--   COUNT(*) = 2 用の SELECT
--   COUNT(*) >= 3 用の SELECT
-- を別々に書き、それを UNION で結合していた。
--
-- つまりアルゴリズムとしては、
--   1. 同じ Employees に対して GROUP BY を行う
--   2. 条件別に結果集合を作る
--   3. その結果を UNION で結合する
-- という多段階構造である。
--
-- 一方、今回の CASE 版は、
--   GROUP BY emp_name を1回だけ行い、
--   その結果に対して CASE で表示内容を分岐している。
--
-- 整理すると、
--
--   UNION 版
--     → 同種の集約を複数回
--     → 条件別集合を作って最後に結合
--
--   CASE 版
--     → 集約は1回だけ
--     → 集約結果をその場で分岐
--
-- という違いがある。
--
-- したがって一般には、
--   CASE 版の方が構造が単純で、
--   パフォーマンス上も有利になりやすい
-- と考えるのが自然である。
--
-- もちろん最終的な優劣は、
--   データ量
--   インデックス
--   統計情報
--   オプティマイザ
-- に依存するため、
-- EXPLAIN で比較するのが適切である。


-- ================================================
-- なぜ CASE 版が有利になりやすいのか
-- ================================================
--
-- UNION 版では、同じ Employees 表に対して
--   GROUP BY emp_name
--   HAVING COUNT(*)
-- を条件別に何度も繰り返していた。
--
-- つまり
--   「1回で済む集約」を
--   「条件別に複数回行っている」
-- 構造になっていた。
--
-- 一方、CASE 版では
--   1回の GROUP BY で
--   各社員の所属数を求め、
--   その結果に CASE を適用するだけでよい。
--
-- したがって構造上は、
--   表参照回数
--   集約回数
--   結果結合段数
-- の面で CASE 版の方が単純になりやすい。
--
-- これが、主に UNION と比較した際のパフォーマンス上の論点である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- 社員ごとの所属数は次のようになる。
--
--   Joe  -> 3行
--   Jim  -> 1行
--   Carl -> 1行
--   Bree -> 4行
--   Kim  -> 2行
--
-- これに CASE 式を適用すると、
--
--   emp_name | team
--   ---------+----------------
--   Joe      | 3つ以上を兼務
--   Jim      | 開発
--   Carl     | 営業
--   Bree     | 3つ以上を兼務
--   Kim      | 2つを兼務
--
-- のような結果になる。
--
-- つまり、
--   単独所属なら実チーム名
--   2兼務なら '2つを兼務'
--   3以上なら '3つ以上を兼務'
-- に変換されている。


-- ================================================
-- 図解: CASE 版のアルゴリズム
-- ================================================
--
--   Employees
--      |
--      v
--   GROUP BY emp_name を1回だけ行う
--      |
--      v
--   各社員について COUNT(*) を求める
--      |
--      v
--   CASE で分岐
--      |
--      +-----------------------------------+
--      | COUNT(*) = 1   → MAX(team)        |
--      | COUNT(*) = 2   → '2つを兼務'      |
--      | COUNT(*) >= 3  → '3つ以上を兼務'  |
--      +-----------------------------------+
--      |
--      v
--   最終結果
--
-- つまり、
--   集約してから分岐する
-- という1段の流れになっている。


-- ================================================
-- UNION 版との図解比較
-- ================================================
--
-- 1. UNION 版
--
--   Employees
--      |
--      v
--   GROUP BY emp_name
--      |
--      v
--   COUNT(*) = 1 の集合
--      |
--      v
--   実チーム名を返す
--
--   Employees
--      |
--      v
--   GROUP BY emp_name
--      |
--      v
--   COUNT(*) = 2 の集合
--      |
--      v
--   '2つを兼務' を返す
--
--   Employees
--      |
--      v
--   GROUP BY emp_name
--      |
--      v
--   COUNT(*) >= 3 の集合
--      |
--      v
--   '3つ以上を兼務' を返す
--
--   3つの結果集合
--      |
--      v
--    UNION
--      |
--      v
--   最終結果
--
--
-- 2. CASE 版
--
--   Employees
--      |
--      v
--   GROUP BY emp_name を1回
--      |
--      v
--   COUNT(*) を求める
--      |
--      v
--   CASE で表示内容を切り替える
--      |
--      v
--   最終結果
--
-- この比較から、
--   CASE 版の方が処理段数が少なく、
--   アルゴリズム構造が単純
-- であることが分かる。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT emp_name, CASE ... END AS team
--   → 社員名と、兼務数に応じた表示内容を返す
--
-- CASE
--   → COUNT(*) の値に応じて返す値を切り替える
--
-- WHEN COUNT(*) = 1 THEN MAX(team)
--   → 単独所属なら実チーム名を返す
--
-- WHEN COUNT(*) = 2 THEN '2つを兼務'
--   → 2兼務なら固定ラベルを返す
--
-- WHEN COUNT(*) >= 3 THEN '3つ以上を兼務'
--   → 3以上兼務なら固定ラベルを返す
--
-- END AS team
--   → CASE の結果列名を team にする
--
-- FROM Employees
--   → 元データは Employees 表
--
-- GROUP BY emp_name
--   → 社員ごとに集約する
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   CASE 式は、上から順に条件を評価し、
--   最初に真になった WHEN の結果を返す。
-- ================================================


