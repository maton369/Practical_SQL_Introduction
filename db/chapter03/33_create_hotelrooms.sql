-- ================================================
-- ファイル名: 33_create_hotelrooms.sql
-- 役割:
--   ホテルの部屋ごとの利用期間を管理する HotelRooms テーブルを作成する。
--
-- このSQLの目的:
--   同じ部屋番号に対して複数の利用期間レコードを持たせ、
--   後続で部屋ごとの総稼働日数を計算できるようにすること。
--
-- 学習上の主題:
--   期間データを複数行で持つ表の設計、
--   PRIMARY KEY(room_nbr, start_date) の意味、
--   1部屋が複数の稼働区間を持つ構造を理解すること。
--
-- 今回の最重要ポイント:
--   この表は
--     1部屋 = 1行
--   ではなく、
--     1部屋 × 1稼働区間 = 1行
--   の構造になっている。
--
--   したがって後続のSQLでは、
--   同じ room_nbr に属する複数区間をまとめて見て、
--   その部屋の総稼働日数を算出する。
-- ================================================

CREATE TABLE HotelRooms
(
    room_nbr INTEGER,
    -- room_nbr:
    --   部屋番号を表す列である。
    --
    --   例:
    --     101
    --     202
    --     303
    --
    --   後続の GROUP BY では、この room_nbr ごとに
    --   複数レコードをまとめる。

    start_date DATE,
    -- start_date:
    --   稼働区間の開始日である。
    --
    --   たとえば
    --     2008-02-01
    -- なら、その日から部屋が使用開始されたことを表す。

    end_date DATE,
    -- end_date:
    --   稼働区間の終了日である。
    --
    --   今回の差分計算では
    --     end_date - start_date
    -- によって稼働日数を求める。
    --
    --   この書き方は、
    --   end_date を「その日で終わる境界」として扱うイメージである。
    --   つまり両端含みの件数ではなく、
    --   日付差としての長さを計算している。

    PRIMARY KEY(room_nbr, start_date)
    -- PRIMARY KEY(room_nbr, start_date):
    --   同じ部屋について同じ開始日の区間を重複登録できないようにする主キーである。
    --
    --   これにより、
    --   部屋ごとの各稼働区間を一意に識別しやすくしている。
);