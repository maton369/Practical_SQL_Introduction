-- ================================================
-- ファイル名: 30_create_price_by_age.sql
-- 役割:
--   年齢帯ごとに価格を管理する PriceByAge テーブルを作成する。
--
-- このSQLの目的:
--   製品ごとに
--     何歳から何歳まで
--   の範囲に対して、いくらの価格を設定するかを表現する表を定義すること。
--
-- 学習上の主題:
--   範囲データを複数行で持つ設計、
--   PRIMARY KEY の意味、
--   CHECK 制約による範囲の妥当性チェックを理解すること。
--
-- 今回の最重要ポイント:
--   この表は「製品1つにつき価格1つ」ではない。
--
--   実際には
--     1製品 × 1年齢範囲 = 1行
-- の構造になっている。
--
--   したがって後続のSQLでは、
--   同じ product_id に属する複数行をまとめて見て、
--   その製品が全年齢範囲をカバーしているかどうかを判定する。
-- ================================================

CREATE TABLE PriceByAge
(
    product_id VARCHAR(32) NOT NULL,
    -- product_id:
    --   製品を識別する列である。
    --
    --   例:
    --     製品1
    --     製品2
    --     製品3
    --     製品4
    --
    --   後続の GROUP BY では、この product_id ごとに行をまとめる。

    low_age INTEGER NOT NULL,
    -- low_age:
    --   価格適用範囲の下限年齢である。
    --
    --   たとえば 0 なら、
    --   0歳から適用される範囲であることを意味する。

    high_age INTEGER NOT NULL,
    -- high_age:
    --   価格適用範囲の上限年齢である。
    --
    --   たとえば 50 なら、
    --   50歳まで適用される範囲であることを意味する。

    price INTEGER NOT NULL,
    -- price:
    --   その年齢範囲に対応する価格である。

    PRIMARY KEY (product_id, low_age),
    -- PRIMARY KEY (product_id, low_age):
    --   製品ごとの各範囲開始年齢を一意にする主キーである。
    --
    --   これにより、
    --   同じ product_id で同じ low_age を持つ行は重複登録できない。
    --
    --   つまり、
    --   同じ製品について同じ開始年齢の範囲を2回定義することを防いでいる。

    CHECK (low_age < high_age)
    -- CHECK (low_age < high_age):
    --   範囲の下限が上限未満でなければならないことを表す制約である。
    --
    --   これにより、
    --     low_age = 50, high_age = 20
    -- のような不正な範囲や、
    --     low_age = high_age
    -- のような長さ0の範囲を防ぎやすくしている。
);