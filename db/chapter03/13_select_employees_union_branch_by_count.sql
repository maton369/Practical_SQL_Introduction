-- ================================================
-- ファイル名: 13_select_employees_union_branch_by_count.sql
-- 役割:
--   社員ごとの所属チーム数に応じて、
--   表示する team 列の内容を切り替える。
--
-- このSQLの目的:
--   社員が
--     1チームだけ所属しているのか
--     2チーム兼務しているのか
--     3チーム以上兼務しているのか
--   を判定し、
--   その結果を人が読みやすいラベルとして返すこと。
--
-- 学習上の主題:
--   UNION によって条件分岐を表現する方法、
--   GROUP BY + HAVING + COUNT(*) の組み合わせ、
--   およびそのパフォーマンス上の特徴を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは CASE 式を使わず、
--   条件ごとに別々の SELECT を書き、
--   それを UNION でつなぐことで分岐を実現している。
--
--   つまりアルゴリズムとしては
--     1. COUNT(*)=1 の集合を作る
--     2. COUNT(*)=2 の集合を作る
--     3. COUNT(*)>=3 の集合を作る
--     4. それらを UNION で結合する
--   という多段階処理である。
--
--   この構造は理解しやすい一方で、
--   同じ Employees 表に対して
--   類似の集約処理を複数回行う形になりやすく、
--   CASE 集約版より構造的に重くなりやすい。
-- ================================================

SELECT
    emp_name,
    -- emp_name:
    --   社員名を表示する。
    --
    --   この SELECT では emp_name ごとに GROUP BY しているため、
    --   結果は社員ごとに1行になる。

    MAX(team) AS team
    -- MAX(team) AS team:
    --   HAVING COUNT(*) = 1 のグループに対してだけ使っている。
    --
    --   COUNT(*) = 1 であれば、その社員は所属チームを1つしか持たない。
    --   したがって team 列の値も1種類しか存在しない。
    --
    --   このとき MAX(team) は、その唯一のチーム名を返す。
    --
    --   つまり
    --     Jim  -> 開発
    --     Carl -> 営業
    --   のように、単独所属者については実際のチーム名をそのまま返す。
FROM
    Employees
GROUP BY
    emp_name
HAVING
    COUNT(*) = 1
    -- COUNT(*) = 1:
    --   その社員の行数が1行だけであることを意味する。
    --
    --   この表では 1行 = 1所属チーム なので、
    --   COUNT(*) = 1 は
    --     1チームだけ所属
    --   を表す。
    --
    --   これが最初の条件分岐である。

UNION

SELECT
    emp_name,

    '2つを兼務' AS team
    -- '2つを兼務' AS team:
    --   実際の team 列を返すのではなく、
    --   2チーム兼務であることを示す固定文字列を返す。
    --
    --   つまりここでは
    --     実チーム名の表示
    --   よりも
    --     兼務状態の分類結果
    --   を優先している。
FROM
    Employees
GROUP BY
    emp_name
HAVING
    COUNT(*) = 2
    -- COUNT(*) = 2:
    --   その社員が2行を持つことを意味する。
    --
    --   1行 = 1所属チーム なので、
    --   COUNT(*) = 2 は
    --     2つのチームを兼務
    --   を表す。
    --
    --   これが2つ目の条件分岐である。

UNION

SELECT
    emp_name,

    '3つ以上を兼務' AS team
    -- '3つ以上を兼務' AS team:
    --   3チーム以上所属している社員に対して、
    --   その兼務状態を示す固定ラベルを返す。
FROM
    Employees
GROUP BY
    emp_name
HAVING
    COUNT(*) >= 3;
    -- COUNT(*) >= 3:
    --   その社員の所属チーム数が3以上であることを意味する。
    --
    --   したがって
    --     Joe  -> 3つ以上を兼務
    --     Bree -> 3つ以上を兼務
    --   のような結果になる。
    --
    --   これが3つ目の条件分岐である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Employees から emp_name ごとに集約し、
--    COUNT(*) = 1 の社員集合を作る
-- 2. その集合については MAX(team) で実チーム名を返す
--
-- 3. Employees から emp_name ごとに集約し、
--    COUNT(*) = 2 の社員集合を作る
-- 4. その集合については '2つを兼務' という固定文字列を返す
--
-- 5. Employees から emp_name ごとに集約し、
--    COUNT(*) >= 3 の社員集合を作る
-- 6. その集合については '3つ以上を兼務' という固定文字列を返す
--
-- 7. 上記3つの結果集合を UNION で縦方向に結合する
--
-- 概念的には次のような処理に近い。
--
--   result = []
--
--   group Employees by emp_name
--
--   for each employee group:
--       if count(group) == 1:
--           result.append(emp_name, actual team name)
--
--   group Employees by emp_name
--
--   for each employee group:
--       if count(group) == 2:
--           result.append(emp_name, '2つを兼務')
--
--   group Employees by emp_name
--
--   for each employee group:
--       if count(group) >= 3:
--           result.append(emp_name, '3つ以上を兼務')
--
--   output result
--
-- つまりこれは、
--   同じ集約処理を条件別に3回行い、
--   それぞれで返す値を変えている
--   UNION 型の条件分岐アルゴリズムである。


-- ================================================
-- なぜ MAX(team) を使うのか
-- ================================================
--
-- 最初の分岐では HAVING COUNT(*) = 1 を満たす社員だけを対象にしている。
--
-- COUNT(*) = 1 なら、
-- その社員は team の値を1つしか持たない。
--
-- したがって
--   MAX(team)
--   MIN(team)
--   ANY_VALUE(team)
-- のような形で、
-- その唯一の team を代表値として返せる。
--
-- 今回は MAX(team) を使っているが、
-- 実質的には
--   「その唯一の team を返す」
-- ための書き方である。
--
-- つまり MAX が数学的な最大値として重要なのではなく、
-- GROUP BY した結果から team を1つ取り出すために使っている。
--
-- ここは読み手が誤解しやすいポイントである。


-- ================================================
-- UNION による条件分岐の意味
-- ================================================
--
-- このSQLでは CASE 式を使わず、
-- 条件ごとに SELECT を分けている。
--
-- 1. COUNT(*) = 1 の集合
-- 2. COUNT(*) = 2 の集合
-- 3. COUNT(*) >= 3 の集合
--
-- をそれぞれ独立に作り、
-- それを UNION で結合する。
--
-- つまりこれは、
--   値レベルの分岐
-- ではなく
--   結果集合レベルの分岐
-- である。
--
-- 読み方としては
--   「1チームならこう返す」
--   「2チームならこう返す」
--   「3つ以上ならこう返す」
-- を SELECT 単位で分けていると考えると分かりやすい。
--
-- 教材としては分かりやすいが、
-- 同じ Employees に対する集約を何度も書いているため、
-- パフォーマンス面ではやや遠回りになりやすい。


-- ================================================
-- UNION と比較した際のパフォーマンス観点
-- ================================================
--
-- 今回のSQLそのものが UNION を使っているので、
-- ここでの比較対象は
--   CASE を使った単一集約版
-- である。
--
-- この UNION 版は構造的に次の特徴を持つ。
--
-- 1. Employees を同じように3回集約している
--    - COUNT(*) = 1 用
--    - COUNT(*) = 2 用
--    - COUNT(*) >= 3 用
--
-- 2. 各分岐ごとに GROUP BY emp_name が必要
--
-- 3. その3つの結果を UNION で結合する
--
-- つまり
--   「集約して、条件判定して、結合する」
-- という処理を複数回繰り返している。
--
-- 一方、CASE を使う代表的な書き方は概念的に次のようになる。
--
--   SELECT emp_name,
--          CASE
--              WHEN COUNT(*) = 1 THEN MAX(team)
--              WHEN COUNT(*) = 2 THEN '2つを兼務'
--              ELSE '3つ以上を兼務'
--          END AS team
--     FROM Employees
--    GROUP BY emp_name;
--
-- こちらは
--   Employees を 1 回集約し、
--   その集約結果に対して CASE で表示内容を切り替える
-- 形である。
--
-- つまり
--   UNION 版:
--     集約を複数回行ってから結合
--
--   CASE 版:
--     集約を1回だけ行って、その結果を分岐
--
-- となる。
--
-- したがって一般には、
--   同じ GROUP BY を何度も繰り返す UNION 版より、
--   1回の GROUP BY の結果に CASE を適用する版の方が
--   構造的に単純で、有利になりやすい
-- と考えるのが自然である。
--
-- ただし、最終的な優劣は
--   データ量
--   インデックス
--   統計情報
--   最適化
-- に依存するため、
-- 厳密には EXPLAIN で比較すべきである。
--
-- それでもアルゴリズム構造だけを見れば、
-- 今回の UNION 版は
--   1回で済む集約を複数回行っている
-- ため、
--   CASE 集約版より重くなりやすい
-- と理解してよい。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- 社員ごとの行数を数えると次のようになる。
--
--   Joe  -> 3行
--   Jim  -> 1行
--   Carl -> 1行
--   Bree -> 4行
--   Kim  -> 2行
--
-- したがって各分岐の結果は次のようになる。
--
-- 1. COUNT(*) = 1
--   Jim  -> 開発
--   Carl -> 営業
--
-- 2. COUNT(*) = 2
--   Kim  -> 2つを兼務
--
-- 3. COUNT(*) >= 3
--   Joe  -> 3つ以上を兼務
--   Bree -> 3つ以上を兼務
--
-- これを UNION すると最終結果は概ね次のようになる。
--
--   emp_name | team
--   ---------+----------------
--   Jim      | 開発
--   Carl     | 営業
--   Kim      | 2つを兼務
--   Joe      | 3つ以上を兼務
--   Bree     | 3つ以上を兼務
--
-- つまり、兼務数に応じて表示ラベルを切り替えている。


-- ================================================
-- 図解: UNION 版のアルゴリズム
-- ================================================
--
--   Employees
--      |
--      v
--   GROUP BY emp_name
--      |
--      v
--   COUNT(*) = 1 の社員だけ
--      |
--      v
--   MAX(team) を返す
--
--
--   Employees
--      |
--      v
--   GROUP BY emp_name
--      |
--      v
--   COUNT(*) = 2 の社員だけ
--      |
--      v
--   '2つを兼務' を返す
--
--
--   Employees
--      |
--      v
--   GROUP BY emp_name
--      |
--      v
--   COUNT(*) >= 3 の社員だけ
--      |
--      v
--   '3つ以上を兼務' を返す
--
--
--   3つの結果集合
--      |
--      v
--    UNION
--      |
--      v
--   最終結果
--
-- つまり、
--   同じ種類の集約を条件別に何度も行い、
--   その結果を結合している。


-- ================================================
-- CASE 集約版との比較イメージ
-- ================================================
--
-- CASE 集約版は概念的には次のようになる。
--
--   Employees
--      |
--      v
--   GROUP BY emp_name を1回だけ行う
--      |
--      v
--   COUNT(*) を見て CASE で表示内容を分岐
--      |
--      v
--   最終結果
--
-- こちらは
--   集約 1回
--   分岐 1回
-- で済む。
--
-- そのため、アルゴリズム構造としては
--   CASE 集約版の方が段数が少ない
-- といえる。
--
-- これが、主に UNION と比較した際のパフォーマンス上のポイントである。


-- ================================================
-- UNION と UNION ALL の点について
-- ================================================
--
-- 今回の3つの集合は
--   COUNT(*) = 1
--   COUNT(*) = 2
--   COUNT(*) >= 3
-- で互いに排他的である。
--
-- したがって、同じ社員が複数の分岐に入ることはない。
--
-- そのため意味的には UNION ALL でも同じ結果になりやすい。
--
-- したがって、
--   UNION の重複除去性
-- は本質ではなく、
-- 今回の本質は
--   そもそも同種の集約を何回も行っている構造そのもの
-- にある。
--
-- つまり性能の主論点は
--   UNION か UNION ALL か
-- よりも、
--   CASE で1回にまとめられる処理を
--   UNION で分割していること
-- にある。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT emp_name, MAX(team)
--   → COUNT(*) = 1 の社員について実チーム名を返す
--
-- GROUP BY emp_name
--   → 社員ごとに所属行をまとめる
--
-- HAVING COUNT(*) = 1
--   → 1チームだけ所属している社員に絞る
--
-- '2つを兼務' AS team
--   → 2チーム所属者には固定ラベルを返す
--
-- HAVING COUNT(*) = 2
--   → 2チーム兼務者に絞る
--
-- '3つ以上を兼務' AS team
--   → 3チーム以上所属者には固定ラベルを返す
--
-- HAVING COUNT(*) >= 3
--   → 3チーム以上所属者に絞る
--
-- UNION
--   → 条件別に作った結果集合を結合する


