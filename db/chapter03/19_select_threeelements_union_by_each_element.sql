-- ================================================
-- ファイル名: 19_select_threeelements_union_by_each_element.sql
-- 役割:
--   3組ある (date, flg) ペアのうち、
--   いずれかが条件に一致する行を UNION で取得する。
--
-- このSQLの目的:
--   「どのペアで一致するか」を個別に検索し、
--   その結果を UNION でまとめることで、
--   条件に一致する行全体を取得すること。
--
-- 学習上の主題:
--   OR 条件を1本にまとめる代わりに、
--   各列ペアごとに SELECT を分けて UNION する書き方を理解すること。
--
-- 今回の最重要ポイント:
--   このケースでは UNION は
--   単なる見た目の分割ではなく、
--   各検索条件をそれぞれ専用インデックスに乗せやすくする
--   という実務上の利点を持ちやすい。
--
--   つまり今回の UNION は、
--   条件分岐の可読性だけでなく、
--   パフォーマンス面でも有効になりやすい典型例である。
-- ================================================

SELECT
    `key`,
    name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
    date_1 = '2013-11-01'
    AND flg_1 = 'T'
    -- 第1ペアに対する検索である。
    --
    -- この条件は
    --   IDX_1(date_1, flg_1)
    -- に対応している。
    --
    -- したがって、この SELECT 単体で見れば
    -- 第1ペア専用インデックスを使った検索になりやすい。

UNION

SELECT
    `key`,
    name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
    date_2 = '2013-11-01'
    AND flg_2 = 'T'
    -- 第2ペアに対する検索である。
    --
    -- この条件は
    --   IDX_2(date_2, flg_2)
    -- に対応している。
    --
    -- つまり第2ペア用の検索を、
    -- 第1ペアや第3ペアとは独立に最適化しやすい。

UNION

SELECT
    `key`,
    name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
    date_3 = '2013-11-01'
    AND flg_3 = 'T';
    -- 第3ペアに対する検索である。
    --
    -- この条件は
    --   IDX_3(date_3, flg_3)
    -- に対応している。
    --
    -- これで、3種類の列ペアそれぞれに対して
    -- 独立した検索を行い、
    -- 最後に UNION で結果を統合している。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 第1ペア (date_1, flg_1) が条件一致する行を探す
-- 2. 第2ペア (date_2, flg_2) が条件一致する行を探す
-- 3. 第3ペア (date_3, flg_3) が条件一致する行を探す
-- 4. それぞれの結果集合を UNION でまとめる
-- 5. 最終的に「どこか1つのペアが一致した行」を返す
--
-- 概念的には次のような処理に近い。
--
--   result = {}
--
--   for each row matched by (date_1='2013-11-01' AND flg_1='T'):
--       add row to result
--
--   for each row matched by (date_2='2013-11-01' AND flg_2='T'):
--       add row to result
--
--   for each row matched by (date_3='2013-11-01' AND flg_3='T'):
--       add row to result
--
--   output result
--
-- つまりこれは、
--   「OR で1本化した検索」を
--   「3本のインデックス対応検索 + 結果統合」
--   に分解したアルゴリズムである。
--
-- ここが、このようなケースで UNION が有効になる理由である。


-- ================================================
-- このようなケースで UNION が有効な理由
-- ================================================
--
-- このテーブルは
--   似た意味の列ペアが複数横並びに存在する
-- 構造である。
--
-- 今回検索したい条件は実質的に
--
--   (date_1='2013-11-01' AND flg_1='T')
--   OR
--   (date_2='2013-11-01' AND flg_2='T')
--   OR
--   (date_3='2013-11-01' AND flg_3='T')
--
-- である。
--
-- これを OR で1本に書くこともできるが、
-- その場合はオプティマイザが
--   複数の異なる複合インデックス条件
-- をどう扱うかが複雑になりやすい。
--
-- 一方、UNION で分ければ、
--
--   1本目 -> IDX_1 に乗せやすい
--   2本目 -> IDX_2 に乗せやすい
--   3本目 -> IDX_3 に乗せやすい
--
-- と、各 SELECT が
--   「自分専用の複合インデックス条件」
-- を単純な形で持つことになる。
--
-- したがってこのようなケースでは、
--   UNION に分割すること自体が
--   オプティマイザにとって分かりやすく、
--   インデックス利用上有利になりやすい。
--
-- つまり今回の UNION は、
--   単なる書き換えではなく、
--   「複数の索引可能条件を個別に実行させる」
--   ための実務的な手法として有効になりやすい。
--
-- これは先ほどまでの
--   同じ表を条件別に何度も集約する UNION
-- とは性質が少し異なる。
--
-- 今回は
--   各 SELECT が別インデックスを使う意味を持つ
-- ため、
--   UNION の分割がむしろ有利に働きやすい。
--
-- ここがこの題材の本質である。


-- ================================================
-- 先ほどの UNION と今回の UNION の違い
-- ================================================
--
-- 先ほどの Employees の UNION は、
--   同じ GROUP BY を条件別に複数回行う
-- 構造だった。
--
-- その場合は
--   集約を何度も繰り返す
--   結果を最後に結合する
-- という点で、
--   CASE 1本にまとめた方が有利
-- になりやすかった。
--
-- 一方、今回の ThreeElements の UNION は、
--   各 SELECT が別の列ペアを対象にしている
--   各 SELECT が別の複合インデックスを利用しやすい
-- という性質を持つ。
--
-- つまり
--
--   Employees の UNION
--     → 同じ仕事を何度も繰り返すので不利になりやすい
--
--   ThreeElements の UNION
--     → 異なる索引条件を個別に実行するので有利になりやすい
--
-- という違いがある。
--
-- ここは「UNION は常に遅い」わけではないことを理解するために非常に重要である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- 条件一致する行は、
--
-- 1. key='1'
--    date_1='2013-11-01' AND flg_1='T'
--
-- 2. key='2'
--    date_2='2013-11-01' AND flg_2='T'
--
-- 3. key='5'
--    date_3='2013-11-01' AND flg_3='T'
--
-- の3行である。
--
-- したがって最終結果は概ね次のようになる。
--
--   key | name | date_1      | flg_1 | date_2      | flg_2 | date_3      | flg_3
--   ----+------+-------------+-------+-------------+-------+-------------+------
--   1   | a    | 2013-11-01  | T     | NULL        | NULL  | NULL        | NULL
--   2   | b    | NULL        | NULL  | 2013-11-01  | T     | NULL        | NULL
--   5   | e    | NULL        | NULL  | NULL        | NULL  | 2013-11-01  | T
--
-- つまり、
--   第1要素でヒットした行
--   第2要素でヒットした行
--   第3要素でヒットした行
-- を1つにまとめて返している。


-- ================================================
-- 図解: このケースでの UNION の有効性
-- ================================================
--
--   ThreeElements
--      |
--      +-------------------------------+
--      |               |               |
--      v               v               v
--   条件1            条件2            条件3
--   date_1, flg_1    date_2, flg_2    date_3, flg_3
--      |               |               |
--      v               v               v
--    IDX_1            IDX_2            IDX_3
--      |               |               |
--      v               v               v
--   部分結果1        部分結果2        部分結果3
--      \               |               /
--       \              |              /
--        +-------------+-------------+
--                      |
--                      v
--                    UNION
--                      |
--                      v
--                   最終結果
--
-- つまり、
--   OR で1本化する代わりに、
--   各インデックスに対応する検索を分離して実行している。
--
-- これが、このようなケースで UNION が有効になりやすい理由である。


