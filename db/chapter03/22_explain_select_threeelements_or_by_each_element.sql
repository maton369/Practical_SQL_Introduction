--
-- このSQLの目的:
--   先ほど確認した UNION 版の実行計画と比較し、
--   OR 版がどのようなアクセス方式になるかを見ること。
--
-- 学習上の主題:
--   OR 条件と UNION 条件分割が、
--   実行計画レベルでどう違って見えるかを確認すること。
-- ================================================

EXPLAIN
SELECT
    `key`, name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
       (date_1 = '2013-11-01' AND flg_1 = 'T')
    OR (date_2 = '2013-11-01' AND flg_2 = 'T')
    OR (date_3 = '2013-11-01' AND flg_3 = 'T');

-- この EXPLAIN で確認したいポイント:
--
-- 1. OR 版が全表走査になるか
-- 2. index_merge のような形になるか
-- 3. 先ほどの UNION 版のように
--      IDX_1, IDX_2, IDX_3
--    を個別枝で明確に使う計画になるか
--
-- 比較の観点としては、
--   UNION 版:
--     各枝が ref + 専用インデックス
--
--   OR 版:
--     1本に束ねた条件をどう処理するか
--
-- の違いを見ることが重要である。