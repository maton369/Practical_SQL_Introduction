-- ================================================
-- ファイル名: 31_insert_price_by_age_sample_data.sql
-- 役割:
--   PriceByAge テーブルへ製品ごとの年齢帯価格サンプルを投入する。
--
-- このSQLの目的:
--   製品ごとに年齢範囲のカバー状況が異なるデータを作り、
--   後続の「全年齢範囲をカバーしているか」の判定SQLを試せるようにすること。
--
-- 学習上の主題:
--   同じ製品が複数の年齢帯行を持つこと、
--   その範囲長を合計することで全体カバーを判定する考え方を理解すること。
--
-- 今回の最重要ポイント:
--   各製品の年齢範囲は次のように異なる。
--
--   - 製品1: 0～50 と 51～100 で全体を埋める
--   - 製品2: 0～100 の1行で全体を埋める
--   - 製品3: 0～20, 31～70, 71～100 で一部に穴がある
--   - 製品4: 0～99 で100歳が抜ける
--
--   つまり、
--   「合計範囲長が101になるかどうか」
--   で全体カバーを判定できる題材になっている。
-- ================================================

INSERT INTO PriceByAge VALUES('製品1',  0 ,  50 , 2000);
INSERT INTO PriceByAge VALUES('製品1', 51 , 100 , 3000);

INSERT INTO PriceByAge VALUES('製品2',  0 , 100 , 4200);

INSERT INTO PriceByAge VALUES('製品3',  0 ,  20 ,  500);
INSERT INTO PriceByAge VALUES('製品3', 31 ,  70 ,  800);
INSERT INTO PriceByAge VALUES('製品3', 71 , 100 , 1000);

INSERT INTO PriceByAge VALUES('製品4',  0 ,  99 , 8900);


-- ================================================
-- データの見方
-- ================================================
--
-- 1. 製品1
--    0～50
--    51～100
--    → 0歳から100歳まで切れ目なく全体をカバーする
--
-- 2. 製品2
--    0～100
--    → 1行だけで0歳から100歳まで全体をカバーする
--
-- 3. 製品3
--    0～20
--    31～70
--    71～100
--    → 21～30 が抜けている
--    → 全体をカバーしていない
--
-- 4. 製品4
--    0～99
--    → 100歳が抜けている
--    → 全体をカバーしていない
--
-- したがって、後続SQLで抽出されるべき製品は
--   製品1
--   製品2
-- の2つである。