-- ================================================
-- ファイル名: 35_select_hotelrooms_working_days.sql
-- 役割:
--   部屋ごとの複数稼働区間を合計して、
--   総稼働日数を算出する。
--
-- このSQLの目的:
--   同じ room_nbr に属する複数レコードをまとめ、
--   その部屋が合計で何日稼働していたかを計算すること。
--
-- 学習上の主題:
--   GROUP BY + SUM + HAVING を使って、
--   期間データの長さを合計する方法を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは
--     どの1区間が長いか
--   ではなく、
--     同じ部屋の複数区間全体を足し合わせた総稼働日数
--   を見ている。
--
--   つまりアルゴリズムとしては
--     1. 部屋ごとに稼働区間を集める
--     2. 各区間の長さを計算する
--     3. それを合計する
--     4. 合計が10日以上の部屋を残す
--   という集約型の判定である。
-- ================================================

SELECT
    room_nbr,
    -- room_nbr:
    --   最終結果として返す部屋番号である。
    --
    --   GROUP BY room_nbr により、
    --   同じ部屋に属する複数の稼働区間をまとめたあと、
    --   その部屋単位で1行が返る。

    SUM(end_date - start_date) AS working_days
    -- end_date - start_date:
    --   各レコードの稼働日数を表す。
    --
    --   ここでは日付差をそのまま長さとして使っている。
    --
    --   たとえば
    --     2008-02-01 から 2008-02-06
    --   は
    --     5
    -- 日と計算される。
    --
    --   これは
    --     2008-02-06 - 2008-02-01 = 5
    -- という日付差であり、
    --   両端含み件数ではない点に注意が必要である。
    --
    -- SUM(...):
    --   同じ部屋の複数区間の長さを全部足し合わせる。
    --
    --   つまり working_days は
    --     総稼働日数
    --   を表している。
    --
    --   たとえば 101号室なら
    --     5 + 2 + 3 = 10
    -- となる。

FROM
    HotelRooms
    -- FROM HotelRooms:
    --   元データの部屋稼働区間表である。
    --
    --   同じ部屋が複数行を持つため、
    --   このままでは部屋ごとの総日数は見えない。
    --
    --   そこでこの後の GROUP BY で部屋単位にまとめる。

GROUP BY
    room_nbr
    -- GROUP BY room_nbr:
    --   同じ部屋番号を持つレコードを1グループにまとめる。
    --
    --   これにより、
    --     101号室の全区間
    --     202号室の全区間
    --     303号室の全区間
    --   をそれぞれまとめて扱える。

HAVING
    SUM(end_date - start_date) >= 10;
    -- HAVING:
    --   GROUP BY で集約した後のグループに対する条件を書く。
    --
    --   今回は
    --     総稼働日数が10日以上の部屋
    --   だけを残している。
    --
    --   WHERE ではなく HAVING を使う理由:
    --   条件が
    --     SUM(end_date - start_date)
    --   という集約結果に対するものだからである。
    --
    --   つまり
    --     各行の条件
    --   ではなく
    --     各部屋グループ全体の条件
    -- を判定している。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. HotelRooms を room_nbr ごとにグループ化する
-- 2. 各レコードについて end_date - start_date を計算する
-- 3. 同じ部屋の区間長を SUM で合計する
-- 4. 合計が 10 以上の部屋だけを残す
-- 5. room_nbr と総稼働日数 working_days を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = group HotelRooms by room_nbr
--
--   for each room group:
--       total_days = 0
--
--       for each period row in group:
--           total_days += (end_date - start_date)
--
--       if total_days >= 10:
--           output room_nbr, total_days
--
-- つまりこれは、
--   「複数の稼働区間を持つ部屋について、
--     その総稼働日数を計算し、しきい値で判定する」
--   集約アルゴリズムである。


-- ================================================
-- 部屋ごとの計算イメージ
-- ================================================
--
-- 1. 101号室
--   2008-02-01 ～ 2008-02-06 -> 5日
--   2008-02-06 ～ 2008-02-08 -> 2日
--   2008-02-10 ～ 2008-02-13 -> 3日
--   合計 10日
--   → 条件を満たす
--
-- 2. 202号室
--   2008-02-05 ～ 2008-02-08 -> 3日
--   2008-02-08 ～ 2008-02-11 -> 3日
--   2008-02-11 ～ 2008-02-12 -> 1日
--   合計 7日
--   → 条件を満たさない
--
-- 3. 303号室
--   2008-02-03 ～ 2008-02-17 -> 14日
--   合計 14日
--   → 条件を満たす
--
-- したがって最終結果は
--   101 | 10
--   303 | 14
-- になる。


-- ================================================
-- 図解: 総稼働日数の算出
-- ================================================
--
--   HotelRooms
--      |
--      v
--   GROUP BY room_nbr
--      |
--      v
--   各部屋の区間群をまとめる
--      |
--      v
--   各区間で end_date - start_date を計算
--      |
--      v
--   SUM で合計
--      |
--      v
--   total_days >= 10 ?
--      |
--      +--------------------------+
--      | yes                      | no
--      v                          v
--   部屋を返す                 部屋を除外
--
-- つまり、
--   区間ごとの長さを足して
--   部屋全体の稼働日数を判定している。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   各区間を個別に見るのではなく、
--   同じ部屋に属する複数区間全体をまとめて評価する
-- 点にある。
--
-- たとえば 101号室は
--   1本目 5日
--   2本目 2日
--   3本目 3日
-- で、個々には10日未満だが、
-- 合計では10日になる。
--
-- つまりこのSQLは、
--   「長い区間がある部屋」
-- を探しているのではなく、
--   「総利用日数が長い部屋」
-- を探している。
--
-- ここがアルゴリズム上の重要ポイントである。


-- ================================================
-- 注意点
-- ================================================
--
-- このSQLは
--   各区間長を単純に足し合わせる
-- だけである。
--
-- そのため、
--   区間が重複していた場合
-- は、その重複日数も二重に数えてしまう。
--
-- 今回のサンプルでは
--   101号室は一部に空白があるが重複はない
--   202号室は連続しているが重複はない
--   303号室は1本だけ
-- なので、この単純合計で問題ない。
--
-- つまりこのSQLは、
--   「重複しない区間群の総長さ」
-- を求めるには正しいが、
--   重複区間が混ざる一般ケースでは注意が必要である。
--
-- また、101号室のように
--   途中に空白があっても
-- 総日数は普通に足し合わせる。
--
-- したがってこれは
--   連続稼働日数
-- ではなく
--   総稼働日数
-- の計算である。
--
-- ここは非常に重要である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT room_nbr, SUM(...) AS working_days
--   → 部屋ごとの総稼働日数を返す
--
-- end_date - start_date
--   → 各区間の長さを計算する
--
-- SUM(...)
--   → 同じ部屋の複数区間の長さを合計する
--
-- FROM HotelRooms
--   → 元データは部屋稼働区間表
--
-- GROUP BY room_nbr
--   → 部屋ごとに区間をまとめる
--
-- HAVING SUM(...) >= 10
--   → 総稼働日数が10日以上の部屋だけ残す
--
-- ;
--   → SQL文の終了を表す


-- ================================================
-- 補足1:
--   もし「連続稼働日数」を求めたいなら、
--   単純な SUM では足りず、
--   区間のつながりやギャップを別途判定する必要がある。
-- ================================================


-- ================================================
-- 補足2:
--   もし区間の重複がありうるなら、
--   このSQLは重複分を二重計上する可能性がある。
--   その場合は区間マージの処理が必要になる。
-- ================================================


-- ================================================
-- 補足3:
--   それでもこのSQLは、
--   複数レコードをまとめて1つの対象の「総量」を評価する
--   GROUP BY + SUM + HAVING の良い教材例である。
-- ================================================