-- ================================================
-- ファイル名: 21_select_threeelements_or_by_each_element.sql
-- 役割:
--   3組ある (date, flg) ペアのうち、
--   いずれか1組が条件に一致する行を OR で取得する。
--
-- このSQLの目的:
--   先ほどの UNION 版と同じ意味の検索を、
--   1本の WHERE 句に OR 条件としてまとめて表現すること。
--
-- 学習上の主題:
--   OR による複数条件の結合と、
--   それを UNION 版と比較したときの
--   パフォーマンス上の考え方を理解すること。
--
-- 今回の最重要ポイント:
--   OR 版は見た目は1本で簡潔だが、
--   3種類の列ペア条件を1つの WHERE にまとめている。
--
--   そのため、実行時には
--     (date_1, flg_1)
--     (date_2, flg_2)
--     (date_3, flg_3)
--   という別々の複合インデックス条件を、
--   1本の問い合わせとしてどう扱うかが問題になる。
--
--   一方、UNION 版は各条件を個別の SELECT に分解していたため、
--   各枝がそれぞれ専用インデックスを使いやすかった。
--
--   つまりこの題材では、
--   OR は「書き方としては短い」が、
--   索引活用の観点では UNION 版に比べて不利になることがある。
-- ================================================

SELECT
    `key`,
    name,
    date_1, flg_1,
    date_2, flg_2,
    date_3, flg_3
FROM
    ThreeElements
WHERE
       (date_1 = '2013-11-01' AND flg_1 = 'T')
    OR (date_2 = '2013-11-01' AND flg_2 = 'T')
    OR (date_3 = '2013-11-01' AND flg_3 = 'T');
    -- この WHERE 句は、
    --   第1ペアが一致
    --   または 第2ペアが一致
    --   または 第3ペアが一致
    -- という意味である。
    --
    -- したがって論理的には、
    -- 先ほどの UNION 版と同じ条件を表している。
    --
    -- ただし書き方としては1本にまとまっているため、
    -- 検索条件の分割は SQL の見た目からは消えている。
    --
    -- 概念的には次のような判定に近い。
    --
    --   if pair1 matches
    --      or pair2 matches
    --      or pair3 matches:
    --       output row
    --
    -- つまりこれは、
    --   「どれか1つの列ペアが一致すれば採用」
    -- という OR 条件検索である。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. ThreeElements の各行を候補として考える
-- 2. 各行について
--      (date_1, flg_1)
--      (date_2, flg_2)
--      (date_3, flg_3)
--    の3組を順に評価する
-- 3. どれか1組でも
--      date_X = '2013-11-01'
--      AND flg_X = 'T'
--    を満たせば、その行を結果へ返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in ThreeElements:
--       match1 = (row.date_1 == '2013-11-01' and row.flg_1 == 'T')
--       match2 = (row.date_2 == '2013-11-01' and row.flg_2 == 'T')
--       match3 = (row.date_3 == '2013-11-01' and row.flg_3 == 'T')
--
--       if match1 or match2 or match3:
--           output row
--
-- つまり、
--   行単位で3つの条件をOR結合して判定する
-- アルゴリズムである。


-- ================================================
-- UNION 版との違い
-- ================================================
--
-- UNION 版は、
--   1本目: (date_1, flg_1) の条件だけ
--   2本目: (date_2, flg_2) の条件だけ
--   3本目: (date_3, flg_3) の条件だけ
-- を別々に実行して、最後に結果を結合していた。
--
-- つまり
--   「索引可能な単純条件に分解してから統合する」
-- 構造だった。
--
-- 一方、OR 版は
--   3種類の列ペア条件を1本の WHERE に埋め込んでいる。
--
-- そのため、構造的には
--   条件の見通しは良い
-- が
--   各複合インデックスを独立枝として素直に使う形
-- からは少し離れる。
--
-- つまりこのケースでは、
--   OR 版 = 論理的には同じ
--   しかし実行戦略の自由度は UNION 版と異なる
-- と理解するとよい。


-- ================================================
-- パフォーマンス比較の観点
-- ================================================
--
-- このケースでは、すでに確認済みの UNION 版の実行計画で
--   1本目 -> IDX_1
--   2本目 -> IDX_2
--   3本目 -> IDX_3
-- が個別に使われていた。
--
-- つまり UNION 版は、
--   各列ペアに対応する複合インデックスを
--   そのまま個別の検索として使える
-- 形になっていた。
--
-- 一方、OR 版では
--   3種類の異なる列ペア条件を1本に束ねるため、
--   実行計画上は
--     全表走査
--     あるいは index_merge
--     あるいは何らかの複合的な最適化
-- のような形になる可能性がある。
--
-- ただし実際にどうなるかは
--   オプティマイザの判断
--   統計情報
--   データ分布
-- に依存するため、
--   EXPLAIN で確認する
-- ことが重要である。
--
-- ここでの理論的な要点は、
--   UNION 版は「各枝で専用インデックスを使う」ことに意味がある
-- のに対し、
--   OR 版は「条件を1本に束ねる」ため、
--   その強みが弱まりやすい
-- という点である。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- 条件に一致する行は
--   key='1'
--   key='2'
--   key='5'
-- の3行である。
--
-- したがって OR 版の結果も UNION 版と同じく、
-- その3行を返す。
--
-- つまり今回の差は
--   論理結果の差
-- ではなく
--   実行方法・索引活用の差
-- にある。


-- ================================================
