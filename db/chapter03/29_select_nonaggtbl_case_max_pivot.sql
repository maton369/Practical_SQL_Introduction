-- ================================================
-- ファイル名: 29_select_nonaggtbl_case_max_pivot.sql
-- 役割:
--   NonAggTbl の縦持ちデータを、
--   CASE 式と MAX 集約を使って id ごとの横持ち形式へ変換する。
--
-- このSQLの目的:
--   各 id について
--     A行から data_1, data_2
--     B行から data_3, data_4, data_5
--     C行から data_6
-- を取り出し、
--   1人 = 1行 の結果表を作ること。
--
-- 学習上の主題:
--   MAX(CASE WHEN ... THEN ... ELSE NULL END)
--   という定番パターンを使って、
--   条件付きで列値を抜き出しながら横持ち化する方法を理解すること。
--
-- 今回の最重要ポイント:
--   このSQLは
--     「大きい値を求めたい」
--   のではなく、
--     「条件に一致する行にだけ値を残し、それ以外は NULL にして、
--       最後に代表値として1つ取り出す」
--   ために MAX を使っている。
--
--   つまり MAX は集約関数として使っているが、
--   本質は
--     条件に合う値を1つだけ拾う
--   ところにある。
--
--   これが
--     どの実装でも通る
-- と言われる理由の中心である。
-- ================================================

SELECT
    id,
    -- id:
    --   最終結果の行キーである。
    --
    --   GROUP BY id により、
    --   同じ id を持つ A/B/C の3行を1行へまとめる。

    MAX(CASE WHEN data_type = 'A' THEN data_1 ELSE NULL END) AS data_1,
    -- data_1 の作り方:
    --
    -- CASE WHEN data_type = 'A' THEN data_1 ELSE NULL END
    --   → A行なら data_1 の値を返す
    --   → A行以外(B/C行)なら NULL を返す
    --
    -- したがって、ある id の3行に対してこの式を評価すると、
    --   A行: 実値
    --   B行: NULL
    --   C行: NULL
    -- のようになる。
    --
    -- その後 MAX を取ると、
    --   実値が1つ + NULLたち
    -- から、その実値が返る。
    --
    -- ここで重要なのは、
    --   「最大値だから選ばれる」のではなく、
    --   「非NULL値が1つだけだから結果としてそれが返る」
    -- という点である。

    MAX(CASE WHEN data_type = 'A' THEN data_2 ELSE NULL END) AS data_2,
    -- data_2 も同じ考え方である。
    --
    -- A行だけから data_2 を取り出し、
    -- B/C行は NULL にする。
    --
    -- そのうえで MAX によって、
    -- A行の値だけを最終結果へ残す。

    MAX(CASE WHEN data_type = 'B' THEN data_3 ELSE NULL END) AS data_3,
    -- data_3:
    --   B行だけを対象に data_3 を取り出す。
    --
    -- A/C行は NULL になるため、
    -- MAX 後には B行の data_3 が残る。

    MAX(CASE WHEN data_type = 'B' THEN data_4 ELSE NULL END) AS data_4,
    -- data_4:
    --   B行だけを対象に data_4 を取り出す。

    MAX(CASE WHEN data_type = 'B' THEN data_5 ELSE NULL END) AS data_5,
    -- data_5:
    --   B行だけを対象に data_5 を取り出す。

    MAX(CASE WHEN data_type = 'C' THEN data_6 ELSE NULL END) AS data_6
    -- data_6:
    --   C行だけを対象に data_6 を取り出す。
    --
    -- A/B行は NULL になるため、
    -- MAX 後には C行の data_6 が残る。

FROM
    NonAggTbl
    -- FROM NonAggTbl:
    --   元データの表である。
    --
    --   この表は id ごとに A/B/C の複数行を持つため、
    --   そのままでは 1人 = 1行 になっていない。
    --
    --   今回はこの縦持ち状態を、
    --   CASE + 集約で横持ちへ変換する。

GROUP BY
    id;
    -- GROUP BY id:
    --   同じ id の行を1つのグループにまとめる。
    --
    --   これにより、
    --   各 id について
    --     A行
    --     B行
    --     C行
    -- をまとめて扱える。
    --
    --   そのグループの中で、
    --   CASE 式によって必要な列だけを残し、
    --   MAX で1つに確定させている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. NonAggTbl を id ごとにグループ化する
-- 2. 各 id グループの中で、
--    data_type に応じて必要な列だけ値を残し、
--    それ以外は NULL にする
-- 3. その列ごとに MAX を取る
-- 4. 非NULL値だけが結果として残る
-- 5. id ごとに1行の横持ち結果を返す
--
-- 概念的には次のような処理に近い。
--
--   groups = group NonAggTbl by id
--
--   for each id group:
--       out.data_1 = value of data_1 from A row
--       out.data_2 = value of data_2 from A row
--       out.data_3 = value of data_3 from B row
--       out.data_4 = value of data_4 from B row
--       out.data_5 = value of data_5 from B row
--       out.data_6 = value of data_6 from C row
--
--       output out
--
-- つまりこれは、
--   「行種別ごとに持っている値を、
--     1人1行の結果へ再配置する」
--   横持ち変換アルゴリズムである。


-- ================================================
-- なぜ MAX を使うのか
-- ================================================
--
-- このSQLを初めて見ると、
--   なぜ MAX なのか
--   SUM ではだめか
--   MIN でもよいのか
-- と感じやすい。
--
-- 今回の本質は次である。
--
--   各 id について
--   各出力列ごとに
--   条件に一致する非NULL値は高々1つ
--
-- という前提がある。
--
-- たとえば data_1 なら、
--   A行だけが候補
-- であり、
--   B行/C行は NULL
-- になる。
--
-- したがって
--   実値1つ + NULL複数
-- から代表値を取り出せばよい。
--
-- このとき
--   MAX
--   MIN
-- はどちらでも同じ値を返しやすい。
--
-- しかし実務・教材では
--   MAX(CASE WHEN ... THEN ... END)
-- が非常によく使われる定番形であり、
--   DB製品間でも比較的素直に通りやすい。
--
-- そのため「これが正解」とされている。
--
-- つまり MAX は
--   数値の大きさ比較が主目的ではなく、
--   条件で残した1値を集約して返すための器
-- として使っている。


-- ================================================
-- なぜ ELSE NULL なのか
-- ================================================
--
-- ELSE NULL を書いているのは、
-- 条件に一致しない行を
--   「その列には関係ない」
-- として無視したいからである。
--
-- もし ELSE 0 のようにしてしまうと、
-- 本来データが存在しない場合と
-- 実際に 0 が入っている場合の区別が曖昧になる。
--
-- 一方、NULL なら
--   「この行はその出力列に寄与しない」
-- ことを自然に表現できる。
--
-- そのうえで MAX は NULL を無視して実値を返しやすいので、
-- このパターンが非常に相性がよい。
--
-- したがって
--   MAX(CASE WHEN ... THEN 値 ELSE NULL END)
-- は、
--   条件付き抽出 + 横持ち変換
-- の定番になる。


-- ================================================
-- 実行結果のイメージ
-- ================================================
--
-- たとえば Jim の3行は次のようになっている。
--
--   Jim, A, data_1=100, data_2=10, ...
--   Jim, B, data_3=167, data_4=77, data_5=90, ...
--   Jim, C, ..., data_6=457
--
-- これを最終結果にすると、
--
--   Jim | 100 | 10 | 167 | 77 | 90 | 457
--
-- のようになる。
--
-- 同様に Ken, Beth も
--   A行から必要列
--   B行から必要列
--   C行から必要列
-- を取り出して1行へ合成する。
--
-- つまり最終結果は概ね次のようになる。
--
--   id   | data_1 | data_2 | data_3 | data_4 | data_5 | data_6
--   -----+--------+--------+--------+--------+--------+-------
--   Jim  | 100    | 10     | 167    | 77     | 90     | 457
--   Ken  | 78     | 5      | 178    | 346    | 85     | 33
--   Beth | 75     | 0      | 183    | NULL   | 4      | 12
--
-- これが
--   縦持ち → 横持ち
-- の変換結果である。


-- ================================================
-- 図解: CASE + MAX の横持ち変換
-- ================================================
--
--   NonAggTbl
--      |
--      v
--   GROUP BY id
--      |
--      v
--   各 id グループの中で CASE 評価
--      |
--      +--------------------------------------------+
--      | data_type='A' のとき data_1, data_2 を残す |
--      | data_type='B' のとき data_3, data_4, data_5|
--      | data_type='C' のとき data_6 を残す         |
--      +--------------------------------------------+
--      |
--      v
--   それ以外は NULL
--      |
--      v
--   各列ごとに MAX を取る
--      |
--      v
--   非NULLの唯一の値が残る
--      |
--      v
--   id ごとに1行の結果
--
-- つまり、
--   条件で値を残す
--   集約で1つにまとめる
-- という2段構造になっている。


-- ================================================
-- なぜ「どの実装でも通る」と言えるのか
-- ================================================
--
-- この書き方の強みは、
--   GROUP BY の中で
--   集約されていない列をあいまいに返さない
-- 点にある。
--
-- たとえば不適切な書き方では、
--   GROUP BY id
-- なのに
--   data_1 や team をそのまま SELECT に出してしまい、
--   DB製品によってはエラーになったり、
--   どの行の値が返るか不定になったりすることがある。
--
-- 一方、今回の SQL は
--   すべての出力列が
--     MAX(...)
--   という集約関数の内側にある。
--
-- そのため、
--   GROUP BY 規則に素直で、
--   DB実装差に左右されにくい
-- という意味で「どの実装でも通る」と言いやすい。
--
-- つまりこの SQL は、
--   論理的にも正しく、
--   集約規則にも忠実
-- な安全な書き方である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT id, ...
--   → 最終結果として id ごとの横持ち列を返す
--
-- MAX(CASE WHEN data_type = 'A' THEN data_1 ELSE NULL END)
--   → A行の data_1 だけを取り出して1つに確定する
--
-- MAX(CASE WHEN data_type = 'A' THEN data_2 ELSE NULL END)
--   → A行の data_2 だけを取り出して1つに確定する
--
-- MAX(CASE WHEN data_type = 'B' THEN data_3 ELSE NULL END)
--   → B行の data_3 だけを取り出して1つに確定する
--
-- MAX(CASE WHEN data_type = 'B' THEN data_4 ELSE NULL END)
--   → B行の data_4 だけを取り出して1つに確定する
--
-- MAX(CASE WHEN data_type = 'B' THEN data_5 ELSE NULL END)
--   → B行の data_5 だけを取り出して1つに確定する
--
-- MAX(CASE WHEN data_type = 'C' THEN data_6 ELSE NULL END)
--   → C行の data_6 だけを取り出して1つに確定する
--
-- FROM NonAggTbl
--   → 元データの表
--
-- GROUP BY id
--   → id ごとに1行へまとめる


-- ================================================
