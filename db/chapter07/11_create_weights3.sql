-- ================================================
-- ファイル名: 11_create_weights3.sql
-- 役割:
--   クラスごとの連番を後から書き込むための Weights3 テーブルを作成する。
--
-- このSQLの目的:
--   既存データに対して
--     class ごとの student_id 順の連番
--   を seq 列へ UPDATE で埋め込む題材を用意すること。
--
-- 学習上の主題:
--   - 連番を「SELECT で表示する」だけでなく「表に保存する」こと
--   - 更新対象列 seq を NULL で初期化しておくこと
--   - 後続の UPDATE 文で class ごと連番を埋める流れ
--
-- 今回の最重要ポイント:
--   前回までは
--     SELECT した結果として連番を表示する
--   だけだった。
--
--   今回は
--     UPDATE で seq 列に実際に書き込む
--   ことがテーマである。
--
--   そのためこの表では、
--     seq
--   という更新対象列を持っている。
--
--   つまり今回の本質は
--     「計算した連番を、結果として見るのではなく、
--       テーブルの列として保存する」
--   ところにある。
-- ================================================

CREATE TABLE Weights3
(
    class INTEGER NOT NULL,
    -- class:
    --   クラス番号を表す。
    --
    --   今回の連番は
    --     class ごと
    -- に独立して振るので、
    --   グループの境界を決める列である。
    --
    --   つまり
    --     class=1 の中で 1,2,3,...
    --     class=2 の中で 1,2,3,...
    --   という番号付けになる。

    student_id CHAR(4) NOT NULL,
    -- student_id:
    --   クラス内の学生IDである。
    --
    --   同じ class の中では
    --   student_id 昇順で seq を付ける。
    --
    --   したがってこの列は
    --     クラス内順位の基準列
    --   である。

    weight INTEGER NOT NULL,
    -- weight:
    --   体重を表す列である。
    --
    --   今回の seq 計算には使わない。
    --
    --   つまり今回の主題は
    --     体重順
    --   ではなく
    --     class ごとの student_id 順
    --   に従って連番を保存することにある。

    seq INTEGER NULL,
    -- seq:
    --   後から UPDATE で埋める連番列である。
    --
    --   初期状態では NULL を入れておき、
    --   後続の UPDATE 文で
    --     class ごとの student_id 順の番号
    --   を書き込む。
    --
    --   つまりこの列は
    --     計算結果の保存先
    --   である。

    PRIMARY KEY(class, student_id)
    -- PRIMARY KEY(class, student_id):
    --   複合主キーである。
    --
    --   class と student_id の組で各行が一意に決まる。
    --
    --   この一意性が重要で、
    --   後続の UPDATE では
    --     計算した連番をどの行へ書き戻すか
    --   を正確に対応付けられる。
);