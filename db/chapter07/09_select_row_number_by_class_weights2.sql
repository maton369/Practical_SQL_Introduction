-- ================================================
-- ファイル名: 09_select_row_number_by_class_weights2.sql
-- 役割:
--   クラスごとに連番を振る。
--
-- このSQLの目的:
--   Weights2 の全体で通し番号を振るのではなく、
--   class ごとに独立した番号列を作ること。
--
-- 学習上の主題:
--   - PARTITION BY の意味
--   - グループごとに連番がリセットされること
--   - 全表通し番号とクラス内番号の違い
--
-- 今回の最重要ポイント:
--   前回の ROW_NUMBER() 版は
--     ORDER BY class, student_id
--   によって
--     全表に対する通し番号
--   を作っていた。
--
--   今回は
--     PARTITION BY class
--   を入れることで、
--   クラスごとに独立して
--     1,2,3,...
--   を振る。
--
--   つまりアルゴリズムとしては
--     1. まず class ごとに行を分ける
--     2. 各 class の中で student_id 順に並べる
--     3. 各 class ごとに 1,2,3,... を振る
--   という「グループ内連番付与」になっている。
--
--   これが PARTITION BY を使った連番の本質である。
-- ================================================

SELECT
       class,
       -- class:
       --   クラス番号を返す。
       --
       --   今回は連番が
       --     class ごと
       -- に振られるので、
       --   どのクラスの何番目かを確認するために必要な列である。
       --
       --   つまりこの列は
       --     パーティションの識別子
       -- でもある。

       student_id,
       -- student_id:
       --   クラス内の学生IDを返す。
       --
       --   各クラスの中では
       --   student_id 昇順で連番を振るため、
       --   クラス内順序の基準列になる。

       ROW_NUMBER() OVER (
           PARTITION BY class
           ORDER BY student_id
       ) AS seq
       -- ROW_NUMBER():
       --   各行へ 1,2,3,... の番号を振るウィンドウ関数である。
       --
       -- PARTITION BY class:
       --   class ごとに別々のグループを作る。
       --
       --   ここが最重要ポイントである。
       --
       --   これにより、
       --   たとえば
       --     class=1 の行群
       --   と
       --     class=2 の行群
       --   は別々に番号付けされる。
       --
       --   したがって、
       --   class=2 に入った時点で
       --   seq は再び 1 から始まる。
       --
       -- ORDER BY student_id:
       --   各クラスの中で
       --   student_id 昇順に並べる。
       --
       --   つまりクラス内の順位は
       --     student_id の小さい順
       --   で決まる。
       --
       -- たとえば class=2 の中では
       --   100,101,102,103
       -- の順に
       --   1,2,3,4
       -- が振られる。
       --
       -- AS seq:
       --   生成したクラス内連番に seq という名前を付けている。
       --
       --   この seq は
       --     「そのクラスの中で何番目か」
       --   を意味する。

  FROM Weights2;
  -- FROM Weights2:
  --   元表 Weights2 の各行に対して、
  --   クラスごとの連番を付与する。
  --
  --   ROW_NUMBER() + PARTITION BY を使うことで、
  --   元行を保持したまま
  --   「クラス内順位」
  --   を追加できる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Weights2 の全行を class ごとに分割する
-- 2. 各 class グループの中で student_id 昇順に並べる
-- 3. 各グループごとに 1,2,3,... の番号を振る
--
-- 概念的には次のような処理に近い。
--
--   for each class_group in Weights2 grouped by class:
--       sort class_group by student_id
--
--       seq = 1
--       for each row in class_group:
--           row.seq = seq
--           seq += 1
--
-- つまりこれは、
--   「グループごとに順序を決め、その中だけで通し番号を振る」
--   アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- class=1 の行:
--   100
--   101
--   102
--
-- これに対して
--   1,2,3
-- を振る。
--
-- class=2 の行:
--   100
--   101
--   102
--   103
--
-- これに対して
--   1,2,3,4
-- を振る。
--
-- したがって最終結果は概ね
--
--   1 100 1
--   1 101 2
--   1 102 3
--   2 100 1
--   2 101 2
--   2 102 3
--   2 103 4
--
-- になる。
--
-- 重要:
--   これは前回の
--     全表通し番号
--   とは異なる。
--
--   今回は
--     class=1 で 1,2,3
--     class=2 で 1,2,3,4
--   と、クラス単位で番号がリセットされる。


-- ================================================
-- 図解: PARTITION BY class による連番
-- ================================================
--
--   Weights2
--      |
--      | PARTITION BY class
--      v
--   class=1                 class=2
--   -------                 -------
--   100                     100
--   101                     101
--   102                     102
--                           103
--      |                       |
--      | ORDER BY student_id   | ORDER BY student_id
--      v                       v
--   100 1                   100 1
--   101 2                   101 2
--   102 3                   102 3
--                           103 4
--
-- つまり、
--   全体に1本の番号を振るのではなく、
--   class ごとに独立した番号列を作っている。


-- ================================================
-- 前回の全表通し番号との違い
-- ================================================
--
-- 前回の SQL:
--
--   ROW_NUMBER() OVER (ORDER BY class, student_id)
--
-- は、
--   class=1 の後に class=2 が続く
--   1本の全順序
-- に対して番号を振っていた。
--
-- そのため結果は
--
--   (1,100) -> 1
--   (1,101) -> 2
--   (1,102) -> 3
--   (2,100) -> 4
--   ...
--
-- となっていた。
--
-- 一方今回の SQL は
--
--   ROW_NUMBER() OVER (PARTITION BY class ORDER BY student_id)
--
-- であり、
--   class ごとに独立して番号を振る。
--
-- そのため
--
--   class=1:
--     100 -> 1
--     101 -> 2
--     102 -> 3
--
--   class=2:
--     100 -> 1
--     101 -> 2
--     102 -> 3
--     103 -> 4
--
-- となる。
--
-- つまり
--
--   前回:
--     全表の中で何番目か
--
--   今回:
--     クラスの中で何番目か
--
-- という違いがある。
--
-- ここが PARTITION BY の意味そのものである。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   順序を1本の全体順序として扱うのではなく、
--   グループごとに独立させる
-- ところにある。
--
-- つまり PARTITION BY は
--   「連番の世界を区切る」
--   役割を持つ。
--
-- 今回でいえば、
--   class=1 の世界
--   class=2 の世界
-- を別々に作り、
--   それぞれの中でだけ
--   1,2,3,...
-- を振っている。
--
-- これがグループ内連番付与の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER()
--   → 各グループ内で 1,2,3,... を振る
--
-- PARTITION BY class
--   → class ごとに独立した番号付け空間を作る
--
-- ORDER BY student_id
--   → 各 class 内での順序を student_id 昇順で決める
--
-- AS seq
--   → そのクラス内連番に seq という名前を付ける
-- ================================================