-- ================================================
-- ファイル名: 19_select_median_procedural_turning_point.sql
-- 役割:
--   手続き型の別表現で Weights テーブルのメジアンを求める。
--
-- このSQLの目的:
--   weight 昇順の位置と全件数を使って、
--   「中央をまたぐ折り返し地点」を数式で表し、
--   その地点にある行だけを残して平均を取ることでメジアンを求めること。
--
-- 学習上の主題:
--   - ROW_NUMBER() と COUNT() OVER() を組み合わせた中心判定
--   - diff という差分量で中央位置を数式化する考え方
--   - 1本の昇順順位からメジアンを見つける方法
--   - リスト8.13とのアルゴリズム比較
--
-- 今回の最重要ポイント:
--   このSQLでは各行に対して
--
--     diff = 2 * 行番号 - 全件数
--
--   を計算している。
--
--   行番号が前半にあるうちは diff は小さく、
--   中央を超えると diff が大きくなる。
--
--   つまり diff は
--     「中央から見て今どこにいるか」
--   を表す指標である。
--
--   この diff が
--     0 から 2 の間
--   に入る行が、
--   ちょうど中央または中央直後に位置する行になる。
--
--   その行だけを残して AVG(weight) を取れば、
--   奇数件でも偶数件でもメジアンが得られる。
--
--   これが「折り返し地点を見つける」方式の本質である。
-- ================================================

SELECT
       AVG(weight) AS median
       -- AVG(weight):
       --   diff 条件を満たして残った中央候補の平均を取る。
       --
       --   奇数件なら中央1行だけが残るので、
       --   AVG(weight) はその値自身になる。
       --
       --   偶数件なら中央2行が残るので、
       --   AVG(weight) はその2値の平均になる。
       --
       --   したがってこの AVG により、
       --   奇数件・偶数件を統一的に扱える。

  FROM
       (
           SELECT
                  weight,
                  -- weight:
                  --   元データの体重値である。
                  --
                  --   この行が中央候補として残るかどうかを、
                  --   diff の値で判定する。

                  2 * ROW_NUMBER() OVER (ORDER BY weight, student_id)
                    - COUNT(*) OVER () AS diff
                  -- ROW_NUMBER() OVER (ORDER BY weight, student_id):
                  --   小さい順に並べたときの位置番号である。
                  --
                  --   つまり
                  --     1,2,3,4,...
                  --   の順位を各行に与える。
                  --
                  --   ここでも同じ weight が複数ある場合に備えて
                  --   student_id を第2キーに入れ、
                  --   順序を安定させている。
                  --
                  -- COUNT(*) OVER():
                  --   全件数 N を各行に持たせる。
                  --
                  --   つまり各行は
                  --     自分の順位
                  --     全件数
                  --   の両方を持つことになる。
                  --
                  -- 2 * ROW_NUMBER() - COUNT(*) の意味:
                  --   行番号を2倍し、全件数を引くことで、
                  --   その行が中央より前か後ろかを数式で表している。
                  --
                  --   たとえば件数 N=7 の場合、
                  --   行番号 i に対して
                  --     diff = 2i - 7
                  --   となる。
                  --
                  --   i=1 -> -5
                  --   i=2 -> -3
                  --   i=3 -> -1
                  --   i=4 ->  1
                  --   i=5 ->  3
                  --   ...
                  --
                  --   中央をまたぐ地点で diff が 0 付近になる。
                  --
                  --   この diff を使うことで、
                  --   中央候補だけを WHERE 句で取り出せる。
             FROM Weights
             -- Weights:
             --   元表である。
             --
             --   ここでは各行に
             --     weight 順の位置
             --     全件数
             --     そこから導かれる diff
             --   を持たせた中間表を作っている。
       ) TMP
       -- TMP:
       --   各行に diff を付けた派生表である。
       --
       --   外側ではこの diff の値で中央候補だけを残す。

 WHERE diff BETWEEN 0 AND 2;
 -- WHERE diff BETWEEN 0 AND 2:
 --   中央に相当する行だけを残す条件である。
 --
 --   diff の意味をもう一度書くと
 --     diff = 2 * 行番号 - 全件数
 --   である。
 --
 --   この値が
 --     0 から 2
 --   に入る行は、
 --   ちょうど中央または中央直後の位置にある。
 --
 -- 奇数件の例:
 --   N=7 なら diff 列は
 --     -5, -3, -1, 1, 3, 5, 7
 --   となる。
 --
 --   0〜2 に入るのは diff=1 の1行だけで、
 --   それが中央行になる。
 --
 -- 偶数件の例:
 --   N=4 なら diff 列は
 --     -2, 0, 2, 4
 --   となる。
 --
 --   0〜2 に入るのは
 --     diff=0
 --     diff=2
 --   の2行であり、
 --   それが中央2行になる。
 --
 --   その平均を取ればメジアンになる。
 --
 -- つまりこの条件は、
 --   diff の符号変化・折り返し地点を使って
 --   中央行を見つけている。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. weight 昇順に各行の順位 i を付ける
-- 2. 全件数 N を各行に持たせる
-- 3. diff = 2i - N を計算する
-- 4. diff が 0〜2 に入る行だけを残す
-- 5. 残った行の平均を取る
--
-- 概念的には次のような処理に近い。
--
--   sort rows by weight ascending
--   N = total number of rows
--
--   for each row with position i:
--       diff = 2*i - N
--       if 0 <= diff <= 2:
--           keep row
--
--   median = average(weight of kept rows)
--
-- つまりこれは、
--   「中央をまたぐ折り返し地点を diff という数式で検出する」
--   手続き型アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Weights の値:
--   50
--   55
--   55
--   60
--   72
--   72
--   72
--
-- 件数 N = 7
--
-- 昇順順位 i:
--   50 -> 1
--   55 -> 2
--   55 -> 3
--   60 -> 4
--   72 -> 5
--   72 -> 6
--   72 -> 7
--
-- diff = 2i - 7:
--   i=1 -> -5
--   i=2 -> -3
--   i=3 -> -1
--   i=4 ->  1
--   i=5 ->  3
--   i=6 ->  5
--   i=7 ->  7
--
-- diff BETWEEN 0 AND 2 を満たすのは
--   diff=1
-- の1行だけである。
--
-- それは weight=60 の行なので、
-- AVG(60)=60 がメジアンになる。


-- ================================================
-- 偶数件にも対応できる理由
-- ================================================
--
-- たとえば値が
--   10,20,30,40
-- の4件なら、
--
-- N = 4
-- i = 1,2,3,4
-- diff = -2,0,2,4
--
-- このとき
--   diff BETWEEN 0 AND 2
-- を満たすのは
--   20 (diff=0)
--   30 (diff=2)
-- の2行である。
--
-- AVG(20,30)=25 となり、
-- 偶数件のメジアンが得られる。
--
-- つまり diff 条件により、
-- 奇数件では1行、
-- 偶数件では2行が自然に選ばれる。


-- ================================================
-- 図解: 折り返し地点を見つける
-- ================================================
--
--   weight順に並べる
--        |
--        v
--   50   i=1  diff=-5
--   55   i=2  diff=-3
--   55   i=3  diff=-1
--   60   i=4  diff= 1   <-- 中央
--   72   i=5  diff= 3
--   72   i=6  diff= 5
--   72   i=7  diff= 7
--
--   diff が負から正へ切り替わる
--   その折り返し地点付近が中央候補になる。
--
--   0〜2 に入る行だけを残して平均を取れば
--   メジアンになる。


-- ================================================
-- リスト8.13との違い
-- ================================================
--
-- リスト8.13では
--   左からの順位 hi
--   右からの順位 lo
-- を両方作って、
--   その2つがぶつかる行を探していた。
--
-- 一方このSQLでは、
--   左からの順位 i と全件数 N だけから
--   diff = 2i - N
-- を作り、
--   その値で中央位置を判定している。
--
-- つまり
--
--   リスト8.13:
--     左右両方向の順位を持って中央を見つける
--
--   リスト8.14:
--     左からの順位と全件数だけで中央の折り返しを見つける
--
-- という違いがある。
--
-- どちらも手続き型だが、
--   8.13 は「両側から出会う」
--   8.14 は「差分が中央で反転する」
-- という表現になっている。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   中央位置を
--     行番号と全件数の差分
--   で数式化しているところにある。
--
-- つまりメジアンを
--   「並べたときの真ん中」
-- として直接言うのではなく、
--   「行番号が全体の折り返し地点に来た場所」
-- として表現している。
--
-- これにより、
--   2本の順位列を持たなくても
--   1本の順位列と全件数だけで
--   中央候補を選べる。
--
-- これがこの方法の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER() OVER (ORDER BY weight, student_id)
--   → 左からの順位 i を作る
--
-- COUNT(*) OVER()
--   → 全件数 N を各行に持たせる
--
-- 2 * ROW_NUMBER() - COUNT(*) OVER()
--   → 中央からの折り返し指標 diff を作る
--
-- WHERE diff BETWEEN 0 AND 2
--   → 中央1行または中央2行だけを残す
--
-- AVG(weight)
--   → 残った中央候補の平均を取り、メジアンを返す
-- ================================================