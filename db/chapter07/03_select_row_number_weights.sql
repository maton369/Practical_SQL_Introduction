-- ================================================
-- ファイル名: 03_select_row_number_weights.sql
-- 役割:
--   ROW_NUMBER() を使って、
--   student_id 順に連番を振る。
--
-- このSQLの目的:
--   主キーが1列の表に対して、
--   ウィンドウ関数で最も素直に連番を付ける方法を示すこと。
--
-- 学習上の主題:
--   - ROW_NUMBER() の意味
--   - ORDER BY に基づく連番付与
--   - 主キーが1列のときに順序が一意に決まること
--
-- 今回の最重要ポイント:
--   ROW_NUMBER() は、
--   指定した並び順に従って
--     1,2,3,...
--   の連番を各行へ振る。
--
--   今回は
--     ORDER BY student_id
--   なので、
--   student_id の昇順に従って
--   連番が付く。
--
--   つまりアルゴリズムとしては
--     1. Weights を student_id 順に並べる
--     2. 先頭から 1,2,3,... を付ける
--   という非常に単純な流れである。
--
--   これが「連番を振る」処理の基本形である。
-- ================================================

SELECT
       student_id,
       -- student_id:
       --   学生IDを返す。
       --
       --   どの行にどの連番が付いたのかを確認するため、
       --   まず基準列をそのまま表示する。

       ROW_NUMBER() OVER (ORDER BY student_id) AS seq
       -- ROW_NUMBER():
       --   各行へ 1,2,3,... の連番を付けるウィンドウ関数である。
       --
       -- OVER (ORDER BY student_id):
       --   連番を付ける順序を
       --     student_id 昇順
       --   で決めている。
       --
       --   つまり
       --     A100
       --     A101
       --     A124
       --     B343
       --     B346
       --     C345
       --     C563
       --   の順に並び、
       --   それぞれへ
       --     1,2,3,4,5,6,7
       --   が振られる。
       --
       --   ここで student_id が主キーなので、
       --   並び順にあいまいさがない。
       --
       --   もし ORDER BY に使う列が重複していた場合、
       --   並び順のあいまいさをどう扱うかを考える必要があるが、
       --   今回は主キーなのでその問題はない。
       --
       -- AS seq:
       --   生成した連番列に seq という名前を付けている。
       --
       --   この seq が今回の主題である
       --     「振られた連番」
       --   を表す。

  FROM Weights;
  -- FROM Weights:
  --   元表 Weights の各行に対して連番を付ける。
  --
  --   ウィンドウ関数版では、
  --   元の行を失わずに
  --   「その行は何番目か」
  --   という情報を追加できる。
  --
  --   つまり
  --     連番付与は元行への属性追加
  --   と考えると分かりやすい。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Weights の全行を student_id 順に並べる
-- 2. 先頭行へ 1 を付ける
-- 3. 次の行へ 2 を付ける
-- 4. これを最後まで繰り返す
--
-- 概念的には次のような処理に近い。
--
--   sort rows by student_id
--
--   seq = 1
--   for each row in sorted rows:
--       row.seq = seq
--       seq += 1
--
-- つまりこれは、
--   「順序を決めて先頭から番号を振る」
--   連番付与アルゴリズムである。


-- ================================================
-- 図解: ROW_NUMBER による連番付与
-- ================================================
--
--   Weights
--      |
--      | ORDER BY student_id
--      v
--   A100
--   A101
--   A124
--   B343
--   B346
--   C345
--   C563
--      |
--      | ROW_NUMBER()
--      v
--   A100  1
--   A101  2
--   A124  3
--   B343  4
--   B346  5
--   C345  6
--   C563  7
--
-- つまり、
--   並べる
--   -> 順に番号を振る
-- の2段階である。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   主キー student_id の順序に基づき、
--   各行へ一意な位置番号を与える
-- ところにある。
--
-- つまり連番とは、
--   行の順序を数値で明示化したもの
-- である。
--
-- 今回は
--   student_id の昇順
-- を行順として採用しているため、
--   その順番がそのまま seq になる。
--
-- これが ROW_NUMBER を使った連番付与のもっとも素直な考え方である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER()
--   → 各行へ 1,2,3,... の連番を付ける
--
-- OVER (ORDER BY student_id)
--   → student_id 昇順で番号を振る
--
-- AS seq
--   → 連番列に seq という名前を付ける
-- ================================================