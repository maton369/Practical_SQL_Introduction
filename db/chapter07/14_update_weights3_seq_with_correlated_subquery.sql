-- ================================================
-- ファイル名: 14_update_weights3_seq_with_correlated_subquery.sql
-- 役割:
--   相関サブクエリを使って、
--   class ごとの連番を Weights3.seq へ UPDATE で書き込む。
--
-- このSQLの目的:
--   各更新対象行について、
--   同じ class の中で自分以下の student_id 件数を数え、
--   その件数を seq として保存すること。
--
-- 学習上の主題:
--   - UPDATE 文の中で相関サブクエリを使う方法
--   - class ごとの順位を COUNT(*) で表現する考え方
--   - ROW_NUMBER 更新版とのアルゴリズム比較
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   UPDATE 対象の現在行 Weights3 に対して、
--   内側サブクエリが
--     1. 同じ class の行だけを見る
--     2. その中で student_id が自分以下の件数を数える
--   ことで、
--   クラス内順位をそのまま seq として計算している。
--
--   つまりアルゴリズムとしては
--     1. 外側で更新対象行を1件取る
--     2. 同じ class の中で自分以下の student_id 件数を数える
--     3. その件数を seq に代入する
--   という「各行ごとに自分の順位を問い合わせて更新する」形である。
--
--   これが相関サブクエリ更新版の本質である。
-- ================================================

UPDATE Weights3
   SET seq =
       (
           SELECT COUNT(*)
             FROM Weights3 W2
            WHERE W2.class = Weights3.class
              AND W2.student_id <= Weights3.student_id
              -- W2.class = Weights3.class:
              --   まず UPDATE 中の現在行と同じ class の行だけを見る。
              --
              --   これが
              --     PARTITION BY class
              -- に相当する役割を持つ。
              --
              --   つまり
              --     クラスごとに独立した番号空間を作る
              -- 条件である。
              --
              -- W2.student_id <= Weights3.student_id:
              --   同じ class の中で、
              --   現在行以下の student_id を持つ行だけを数える。
              --
              --   これにより
              --     「そのクラスの中で自分は何番目か」
              --   が件数として得られる。
              --
              -- COUNT(*) の意味:
              --   class ごとの student_id 順で見たときの
              --   現在行の順位を表す。
              --
              --   たとえば現在行が
              --     class=2, student_id='102'
              --   なら、
              --   同じ class=2 で自分以下は
              --     100
              --     101
              --     102
              --   の3件なので、
              --   seq = 3 になる。
       );

-- ================================================
-- このSQLが何をしているか
-- ================================================
--
-- 各更新対象行について、
-- 次の問い合わせをしていると考えると分かりやすい。
--
--   「自分と同じ class の中で、
--     student_id が自分以下の行は何件あるか？」
--
-- その答えがそのまま
--   クラス内連番
-- になる。
--
-- したがって、
-- その件数を seq 列へ書き込んでいる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. UPDATE 対象の1行を現在行として見る
-- 2. 同じ class の行だけを内側で取り出す
-- 3. その中で student_id が現在行以下の件数を数える
-- 4. その件数を現在行の seq へ代入する
-- 5. 全行についてこれを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each row in Weights3:
--       seq =
--           count(rows w2
--                 where w2.class == row.class
--                   and w2.student_id <= row.student_id)
--       row.seq = seq
--
-- つまりこれは、
--   「各行が自分のクラス内順位を件数として計算し、
--     その値をそのまま保存する」
--   アルゴリズムである。


-- ================================================
-- 図解: 相関サブクエリ版の更新
-- ================================================
--
--   現在行 = (2,102)
--         |
--         v
--   同じ class=2 の行だけを見る
--         |
--         v
--   100
--   101
--   102
--   103
--         |
--         | student_id <= 102
--         v
--   100
--   101
--   102
--         |
--         v
--     COUNT(*) = 3
--         |
--         v
--   現在行の seq に 3 を書く
--
-- つまり、
--   まず同じ class に絞る
--   次に自分以下を数える
--   その件数を書き込む
-- という流れである。


-- ================================================
-- ROW_NUMBER 更新版との違い
-- ================================================
--
-- ROW_NUMBER 更新版では
--   先に SeqTbl という連番対応表を作ってから、
--   その結果を class, student_id で対応付けて
--   書き戻していた。
--
--   つまり発想は
--     連番表を作る -> それを参照して更新する
--   である。
--
-- 一方、相関サブクエリ更新版では
--   各行ごとに
--     自分のクラス内順位を COUNT(*) で直接計算
--   している。
--
--   つまり発想は
--     各行が自分の順位を問い合わせ、その答えを書き込む
--   である。
--
-- したがって
--
--   ROW_NUMBER 更新版:
--     一括で対応表を作って更新
--
--   相関サブクエリ更新版:
--     各行ごとに順位を直接計算して更新
--
-- という違いがある。
--
-- 結果は同じでも、
--   アルゴリズムの見え方はかなり異なる。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   グループ内順位を
--   「同じ class の中で自分以下の件数」
--   として定義し、
--   それを UPDATE の値としてそのまま使っている
-- ところにある。
--
-- つまり
--   SELECT で順位を表示する
-- のではなく、
--   順位を列値として保存する
-- ことに踏み込んでいる。
--
-- ここで重要なのは、
--   class ごとの世界を
--     W2.class = Weights3.class
--   で作り、
--   その中の順位を
--     W2.student_id <= Weights3.student_id
--   の件数で表している点である。
--
-- これが相関サブクエリ更新版の核心である。


