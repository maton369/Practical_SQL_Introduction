-- 役割:
--   リスト8.14の手続き型メジアンSQLの実行計画を確認する。
--
-- このSQLの目的:
--   DBMS が
--     ROW_NUMBER() の計算
--     COUNT(*) OVER() の計算
--     diff の生成
--     派生表 TMP の生成
--     diff 条件による絞り込み
--     外側の AVG(weight)
--   をどう処理するかを見ること。
--
-- 学習上の主題:
--   - ウィンドウ関数を複数使う場合の EXPLAIN の見方
--   - 行番号と全件数を同時に扱う中間表の構造
--   - 手続き型メジアンの別表現
--
-- 今回の最重要ポイント:
--   このSQLは
--     昇順順位
--     全件数
--   の2つを各行へ付け、
--   その差分 diff により中央候補を判定する。
--
--   したがって実行計画では
--     派生表 TMP
--     ソート
--     temporary
--   などが現れる可能性がある。
--
--   重要なのは、
--   このSQLが
--     先に diff 付き中間表を作る
--     その後で中央候補だけ残す
--   という二段構造である点である。
-- ================================================

EXPLAIN
SELECT AVG(weight)
  FROM
       (
           SELECT weight,
                  2 * ROW_NUMBER() OVER (ORDER BY weight, student_id)
                    - COUNT(*) OVER () AS diff
             FROM Weights
       ) TMP
 WHERE diff BETWEEN 0 AND 2;

-- ================================================
-- 実行計画で見たいポイント
-- ================================================
--
-- 1. 派生表 TMP の生成
--    -> まず diff 付き中間表を作る構造なので DERIVED が見えやすい
--
-- 2. ROW_NUMBER() の計算
--    -> ORDER BY weight, student_id に伴うソートが見える可能性がある
--
-- 3. COUNT(*) OVER()
--    -> 全件数を各行へ配る処理が中間表生成に含まれる
--
-- 4. 外側の WHERE diff BETWEEN 0 AND 2
--    -> 中央候補だけを残すフィルタ
--
-- 5. 最後の AVG(weight)
--    -> 残った1行または2行の平均を取る
-- ================================================