-- ================================================
-- ファイル名: 20_create_weights.sql
-- 役割:
--   体重テーブル Weights を作成する。
--
-- このSQLの目的:
--   学生ごとの体重データを保持し、
--   後続のSQLで
--     student_id の順に連番を振る
--   練習ができるようにすること。
--
-- 学習上の主題:
--   - 主キーが1列の表
--   - 連番付与の基準列としての主キー
--   - ROW_NUMBER() と相関サブクエリの比較の土台
--
-- 今回の最重要ポイント:
--   連番を振るときに最も大事なのは、
--   何の順序で番号を振るか
--   を一意に決められる列があることである。
--
--   今回は student_id が主キーなので、
--   student_id の昇順に並べれば
--   各行の順番が曖昧にならない。
--
--   つまりこの表は、
--   「主キー1列に対して安定した連番を振る」
--   という題材になっている。
-- ================================================

CREATE TABLE Weights
(
    student_id CHAR(4) PRIMARY KEY,
    -- student_id:
    --   学生を一意に識別する主キーである。
    --
    --   例:
    --     A100
    --     A101
    --     A124
    --     B343
    --   など。
    --
    --   後続のクエリでは
    --     ORDER BY student_id
    --   や
    --     W2.student_id <= W1.student_id
    --   のように、
    --   連番を決める順序基準として使われる。
    --
    --   主キーであるため重複がなく、
    --   昇順に並べた順番が一意に定まる。
    --
    --   これが「安定した連番」を振れる理由である。

    weight INTEGER
    -- weight:
    --   体重を表す列である。
    --
    --   今回の連番付与ロジックそのものには使わない。
    --
    --   つまり今回の主題は
    --     weight の大小順
    --   ではなく
    --     student_id の順に連番を振る
    --   ことにある。
);