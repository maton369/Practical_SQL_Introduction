-- ================================================
-- ファイル名: 15_select_median_set_oriented.sql
-- 役割:
--   集合指向的な考え方で Weights テーブルのメジアンを求める。
--
-- このSQLの目的:
--   各 weight 値について、
--     その値以上の要素数
--     その値以下の要素数
--   を調べ、
--   どちらも「全体の半分以上」になる値だけを候補として残し、
--   最後にその平均を取ることでメジアンを求めること。
--
-- 今回の最重要ポイント:
--   このSQLは
--     「並べて真ん中を取る」
--   のではなく、
--     「中央に位置できる値の条件」
--   を集合の性質として表現している。
--
--   つまり各候補値 w について、
--     1. w 以上の値が十分にあるか
--     2. w 以下の値が十分にあるか
--   を判定する。
--
--   両方を満たす値は中央候補である。
--
--   奇数件なら中央の1値だけが残る。
--   偶数件なら中央の2値が残る。
--
--   最後に AVG(weight) を取ることで、
--   奇数件でも偶数件でもメジアンを1本で求められる。
-- ================================================

SELECT AVG(TMP.weight) AS median_weight
  FROM
  (
      SELECT W1.weight
        FROM Weights AS W1
       CROSS JOIN Weights AS W2
       GROUP BY W1.weight
      HAVING
             SUM(CASE WHEN W2.weight >= W1.weight THEN 1 ELSE 0 END)
             >= COUNT(*) / 2
         AND SUM(CASE WHEN W2.weight <= W1.weight THEN 1 ELSE 0 END)
             >= COUNT(*) / 2
  ) AS TMP;

-- ================================================
-- アルゴリズムの考え方
-- ================================================
--
-- 1. W1.weight を「中央値候補」として1つ考える
-- 2. W2 側で母集合全体をなめる
-- 3. 候補値以上の個数を数える
-- 4. 候補値以下の個数を数える
-- 5. 両方が全体の半分以上なら、その値は中央候補とみなす
-- 6. 中央候補の平均を取る
--
-- つまりこれは、
--   「中央に位置しうる値を条件で抽出し、その平均を返す」
-- 集合指向型アルゴリズムである。
--
--
-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Weights の値:
--   50
--   55
--   55
--   60
--   72
--   72
--   72
--
-- 候補値ごとの判定:
--
-- 候補 50
--   50以上 = 7件
--   50以下 = 1件
--   -> 下側が少なすぎるので不採用
--
-- 候補 55
--   55以上 = 6件
--   55以下 = 3件
--   -> 下側がまだ中央条件を満たさないので不採用
--
-- 候補 60
--   60以上 = 4件
--   60以下 = 4件
--   -> 両側とも十分あるので採用
--
-- 候補 72
--   72以上 = 3件
--   72以下 = 7件
--   -> 上側が少なすぎるので不採用
--
-- よって候補は 60 だけ残る。
-- AVG(60) = 60 なのでメジアンは 60 になる。
--
--
-- ================================================
-- 図解
-- ================================================
--
--   候補値 W1.weight を1つ選ぶ
--            |
--            v
--   母集合 W2 全体と比較する
--            |
--            +-------------------------------+
--            | W2.weight >= W1.weight の件数 |
--            +-------------------------------+
--            |
--            +-------------------------------+
--            | W2.weight <= W1.weight の件数 |
--            +-------------------------------+
--            |
--            v
--   両方が半分以上か？
--        |                     |
--       yes                   no
--        |                     |
--        v                     v
--   候補として残す          捨てる
--        |
--        v
--   最後に AVG(weight)
--        |
--        v
--      メジアン
--
--
-- ================================================
-- 確認用
-- ================================================
--
-- 実行結果は概ね次になる:
--
--   median_weight
--   -------------
--   60.0000
--
-- ================================================