-- ================================================
-- ファイル名: 10_select_correlated_subquery_by_class_weights2.sql
-- 役割:
--   相関サブクエリを使って、
--   class ごとに連番を振る。
--
-- このSQLの目的:
--   PARTITION BY を使った ROW_NUMBER() と同じ結果を、
--   相関サブクエリで
--     「同じ class 内で自分以下の student_id が何件あるか」
--   を数えることで再現すること。
--
-- 学習上の主題:
--   - 相関サブクエリによるグループ内連番
--   - パーティションを WHERE 条件で表現する考え方
--   - student_id 順のクラス内順位
--   - PARTITION BY 版との比較
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   外側の現在行 W1 に対して、
--   内側サブクエリが
--     1. W2.class = W1.class
--        -> 同じ class に限定する
--     2. W2.student_id <= W1.student_id
--        -> その class の中で自分以下を数える
--   という条件で COUNT(*) を取る。
--
--   これにより
--     「その class の中での順位」
--   が得られる。
--
--   つまりアルゴリズムとしては
--     1. 外側で1行取る
--     2. 同じ class の行だけを見る
--     3. その中で自分以下の student_id を数える
--     4. その件数を class 内連番とする
--   という「グループ内順位 = 同グループ内自分以下件数」
--   になっている。
--
--   これが相関サブクエリ版の本質である。
-- ================================================

SELECT
       class,
       -- class:
       --   クラス番号を返す。
       --
       --   今回の連番は class ごとに独立しているので、
       --   どのクラス内の順位かを示すために必要である。

       student_id,
       -- student_id:
       --   学生IDを返す。
       --
       --   同じ class の中で
       --   student_id 順に番号を振るため、
       --   クラス内順序の基準列である。

       (
           SELECT COUNT(*)
             FROM Weights2 W2
            WHERE W2.class = W1.class
              AND W2.student_id <= W1.student_id
       ) AS seq
       -- 相関サブクエリ:
       --   ここが今回の主役である。
       --
       --   内側は W2 の行を数えるが、
       --   条件は2段階になっている。
       --
       -- W2.class = W1.class:
       --   まず外側の現在行 W1 と同じ class の行だけに絞る。
       --
       --   これが
       --     PARTITION BY class
       --   に相当する考え方である。
       --
       --   つまり
       --     連番の世界をクラスごとに分ける
       --   条件になっている。
       --
       -- W2.student_id <= W1.student_id:
       --   同じ class の中で、
       --   自分以下の student_id を持つ行だけを数える。
       --
       --   これにより
       --     クラス内で自分が何番目か
       --   が件数として得られる。
       --
       -- COUNT(*) の意味:
       --   「同じ class に属し、かつ student_id 順で自分以下の件数」
       --   を表す。
       --
       --   これがそのまま
       --     クラス内順位
       --   になる。
       --
       -- たとえば W1=(class=2, student_id='102') のとき、
       --   同じ class=2 で
       --   student_id <= '102'
       --   を満たすのは
       --     100
       --     101
       --     102
       --   の3件なので、
       --   seq = 3 になる。
       --
       -- 相関の意味:
       --   内側条件が
       --     W1.class
       --     W1.student_id
       --   に依存しているため、
       --   外側行ごとに内側の対象集合が変わる。
       --
       --   これが相関サブクエリである。
       --
       -- AS seq:
       --   数え上げ結果をクラス内連番 seq として返している。

  FROM Weights2 W1;
  -- FROM Weights2 W1:
  --   外側クエリの元表である。
  --
  --   各行 W1 について
  --   「そのクラスの中で何番目か」
  --   を内側サブクエリで問い合わせる構造になっている。
  --
  --   つまり各行が
  --     自分のクラス内順位を件数で計算する
  --   アルゴリズムと見ると分かりやすい。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 外側で Weights2 の1行 W1 を取る
-- 2. 内側で W2.class = W1.class の行だけを見る
-- 3. その中で W2.student_id <= W1.student_id を満たす件数を数える
-- 4. その件数を W1 のクラス内連番とする
-- 5. 全行についてこれを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each row w1 in Weights2:
--       seq = count of rows w2
--             where w2.class == w1.class
--               and w2.student_id <= w1.student_id
--       output w1.class, w1.student_id, seq
--
-- つまりこれは、
--   「同一グループ内で自分以下の件数を数えて順位にする」
--   アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- class=1:
--   100
--   101
--   102
--
-- 各行の件数は
--
--   100:
--     同じ class=1 で <=100 は 1件
--     -> seq = 1
--
--   101:
--     同じ class=1 で <=101 は 2件
--     -> seq = 2
--
--   102:
--     同じ class=1 で <=102 は 3件
--     -> seq = 3
--
-- class=2:
--   100
--   101
--   102
--   103
--
-- 各行の件数は
--
--   100 -> 1
--   101 -> 2
--   102 -> 3
--   103 -> 4
--
-- したがって最終結果は
--
--   1 100 1
--   1 101 2
--   1 102 3
--   2 100 1
--   2 101 2
--   2 102 3
--   2 103 4
--
-- になる。
--
-- これは ROW_NUMBER() + PARTITION BY 版と同じ結果である。


-- ================================================
-- 図解: 相関サブクエリでクラスごとに連番
-- ================================================
--
--   外側行 W1 = (class=2, student_id='102')
--                |
--                v
--   内側条件:
--     W2.class = 2
--     AND W2.student_id <= '102'
--                |
--                v
--   該当行:
--     (2,100)
--     (2,101)
--     (2,102)
--                |
--                v
--            COUNT(*) = 3
--                |
--                v
--     class=2 内で student_id='102' の seq は 3
--
-- つまり、
--   まず同じ class に絞る
--   次にその class 内で自分以下を数える
-- という2段階でクラス内順位を作っている。


-- ================================================
-- PARTITION BY 版との違い
-- ================================================
--
-- ROW_NUMBER() + PARTITION BY 版では、
--   まず class ごとに世界を分け、
--   その中で student_id 順に 1,2,3,... を振っていた。
--
--   つまり発想は
--     グループに分ける -> 順番に番号を振る
--   であった。
--
-- 一方、相関サブクエリ版では、
--   各行ごとに
--     同じ class の中で
--     自分以下の student_id 件数
--   を数えている。
--
--   つまり発想は
--     グループ内順位 = グループ内自分以下件数
--   である。
--
-- したがって
--
--   PARTITION BY 版:
--     グループ内順位を直接付ける
--
--   相関サブクエリ版:
--     グループ内件数を数えて順位を間接的に求める
--
-- という違いがある。
--
-- 結果は同じでも、
--   アルゴリズムの見え方はかなり異なる。
--
-- 今回は特に
--   PARTITION BY に相当するものが
--   WHERE W2.class = W1.class
--   で表現されている
-- ことが重要な学習ポイントである。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   グループ内の順位を
--   「同じグループ内で自分以下の件数」
--   として表現しているところにある。
--
-- 全表通し番号版では
--   全行を1つの世界として数えていた。
--
-- しかし今回は
--   class ごとに世界を分ける。
--
-- そのため、
--   順位を定義する件数条件にも
--     class の一致
--   が必要になる。
--
-- つまり
--   グループ内順位を作るには、
--   まずグループ境界を条件で明示する必要がある。
--
-- これが class ごと連番版の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- W2.class = W1.class
--   → 同じ class の行だけを見る
--
-- W2.student_id <= W1.student_id
--   → その class の中で自分以下の student_id を数える
--
-- COUNT(*)
--   → その件数をクラス内順位とする
--
-- AS seq
--   → クラス内連番として返す
-- ================================================