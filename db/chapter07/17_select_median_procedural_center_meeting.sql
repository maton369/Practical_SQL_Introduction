-- ================================================
-- ファイル名: 17_select_median_procedural_center_meeting.sql
-- 役割:
--   手続き型の発想で Weights テーブルのメジアンを求める。
--
-- このSQLの目的:
--   体重を
--     小さい順
--     大きい順
-- の両方向から同時に順位付けし、
--   その順位が中央付近でぶつかる行だけを残して
--   平均を取ることでメジアンを求めること。
--
-- 学習上の主題:
--   - メジアンを「両端から詰めていく」手続きとして捉える考え方
--   - 2本の ROW_NUMBER() を逆順で使う意味
--   - 奇数件・偶数件を1本で扱う条件式
--   - 集合指向型メジアンとの発想の違い
--
-- 今回の最重要ポイント:
--   このSQLは、
--   「最小側から1歩ずつ進む番号 hi」と
--   「最大側から1歩ずつ進む番号 lo」を各行に持たせる。
--
--   すると、
--   真ん中の行では
--     hi と lo が一致
--   するか、
--   偶数件なら
--     hi と lo が前後1だけずれる
--   状態になる。
--
--   その中央付近の行だけを残して AVG(weight) を取れば、
--   奇数件でも偶数件でもメジアンが得られる。
--
--   つまりアルゴリズムとしては
--     1. 昇順の位置 hi を付ける
--     2. 降順の位置 lo を付ける
--     3. 両者が中央でぶつかる行だけを残す
--     4. その平均を取る
--   という「両端から1行ずつ数えて中心を見つける」形である。
--
--   これがこの手続き型メジアン計算の本質である。
-- ================================================

SELECT
       AVG(weight) AS median
       -- AVG(weight):
       --   最後に中央候補として残った行の weight の平均を取る。
       --
       --   ここが重要である。
       --
       --   奇数件なら中央の1行だけが残るため、
       --   AVG(weight) はその値自身になる。
       --
       --   偶数件なら中央の2行が残るため、
       --   AVG(weight) はその2値の平均になる。
       --
       --   したがって、
       --   奇数件・偶数件を区別せずに
       --   1本の SQL でメジアンを返せる。

  FROM
       (
           SELECT
                  weight,
                  -- weight:
                  --   元データの体重値である。
                  --
                  --   この行が中央候補として残るかどうかを、
                  --   hi と lo の関係で判定する。

                  ROW_NUMBER() OVER (
                      ORDER BY weight ASC, student_id ASC
                  ) AS hi,
                  -- hi:
                  --   小さい順に見たときの位置番号である。
                  --
                  --   つまり
                  --     最小の行が hi=1
                  --     次が hi=2
                  --   となる。
                  --
                  -- ORDER BY weight ASC, student_id ASC:
                  --   まず weight 昇順に並べる。
                  --   同じ weight がある場合は student_id 昇順で順序を固定する。
                  --
                  --   ここで student_id を入れている理由は、
                  --   同じ体重が複数あるときでも
                  --   並び順があいまいにならないようにするためである。
                  --
                  --   つまり hi は
                  --     「左端から何歩目か」
                  --   を表す番号である。

                  ROW_NUMBER() OVER (
                      ORDER BY weight DESC, student_id DESC
                  ) AS lo
                  -- lo:
                  --   大きい順に見たときの位置番号である。
                  --
                  --   つまり
                  --     最大の行が lo=1
                  --     次が lo=2
                  --   となる。
                  --
                  -- ORDER BY weight DESC, student_id DESC:
                  --   こちらは逆向きで並べている。
                  --
                  --   したがって lo は
                  --     「右端から何歩目か」
                  --   を表す番号である。
                  --
                  -- hi と lo を両方持たせることで、
                  --   左右から詰めていったとき
                  --   どの行が中央に来るか
                  -- が分かるようになる。
             FROM Weights
             -- Weights:
             --   元表である。
             --
             --   ここでは Weights の各行に対して
             --     左からの順位 hi
             --     右からの順位 lo
             --   を付けた中間表を作っている。
       ) TMP
       -- TMP:
       --   各行に hi と lo を付けた派生表である。
       --
       --   この表の中から、
       --   中央付近に位置する行だけを外側で選ぶ。

 WHERE hi IN (lo, lo + 1, lo - 1);
 -- WHERE hi IN (lo, lo + 1, lo - 1):
 --   ここが中央判定の条件である。
 --
 --   意味は次の通りである。
 --
 --   1. hi = lo
 --      -> 左右から数えた順位がぴったり一致する
 --      -> 奇数件の中央行に対応する
 --
 --   2. hi = lo + 1
 --      -> 偶数件の中央2行の片方に対応しうる
 --
 --   3. hi = lo - 1
 --      -> 偶数件の中央2行のもう片方に対応しうる
 --
 --   つまりこの条件で
 --     真ん中1行
 --   または
 --     真ん中2行
 --   だけを残している。
 --
 --   その後 AVG(weight) を取ることでメジアンが得られる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Weights を weight 昇順で並べ、左からの番号 hi を振る
-- 2. Weights を weight 降順で並べ、右からの番号 lo を振る
-- 3. hi と lo が一致、または1だけずれる行を残す
-- 4. 残った行の weight の平均を取る
--
-- 概念的には次のような処理に近い。
--
--   sort ascending  -> assign hi = 1,2,3,...
--   sort descending -> assign lo = 1,2,3,...
--
--   keep rows where hi == lo
--                  or hi == lo + 1
--                  or hi == lo - 1
--
--   median = average(weight of kept rows)
--
-- つまりこれは、
--   「左からと右からの歩数が中央で出会う行を探す」
--   手続き型アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- Weights の値:
--   50
--   55
--   55
--   60
--   72
--   72
--   72
--
-- 昇順（左から）:
--   50 -> hi=1
--   55 -> hi=2
--   55 -> hi=3
--   60 -> hi=4
--   72 -> hi=5
--   72 -> hi=6
--   72 -> hi=7
--
-- 降順（右から）:
--   72 -> lo=1
--   72 -> lo=2
--   72 -> lo=3
--   60 -> lo=4
--   55 -> lo=5
--   55 -> lo=6
--   50 -> lo=7
--
-- 中央判定:
--   60 の行では hi=4, lo=4
--   -> hi=lo を満たすので残る
--
-- 他の行では hi と lo が大きく離れているので落ちる
--
-- よって残るのは 60 だけであり、
-- AVG(60)=60 がメジアンになる。


-- ================================================
-- 偶数件にも対応できる理由
-- ================================================
--
-- たとえば値が
--   10,20,30,40
-- の4件なら、
--
-- 昇順順位 hi:
--   10 -> 1
--   20 -> 2
--   30 -> 3
--   40 -> 4
--
-- 降順順位 lo:
--   40 -> 1
--   30 -> 2
--   20 -> 3
--   10 -> 4
--
-- 中央2行は
--   20: hi=2, lo=3
--   30: hi=3, lo=2
--
-- なので
--   hi = lo + 1
--   hi = lo - 1
-- の条件により両方残る。
--
-- その後 AVG(20,30)=25 となり、
-- 偶数件のメジアンが得られる。
--
-- ここがこの SQL の美しい点である。


-- ================================================
-- 図解: 両端から数えて中央でぶつかる
-- ================================================
--
--   左から hi を振る        右から lo を振る
--
--   50  -> 1               50  -> 7
--   55  -> 2               55  -> 6
--   55  -> 3               55  -> 5
--   60  -> 4   <---->      60  -> 4
--   72  -> 5               72  -> 3
--   72  -> 6               72  -> 2
--   72  -> 7               72  -> 1
--
--   hi と lo が中央で一致する
--   または前後1だけずれる行が
--   中央候補になる。
--
--
-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   メジアンを「世界の中心で左右が出会う位置」
--   として表現しているところにある。
--
-- 集合指向型のSQLでは
--   候補値の上下件数で中央条件を定義していた。
--
-- 一方このSQLでは
--   左から何番目か
--   右から何番目か
-- を各行に持たせ、
--   その2つが中央でぶつかる行を選んでいる。
--
-- つまり
--   メジアンを順序上の折り合い地点として捉える
--   手続き型の考え方
-- がそのまま SQL になっている。
--
-- これがこの方法の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER() OVER (ORDER BY weight ASC, student_id ASC)
--   → 左からの順位 hi を作る
--
-- ROW_NUMBER() OVER (ORDER BY weight DESC, student_id DESC)
--   → 右からの順位 lo を作る
--
-- WHERE hi IN (lo, lo+1, lo-1)
--   → 中央1行または中央2行だけを残す
--
-- AVG(weight)
--   → 残った中央候補の平均を取り、メジアンを返す
-- ================================================