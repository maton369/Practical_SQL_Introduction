-- ================================================
-- ファイル名: 04_select_correlated_subquery_weights.sql
-- 役割:
--   相関サブクエリを使って、
--   student_id 順の連番を求める。
--
-- このSQLの目的:
--   ROW_NUMBER() を使わずに、
--   各行より「前にある行の数」を COUNT(*) で数えることで、
--   連番を再現する考え方を示すこと。
--
-- 学習上の主題:
--   - 相関サブクエリによる連番付与
--   - COUNT(*) が「順位」を表すこと
--   - W2.student_id <= W1.student_id の意味
--   - ROW_NUMBER() とのアルゴリズム比較
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   外側の現在行 W1 に対して、
--   内側サブクエリが
--     student_id が W1.student_id 以下である行数
--   を数えている。
--
--   student_id は主キーで一意なので、
--   その件数はちょうど
--     その行が student_id 順で何番目か
--   を表す。
--
--   つまりアルゴリズムとしては
--     1. 外側で1行取る
--     2. その行以下の student_id を持つ行数を数える
--     3. その件数を連番とみなす
--   という「順位を件数で表現する」形になっている。
--
--   これが相関サブクエリ版の本質である。
-- ================================================

SELECT
       student_id,
       -- student_id:
       --   学生IDを返す。
       --
       --   どの行にどの連番が付いたかを見るための基準列である。

       (
           SELECT COUNT(*)
             FROM Weights W2
            WHERE W2.student_id <= W1.student_id
       ) AS seq
       -- 相関サブクエリ:
       --   ここが今回の主役である。
       --
       --   内側クエリは、
       --   Weights W2 の中から
       --     W2.student_id <= W1.student_id
       --   を満たす行数を COUNT(*) で数えている。
       --
       -- W1 と W2:
       --   W1 は外側クエリの現在行、
       --   W2 は内側クエリで数える対象である。
       --
       --   同じ Weights 表を
       --     外側用
       --     内側用
       --   として2回使っている。
       --
       --   しかも内側が外側の W1.student_id を参照しているので、
       --   これは相関サブクエリである。
       --
       -- WHERE W2.student_id <= W1.student_id:
       --   「現在行 W1 より student_id が小さいか、同じである行」
       --   を全部数える条件である。
       --
       --   たとえば外側行が
       --     W1.student_id = 'B343'
       --   のとき、
       --   条件を満たすのは
       --     A100
       --     A101
       --     A124
       --     B343
       --   の4行である。
       --
       --   したがって
       --     seq = 4
       --   になる。
       --
       -- COUNT(*) の意味:
       --   主キーが1列で student_id が一意なので、
       --   「自分以下の件数」は
       --   そのまま
       --     student_id 昇順での位置番号
       --   と一致する。
       --
       --   つまりこの COUNT(*) は
       --     連番
       --   を表している。
       --
       --   これがROW_NUMBER()を使わずに連番を求める考え方である。
       --
       -- AS seq:
       --   数えた件数を seq という連番列として返している。

  FROM Weights W1;
  -- FROM Weights W1:
  --   外側クエリの元表である。
  --
  --   ここでは Weights の各行を1件ずつ取り、
  --   その行について
  --     「自分は student_id 順で何番目か」
  --   を内側サブクエリへ問い合わせる形になっている。
  --
  --   つまり
  --     各行が、自分の順位を件数で問い合わせる
  --   アルゴリズムと見ると分かりやすい。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 外側クエリで Weights の1行 W1 を取る
-- 2. 内側サブクエリで
--      W2.student_id <= W1.student_id
--    を満たす行数を数える
-- 3. その件数を W1 の連番とする
-- 4. 全行についてこれを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each row w1 in Weights:
--       seq = count of rows w2
--             where w2.student_id <= w1.student_id
--       output w1.student_id, seq
--
-- つまりこれは、
--   「自分以下の件数 = 自分の順位」
--   という考え方で連番を求めるアルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- student_id 順:
--
--   A100
--   A101
--   A124
--   B343
--   B346
--   C345
--   C563
--
-- 各行について
--   自分以下の student_id を数えると、
--
--   A100:
--     A100
--     -> 1件
--
--   A101:
--     A100, A101
--     -> 2件
--
--   A124:
--     A100, A101, A124
--     -> 3件
--
--   B343:
--     A100, A101, A124, B343
--     -> 4件
--
--   B346:
--     A100, A101, A124, B343, B346
--     -> 5件
--
--   C345:
--     A100, A101, A124, B343, B346, C345
--     -> 6件
--
--   C563:
--     A100, A101, A124, B343, B346, C345, C563
--     -> 7件
--
-- となる。
--
-- したがって結果は
--
--   A100 1
--   A101 2
--   A124 3
--   B343 4
--   B346 5
--   C345 6
--   C563 7
--
-- になる。
--
-- これは ROW_NUMBER() 版と同じ結果である。


-- ================================================
-- 図解: 相関サブクエリで連番を振る
-- ================================================
--
--   外側行 W1 = B343
--          |
--          v
--   内側で数える条件:
--     W2.student_id <= 'B343'
--          |
--          v
--   該当行:
--     A100
--     A101
--     A124
--     B343
--          |
--          v
--        COUNT(*) = 4
--          |
--          v
--       B343 の seq は 4
--
-- つまり、
--   「今の行より前に何行あるか」
--   を件数として求め、
--   それをその行の順位とみなしている。


-- ================================================
-- ROW_NUMBER版との違い
-- ================================================
--
-- ROW_NUMBER版では、
--   先に全体を並べて
--   上から 1,2,3,... を付けていた。
--
--   つまり発想は
--     並べる -> 順番に番号を振る
--   であった。
--
-- 一方、相関サブクエリ版では、
--   各行ごとに
--     「自分以下は何件あるか」
--   を数えている。
--
--   つまり発想は
--     自分の順位 = 自分以下の件数
--   である。
--
-- したがって
--
--   ROW_NUMBER版:
--     順位を直接付ける
--
--   相関サブクエリ版:
--     件数を数えて順位を間接的に求める
--
-- という違いがある。
--
-- 結果は同じでも、
--   アルゴリズムの見え方がかなり違う。
--
-- 今回はその違いを理解することが重要である。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   一意な順序を持つ列に対して
--   「自分以下の件数」を数えれば
--   そのまま順位になる
-- という発想にある。
--
-- つまり連番とは、
--   順序の中での位置
-- であり、
--   その位置は件数として表現できる。
--
-- 今回は
--   student_id が主キー
--   -> 一意な順序がある
-- ため、
--   COUNT(*)
-- で安全に順位を表現できる。
--
-- これが相関サブクエリ版の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- SELECT student_id
--   → どの行にどの連番が付いたかを見るための基準列
--
-- (SELECT COUNT(*) FROM Weights W2 WHERE W2.student_id <= W1.student_id)
--   → 現在行以下の件数を数え、その行の順位とする
--
-- AS seq
--   → 数えた件数を連番列として返す
--
-- FROM Weights W1
--   → 外側クエリの現在行集合
-- ================================================


-- ================================================
-- 補足1:
--   この方法は、
--   主キーが1列で順序が一意に決まるからこそ
--   分かりやすく使える。
-- ================================================


-- ================================================
-- 補足2:
--   相関サブクエリ版は
--   各外側行ごとに内側で件数を数える構造なので、
--   データ量が増えるとコストに注意が必要である。
-- ================================================


