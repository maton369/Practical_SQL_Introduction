-- ================================================
-- ファイル名: 08_select_correlated_subquery_weights2.sql
-- 役割:
--   相関サブクエリと行式を使って、
--   複合主キー順の連番を求める。
--
-- このSQLの目的:
--   ROW_NUMBER() を使わずに、
--   (class, student_id) の組に対して
--   「自分以下の行が何件あるか」を COUNT(*) で数えることで、
--   連番を再現すること。
--
-- 学習上の主題:
--   - 相関サブクエリによる複合キー順位付け
--   - 行式(row constructor) の大小比較
--   - 複数列の辞書順で「自分以下」を表現する方法
--   - ROW_NUMBER() 版との発想の違い
--
-- 今回の最重要ポイント:
--   このSQLでは、
--   外側の現在行 W1 に対して、
--   内側の W2 の中から
--     (W2.class, W2.student_id)
--       <=
--     (W1.class, W1.student_id)
--   を満たす行数を数える。
--
--   これは
--     複合キー辞書順で見たときの「自分以下」
--   を意味する。
--
--   したがってその件数は、
--   その行が複合キー順で何番目かを表す。
--
--   つまりアルゴリズムとしては
--     1. 外側で1行取る
--     2. 複合キー順で自分以下の行数を数える
--     3. その件数を連番とする
--   という「複合キー順位 = 自分以下件数」型である。
--
--   これが相関サブクエリ + 行式版の本質である。
-- ================================================

SELECT
       class,
       -- class:
       --   クラス番号を返す。
       --
       --   連番の順序の第1キーでもある。
       --
       --   結果の中で、
       --   class の増加に従って大きなブロック順序が決まる。

       student_id,
       -- student_id:
       --   学生IDを返す。
       --
       --   連番の順序の第2キーである。
       --
       --   同じ class の中では
       --   student_id の昇順で順位が決まる。

       (
           SELECT COUNT(*)
             FROM Weights2 W2
            WHERE (W2.class, W2.student_id)
                    <=
                  (W1.class, W1.student_id)
       ) AS seq
       -- 相関サブクエリ:
       --   ここが今回の主役である。
       --
       --   内側クエリは、
       --   W2 の全行の中から
       --     (W2.class, W2.student_id)
       --       <=
       --     (W1.class, W1.student_id)
       --   を満たす行数を数える。
       --
       -- 行式 (row constructor):
       --   (a, b) のように複数列を1組として扱う表現である。
       --
       --   今回は
       --     (class, student_id)
       --   という複合キー全体を1つの比較対象としている。
       --
       -- 行式の <= 比較の意味:
       --   これは辞書順比較と考えると分かりやすい。
       --
       --   まず class を比較し、
       --   class が同じなら student_id を比較する。
       --
       --   つまり
       --     (1,'102') < (2,'100')
       --   である。
       --
       --   したがって
       --     (W2.class, W2.student_id)
       --       <=
       --     (W1.class, W1.student_id)
       --   は、
       --   「複合キー辞書順で W1 以下の行」
       --   を意味する。
       --
       -- COUNT(*) の意味:
       --   外側の現在行 W1 より前、または同じ位置にある行数を数えている。
       --
       --   主キー (class, student_id) は一意なので、
       --   この件数はそのまま
       --     W1 の順位
       --   になる。
       --
       -- たとえば W1 = (2,'100') のとき、
       --   それ以下の行は
       --     (1,100)
       --     (1,101)
       --     (1,102)
       --     (2,100)
       --   の4行なので、
       --   seq = 4 になる。
       --
       -- 相関の意味:
       --   内側は
       --     W1.class
       --     W1.student_id
       --   を参照しているため、
       --   外側行ごとに条件が変わる。
       --
       --   これが相関サブクエリである。
       --
       -- AS seq:
       --   数え上げ結果を連番 seq として返している。

  FROM Weights2 W1;
  -- FROM Weights2 W1:
  --   外側クエリの元表である。
  --
  --   各行 W1 について
  --   「自分は複合キー順で何番目か」
  --   を内側サブクエリへ問い合わせる形になっている。
  --
  --   つまり各行が
  --     自分の順位を複合キー比較で数え上げる
  --   アルゴリズムと見なせる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. 外側で Weights2 の1行 W1 を取る
-- 2. 内側で
--      (W2.class, W2.student_id) <= (W1.class, W1.student_id)
--    を満たす行数を数える
-- 3. その件数を W1 の連番とする
-- 4. 全行についてこれを繰り返す
--
-- 概念的には次のような処理に近い。
--
--   for each row w1 in Weights2:
--       seq = count of rows w2
--             where (w2.class, w2.student_id)
--                   <=
--                   (w1.class, w1.student_id)
--       output w1.class, w1.student_id, seq
--
-- つまりこれは、
--   「複合キー辞書順における自分以下件数」
--   を数えて順位とするアルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- 複合キー順:
--
--   (1,100)
--   (1,101)
--   (1,102)
--   (2,100)
--   (2,101)
--   (2,102)
--   (2,103)
--
-- たとえば各行について自分以下を数えると、
--
--   (1,100):
--     (1,100)
--     -> 1件
--
--   (1,101):
--     (1,100), (1,101)
--     -> 2件
--
--   (1,102):
--     (1,100), (1,101), (1,102)
--     -> 3件
--
--   (2,100):
--     (1,100), (1,101), (1,102), (2,100)
--     -> 4件
--
--   (2,101):
--     5件
--
--   (2,102):
--     6件
--
--   (2,103):
--     7件
--
-- したがって結果は
--
--   1 100 1
--   1 101 2
--   1 102 3
--   2 100 4
--   2 101 5
--   2 102 6
--   2 103 7
--
-- になる。
--
-- これは ROW_NUMBER() 版と同じ結果である。


-- ================================================
-- 図解: 行式比較で連番を振る
-- ================================================
--
--   外側行 W1 = (2,'100')
--            |
--            v
--   内側で数える条件:
--     (W2.class, W2.student_id)
--       <=
--     (2,'100')
--            |
--            v
--   該当行:
--     (1,100)
--     (1,101)
--     (1,102)
--     (2,100)
--            |
--            v
--        COUNT(*) = 4
--            |
--            v
--      (2,'100') の seq は 4
--
-- つまり、
--   複合キー辞書順で自分以下の行数を数え、
--   それを順位として使っている。


-- ================================================
-- ROW_NUMBER版との違い
-- ================================================
--
-- ROW_NUMBER版では、
--   先に全体を
--     ORDER BY class, student_id
--   で並べて、
--   上から 1,2,3,... を付けていた。
--
--   つまり発想は
--     並べる -> 番号を振る
--   である。
--
-- 一方、相関サブクエリ版では、
--   各行ごとに
--     複合キーで自分以下の行数
--   を数えている。
--
--   つまり発想は
--     自分の順位 = 複合キー順で自分以下の件数
--   である。
--
-- したがって
--
--   ROW_NUMBER版:
--     順位を直接付与する
--
--   相関サブクエリ版:
--     件数を数えて順位を間接的に求める
--
-- という違いがある。
--
-- 結果は同じでも、
--   アルゴリズムの見え方はかなり異なる。
--
-- 今回は特に
--   行式によって複数列順序を1つの比較式で表せる
-- ことが重要な学習ポイントである。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   複合主キーに対しても
--   「自分以下の件数 = 自分の順位」
--   という考え方が成り立つことにある。
--
-- 単一主キー版では
--     student_id
-- だけを比較すればよかった。
--
-- しかし複合主キー版では
--     class
--     student_id
-- の2列をまとめて比較する必要がある。
--
-- そこで行式
--     (class, student_id)
-- を使うことで、
--   複合キー辞書順の比較を1つの条件として書いている。
--
-- これが主キー複数列版の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- (W2.class, W2.student_id) <= (W1.class, W1.student_id)
--   → 複合キー辞書順で W1 以下の行を選ぶ
--
-- COUNT(*)
--   → その件数を数え、順位とする
--
-- AS seq
--   → 数えた件数を連番列として返す
--
-- FROM Weights2 W1
--   → 外側クエリの現在行集合
-- ================================================


-- ================================================
-- 補足1:
--   行式比較は
--   「複数列をまとめて順序比較する」
--   ときに非常に便利である。
-- ================================================


-- ================================================
-- 補足2:
--   この方法も相関サブクエリなので、
--   外側行ごとに内側件数計算を行う構造になる。
--
--   そのため大規模データではコストに注意が必要である。
-- ================================================


