-- このSQLの目的:
--   seq 列がまだ NULL の状態から、
--   後続の UPDATE で連番を埋める流れを確認できるようにすること。
--
-- 学習上の主題:
--   - 連番列を後から埋める前提データ
--   - class ごとの student_id 順
--   - UPDATE 前は seq がすべて NULL であること
--
-- 今回の最重要ポイント:
--   この時点では seq はまだ計算していないので、
--   全行が NULL である。
--
--   後続の UPDATE により、
--     class=1 では 1,2,3
--     class=2 では 1,2,3,4
--   が入ることになる。
-- ================================================

INSERT INTO Weights3 VALUES(1, '100', 50, NULL);
INSERT INTO Weights3 VALUES(1, '101', 55, NULL);
INSERT INTO Weights3 VALUES(1, '102', 56, NULL);
INSERT INTO Weights3 VALUES(2, '100', 60, NULL);
INSERT INTO Weights3 VALUES(2, '101', 72, NULL);
INSERT INTO Weights3 VALUES(2, '102', 73, NULL);
INSERT INTO Weights3 VALUES(2, '103', 73, NULL);

-- ================================================
-- UPDATE 後に期待される seq のイメージ
-- ================================================
--
-- class=1:
--   100 -> 1
--   101 -> 2
--   102 -> 3
--
-- class=2:
--   100 -> 1
--   101 -> 2
--   102 -> 3
--   103 -> 4
--
-- つまり最終的には概ね
--
--   (1,100,50,1)
--   (1,101,55,2)
--   (1,102,56,3)
--   (2,100,60,1)
--   (2,101,72,2)
--   (2,102,73,3)
--   (2,103,73,4)
--
-- になる。