-- ================================================
-- ファイル名: 13_update_weights3_seq_with_row_number.sql
-- 役割:
--   ROW_NUMBER() を使って計算した class ごとの連番を、
--   Weights3.seq へ UPDATE で書き込む。
--
-- このSQLの目的:
--   class ごとの student_id 順の連番を
--   サブクエリで一度計算し、
--   それを元表の各行へ対応付けて保存すること。
--
-- 学習上の主題:
--   - UPDATE の中でウィンドウ関数結果を利用する方法
--   - 派生表 SeqTbl を経由して更新値を取り出すこと
--   - class, student_id で元行と計算結果を対応付ける考え方
--
-- 今回の最重要ポイント:
--   このSQLは2段階で考えると理解しやすい。
--
--   1. まず SeqTbl という中間結果を作る
--      -> 各行に対して class ごとの連番 seq を計算する
--
--   2. UPDATE で各行について
--      -> 自分と同じ (class, student_id) を SeqTbl から探し
--      -> その seq を書き込む
--
--   つまりアルゴリズムとしては
--     「連番付きの対応表を作る -> その値を元表へ書き戻す」
--   という流れになっている。
--
--   これが ROW_NUMBER() 更新版の本質である。
-- ================================================

UPDATE Weights3
   SET seq =
       (
           SELECT seq
             FROM
             (
                 SELECT
                        class,
                        student_id,
                        ROW_NUMBER()
                        OVER (
                            PARTITION BY class
                            ORDER BY student_id
                        ) AS seq
                   FROM Weights3
             ) SeqTbl
             -- SeqTbl:
             --   ここで作っているのは、
             --   Weights3 の各行に対して
             --     class ごとの連番 seq
             --   を付けた中間表である。
             --
             --   概念的には次のような表になる。
             --
             --   class student_id seq
             --   --------------------
             --   1     100        1
             --   1     101        2
             --   1     102        3
             --   2     100        1
             --   2     101        2
             --   2     102        3
             --   2     103        4
             --
             --   つまり SeqTbl は
             --     「この行には何番を書けばよいか」
             --   を表す対応表である。
             --
             -- なぜ SeqTbl というサブクエリが必要か:
             --   MySQL では UPDATE 対象表を直接読みながら
             --   同じ SELECT の中でウィンドウ関数結果をそのまま
             --   参照する形に制約が出ることがある。
             --
             --   そのため、
             --   まず別名付きの派生表 SeqTbl として
             --   連番結果を明示的に作ってから参照している。
            WHERE Weights3.class      = SeqTbl.class
              AND Weights3.student_id = SeqTbl.student_id
              -- ここで UPDATE 中の現在行 Weights3 と、
              -- SeqTbl の中の計算済み行を対応付けている。
              --
              -- 対応条件は
              --   class
              --   student_id
              -- の両方である。
              --
              -- つまり
              --   同じ class・同じ student_id の行に対して
              --   その seq を返す
              -- という意味である。
              --
              -- 複合主キーで行が一意なので、
              -- この対応付けにより seq は1つに決まる。
       );

-- ================================================
-- 内側の SeqTbl だけを見ると何をしているか
-- ================================================
--
--   SELECT class, student_id,
--          ROW_NUMBER() OVER (PARTITION BY class ORDER BY student_id) AS seq
--     FROM Weights3
--
-- これは
--   class ごとの student_id 順連番
-- を計算しているだけである。
--
-- つまり
--
-- class=1:
--   100 -> 1
--   101 -> 2
--   102 -> 3
--
-- class=2:
--   100 -> 1
--   101 -> 2
--   102 -> 3
--   103 -> 4
--
-- という表を作る。
--
-- その表の値を UPDATE で書き戻している。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Weights3 の各行に対して class ごとの ROW_NUMBER を計算する
-- 2. その結果を SeqTbl という中間表として持つ
-- 3. UPDATE 対象の各行について
--      自分と同じ (class, student_id) を SeqTbl から探す
-- 4. 見つかった seq を Weights3.seq に代入する
--
-- 概念的には次のような処理に近い。
--
--   seq_map = {
--       (class, student_id) -> row_number within class
--   }
--
--   for each row in Weights3:
--       row.seq = seq_map[(row.class, row.student_id)]
--
-- つまりこれは、
--   「先に連番対応表を作り、その辞書を使って更新する」
--   アルゴリズムである。


-- ================================================
-- 図解: ROW_NUMBER 版の更新
-- ================================================
--
--   Weights3
--      |
--      | ROW_NUMBER() OVER (PARTITION BY class ORDER BY student_id)
--      v
--   SeqTbl
--   ----------------------
--   1 100 -> 1
--   1 101 -> 2
--   1 102 -> 3
--   2 100 -> 1
--   2 101 -> 2
--   2 102 -> 3
--   2 103 -> 4
--      |
--      | class, student_id で対応付け
--      v
--   Weights3.seq に書き戻す
--
-- つまり、
--   先に番号表を作る
--   -> その番号表を見ながら更新する
-- という流れである。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   連番を直接 UPDATE の中で一件ずつ計算するのではなく、
--   まず「正しい連番表」を作り、
--   それを元に各行を更新している
-- ところにある。
--
-- つまり
--   計算フェーズ
--   書き戻しフェーズ
-- を分けている。
--
-- これは更新系SQLを安全に考える上で非常に重要である。
--
-- 特にウィンドウ関数のように
--   全体順序を見て番号を付ける
-- 処理は、
--   先に結果表を作る
-- という見方をすると理解しやすい。
--
-- これが ROW_NUMBER 更新版の核心である。


