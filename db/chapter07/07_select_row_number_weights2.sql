-- ================================================
-- ファイル名: 07_select_row_number_weights2.sql
-- 役割:
--   ROW_NUMBER() を使って、
--   複合主キー (class, student_id) 順に連番を振る。
--
-- このSQLの目的:
--   主キーが複数列の場合でも、
--   ORDER BY に同じ列順を与えることで
--   安定した通し番号を作れることを示すこと。
--
-- 学習上の主題:
--   - ROW_NUMBER() の複数列 ORDER BY
--   - 辞書順による連番付与
--   - 単一主キー版との違い
--
-- 今回の最重要ポイント:
--   今回の順序は
--     ORDER BY class, student_id
--   で決まる。
--
--   これは
--     まず class を比較し、
--     class が同じなら student_id を比較する
--   という辞書順である。
--
--   つまりアルゴリズムとしては
--     1. (class, student_id) で全行を整列する
--     2. 先頭から 1,2,3,... を付ける
--   という「複合キー順の走査による連番生成」である。
--
--   これが主キー複数列版の基本形である。
-- ================================================

SELECT
       class,
       -- class:
       --   クラス番号を返す。
       --
       --   連番を振る順序の第1キーでもある。
       --
       --   最終結果を見ると、
       --   class=1 の行群が先に並び、
       --   その後に class=2 の行群が続く。

       student_id,
       -- student_id:
       --   学生IDを返す。
       --
       --   連番を振る順序の第2キーである。
       --
       --   同じ class の中では
       --   student_id の昇順に連番が付く。

       ROW_NUMBER() OVER (ORDER BY class, student_id) AS seq
       -- ROW_NUMBER():
       --   各行へ 1,2,3,... の通し番号を付ける。
       --
       -- OVER (ORDER BY class, student_id):
       --   並び順を
       --     まず class 昇順
       --     次に student_id 昇順
       --   で決める。
       --
       --   これは複合主キーの辞書順と一致している。
       --
       --   そのため
       --     (1,100) -> 1
       --     (1,101) -> 2
       --     (1,102) -> 3
       --     (2,100) -> 4
       --     ...
       --   という連番になる。
       --
       --   ここで重要なのは、
       --   student_id 単独ではなく
       --   class と組み合わせて順序を決めている点である。
       --
       --   なぜなら student_id='100' は class=1 と class=2 の両方に存在し、
       --   student_id 単独では一意な全順序が作れないからである。
       --
       -- AS seq:
       --   生成した連番に seq という名前を付けている。
       --
       --   この seq は
       --     複合キー順の通し番号
       --   を意味する。

  FROM Weights2;
  -- FROM Weights2:
  --   元表の各行に対して、
  --   複合キー順の連番を付与する。
  --
  --   ウィンドウ関数版では
  --   元行を保ったまま seq 列を追加できるので、
  --   どの行に何番が付いたかを自然に表現できる。


-- ================================================
-- このSQLの処理の流れ
-- ================================================
--
-- 1. Weights2 の全行を (class, student_id) 順に並べる
-- 2. 先頭行へ 1 を付ける
-- 3. 次の行へ 2 を付ける
-- 4. 以下同様に最後まで繰り返す
--
-- 概念的には次のような処理に近い。
--
--   sort rows by (class, student_id)
--
--   seq = 1
--   for each row in sorted rows:
--       row.seq = seq
--       seq += 1
--
-- つまりこれは、
--   「複合キー順に並べた結果に対する通し番号付け」
--   アルゴリズムである。


-- ================================================
-- 実データでのイメージ
-- ================================================
--
-- (class, student_id) 順:
--
--   (1,100)
--   (1,101)
--   (1,102)
--   (2,100)
--   (2,101)
--   (2,102)
--   (2,103)
--
-- したがって連番は
--
--   1 100  1
--   1 101  2
--   1 102  3
--   2 100  4
--   2 101  5
--   2 102  6
--   2 103  7
--
-- になる。
--
-- 重要:
--   class=2 に入ったからといって
--   連番が 1 に戻るわけではない。
--
--   今回は PARTITION BY を使っていないため、
--   全表を通した連番になっている。
--
--   ここを
--     クラスごとの連番
--   と混同しないことが大切である。


-- ================================================
-- 図解: 複合主キー順の連番
-- ================================================
--
--   Weights2
--      |
--      | ORDER BY class, student_id
--      v
--   (1,100)
--   (1,101)
--   (1,102)
--   (2,100)
--   (2,101)
--   (2,102)
--   (2,103)
--      |
--      | ROW_NUMBER()
--      v
--   (1,100) 1
--   (1,101) 2
--   (1,102) 3
--   (2,100) 4
--   (2,101) 5
--   (2,102) 6
--   (2,103) 7
--
-- つまり、
--   複数列で順序を決め、
--   その順に通し番号を振っている。


-- ================================================
-- このSQLの本質
-- ================================================
--
-- このSQLの本質は、
--   一意な順序を複数列で定義し、
--   その辞書順に対して連番を与える
-- ところにある。
--
-- 単一主キー版では
--   1列だけで順序が決まった。
--
-- しかし複合主キー版では
--   1列だけでは順序が足りないことがある。
--
-- そのため
--     ORDER BY class, student_id
-- のように、
--   複数列を組み合わせて全順序を作る必要がある。
--
-- これが主キー複数列版における
--   連番付与
-- の核心である。


-- ================================================
-- 構文ごとの意味
-- ================================================
--
-- ROW_NUMBER()
--   → 各行へ 1,2,3,... の連番を振る
--
-- OVER (ORDER BY class, student_id)
--   → class, student_id の辞書順で番号を振る
--
-- AS seq
--   → 連番列に seq という名前を付ける
-- ================================================