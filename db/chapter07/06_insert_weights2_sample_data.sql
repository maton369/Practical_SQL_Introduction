-- (class, student_id) 順に並べたときの順序
-- ================================================
--
--   (1, 100)
--   (1, 101)
--   (1, 102)
--   (2, 100)
--   (2, 101)
--   (2, 102)
--   (2, 103)
--
-- したがって期待される連番は
--
--   (1,100) -> 1
--   (1,101) -> 2
